	/**
     * Sets up the JMS connection.
     */
    public void initialize() {
        
        // Chain to parent.
        super.initialize();
        
        // Register for throttle event callbacks.
        if(throttle != null) {
        	throttle.setListener(new MessageDroppingThrottleListener());
        }
        
        // Get the statistics accumulator for this service's data source.
        stats = getManager().source(getDestinationName());
        
        // Setup the JNDI initial context if applicable.
        if (jndiProperties != null) try {
            String propertiesFile = jndiProperties.getPropertiesFile();
            Properties properties = PropertiesUtils.loadProperties(propertiesFile);
            context = new InitialContext(properties);
            user = properties.getProperty("java.naming.security.principal");
            password = properties.getProperty("java.naming.security.credentials");
        } catch(Exception e) {
        	throw new InitializationException("Unable to load JNDI configuration file.", e);
        }
        
        // Setup SSL Transport, if necessary.
        if (activeMQSslTransportFactory != null) {
        	log.info("Starting SSL Transport Factory");
        	try {
        		activeMQSslTransportFactory.initialize();
        	} catch(Exception ex) {
        		log.error(String.format("Error initializing SSL Connection: %s", ex.getMessage()), ex);
        	}
        } 
        
        
        // Connect to the JMS server.
    	try {
    		openJMSConnection(null);
        } catch (Exception e) {
            // Clean up.
            closeJMSConnection();
            // Log the error
            log.error("Unable to connect to socket.", e);
    	} 
        
    	dlqProducer = new DeadLetterQueueMessageProducer(connectionFactory);
    	try {
	    	dlqProducer.start();
    	} catch (JMSException e) {
    		dlqProducer.stop();
            log.error("Unable to connect to Dead Letter Queue.", e);
    	} 
    }

    /**
     * Creates a connection to the configured JMS server. If JNDI properties are
     * set, then the connection is created using the connection factory bound to
     * the configured JNDI service; otherwise the default connection factory is
     * used.
     */
    private JMSConnection createConnection(String url) throws JMSException, NamingException {
        // If JNDI is not being used, get a connection using the default connection factory.
    	
    	JMSConnection jmsConnection = null;
    	Connection connection = null;
    	
        if (context == null) {
        	if (connectionFactory instanceof RoundRobinJMSConnectionFactory) {
        		jmsConnection = ((RoundRobinJMSConnectionFactory) connectionFactory).createConnection(url);
        	}
        	else {
        		connection = connectionFactory.createConnection();
        	}

        // Otherwise, get the connection factory from the configured JNDI service.
        } else {
        	log.debug("AbstractJMSProcessingService: using JNDI..check the jndi.properties for ConnectionFactoryName");	
            String connectionFactoryName = jndiProperties.getConnectionFactoryName();
            ConnectionFactory factory = (ConnectionFactory) context.lookup(connectionFactoryName);
            
            if(user == null || password == null) {
            	if (connectionFactory instanceof RoundRobinJMSConnectionFactory) {
            		jmsConnection = ((RoundRobinJMSConnectionFactory) connectionFactory).createConnection(url);
            	} else {
            		connection = factory.createConnection();
            	}
            } else {
            	if (connectionFactory instanceof RoundRobinJMSConnectionFactory) {
            		jmsConnection = ((RoundRobinJMSConnectionFactory) connectionFactory).createConnection(url,user, password);
            	} else {
            		connection = factory.createConnection(user, password);
            	}
            }
        }
        
        if (jmsConnection == null) {
        	return new JMSConnection(connection);
        }
        
        return jmsConnection;
        
        
    }
    
    /**
     * Creates a JMS destination. Either a queue or topic is created depending
     * on which property was specified in the XML configuration. It is an error
     * to specify both a queue and topic.
     */
    private Destination createJMSDestination(Session session) throws JMSException, NamingException {
    	if (queueName != null) {
    		log.debug("createJMSDestination: queueName in config file not null; use session obj to create and return queueName");
            return session.createQueue(queueName);
        } else if (topicName != null) {
            return session.createTopic(topicName);
        } else {
            throw new JMSException("No queue or topic is configured.");
        }
    }
    
    /**
     * Processing incoming JMS messages. Actually, it handles the receipt
     * but delegates the work to extending classes.
     */
    public final void execute() {
    	
    	String brokerURL = null;
        
        // Notify extending classes that processing has started.
        started();
        
        // flag to drop messages
        boolean dropMessage = false;
        
    	long brokerTimeoutStart = 0;
        
        // Process messages until the quit flag is set.
        while (!quit())	{ 
        	try {
	            
	        	// Evaluate whether data processing needs to be throttled back.
	        	if(throttle != null) {
	        		State state = throttle.evaluate();
	        		if(state == State.TERMINATE) {
	        			continue;
	        		} else if (state == State.SUSPEND) {
	        			dropMessage = true;        						
	        		} else {
	        			dropMessage = false;        			
	        		}
	        	}
	        	
	        	if (connection != null) {
	        		// init brokerURL for the first valid connection
		        	brokerURL = connection.getBrokerURL();
	        	} else try {
	            // Re-initialize the connection if needed.
	        		brokerURL = openJMSConnection(brokerURL);
	            	
	            // If no connection could be established.
	            } catch (Exception e) {
	                // Clean up.
	                closeJMSConnection();
	                // Log the error, but only once in a while.
	                String service = getClass().getSimpleName();
	                if (System.currentTimeMillis() >= logTime) {
	                    log.error(service + " was unable to connect to '" + ((queueName != null) ? queueName: topicName) + "'.", e);
	                    logTime = System.currentTimeMillis() + 15 * 60 * 1000;
	                }
	                // Wait a few minutes and retry.
	                try {Thread.sleep(30 * 1000);} catch (InterruptedException ignore) { }
	                continue;
	            }
	            
	            // Wait for the next message, but not too long, we still
	            // need to check for the quit flag in a timely fashion.
	        	long begin = System.currentTimeMillis();
	            Message message = consumer.receive(timeout);
	            long end = System.currentTimeMillis();
	            long deliveryDuration = end - begin;
	            
	            if (message == null) {
	                // If no message was returned, that means that the timeout expired. In general, that just means that
	            	// the queue is currently empty, and we can just attempt the receive again until data arrives. However,
	            	// if the JMS server is actually a cluster, it might indicate that active producers are connected to
	            	// other nodes. Closing the connection allows the re-connect to load balance onto another node.
	            	
	            	// No longer closing because of RoundRobin Connection Factory.
	            	//closeJMSConnection();
	            } else {
	            	// Update that there is activity for this processing service 
	            	IngestActivityMonitor.getInstance().registerActivity();
	    			
	            	begin = System.currentTimeMillis();
	            	List<PipelineError> pipelineErrors = null;
	            	
	            	if (dropMessage) {
	            		// Drop Message.  Logging and alerting of condition is handled by the state transition listener
	            		
	            	} else {
	            		if (message instanceof BytesMessage) {
	            			pipelineErrors = processMessage((BytesMessage) message);
	            		} else if (message instanceof MapMessage) {
	            			pipelineErrors = processMessage((MapMessage) message);
	            		} else if (message instanceof ObjectMessage) {
	            			pipelineErrors = processMessage((ObjectMessage) message);
	            		} else if (message instanceof StreamMessage) {
	            			pipelineErrors = processMessage((StreamMessage) message);
	            		} else if (message instanceof TextMessage) {
	            			pipelineErrors = processMessage((TextMessage) message);
	            		} else {
	            			log.error("Unable to process JMS message of unknown type.");
	            		}           	
	            	}
	                message.acknowledge();
	                
	                end = System.currentTimeMillis();
	                long processingDuration = end - begin;
	                stats.accumulate(deliveryDuration, processingDuration);
	                
	                if (pipelineErrors != null && pipelineErrors.size() > 0) {
	                	for (PipelineError pLineError: pipelineErrors) {
	                		TextMessage txtMessage = dlqProducer.createTextMessage();
	                		txtMessage.setText(pLineError.getRecord());
	                		txtMessage.setStringProperty("Error", pLineError.getErrorMessage());
	                		dlqProducer.send(txtMessage);
	                	}
	                }
	            }
	            
	        } catch (JMSException e) {
	            log.error("JMS Exception reading message on (" + getDestinationName() + ").", e);
	            closeJMSConnection();
	        } catch (Exception e) {
	            log.error("Exception reading message on (" + getDestinationName() + ").", e);
	        }
        }
        
        // Notify extending classes that processing has completed.
        finished();
        
        // Clean up.
        closeJMSConnection();
        
    }
    
    /**
     * Shuts down the JMS queue.
     */
    public final synchronized void terminate() {
        quit = true;
    }
    
    /**
     * Determines whether the quit flags is set
     */
