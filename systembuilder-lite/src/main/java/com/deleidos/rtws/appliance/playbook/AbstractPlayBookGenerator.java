	public void loadToStorageInterface(String email, String domain, String swVersion,
			String aZone, StorageInterface storageInterface, String bucket) throws StorageException {
		StringBuilder tmpModelsDir = new StringBuilder();
		tmpModelsDir.append("/tmp/").append(domain).append("/ingest/models/");

		File destFile = new File(tmpModelsDir.toString());
		if (destFile.isDirectory()) {

			try {
				String debug = String.format("FROM %s TO [%s]%s", tmpModelsDir.toString(), bucket, String.format("%s/models/%s/", swVersion, domain));
				logger.info("Uploading modified models to S3:  " + debug);

				storageInterface.putFiles(tmpModelsDir.toString(), bucket, String.format("%s/models/%s/", swVersion, domain));
				
			} catch(Exception e){
				logger.error(String.format("Failed to upload models. %s", e.toString()), e);
			}
		} else {
			logger.error(String.format("Models for {%s} are not in {%s}", domain, destFile.getAbsolutePath()));
		}
		storageInterface.putFiles(tmpModelsDir.toString(), bucket, String.format("%s/models/%s/", swVersion, domain));
	}
	
