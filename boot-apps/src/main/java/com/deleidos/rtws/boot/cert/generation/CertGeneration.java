	/**
	 * Stores self signed public key for retrieval by other instances.
	 * 
	 * @throws IOException
	 * @throws InterruptedException 
	 * @throws StorageUtilsException 
	 */
	private void savePublicCert() throws IOException, StorageUtilsException, InterruptedException{
		
		//public keypair gets generated where a caapi is installed, only going to store the pair locally
		File pubCert = new File(CAProperties.getPublicCertLocation());
		if(pubCert.exists()){
			//store public cert
			StorageUtils.encryptAndStoreCertificate(new FileInputStream(pubCert));
			
			//create tmp truststore
			File tmpTrust = File.createTempFile("publicTrust", ".jks");
			tmpTrust.delete();  //unique file name only needed
			
			//store public trust store
			FileInputStream pubCertStream=null;
			FileInputStream tmpTrustStream=null;
			
			try{
				pubCertStream = new FileInputStream(pubCert);
				KeystoreUtil.addTrustedCertToKeystore(IOUtils.toByteArray(pubCertStream), CAProperties.getRootCAPassword(), tmpTrust.getCanonicalPath(), CAProperties.getPublicAlias());
			
				tmpTrustStream = new FileInputStream(tmpTrust);
				StorageUtils.storeTruststore(tmpTrustStream);
			}finally{
				if(pubCertStream!=null){
					pubCertStream.close();
				}
				if(tmpTrustStream!=null){
					tmpTrustStream.close();
				}
			}
		}
		else{
			logger.info("No public certificate exists on instance.");
		}
		
		File pubKey = new File(CAProperties.getPublicKeyLocation());
		if(pubKey.exists()){
			StorageUtils.encryptAndStorePrivateKey(new FileInputStream(pubKey));
		}
		else{
			logger.info("No public key exists on instance.");
		}
		
		//cleanup generated keys that are not encrypted
	}
	
