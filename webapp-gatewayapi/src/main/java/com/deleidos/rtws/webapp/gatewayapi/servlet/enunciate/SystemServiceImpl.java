	public String getDefaultAddress(String domain) {
		ApiAuthUtils.assertTmsOrTenantFromUserData();
		if (UserDataUtil.isVpcEnabled()) {
			// This gateway is running inside a vpc, in order
			// to contact the default instance in ec2 we must use
			// the nat at port 443

			return domain + ":" + RtwsConfig.getInstance().getInt("rtws.default.webapp.port");// 443
		} else {
			// some euca systems in NOT vpc do not have DNS entries, so we have
			// to get the private ip.

			String master = MasterUtil.getDns(domain);// actually gets the
														// master private ip

			String baseNodeInfoURI = "%s://%s:%s%s/json/process/retrieve/all";
			String scheme = RtwsConfig.getInstance().getString("rtws.internal.webapp.scheme");
			String port = RtwsConfig.getInstance().getString("rtws.internal.webapp.port");
			String context = RtwsConfig.getInstance()
					.getString("webapp.repository.url.contextPath");

			String requestUrl = String.format(baseNodeInfoURI, scheme, master, port, context);

			Client client = Client.create(JerseyClientConfig.getInstance().getInternalConfig());
			String systemInfo = client.resource(requestUrl).get(String.class);

			JSONObject systemObj = JSONObject.fromObject(systemInfo);
			JSONArray nodes = systemObj.getJSONArray("node");
			for (int i = 0; i < nodes.size(); i++) {
				JSONObject nodeInfo = nodes.getJSONObject(i);
				if (nodeInfo.getString("host").equalsIgnoreCase("default")) {
					return nodeInfo.getString("privateIp") + ":"
							+ RtwsConfig.getInstance().getInt("rtws.internal.webapp.port");// 8443";
				}
			}
			return null; // no default found

		}
	}

