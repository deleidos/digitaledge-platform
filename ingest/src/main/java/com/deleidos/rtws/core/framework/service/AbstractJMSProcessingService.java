    /**
     * Open the connection to the JMS server and setup the queue/topic consumer.
     */
    private String openJMSConnection(String url) throws Exception {

    	long startTime = System.currentTimeMillis();
    		
    	// try and connect for 3 min, or move on to the next RR connection
    	while ((System.currentTimeMillis()-startTime)< 3*60*1000) {
        	try {
                connection = createConnection(url);
                session = connection.getConnection().createSession(false, Session.CLIENT_ACKNOWLEDGE);
                Destination dest = createJMSDestination(session);
                if (dest instanceof Topic && durable) {
                	consumer = session.createDurableSubscriber((Topic)dest, topicName, messageSelector, false);
                }
                else {
                consumer = session.createConsumer(dest, messageSelector, false);
                }
                connection.getConnection().start();
                log.debug("JMS Connection started on " + getDestinationName());
        		
                return connection.getBrokerURL();
        	} catch (Exception e) {
        		log.error("Unable to connect to JMS broker...");
        		Thread.sleep(5000);
        	}
    	}
    	
    	// Unable to connect to previous JMS broker, so return a null value for the URL
        return null;
    }
    
    /**
     * Close the connection to the JMS server and tears down the consumer.
     */
    private void closeJMSConnection() {
        log.debug("Closing JMS Connection on " + getDestinationName());
        try {consumer.close();} catch (Exception ignore) { }
        try {session.close();} catch (Exception ignore) { }
        try {connection.getConnection().close();} catch (Exception ignore) { }
        consumer = null;
        session = null;
        connection = null;
    }
    
    /**
     * Gets the names of the JMS destination.
     */
    public String getDestinationName() {
    	if (queueName != null) {
            return queueName;
        } else if (topicName != null) {
            return topicName;
        } else {
            return null;
        }
    }

	/**
	 * @param durable Is the topic a durable topic?
	 */
	public void setDurable(boolean durable) {
		this.durable = durable;
	}
    
	@SuppressWarnings("unused")
	private class ThrottleListener implements StateTransitionListener {
		public void onPause() {
			// Do nothing.
		}
		public void onTerminate() {
			terminate();
		}
		public void onSuspend() {
			closeJMSConnection();
		}
		public void onResume() {
			// Connection is reopened anyway, no need to do it twice.
		}
	}
	
	private class MessageDroppingThrottleListener implements StateTransitionListener {
		
		boolean alreadyLoggedMsgDrop = false;
		
		private final File pipelineDroppingMessagesFlag = new File("/tmp/.droppingMsgs.txt");
		
		private SystemClock clock = new SystemClock();
		
		public MessageDroppingThrottleListener() {
			
			// Ensure that the flag is deleted on jvm exit
			Runtime.getRuntime().addShutdownHook(new Thread() {

				@Override
				public void run() {
					if (pipelineDroppingMessagesFlag.exists()) {
						if (!pipelineDroppingMessagesFlag.delete())
							pipelineDroppingMessagesFlag.deleteOnExit();
					}						
				}				
			});
		}		
		
		public void onTerminate() {
			terminate();	
			
			alreadyLoggedMsgDrop = false; // reset
			if (pipelineDroppingMessagesFlag.exists()) {
				if (!pipelineDroppingMessagesFlag.delete()) {
					pipelineDroppingMessagesFlag.deleteOnExit();
				}
			}
		}
		public void onSuspend() {		
			
			if (!alreadyLoggedMsgDrop) {
    			log.warn("In throttle condition, dropping message.");
    			alreadyLoggedMsgDrop = true;
    		}
			// Raise message dropping condition to master
			if (!pipelineDroppingMessagesFlag.exists())
				try {
					pipelineDroppingMessagesFlag.createNewFile();
				} catch (IOException e) {
					log.error(e.getMessage(),e);
				}
		}
		public void onResume() {			
			// Clear message dropping condition from master
			// Also, keep in mind this method is now also called on 
			// normal conditions in order to resume message receiving
			if (pipelineDroppingMessagesFlag.exists()) {
				do{
					log.warn("Exit throttle condition.");
					if (!pipelineDroppingMessagesFlag.delete()) {
						log.warn("Unable to delete monitor notification flag.");
						clock.sleep(5000);
					}
				} while(pipelineDroppingMessagesFlag.exists() == true);		
			}
			
			alreadyLoggedMsgDrop = false; // reset
		}
		public void onPause() {
			// Do nothing.			
		}		
	}
	
