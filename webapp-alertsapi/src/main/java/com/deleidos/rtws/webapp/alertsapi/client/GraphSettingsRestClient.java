			String name = lastBackslash == -1 ? dir : dir.substring(lastBackslash+1);
			StringBuffer where = new StringBuffer("");
			if (StringUtils.isNotBlank(prefix)) {
				where.append("PREFIX='").append(prefix).append("'");
			}
			if (StringUtils.isNotBlank(name)) {
				if (where.length() > 0) {
					where.append(" AND ");
				}
				where.append("NAME='").append(name).append("'");
			}

			// Send DELETE to DB API
			WebResource resource = getClient().resource(DBAPI_BASE_URL);
			resource = resource.path("json/update/delete")
				.path(DB_HOST) // database
				.path(GRAPHSETTINGS_DB_SCHEMA) // schema
				.path(GRAPHSETTINGS_DB_TABLE) // table
				.path(where.toString()); // where expression

