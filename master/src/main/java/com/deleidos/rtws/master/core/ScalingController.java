package com.deleidos.rtws.master.core;

import java.util.Timer;
import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.util.time.Clock;
import com.deleidos.rtws.commons.util.time.SystemClock;
import com.deleidos.rtws.master.core.beans.ScalingMonitorCircuitBreaker;
import com.deleidos.rtws.master.core.exception.ServiceException;

/**
 * Business logic implementation for regulating the number of allocated processes base on the current system load.
 * 
 * This class is modeled as a state machine, allowing it to decide what actions are appropriate at the current time
 * based on what actions were taken in the recent past. Three states are allowed (ADJUST, CREST, SETTLE).
 * 
 * ADJUST is the starting/normal state; in it, calls to evaluate will compute the current system load and either
 * allocate or deallocate processes as appropriate (with the limits established by the allocation policy).
 * 
 * CREST is entered after additional processes are allocated; it is a wait state the prevents more adjustments from
 * being made while the new processes stabilize. After the wait time, the state returns to ADJUST.
 * 
 * SETTLE is entered after processes are deallocated; it is a wait state the prevents more adjustments from being made
 * while the processes shuts down. After the wait time, the state returns to ADJUST.
 */
public class ScalingController extends AbstractDelegatingController {

	private static final Logger log = Logger.getLogger(ScalingController.class);

	/** Enumeration define the states that this controller can be in. */
	private static enum State {
		ADJUST, CREST, SETTLE
	};

	/** Substitute for System.currentTimeMillis that allows for controllable testing. */
	private Clock clock = new SystemClock();

	/** The current state that this controller is in. */
	private State state = State.CREST;

	/** The time at which the last state transition occurred. */
	private long stateTransitionTimestamp = clock.time();

	
	private ScalingMonitorCircuitBreaker scalingMonitorCircuitBreaker;
	/**
	 * Constructor.
	 */
	public ScalingController() {
		super();
		if (scalingMonitorCircuitBreaker != null) {
			// Needs the reference to Scaling Controller to start and stop it.
			scalingMonitorCircuitBreaker.setScalingController(this);
		}
	}

	/**
	 * Initializes this group's ScalingMonitor and schedules it to run with the given timer.
	 */
	public void initialize(Timer timer) {
		if (monitor != null) {
			monitor.initialize();
			monitor.schedule(timer);
		}
		
		if (choke != null) {
			choke.initialize();
		}
	}

	/**
	 * Cancels the timer task associated with this group's ScalingMonitor and disposes it.
	 */
	public void dispose() {
		if (monitor != null) {
			monitor.cancel();
			monitor.dispose();
		}
		
		if (choke != null) {
			choke.dispose();
		}
	}

	/**
	 * Indicates the current status of scaling operation.
	 */
	public String status() {
		if(monitor == null) {
			return "Disabled";
		} else if(monitor.isStale()) {
			return "Malfunctioning";
		} else if(state == State.ADJUST) {
			return "Monitoring";
		} else {
			return "Stabilizing";
		}
	}
	
	/**
	 * Evaluates whether current number of allocated processes is appropriate for the current work load, how many are
	 * needed, and whether an adjustment can be made at this time.
	 */
	public void evaluate() {
		switch (state) {
		case ADJUST:
			adjust();
			break;
		case CREST:
			spinUp();
			break;
		case SETTLE:
			spinDown();
			break;
		default:
			throw new IllegalStateException(state.toString());
		}
	}

	/**
	 * Adjusts the number of allocated processes based on the current work load. The configured ScalingMonitor is used
	 * to compute the load as a comparative ratio of the current processing capacity. The current number of allocated
	 * processes is then scaled by that factor to match the load proportionally.
	 * 
	 * The limits configured in the process allocation policy are also applied. This establishes a maximum for the
	 * number of processes that can be allocated or terminated at once to try an avoid overcompensating to quickly.
	 */
	private void adjust() {
		boolean chokeOn = false;
		// If there is no scaling monitor defined, then there is nothing to scale.
		if (monitor == null) { return; }

		if (choke != null) { 
			chokeOn = choke.checkForRestrict();
			if (chokeOn) {
				log.debug("Choke is on, new processes will not be added.");
			}
		}
		
		// Estimate the current system load for this process group.
		double factor = monitor.loadFactor();
		String info = monitor.toString();

		try {

			int count = controller.getActiveProcessCount();
			if (Double.isNaN(factor)) {

				log.warn("Unable to adjust the number of allocated processes for group '" + name + "'; the computed load factor is not a real number.");

			} else if ((factor == Double.POSITIVE_INFINITY) && (!(chokeOn))) {

				if (count < policy.getMax()) {
					if( (scalingMonitorCircuitBreaker != null) && (scalingMonitorCircuitBreaker.checkForBreakCondition())) {
						log.info("Shutting Down Scaling");
						scalingMonitorCircuitBreaker.shutdownMonitor();
						return;
					}
					log.info("Allocating 1 additional process for group '" + name + "'.");
					controller.allocateProcess();
					changeState(State.CREST);
				} else {
					log.info("Unable to allocate additional processes for group '" + name + "' because the max has been reached.");
				}

			} else if (factor == Double.NEGATIVE_INFINITY) {

				if (count > policy.getMin()) {
					log.info("Deallocating 1 process from group '" + name + "'.");
					controller.deallocateProcess();
					changeState(State.SETTLE);
				} else {
					log.info("Unable to deallocate processes from group '" + name + "' because the min has been reached.");
				}

			} else {

				int delta = (int) Math.ceil(count * factor) - count;

				if (delta == 0) {
					// No change needed.
					log.info("Maintaining " + count + " processes for group '" + name + "'.");

				} else if ((delta > 0) && (!(chokeOn))) {

					// Limit the delta to no more than the number of processes that can be allocated at a time.
					delta = Math.min(delta, policy.getAutoAllocationLimit());
					// Insure that the delta does not exceed the maximum total number of processes allowed.
					delta = Math.min(delta, policy.getMax() - count);

					// Perform the calculated reallocation.
					if (delta != 0) {
						if( (scalingMonitorCircuitBreaker != null) && (scalingMonitorCircuitBreaker.checkForBreakCondition())) {
							log.info("Shutting Down Scaling");
							scalingMonitorCircuitBreaker.shutdownMonitor();
							this.dispose();
							return;
						} 
						log.info("Allocating " + delta + " additional processes for group '" + name + "'.");
						controller.allocateProcesses(delta);
						changeState(State.CREST);
					} else {
						log.info("Unable to allocate additional processes for group '" + name + "' because the max has been reached.");
					}

				} else if (delta < 0) {

					// Limit the delta to no more than the number of processes that can be deallocated at a time.
					delta = Math.max(delta, -policy.getAutoTerminationLimit());
					// Insure that the delta does not exceed the minimum total number of processes allowed.
					delta = Math.max(delta, policy.getMin() - count);

					// Perform the calculated reallocation.
					if (delta != 0) {
						log.info("Deallocating " + -delta + " processes from group '" + name + "'.");
						controller.deallocateProcesses(-delta);
						changeState(State.SETTLE);
					} else {
						log.info("Unable to deallocate processes from group '" + name + "' because the min has been reached.");
					}

				}

			}

			int processes = controller.getActiveProcessCount();
			log.debug(String.format("Group=%s, Processes=%d, Load=%f", name, processes, factor) + ", " + info);

		} catch (ServiceException e) {
			log.error("Unable to adjust processes allocation for group '" + name + "'.", e);
		}

	}

	/**
	 * Checks whether the configured grace period that needs the follow the allocation of additional process has passed,
	 * and if so, returns to the normal adjustment monitoring state.
	 */
	private void spinUp() {
		log.debug("Deferring evaluation of process load for group '" + name + "' because additional processes were allocated recently.");
		if (clock.time() > stateTransitionTimestamp + policy.getAutoAllocationLag()) {
			changeState(State.ADJUST);
			evaluate();
		}
	}

	/**
	 * Checks whether the configured grace period that needs the follow the termination of excess process has passed,
	 * and if so, returns to the normal adjustment monitoring state.
	 */
	private void spinDown() {
		log.debug("Deferring evaluation of process load because processes were deallocated recently.");
		if (clock.time() > stateTransitionTimestamp + policy.getAutoTerminationLag()) {
			changeState(State.ADJUST);
			evaluate();
		}
	}

	/**
	 * Changes the current state (and records the time of the transition).
	 * 
	 * @param value
	 *            The state to be transitioned into.
	 */
	private void changeState(State value) {
		state = value;
		stateTransitionTimestamp = clock.time();
	}

	/**
	 * Sets the time keeping doodad; intended for testing.
	 */
	protected void setClock(Clock value) {
		clock = value;
		stateTransitionTimestamp = clock.time();
	}

	public void setScalingMonitorCircuitBreaker(ScalingMonitorCircuitBreaker scalingMonitorCircuitBreaker) {
		this.scalingMonitorCircuitBreaker = scalingMonitorCircuitBreaker;
	}
	
	public ScalingMonitorCircuitBreaker getScalingMonitorCircuitBreaker() {
		return scalingMonitorCircuitBreaker;
	}
	
}
