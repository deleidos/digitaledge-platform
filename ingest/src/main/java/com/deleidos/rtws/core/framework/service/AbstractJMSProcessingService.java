    private String user;
    
    /** The password needed to connect to the JMS server. - left for JNDI, connectionFactory has its own password*/
    private String password;
    
    /** The ActiveMQ SSL Context */
    private ActiveMQSslTransportFactory activeMQSslTransportFactory;
    
    /** The name of the queue to pull data from. */
    private String queueName;
    
    /** The name of the topic to pull data from. */
    private String topicName;
    
    /** Is the topic durable? */
    private boolean durable;
    
    /** Message selector if destination is a topic */
    private String messageSelector;
    
    
    /** Settings for using JNDI instead of default connection factory. */
    private JNDIContextDefinition jndiProperties;
    
    /** JNDI initial context. */
    private InitialContext context;
    
    /** Topic or Queue */
    private MessageConsumer consumer;
    
    /** JMS session */
    private Session session;
    
    /** JMS connection */
    private JMSConnection connection;
    
    
    /** Optional monitoring object used to regulate data consumption. */
    private Throttle throttle;
    
