	// -----------------VALIDATION----------------------

	private static void validateCommand(JSONObject o) throws Exception {
		if (o.containsKey("Operation Status")) {
			String status = o.getString("Operation Status");
			if (status.equalsIgnoreCase("FAILED")) {
				throw new Exception("Operation Failed.  Message was: "
						+ o.getString("Message"));
			}
			return;
		} else {
			throw new Exception(
					"Command failed: there was no operation completion status returned.");
		}
	}

	private static void validateSelect(JSONObject o, int expectedNumRows)
			throws Exception {
		if (o.containsKey("Rows Returned")) {
			if (o.getInt("Rows Returned") != expectedNumRows)
				throw new Exception("Expected " + expectedNumRows
						+ " rows to be returned but got "
						+ o.getInt("Rows Returned"));
		} else {
			throw new Exception(
					"Expected a \"Rows Returned\" count, received none.");
		}
	}

	// -----------------LONG-RUNNING----------------------
	private static class LongRunningHandler extends Thread {
		String taskName;
		String taskToken;
		Logger log = Logger.getLogger(getClass());

		public LongRunningHandler(String name, String token) {
			taskName = name;
			taskToken = token;
			this.start();
		}

		public void run() {
			while (true) {
				HttpURLConnection c;
				try {
					c = getConnection(GET, connectionStr
							+ "status/status?statusToken=" + taskToken);
					InputStream response = c.getInputStream();
					BufferedReader rdr = new BufferedReader(
							new InputStreamReader(response));
					String line = rdr.readLine();
					line = "TASK -" + taskName + "- STATUS: " + line;
					System.out.println(line);
					log.info(line);
					if (line.contains("100") || line.contains("ERROR:"))
						;
					return;
				} catch (Exception e) {
					// TODO Auto-generated catch block
					e.printStackTrace();
				}
			}
		}
	}
