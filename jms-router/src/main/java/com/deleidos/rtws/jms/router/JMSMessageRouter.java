import java.util.ArrayList;
import java.util.HashMap;
import java.util.Map;
import java.util.Timer;
import java.util.TimerTask;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import javax.jms.Connection;
import javax.jms.ConnectionFactory;
import javax.jms.JMSException;
import javax.jms.Message;
import javax.jms.MessageListener;

import org.apache.activemq.ActiveMQConnectionFactory;
import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.config.ConfigEncryptor;
import com.deleidos.rtws.commons.exception.InitializationException;
import com.deleidos.rtws.commons.net.jms.BasicMessageConsumer;
import com.deleidos.rtws.commons.net.jms.BasicMessageProducer;
import com.deleidos.rtws.commons.net.jms.JMSFactory;
import com.deleidos.rtws.commons.util.Initializable;
import com.deleidos.rtws.jms.router.metrics.JMSMetricsCollector;
import com.deleidos.rtws.jms.router.metrics.JMSNodeMetrics;
import com.deleidos.rtws.jms.router.strategy.JMSRouterStrategy;

public class JMSMessageRouter implements Initializable, MessageListener {

	private static Logger log = Logger.getLogger(JMSMessageRouter.class);

	private static final int FAILED_CONNECT_SLEEP_TIME = 5000;

	private static final int CONNECTION_CHECK_INTERVAL = 30 * 1000;
	private static final int ROUTE_CALC_INTERVAL = 60 * 1000;

	private JMSConnectionDefinition jmsSource;
	private JMSConnectionDefinition[] jmsDestinations;
	private JMSRouterStrategy routingStrategy;
	private ModelQueuesMappingCache modelsCache;

	private BasicMessageConsumer jmsConsumer;
	private JMSMetricsCollector[] metricsCollectors;
	private BasicMessageProducer[] jmsProducers;

	private Timer connectTimer;
	private Timer routeTimer;

	private int calculatedProducerIndex = 0;
	private int localProducerIndex = 0;
	private int messageCounter;

	private BasicMessageProducer waitForWorkingProducer(String messageId) {

		// the calculatedProducerIndex is calculated in the CalculateRouteTask Timer
		int currentIndex = calculatedProducerIndex;
		BasicMessageProducer producer = jmsProducers[currentIndex];

		// block until a producer has been found
		int retryCount = 0;
		while (producer == null) {

			if (retryCount >= 100) {
				log.warn(String.format("Unable to find acceptable producer to send message '{%s}' to.  Retrying...", messageId));
				retryCount = 0; // reset
			}

			retryCount++;
			producer = jmsProducers[calculatedProducerIndex];

			// Verify health of producer before returning
			if (producer != null) {
				if (!producer.isConnected()) {
					// having trouble connecting to this JMS node, disconnect it and allow the reconnect thread
					// a chance to re-establish a connection to it
					disconnectProducer(currentIndex);
				}
			} else {
				// Delay for reconnect timer task to re-establish connection
				try {
					Thread.sleep(CONNECTION_CHECK_INTERVAL);
				} catch (InterruptedException e) {
					log.error(e);
				}
			}
		}

		return producer;
	}

	@Override
	public void onMessage(Message message) {

		logAtInterval("Received message number: " + (++messageCounter), messageCounter, 1000);

		// block until message has been sent
		int retryCount = 0;
		boolean msgSent = false;
		while (!msgSent) {
			try {
				BasicMessageProducer producer = waitForWorkingProducer(message.getJMSMessageID());
				producer.send(message);
				msgSent = true;
			} catch (JMSException jmse) {
				// TODO what to do if other types of JMSExceptions are thrown ???
				// Should it continue to try indefinitely, or log and fail ???
				if (retryCount >= 100) {
					log.warn("Error attempting to transmit message.  Retrying...", jmse);
					retryCount = 0; // reset
				}

			} catch (Exception e) {
				if (retryCount >= 100) {
					log.warn("Error attempting to transmit message.  Retrying...", e);
					retryCount = 0; // reset
				}

			}

			retryCount++;
		}
	}

	@Override
	public void initialize() throws InitializationException {
		if (jmsSource == null) {
			throw new InitializationException("jmsSource cannot be null");
		} else if (jmsDestinations == null) {
			throw new InitializationException("jmsDestinations cannot be null");
		} else if (routingStrategy == null) {
			throw new InitializationException("routingStrategy cannot be null");
		} else {
			log.info("JMSMessageRouter using router strategy: " + routingStrategy.getClass().getSimpleName());

			localProducerIndex = getNodeNumber() - 1;
			log.info("Local JMS Destination index: " + localProducerIndex);

			// TODO need a better way
			decryptPassword(jmsSource.getConnectionFactory());
			for (JMSConnectionDefinition con : jmsDestinations) {
				decryptPassword(con.getConnectionFactory());
			}

			jmsProducers = new BasicMessageProducer[jmsDestinations.length];
			metricsCollectors = new JMSMetricsCollector[jmsDestinations.length];

			connectTimer = new Timer();
			connectTimer.schedule(new ReconnectTask(), 0, CONNECTION_CHECK_INTERVAL);

			routeTimer = new Timer();
			routeTimer.schedule(new CalculateRouteTask(), 0, ROUTE_CALC_INTERVAL);
		}
	}

	private void logAtInterval(String message, int counter, int interval) {
		if (counter % interval == 0) {
			log.info(message);
		}
	}

	private int getNodeNumber() {
		String fqdn = System.getProperty("RTWS_FQDN");
		int number = 0;
		if (fqdn != null) {
			String name = fqdn.substring(0, fqdn.indexOf("."));
			Pattern p = Pattern.compile("(\\d+)$");
			Matcher m = p.matcher(name);
			String num = "0";
			if (m.find()) {
				num = m.group();
			}
			number = Integer.parseInt(num);
		}
		return number;
	}

	private class ReconnectTask extends TimerTask {
		@Override
		public void run() {
			Connection con1 = null;
			try {
				con1 = jmsSource.getConnectionFactory().createConnection();
				con1.start();
				if (jmsConsumer != null && !jmsConsumer.isConnected()) {
					log.warn("Connection failed to JMS Node " + (localProducerIndex + 1) + ", closing Consumer ");
					disconnectConsumer();
				}
				if (jmsConsumer == null) {
					log.info("Connection established to JMS Node " + (localProducerIndex + 1) + ", connecting Consumer");
					connectConsumer(1);
				}
			} catch (Exception e) {
				if (jmsConsumer != null) {
					log.warn("Connection failed to JMS Node " + (localProducerIndex + 1) + ", closing Consumer ");
					disconnectConsumer();
				}
			} finally {
				if (con1 != null) {
					try {
						con1.stop();
						con1.close();
					} catch (JMSException e) {
						log.error(e);
					}

				}
			}

			for (int i = 0; i < jmsDestinations.length; i++) {
				JMSConnectionDefinition jmsDef = jmsDestinations[i];
				Connection con = null;
				try {
					// testing the connection to the JMS Node via createConnection because jmsProducers[i].isConnected()
					// does not seem to work when the JMS Node goes down
					con = jmsDef.getConnectionFactory().createConnection();
					con.start();
					if (jmsProducers[i] == null) {
						log.info("Connection established to JMS Node " + (i + 1) + ", connecting Producer");
						connectProducer(i, 1);
					}
				} catch (Exception e) {
					log.warn(e.toString());
					if (jmsProducers[i] != null) {
						log.warn("Connection failed to JMS Node " + (i + 1) + ", closing Producer ");
						disconnectProducer(i);
					}
				} finally {
					if (con != null) {
						try {
							con.stop();
							con.close();
						} catch (JMSException e) {
							log.error(e);
						}
					}
				}
			}
		}
	}

	private class CalculateRouteTask extends TimerTask {
		@Override
		public void run() {
			try {
				Map<Integer, Integer> metricProducerIndexMap = new HashMap<Integer, Integer>();
				int localNodeMetricIndex = -1;

				ArrayList<JMSNodeMetrics> metricsList = new ArrayList<JMSNodeMetrics>();
				int metricIndex = 0;
				for (int producerIndex = 0; producerIndex < jmsProducers.length; producerIndex++) {
					JMSMetricsCollector collector = metricsCollectors[producerIndex];
					if (collector != null && collector.isConnected()) {
						metricsList.add(collector.getMetrics());
						metricProducerIndexMap.put(metricIndex, producerIndex);
						if (producerIndex == localProducerIndex) {
							localNodeMetricIndex = metricIndex;
						}
						metricIndex++;
					} else {
						log.warn("Not currently connected to JMS Node " + (producerIndex + 1) + ", removing from metrics calculation");
					}
				}

				if (metricsList.size() == 0) {
					calculatedProducerIndex = localProducerIndex;
					log.info("Not connected to any nodes, default routing to local JMS Node: " + (calculatedProducerIndex + 1));
				} else {
					int selectedMetricIndex = routingStrategy.getNextRoute(metricsList.toArray(new JMSNodeMetrics[0]), localNodeMetricIndex);
					calculatedProducerIndex = metricProducerIndexMap.get(selectedMetricIndex);

					// sanity check the producerIndex that we get
					if (calculatedProducerIndex < 0 || calculatedProducerIndex > jmsProducers.length) {
						log.error("RoutingStrategy returned invalid jms index:" + calculatedProducerIndex + ", routing to the local JMS Node");
						calculatedProducerIndex = localProducerIndex;
					}
					log.info("New route calculated, Index: " + selectedMetricIndex + " JMS Node: " + (calculatedProducerIndex + 1));
				}
			} catch (Exception e) {
				log.error("Error in route calculation thread", e);
			}
		}
	}

	private synchronized void connectConsumer(int maxRetries) {
		if (jmsConsumer == null) {
			JMSFactory consumerFactory = new JMSFactory(jmsSource.getConnectionFactory());
			jmsConsumer = consumerFactory.createSimpleQueueConsumer(jmsSource.getQueues()[0]);
		}

		int attempt = 1;
		while (!jmsConsumer.isConnected() && attempt <= maxRetries) {
			try {
				jmsConsumer.open();
				jmsConsumer.setMessageListener(this);
				jmsConsumer.start();
			} catch (Exception e) {
				log.warn("Failed to start JMSConsumer, attempt number " + attempt + ", " + e);
				try {
					jmsConsumer.stop();
					jmsConsumer.close();
				} catch (Exception e2) {
					log.warn("Error stopping JMSConsumer, attempt number " + attempt + ", " + e);
				}
				try {
					Thread.sleep(FAILED_CONNECT_SLEEP_TIME);
				} catch (InterruptedException ignore) {
				}
			}
			attempt++;
		}
		if (!jmsConsumer.isConnected()) {
			log.warn("Failed to start JMSConsumer after max attempts");
			disconnectConsumer();
		}
	}

	private synchronized void connectProducer(int index, int maxRetries) {
		JMSConnectionDefinition jmsDef = jmsDestinations[index];
		BasicMessageProducer producer = jmsProducers[index];
		if (producer == null) {
			if ( modelsCache != null ) {
				producer = ModelFanoutMessageProducer.createInstance(jmsDef.getConnectionFactory(), modelsCache, jmsDef.getQueues());
			} else {
				JMSFactory factory = new JMSFactory(jmsDef.getConnectionFactory());
				producer = factory.createFanoutProducer(jmsDef.getQueues());
			}
			jmsProducers[index] = producer;
		}

		int attempt = 1;
		while (!producer.isConnected() && attempt <= maxRetries) {
			log.info("Starting Producer for JMS Node " + index + " attempt number " + attempt);
			try {
				producer.open();
			} catch (JMSException e) {
				producer.close();
				log.warn("Failed to open Producer, attempt number " + attempt + ", " + e.toString());
				try {
					Thread.sleep(FAILED_CONNECT_SLEEP_TIME);
				} catch (InterruptedException ignore) {
				}
			}
			attempt++;
		}

		if (producer.isConnected()) {
			// we can connect to the jms node, so now try to start the jms metrics collector
			log.info("Starting Metrics collector " + index);
			JMSMetricsCollector collector = metricsCollectors[index];
			if (collector == null) {
				collector = new JMSMetricsCollector();
				metricsCollectors[index] = collector;
			}
			collector.setJmxURL(jmsDef.getJmxURL());
			collector.setQueues(jmsDef.getQueues());
			collector.setMaxConnectionRetries(maxRetries);
			collector.initialize();
			if (collector.isConnected()) {
				collector.start();
			}
		} else {
			log.warn("Failed to open Producer connection for JMS Node " + index);
			disconnectProducer(index);
		}
	}

	private synchronized void disconnectProducer(int index) {
		if (jmsProducers[index] != null) {
			jmsProducers[index].close();
		}
		jmsProducers[index] = null;
		if (metricsCollectors[index] != null) {
			metricsCollectors[index].dispose();
		}
		metricsCollectors[index] = null;
	}

	private synchronized void disconnectConsumer() {
		if (jmsConsumer != null) {
			try {
				jmsConsumer.stop();
			} catch (Exception e) {
				log.warn("Error stopping Consumer " + e.toString());
			}
			jmsConsumer.close();
			jmsConsumer = null;
		}

	}

	@Override
	public synchronized void dispose() {
		connectTimer.cancel();
		routeTimer.cancel();

		disconnectConsumer();
		if (jmsProducers != null) {
			for (int i = 0; i < jmsProducers.length; i++) {
				disconnectProducer(i);
			}
		}
	}

	public JMSConnectionDefinition getJmsSource() {
		return jmsSource;
	}

	public void setJmsSource(JMSConnectionDefinition jmsSource) {
		this.jmsSource = jmsSource;
	}

	public JMSConnectionDefinition[] getJmsDestinations() {
		return jmsDestinations;
	}

	public void setJmsDestinations(JMSConnectionDefinition[] jmsDestinations) {
		this.jmsDestinations = jmsDestinations;
	}

	public JMSRouterStrategy getRoutingStrategy() {
		return routingStrategy;
	}

	public void setRoutingStrategy(JMSRouterStrategy routingStrategy) {
		this.routingStrategy = routingStrategy;
	}

	private void decryptPassword(ConnectionFactory factory) {
		if (factory instanceof ActiveMQConnectionFactory) {
			ActiveMQConnectionFactory fact = (ActiveMQConnectionFactory) factory;
			fact.setPassword(ConfigEncryptor.instance().decryptWithWrapper(fact.getPassword()));
		}
	}

	public ModelQueuesMappingCache getModelsCache() {
		return modelsCache;
	}

	public void setModelsCache(ModelQueuesMappingCache modelsCache) {
		this.modelsCache = modelsCache;
	}

}
