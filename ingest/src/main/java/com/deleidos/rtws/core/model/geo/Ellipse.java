package com.deleidos.rtws.core.model.geo;

import org.geotools.referencing.GeodeticCalculator;

/**
 * Anchored shape representing an elliptical margin of error around a fixed
 * point.
 */
public final class Ellipse extends CenteredShape {
    
    public static final int PARAMETER_ORIENTATION = 0;
    public static final int PARAMETER_SEMI_MAJOR_AXIS = 1;
    public static final int PARAMETER_SEMI_MINOR_AXIS = 2;
    
    private static final int COORDINATE_PAIRS = 50;
    
    /**
     * Constructor... Used only by the shape factory.
     * 
     * @param latitude Latitude of cell tower location.
     * @param longitude Longitude of cell tower location.
     * @param parameters Parameter list; azimuth, timing advance.
     */
    protected Ellipse(double latitude, double longitude, double... parameters) {
        this(latitude, longitude, parameters[PARAMETER_ORIENTATION], parameters[PARAMETER_SEMI_MAJOR_AXIS], parameters[PARAMETER_SEMI_MINOR_AXIS]);
    }
    
    /**
     * Constructor.
     * 
     * @param latitude Latitude of center point.
     * @param longitude Longitude of center point.
     * @param orientationDegrees Direction of tilt of the ellipse.
     * @param semiMajorAxisMeters Length of radius along the longer axis.
     * @param semiMinorAxisMeters Length of radius along the shorter axis.
     */
    public Ellipse(double latitude, double longitude, double orientationDegrees, double semiMajorAxisMeters, double semiMinorAxisMeters) {
        
        super(ELLIPSE, latitude, longitude, normalizeOrientation(orientationDegrees), semiMajorAxisMeters, semiMinorAxisMeters);
        
        if(semiMajorAxisMeters <= 0.0 || semiMajorAxisMeters > 750000.0) {
            throw new IllegalArgumentException("Semi-major axis '" + semiMajorAxisMeters + " is outside valid range (0.0 - 750,000.0).");
        }
        
        if(semiMinorAxisMeters <= 0.0 || semiMinorAxisMeters > 750000.0) {
            throw new IllegalArgumentException("Semi-minor axis '" + semiMinorAxisMeters + " is outside valid range (0.0 - 750,000.0).");
        }
        
        if(semiMinorAxisMeters > semiMajorAxisMeters) {
            throw new IllegalArgumentException("Semi-minor axis '" + semiMinorAxisMeters + " is larger than Semi-major axis '" + semiMajorAxisMeters + "'.");
        }
        
    }
    
    /**
     * Get parameter names.
     * @return Parameter names array.
     */
    public String[] getParameterNames() {
        return new String[] {"orientation", "semi-major-axis", "semi-minor-axis"};
    }

    /** 
     * Determine if is bounded.
     * @return boolean
     */
    public boolean isBounded() {
        return true;
    }

    /**
     * Determine if is scalable.
     * @return boolean
     */
    public boolean isScalable() {
        return true;
    }
    
    /**
     * Direction of the tilt of the ellipse measured in degrees from true north.
     */
    public double getAxisOrientationDegrees() {
        return getParameter(PARAMETER_ORIENTATION);
    }
    
    /**
     * Length of the radius along the longer axis.
     */
    public double getSemiMajorAxisMeters() {
        return getParameter(PARAMETER_SEMI_MAJOR_AXIS);
    }
    
    /**
     * Length of the radius along the shorter axis.
     */
    public double getSemiMinorAxisMeters() {
        return getParameter(PARAMETER_SEMI_MINOR_AXIS);
    }
    
    /**
     * Computes the set of points used to represent this shape as a polygon.
     */
    public double[] computeOrdinates() {
        
        // number of ordinates
        int ordinates = COORDINATE_PAIRS * 2;
        
        // add 1st lon/lat pair as last pair to have a closed shape
        double[] ordinateArray = new double[ordinates + 2];
        
        double lon = getLongitude();
        double lat = getLatitude();
        double maj = getSemiMajorAxisMeters();
        double min = getSemiMinorAxisMeters();
        double orn = normalizeOrientation(getAxisOrientationDegrees());
        
        // Convert 1 degree of lon/lat into meters, specific to geo-location
        double lonDegreeMeters = convertLongitudinalDegreesToMeters(lat, lon);
        double latDegreeMeters = convertLatitudinalDegreesToMeters(lat, lon);
        
        GeodeticCalculator calc = new GeodeticCalculator();
        calc.setStartingGeographicPoint(lon, lat); // reset to center point
        
        // Factor to adjust the meters depending on the angle of orientation:
        // Use the slope with pythagorean theorum to convert 1 degree lat/lon
        // into meters
        // Normal slope uses angle from positive x-axis
        // Modified slope uses angle from positive y-axis (ie. North)
        // Thus, the orientation will work to get the semi-MINOR value,
        // but it must be modified to get the semi-MAJOR value.
        double minorAxisSlope = Math.tan(Math.toRadians(orn));
        double minorAxisSlopeMeters = Math.sqrt(Math.pow((latDegreeMeters * minorAxisSlope), 2) + Math.pow(lonDegreeMeters, 2)) / Math.sqrt(Math.pow(minorAxisSlope, 2) + 1);
        
        double majorAxisSlope = Math.tan(Math.toRadians(90 - orn));
        double majorAxisSlopeMeters = Math.sqrt(Math.pow((latDegreeMeters * majorAxisSlope), 2) + Math.pow(lonDegreeMeters, 2)) / Math.sqrt(Math.pow(majorAxisSlope, 2) + 1);
        
        // major and minor axis conversion (degrees to meters) factors are
        // switched due to
        // difference between normal slope and modified slope
        double minFactor = maj / majorAxisSlopeMeters;
        double majFactor = min / minorAxisSlopeMeters;
        
        // Ellipse formula:
        double beta = (-1.0 * orn) / 180.0 * Math.PI;
        double sinBeta = Math.sin(beta);
        double cosBeta = Math.cos(beta);
        
        int index = 0;
        for (double angle = 0.0; angle < 360.0 && index < ordinates; angle += (360.0 / COORDINATE_PAIRS)) {
            double alpha = angle / 180.0 * Math.PI;
            double sinAlpha = Math.sin(alpha);
            double cosAlpha = Math.cos(alpha);
            double x = wrapLongitude(lon + ((majFactor * cosAlpha * cosBeta) - (minFactor * sinAlpha * sinBeta)));
            double y = wrapLatitude(lat + ((majFactor * cosAlpha * sinBeta) + (minFactor * sinAlpha * cosBeta)));
            ordinateArray[index++] = x;
            ordinateArray[index++] = y;
        }
        
        // Close the polygon.
        ordinateArray[index++] = ordinateArray[0];
        ordinateArray[index++] = ordinateArray[1];
        
        return ordinateArray;
        
    }
    
    /**
     * Computes the furthest distance of any part of the shape
     * from the anchor point, in meters.
     * 
     * @return meters from centroid location or -1 if not known
     */
    @Override
    public double getFurthestPointDistanceMeters() {
        return getSemiMajorAxisMeters();
    }
    
    /**
     * Approximates the length in meters of a single degree of longitude as it
     * would be near the given latitude and longitude. Because degrees are a
     * measure of angular distance, their equivalent linear measure along the
     * earths surface varies at different latitudes. In other words, the
     * distance between two lines of longitude is greater at the equator then it
     * is at the poles.
     */
    private static double convertLongitudinalDegreesToMeters(double lat, double lon) {
        GeodeticCalculator calc = new GeodeticCalculator();
        calc.setStartingGeographicPoint(wrapLongitude(lon - 1), lat);
        calc.setDestinationGeographicPoint(wrapLongitude(lon + 1), lat);
        return calc.getOrthodromicDistance() / 2;
    }
    
    /**
     * Approximates the length in meters of a single degree of latitude as it
     * would be near the given latitude and longitude. Because degrees are a
     * measure of angular distance, their equivalent linear measure along the
     * earths surface varies at different latitudes. In other words, the
     * distance between two lines of longitude is greater at the equator then it
     * is at the poles.
     */
    private static double convertLatitudinalDegreesToMeters(double lat, double lon) {
        GeodeticCalculator calc = new GeodeticCalculator();
        calc.setStartingGeographicPoint(lon, wrapLatitude(lat - 1));
        calc.setDestinationGeographicPoint(lon, wrapLatitude(lat + 1));
        return calc.getOrthodromicDistance() / 2;
    }
    
    /**
     * Sets latitude to something sane by wrapping.
     */
	private static double wrapLatitude(double lat) {
		return wrap(90.0, lat);
	}

    /**
     * Sets longitude to something sane by wrapping.
     */
	private static double wrapLongitude(double lon) {
		return wrap(180.0, lon);
	}

    /**
     * Wraps the value to be in the range of -max and max.
     */
	private static double wrap(double max, double value) {
		if ((value < -max) || (value > max)) {
			value += max;
			value %= (max * 2.0);
			value = (value < 0.0) ? (max + value) : (-max + value);
		}

		return value;
	}

	/**
     * Normalizes axis orientation to a positive number.
     */
    private static double normalizeOrientation(double azimuth) {
        azimuth = normalizeAzimuth(azimuth);
        return (azimuth < 0) ? azimuth + 180.0 : azimuth;
    }

	@Override
	public Double computeNominalCoverage() {
		double semiMinor = getParameter(PARAMETER_SEMI_MINOR_AXIS);
		double semiMajor = getParameter(PARAMETER_SEMI_MAJOR_AXIS);
		return semiMinor * semiMajor * Math.PI;
	}
    
}
