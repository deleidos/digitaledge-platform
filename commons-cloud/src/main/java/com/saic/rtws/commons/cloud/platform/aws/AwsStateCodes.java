package com.saic.rtws.commons.cloud.platform.aws;

/**
 * Repository of AWS Codes.
 */
public class AwsStateCodes {
	
	/** Constructor. */
	protected AwsStateCodes() { }
	
	/** Code for an instance starting. */
	public static final int INSTANCE_CODE_STARTING = 16;
	
	/** Code for an instance stopped. */
	public static final int INSTANCE_CODE_STOPPED = 80;
	
	/** Code for an instance stopping. */
	public static final int INSTANCE_CODE_STOPPING = 16;
	
	/** Code for an instance termination. */
	public static final int INSTANCE_CODE_TERMINATED = 48;
	
}
