	public Map<String, FirewallGroup> listVpcSecurityGroupsByName(String vpcId) {
		Collection<FirewallGroup> fgroups = RestClientManager.getServiceInterface().listFirewallGroups();
		
		if (fgroups != null) {
			HashMap<String, FirewallGroup> result = new HashMap<String, FirewallGroup>();
			
			for (FirewallGroup fgroup : fgroups) {
				if (vpcId.equals(fgroup.getVpcId())) {
					result.put(fgroup.getName(), fgroup);
				}
			}
			
			return result;
		}
		
		return Collections.emptyMap();
	}
	
	public void createVpcSecurityGroups(String vpcId) {
		Map<String, FirewallGroup> sgMapping = listVpcSecurityGroupsByName(vpcId);
		
		if (! sgMapping.containsKey(SecurityGroupUtil.VPC_DATASINK_SG_NAME)) {
			try {
				createVpcSecurityGroup(SecurityGroupUtil.VPC_DATASINK_SG_NAME, "datasink default VPC security group", vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to create vpc security groups.", ex);
			}
		} 
		
		if (! sgMapping.containsKey(SecurityGroupUtil.VPC_INTERNAL_SG_NAME)) {
			try {
				createVpcSecurityGroup(SecurityGroupUtil.VPC_INTERNAL_SG_NAME, "internal default VPC security group", vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to create vpc security groups.", ex);
			}
		}
		
		if (! sgMapping.containsKey(SecurityGroupUtil.VPC_WEBAPP_SG_NAME)) {
			try {
				createVpcSecurityGroup(SecurityGroupUtil.VPC_WEBAPP_SG_NAME, "webapp default VPC security group", vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to create vpc security groups.", ex);
			}
		}
		
		if (! sgMapping.containsKey(SecurityGroupUtil.VPC_NAT_SG_NAME)) {
			try {
				createVpcSecurityGroup(SecurityGroupUtil.VPC_NAT_SG_NAME, "NAT default VPC security group", vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to create vpc security groups.", ex);
			}
		}
	}
	
	public void deleteVpcSecurityGroups(String vpcId) {
		Map<String, FirewallGroup> sgMapping = listVpcSecurityGroupsByName(vpcId);
		
		// Delete all the security group rules for each security group.
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_DATASINK_SG_NAME)) {
			deleteVpcSecurityGroupRules(sgMapping.get(SecurityGroupUtil.VPC_DATASINK_SG_NAME));
		}
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_INTERNAL_SG_NAME)) {
			deleteVpcSecurityGroupRules(sgMapping.get(SecurityGroupUtil.VPC_INTERNAL_SG_NAME));
		}
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_WEBAPP_SG_NAME)) {
			deleteVpcSecurityGroupRules(sgMapping.get(SecurityGroupUtil.VPC_WEBAPP_SG_NAME));
		}
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_NAT_SG_NAME)) {
			deleteVpcSecurityGroupRules(sgMapping.get(SecurityGroupUtil.VPC_NAT_SG_NAME));
		}
		
		// Delete the vpc security groups.
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_DATASINK_SG_NAME)) {
			try {
				deleteVpcSecurityGroup(SecurityGroupUtil.VPC_DATASINK_SG_NAME, vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to delete vpc security groups.", ex);
			}
		} 
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_INTERNAL_SG_NAME)) {
			try {
				deleteVpcSecurityGroup(SecurityGroupUtil.VPC_INTERNAL_SG_NAME, vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to delete vpc security groups.", ex);
			}
		}
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_WEBAPP_SG_NAME)) {
			try {
				deleteVpcSecurityGroup(SecurityGroupUtil.VPC_WEBAPP_SG_NAME, vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to delete vpc security groups.", ex);
			}
		}
		
		if (sgMapping.containsKey(SecurityGroupUtil.VPC_NAT_SG_NAME)) {
			try {
				deleteVpcSecurityGroup(SecurityGroupUtil.VPC_NAT_SG_NAME, vpcId);
			} catch (Exception ex) {
				logger.debug("Fail to delete vpc security groups.", ex);
			}
		}
	}
	
	public void deleteVpcSecurityGroupRules(FirewallGroup fgroup) {
		for (FirewallRule rule : fgroup.getRules()) {
			for (FirewallGroupSource fgSource : rule.getFirewallGroupSources()) {
				try {
					String source = (fgSource.getUserId() != null) ? fgSource.getUserId() + "/" + fgSource.getGroupId() : fgSource.getGroupId();
					RestClientManager.getSecurityGroupServiceClient().deleteRuleByGroupId(fgroup.getId(), rule.getProtocol(), 
							rule.getFromPort(), rule.getToPort(), source);
				} catch (SecurityServiceClientException e) {
					logger.debug(String.format("Fail to delete firewall rule with group id %s for sgroup %s.", fgSource.getGroupId(), fgroup.getId()));
				}
			}
			
			for (FirewallIpRangeSource ipRange : rule.getIpRanges()) {
				try {
					RestClientManager.getSecurityGroupServiceClient().deleteRuleByGroupId(fgroup.getId(), rule.getProtocol(), rule.getFromPort(), rule.getToPort(), ipRange.getIpRange());
				} catch (SecurityServiceClientException e) {
					logger.debug(String.format("Fail to delete firewall rule with ip range %s for sgroup %s.", ipRange.getIpRange(), fgroup.getId()));
				}
			}
		}
	}
	
