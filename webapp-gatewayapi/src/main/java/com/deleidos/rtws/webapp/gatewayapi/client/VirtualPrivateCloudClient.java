	public String getMainRouteTableId(String vpcId, Boolean onlyMain) throws RestClientException {
		List<RouteTable> routeTables = null;
		
		String message = String.format("Fail to get main route table id for vpc %s.", vpcId);
		
		try {
			routeTables = RestClientManager.getServiceInterface().describeRouteTables(vpcId, onlyMain);
			
			if (routeTables != null && ! routeTables.isEmpty()) {
				return routeTables.get(0).getRouteTableId();
			}
		} catch (Exception ex){
			logger.error(message, ex);
			throw new RestClientException(message, ex);
		}
		
		throw new RestClientException(message);
	}
	
	public com.deleidos.rtws.commons.cloud.beans.vpc.RouteTable describeRouteTable(String vpcId, String routeTableId) throws RestClientException {
		List<RouteTable> routeTables = null;
		
		String message = String.format("Fail to describe route table %s for vpc %s.", routeTableId, vpcId);
		
		try {
			routeTables = RestClientManager.getServiceInterface().describeRouteTables(vpcId, false);
			
			if (routeTables != null) {
				for (com.deleidos.rtws.commons.cloud.beans.vpc.RouteTable table : routeTables) {
					if (routeTableId.equals(table.getRouteTableId())) {
						return table;
					}
				}
			}
		} catch (Exception ex){
			logger.error(message, ex);
			throw new RestClientException(message, ex);
		}
		
		throw new RestClientException(message);
	}
	
