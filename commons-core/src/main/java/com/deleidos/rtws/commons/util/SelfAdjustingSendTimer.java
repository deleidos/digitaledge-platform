package com.deleidos.rtws.commons.util;

import java.util.Timer;
import java.util.TimerTask;

import org.apache.log4j.Logger;

/**
 * A timer utility for classes that buffer data and need to send/flush/commit that data on a
 * time based interval.  This timer is intended for classes that send data based on some buffer limit,
 * but need a timer running to ensure that even during times of inactivity all data in the buffer is
 * always flushed.  This class adjusts its time interval for sending based on the data flow of 
 * the sending class.  When data throughput is high, the timer interval is increased to a max value
 * and when throughput is low, the timer interval is decreased to ensure that small trickles of
 * data are sent in a timely manner.
 * 
 * Sending classes must implement the sendDataByTimer method to tell this timer how to send the
 * data in the buffer.  Sending classes must also provide the time data was last sent by reasons 
 * other than this timer (data sent based on buffer size, etc.)  This is critical for the timer to 
 * calculate its dynamic timer interval.
 */
public abstract class SelfAdjustingSendTimer {

	private static final Logger logger = Logger.getLogger(SelfAdjustingSendTimer.class);
	private String name;
	private int minInterval;
	private int maxInterval;
	private int adjustSize;
	private int currentInterval;
	private boolean lowVolumeFlushStrategy;
	private int adjustCheckCount = 0;
	private int maxChecksBeforeAdjust = 3;
	private Timer timer;
	
	
	/**
	 * Construct a new Timer with a given name and min/max timer intervals.  This timer will
	 * adjust down to and up to the respective values based on data flow.
	 * @param name
	 * @param minInterval
	 * @param maxInterval
	 */
	public SelfAdjustingSendTimer(String name, int minInterval, int maxInterval, boolean lowVolumeFlushStrategy) {
		super();
		this.name = name;
		this.minInterval = minInterval;
		this.maxInterval = maxInterval;
		this.adjustSize = ((maxInterval - minInterval) / 5);
		this.currentInterval = minInterval;
		this.lowVolumeFlushStrategy = lowVolumeFlushStrategy;
	}

	/**
	 * Starts the execution of this timer
	 */
	public synchronized void start() {
		timer = new Timer(name, true);
		timer.schedule(new SendTimerTask(), currentInterval, currentInterval);
		logger.info("Starting " + this.name + ", time interval: " + currentInterval);
	}
	
	/**
	 * Stops the execution of this timer
	 */
	public synchronized void stop() {
		timer.cancel();
	}
	
	/**
	 * @return The current time interval being used by this timer (will be somewhere between
	 * the initial minInterval and maxInterval)
	 */
	public int getCurrentInterval() {
		return currentInterval;
	}
	
	/**
	 * Provide the the last time data was sent by means other than by this timer (eg sending because of buffer size, etc.)
	 * @return
	 */
	public abstract long getLastNonTimerSendTime();
	
	/**
	 * Implement this method to tell the timer how to send the data from your buffer
	 */
	public abstract void sendDataByTimer();
	
	private class SendTimerTask extends TimerTask {
		public void run() {
			if (isOldDataInBuffer()) {
				sendDataByTimer();
				adjustIntervalDown();
			} else {
				adjustIntervalUp();
			}
		}
	}
	
	private boolean isOldDataInBuffer() {
		return (System.currentTimeMillis() > getLastNonTimerSendTime() + currentInterval);
	}
	
	private synchronized void adjustIntervalUp() {
		if (lowVolumeFlushStrategy && adjustCheckCount < maxChecksBeforeAdjust)
			adjustCheckCount++;
		if (currentInterval < maxInterval) {
			if (!lowVolumeFlushStrategy || adjustCheckCount >= maxChecksBeforeAdjust) {
				adjustCheckCount = 0;
				int newInterval = currentInterval + adjustSize;
				currentInterval = newInterval < maxInterval ? newInterval: maxInterval;
				timer.cancel();
				timer = new Timer(name, true);
				timer.schedule(new SendTimerTask(), currentInterval, currentInterval);
				logger.info("Adjusting timer UP for " + this.name + ", New time interval: " + currentInterval);
			}
		}
	}
	
	private synchronized void adjustIntervalDown() {
		if (currentInterval > minInterval) {
			if (lowVolumeFlushStrategy) {
				currentInterval = minInterval;
				adjustCheckCount = 0;
			} else {
				int newInterval = currentInterval - adjustSize;
				currentInterval = newInterval > minInterval ? newInterval: minInterval;
			}
			timer.cancel();
			timer = new Timer(name, true);
			timer.schedule(new SendTimerTask(), currentInterval, currentInterval);
			logger.info("Adjusting timer DOWN for " + this.name + ", New time interval: " + currentInterval);
		}
	}
}
