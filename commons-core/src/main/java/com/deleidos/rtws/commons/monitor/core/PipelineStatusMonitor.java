package com.deleidos.rtws.commons.monitor.core;

import java.io.IOException;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.Iterator;
import java.util.Map;
import java.util.Map.Entry;
import java.util.Properties;
import java.util.Set;

import javax.management.ObjectName;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.config.UserDataProperties;
import com.deleidos.rtws.commons.monitor.pipeline.PipelineStatus;
import com.deleidos.rtws.commons.net.jmx.JmxConnectionFactory;
import com.deleidos.rtws.commons.net.jmx.JmxConnectionFactoryImpl;
import com.deleidos.rtws.commons.net.jmx.JmxMBeanServerConnection;
import com.deleidos.rtws.commons.net.listener.util.UserDataUtil;

public class PipelineStatusMonitor extends ManagedMonitor {

	/** Factory used to connect to the master node's JMX server. */
	private JmxConnectionFactory connectionFactory = new JmxConnectionFactoryImpl();
	
	private String[] pipelineComponentTypes = new String[] {"transport", "jms", "ingest", "datasink"};
	
	private Map<String, PipelineStatus> pipelineComponentStatuses = new HashMap<String, PipelineStatus>();
	
	private Logger logger = Logger.getLogger(PipelineStatusMonitor.class);
	
	/**
	 * Instantiates a new pipeline monitor with an immutable name.
	 *
	 * @param name the immutable name
	 */
	public PipelineStatusMonitor(String name) {
		super(name);
		setStartupPeriod(1000 * 60 * 60);	// 1 hour startup
		setMonitorInterval(1000 * 60 * 10); // 10 minute intervals
		
		// Make sure this monitor starts in ACTIVE state until the system
		// is ready to have it's pipeline monitored
		setPipelineStatus(PipelineStatus.ACTIVE);
	}

	@Override
	public void buildObjectNameKeys(Properties keys) {
		keys.put("type", "PipelineStatusMonitor");
	}
	
	/**
	 * Exposes the status of each component of the pipeline in the format:
	 * 		componentName=status
	 * 
	 * @return An array of pipeline components & status value
	 */
	public String[] getPipelineStatuses() {
		String[] pipelineStatuses = new String[pipelineComponentStatuses.size()];
		
		Iterator<Entry<String, PipelineStatus>> componentStatusesIt = pipelineComponentStatuses.entrySet().iterator();
		int i = 0;
		while(componentStatusesIt.hasNext()) {
			Entry<String, PipelineStatus> componentEntry = componentStatusesIt.next();
			String componentName = componentEntry.getKey();
			String componentStatus = componentEntry.getValue().toString();
			
			String value = String.format("%s=%s", componentName, componentStatus);
			
			pipelineStatuses[i] = value;
			i++;
		}
		
		return pipelineStatuses;
	}

	/* (non-Javadoc)
	 * @see com.deleidos.rtws.commons.management.ManagedBean#buildObjectNameKeys(java.util.Properties)
	 */
	@Override
	protected void monitor() {
		// Check the status of the SystemStatusMonitor
		JmxMBeanServerConnection connection = null;

		try {
			// We have to build the fqdn because Master User Data Properties does not have an FQDN
			String hostName = UserDataUtil.getHostFromFqdn();
			String domain = UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN);
			String fqdn = String.format("%s.%s", hostName, domain);
			
			// Connect to the master node's command listener JMX server
			String portString = System.getProperty("com.deleidos.rtws.management.commandListener.rmiPort", "1098");
			int rmiPort = Integer.parseInt(portString);

			((JmxConnectionFactoryImpl) connectionFactory).setHost(fqdn);
			((JmxConnectionFactoryImpl) connectionFactory).setPort(rmiPort);
			connection = establishJmxConnection(connectionFactory);
			
			if(connection != null) {
				// Get the SystemStatusMonitor
				ObjectName pattern = ObjectName.getInstance("rtws.deleidos.com:type=SystemStatusMonitor");
				Set<ObjectName> beans = connection.queryNames(pattern, null);
				ObjectName[] beanArray = beans.toArray(new ObjectName[beans.size()]);
				ObjectName systemStatusMonitor = beanArray[0];
				
				MonitorStatus systemStatus = MonitorStatus.fromString((String)connection.getAttribute(systemStatusMonitor, "Status"));

				// Set this monitor to be the same state as the system status.
				// If the system is in an OK state, this monitor will run the
				// monitorPipeline() function.  If it is in any other state,
				// monitorPipeline() will not run so the pipeline state will
				// remain unchanged.
				setStatus(systemStatus);
			}
			else {
				// Unable to connect to SystemStatusMonitor
				addError("Unable to connect to SystemStatusMonitor.");
			}
		} catch (Exception e) {
			// Something is wrong, we have an error
			recordExceptionAsError(e);
		} finally {
			try {connection.close();} catch (Exception ignore) { }
		}
	}

	/**
	 * Monitors the pipeline status of each basic process controller monitors
	 * which is a part of the pipeline(i.e. transport, ingest, and datasinks).
	 * 
	 * The pipeline statuses will be aggregated into a single status value to
	 * represent the whole pipeline.  If any component of the pipeline is active,
	 * the whole pipeline is set to be active.
	 * 
	 * In addition to aggregating the system's pipeline status, the monitor
	 * will also maintain a map of each pipeline component's individual
	 * pipeline status.
	 */
	@Override
	protected void monitorPipeline() {
		JmxMBeanServerConnection connection = null;

		boolean pipelineStatusINACTIVE = true;
		try {
			// Connect to the master node's JMX server.
			((JmxConnectionFactoryImpl) connectionFactory).setPort(1099);
			connection = establishJmxConnection(connectionFactory);
			
			if(connection != null) {
				// Check the names of the process group to verify we have monitors for each,
				// since each BasicProcessController controls one process group
				ArrayList<String> pipelineProcessGroupNames = new ArrayList<String>();
				ObjectName pattern = ObjectName.getInstance("rtws.deleidos.com:type=ProcessGroup,*");
				for (ObjectName processGroup : connection.queryNames(pattern, null)) {
					String processGroupName = (String)connection.getAttribute(processGroup, "Name");

					// Only save pipeline components
					for(String pipelineComponent : pipelineComponentTypes) {
						if(processGroupName.contains(pipelineComponent)) {
							pipelineProcessGroupNames.add(processGroupName);
							break;
						}
					}
				}
				
				if(pipelineProcessGroupNames.isEmpty()) {
					addError("No basic process controller monitors found for pipeline components.");
					
					// Mark the pipeline as active
					pipelineStatusINACTIVE = false;
				}
				else {
					// Get the BasicProcessControllers 
					pattern = ObjectName.getInstance("rtws.deleidos.com:type=BasicProcessControllerMonitor,*");
					for (ObjectName monitor : connection.queryNames(pattern, null)) {
						String monitorName = (String)connection.getAttribute(monitor, "Name");
						// Only look at the pipeline components
						if(pipelineProcessGroupNames.contains(monitorName)) {
							PipelineStatus processGroupPipelineStatus =
									PipelineStatus.fromString((String)connection.getAttribute(monitor, "PipelineStatus"));
							
							if(processGroupPipelineStatus == PipelineStatus.ACTIVE) {
								pipelineStatusINACTIVE = false;
							}
							
							pipelineComponentStatuses.put(monitorName, processGroupPipelineStatus);
							logger.info(String.format("Status for monitor %s : %s", monitorName,processGroupPipelineStatus));
							
							// Remove the monitorName from the known process group names to track
							// which monitors we have checked
							pipelineProcessGroupNames.remove(monitorName);
						}
					}
					
					// Make sure we checked all the known process groups
					if(!pipelineProcessGroupNames.isEmpty()) {
						for(String processGroupName : pipelineProcessGroupNames) {
							addError(String.format("No process controller monitor found for %s.", processGroupName));
						}
					}
				}
			}
			else {				
				// Cannot establish a connection
				addError("Unable to connect to basic process controller monitors in the cluster manager.");
				
				// Mark the pipeline as active
				pipelineStatusINACTIVE = false;
			}
		} catch (Exception e) {
			// Something is wrong, we have an error
			recordExceptionAsError(e);

			// Mark the pipeline as active
			pipelineStatusINACTIVE = false;
		} finally {
			try {connection.close();} catch (Exception ignore) { }
		}
		
		if(pipelineStatusINACTIVE) {			
			setPipelineStatus(PipelineStatus.INACTIVE);
		}
		else {
			setPipelineStatus(PipelineStatus.ACTIVE);
		}
	}
	
	private JmxMBeanServerConnection establishJmxConnection(JmxConnectionFactory connectionFactory) {
		int connectionRetryCount = 15;	// Try multiple times to establish a JMX connection
			
		while(connectionRetryCount > 0) {
			try {
				return connectionFactory.getConnection();
			} catch (IOException ioe) {
				connectionRetryCount--;
			}
		}
		
		return null;
	}
	
	/**
	 * Record exception as error.
	 *
	 * @param e the exception
	 */
	private void recordExceptionAsError(Exception e) {
		addError(String.format("Exception caught: %s", e.getMessage()));
	}
	
	/**
	 * Make sure to mark the pipeline as Active if there are errors.
	 */
	@Override
	protected void addError(String errorMessage) {
		setPipelineStatus(PipelineStatus.ACTIVE);
		super.addError("[PipelineStatusMonitor]: " + errorMessage);
	}
}
