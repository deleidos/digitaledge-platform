	@Test
	public void testToString() {
		String s = "";
		
		// Test char to String
		final char c = 'c';
		s = "c";
		assertEquals(Conversion.toString(c), s);
		
		// Test int to String
		final int i = 1234;
		s = "1234";
		assertEquals(Conversion.toString(i), s);
		
		// Test long to String
		final long l = 1234L;
		s = "1234";
		assertEquals(Conversion.toString(l), s);
		
		// Test short to String
		final short si = 1234;
		s = "1234";
		assertEquals(Conversion.toString(si), s);
		
		// Test float to String
		final float f = 1234.56f;
		s = "1234.56";
		assertEquals(Conversion.toString(f), s);
		
		// Test double to String
		final double d = 1234.56d;
		s = "1234.56";
		assertEquals(Conversion.toString(d), s);
		s = "1235";
		assertEquals(Conversion.toString(d,0), s);
		s = "1234.6";
		assertEquals(Conversion.toString(d,1), s);
		s = "1234.56";
		assertEquals(Conversion.toString(d,2), s);
		
		// Test byte to String
		final byte by = 'c';
		s = "99";
		assertEquals(Conversion.toString(by), s);
		
		// Test Double to String
		final Double db = new Double(1234.56);
		s = "1234.56";
		assertEquals(Conversion.toString(db), s);
		
		// Test boolean to String
		final boolean b1 = true;
		s = "true";
		assertEquals(Conversion.toString(b1), s);
		
		// Test boolean to String
		final boolean b2 = false;
		s = "false";
		assertEquals(Conversion.toString(b2), s);
	}
	
