	@Override
	public String getProgress(String reqId) throws ServiceException {
		return statusMap.get(reqId);
	}

	@Override
	public Response getCompletedModel(String reqId) {
		if(statusMap.get(reqId).equals("FINISHED")){
			File model = creatorMap.get(reqId).getCompletedModel();
			InputStream modelStream=null;
			try {
				modelStream = new FileInputStream(model);
				return Response.ok(modelStream).build();
			} catch (FileNotFoundException e) {
				logger.error("Error reading completed model for request id "+reqId+": "+e.getMessage(),e);
				return Response.serverError().build();
			}
			
		}
		return Response.status(404).build();
	}
	
	public JSONObject getCompletedModelJSON(String reqId){
		return creatorMap.get(reqId).getFinishedJSON();
	}
	
	@Override
	public JSONArray getDecisionInformation(String reqId) {
		ModelCreator creator = creatorMap.get(reqId);
		Map<String,ColumnDecision> columnTypeInfo = creator.getDecisionInfo();
		
		Map<Integer,ColumnDecision> orderMap = new HashMap<Integer, ColumnDecision>();
		for(String header : columnTypeInfo.keySet()){
			ColumnDecision colDec = columnTypeInfo.get(header);
			orderMap.put(colDec.getColumnOrdinal(), colDec);
		}
		
		JSONArray outInfo = new JSONArray();
		
		for(Integer ordinal : orderMap.keySet()){
			ColumnDecision colDec = orderMap.get(ordinal);
			outInfo.add(colDec.getJSONInfo());
		}
		return outInfo;
	}
	
	private InputStream readFile(InputStream is) throws IOException {
		File tempDir = new File("/mnt/ingestapi");
		if(!tempDir.exists())
			tempDir.mkdirs();
		File temp = File.createTempFile("localFile", "txt", tempDir);
		FileOutputStream fos = new FileOutputStream(temp);
		byte[] buff = new byte[1024];
		int numRead = is.read(buff);
		while(numRead>=0){
			fos.write(buff,0,numRead);
			numRead = is.read(buff);
		}
		is.close();
		fos.close();
		
		InputStream saved = new FileInputStream(temp);
		new ExpiredFileDelete(temp).start();
		return saved;
	}

