	// ID SIZE DATA HASH
	static class DataPacket {
		
		private final int packetId;
		private final byte[] hash;
		private final String data;
		
		public DataPacket(int packetId, String data) {
			this.packetId = packetId;
			this.data = data != null && data.length() > 0 ? data : null;
			this.hash = this.data != null ? messageDigest.digest(data.getBytes()) : null;
		}
		
		public DataPacket(byte[] bytes) {
			this(bytes, 0, bytes != null ? bytes.length : 0);
		}
		
		public DataPacket(byte[] bytes, int offset, int length) {
			assertNotNull(bytes);
			ByteBuffer byteBuffer = ByteBuffer.wrap(bytes, offset, length);
			showStats("Created", byteBuffer);
			byteBuffer.order(ByteOrder.nativeOrder());
			// ID
			packetId = byteBuffer.getInt();
			showStats("ID", byteBuffer);
			log.debug("Data ID: " + packetId );
			// SIZE
			int size = byteBuffer.getInt();
			showStats("SIZE", byteBuffer);
			log.debug("Data size: " + size );
			if ( size > 0 ) {
				if ( size + HASH_SIZE > byteBuffer.remaining() ) {
					log.error(String.format("Packet data size is out of bounds. Packet length: %d, size %d", length, size));
					size = byteBuffer.remaining() - HASH_SIZE;
				}
				// DATA
				byte[] dataBytes = new byte[size];
				byteBuffer.get(dataBytes);
				showStats("DATA", byteBuffer);
				data = new String(dataBytes);
				// HASH
				hash = new byte[HASH_SIZE];
				byteBuffer.get(hash);
				showStats("HASH", byteBuffer);
			} else {
				// DATA
				this.data = null;
				// HASH
				this.hash = null;
			}
		}
		
		public final byte[] getBytes() {
			// ID SIZE DATA HASH
			int capacity = (Integer.SIZE/Byte.SIZE)*2;
			int dataSize = 0;
			byte[] dataBytes = null;
			if ( data != null ) {
				dataBytes = data.getBytes();
				dataSize = dataBytes.length;
				capacity +=  dataSize + HASH_SIZE;
			}
			ByteBuffer byteBuffer = ByteBuffer.allocate(capacity);
			byteBuffer.order(ByteOrder.nativeOrder());
			showStats("Created", byteBuffer);
			// ID
			byteBuffer.putInt(packetId);
			showStats("ID", byteBuffer);
			// SIZE
			byteBuffer.putInt(dataSize);
			showStats("SIZE", byteBuffer);
			if ( dataSize > 0 ) {
				// DATA
				byteBuffer.put(dataBytes);
				showStats("DATA", byteBuffer);
				// HASH
				byteBuffer.put(hash);
				showStats("HASH", byteBuffer);
			}
			showStats("return", byteBuffer);
			return byteBuffer.array();
		}
		
		public int getPacketId() {
			return packetId;
		}
		
		public String getData() {
			return data;
		}
		
		public byte[] getHash() {
			return hash;
		}
		
		boolean isHashValid() {
			return data != null ? Arrays.equals(hash, messageDigest.digest(data.getBytes())) : hash == null;
		}
		
	    private static void showStats( String where, ByteBuffer b )
        {
	        String msg = where +
				" bufferPosition: " + b.position() +
				" limit: " + b.limit() +
				" remaining: " + b.remaining() +
				" capacity: " + b.capacity();
	        log.debug(msg);
        }
	}
	
	public class ExecuteUDPTransportService implements Runnable {

	    private UDPTransportService transportService;

	    public ExecuteUDPTransportService(UDPTransportService transportService) {
	        this.transportService = transportService;
	    }

	    public void run() {
	    	transportService.execute();
	    }
	}
	
	private static AtomicInteger packetCount = new AtomicInteger(0);
	private static AtomicLong packetBytesCount = new AtomicLong(0);
	private static ConcurrentMap<Integer,Integer> packetIDs = new ConcurrentHashMap<Integer,Integer>();
	
	public static class TestUDPMessageProcessor implements Runnable {

		private DatagramPacket packet;
		private Encoding encoding = Encoding.None;

		public TestUDPMessageProcessor(DatagramPacket packet, AbstractTransportService reciever) {
			this.packet = packet;
			if ( reciever != null && reciever instanceof UDPTransportService )
				encoding = ((UDPTransportService)reciever).getEncoding();
		}

		@Override
		public void run() {
			assertNotNull(packet);
			
			try {
				packetBytesCount.addAndGet(packet.getLength());
				DataPacket dataPacket = new DataPacket(packet.getData(),0,packet.getLength());
				// count the message
				int currentCount = packetCount.incrementAndGet();
				// check that hash is valid
				assertTrue(dataPacket.isHashValid());
				// check that id is unique
				Integer previousCount = packetIDs.put(dataPacket.getPacketId(), currentCount);
				if ( previousCount != null )
					log.error(String.format("Duplicate packet. Packet with ID %d was receied as no. %d and now as no. %d", dataPacket.getPacketId(), currentCount, previousCount));
				assertNull(previousCount);
				log.debug(String.format("Processing packet with ID %d as no. %d", dataPacket.getPacketId(), currentCount));
				// encoding
				String encodedPacketData = null;
				switch(encoding) {
					case Base16:
						encodedPacketData = DatatypeConverter.printHexBinary( Arrays.copyOf(packet.getData(), packet.getLength()) );
						break;
					case Base64:
						encodedPacketData = DatatypeConverter.printBase64Binary( Arrays.copyOf(packet.getData(), packet.getLength()) );
						break;
					default:
						encodedPacketData = new String(packet.getData(),0,packet.getLength());
						break;
				}
				final int leadingCharsCount = 80;
				log.debug(String.format("Encoding %s. Leading %d chars: %s", encoding.toString(),leadingCharsCount, encodedPacketData.substring(0, leadingCharsCount-1)));
			} catch (Exception ex ) {
				log.error("Caught exception while processing a packet", ex );
				assertFalse(true);
			}
		}

	}
	
