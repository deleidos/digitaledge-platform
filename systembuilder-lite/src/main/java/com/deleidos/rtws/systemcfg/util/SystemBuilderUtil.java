	public static String generateDataModelQueueName(String fqn){
		String name = fqn.substring(fqn.lastIndexOf(".") + 1);
		name = name.toLowerCase();
		name = name.replace("datasink", "");

		return String.format("com.deleidos.rtws.%s", name);
	}

	/**
	 * Gets the unique queue name for a datasink using a process group name.
	 * 
	 * @param configList {@link DataSinkConfig} list of DataSinks
	 * @param processGroupName String process group name
	 * @return String queue name
	 */
	public static String getQueueName(List<DataSinkConfig> configList, String processGroupName){

		for(DataSinkConfig config : configList){
			if(config.getProcessGroupDependencies().contains(processGroupName)){
				/**
				 * Handle case where one or more datasinks have a process group depencency list containing the same prefix as another
				 * e.g)
				 * 
				 * external hive datasink   depends on      zookeeper, datasink.hive.external
				 * 
				 * and 
				 * hive datasink 			depends on 		zookeeper, hadoop.namenode, hadoop.jobtracker, hadoop.datanode, hive.metastore, datasink.hive
				 * 
				 * 
				 */
				String [] tokens = config.getProcessGroupDependencies().trim().split(",");				
				for (String pgName : tokens) {
					System.out.println(String.format("{%s} matches? {%s}", processGroupName.trim(), pgName.trim()));
					if (processGroupName.trim().equals(pgName.trim()))
						return generateDataModelQueueName(config.getFqn());
				}			
			}
		}

		if (processGroupName.contains("datasink"))
			throw new IllegalStateException(String.format("Unable to determine data model queue name for process group: {%s}", processGroupName));
		
		return null;
	}

	/**
	 * In a JSON object, this will find a "systemParameters" object. It will
	 * then loop through all objects contained therein, and create an XML
	 * element for each containing its default value.
	 * 
	 */
	public static String createSystemParametersXml(JSONObject json) {
		StringBuffer xml = new StringBuffer("");

		try {
			JSONObject systemParams = json.optJSONObject("systemParameters");
			if (systemParams != null) {
				Iterator<?> nodeit = systemParams.keys();
				while (nodeit.hasNext()) {
					String param = (String) nodeit.next();
					xml.append("\t");
					xml.append(SystemBuilderUtil.createXmlElementFromParameter(param, systemParams.getJSONObject(param)));
					xml.append("\n");
				}
			}
		} catch (JSONException e) {
			e.printStackTrace();
		}

		return xml.toString();
	}

