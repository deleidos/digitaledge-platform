public class RepositorySyncTask extends SyncTask {
	
	private final Logger log = LoggerFactory.getLogger(this.getClass());
	
	private static final int BUFFER_SIZE = 2048;
	
	public RepositorySyncTask(SyncRequest request, String token) {
		super(request, token);
	}
	
	@Override
	public void run() {
		
		try {
			performSync();
			
			SyncManager.getInstance().setStatusMessage(getToken(), "Repository sync complete.", SyncState.Complete);
		} catch (Exception ex) {
			SyncManager.getInstance().setStatusMessage(getToken(), getExceptionMessage(ex), SyncState.Error);
			log.error("Fail to process repository sync task. Message: " + ex.getMessage(), ex);
		}
		
	}
	
	protected void performSync() throws IOException {
		
		RepositorySyncRequest request = (RepositorySyncRequest) getRequest();
		
		SyncManager.getInstance().setStatusMessage(getToken(), "Extracting repository sync request ...", SyncState.Processing);
		
		log.debug("Processing task " + request.toString());
		
		SyncManager.getInstance().setStatusMessage(getToken(), "Retrieving content into temporary directory ...", SyncState.Processing);
		
		String compressedFilePath = retrieveContentIntoTempDir(request.getServiceUrl(), 
				request.getTenantId(), request.getPassword());
		
		String destDir = new File(compressedFilePath).getParent();
		
		if (ZipUtil.unpack(compressedFilePath, destDir)) {
			String decompressedFilePath = compressedFilePath.substring(0, compressedFilePath.lastIndexOf('.'));
			
			SyncManager.getInstance().setStatusMessage(getToken(), "Ingesting files into the repository ...", SyncState.Processing);
				
			ingestFilesIntoRepository(buildFileListToIngest(decompressedFilePath));
			
			SyncManager.getInstance().setStatusMessage(getToken(), "Adding files to cleanup queue ...", SyncState.Processing);
				
			FileCleanupQueue.getInstance().enqueue(compressedFilePath, new Date(System.currentTimeMillis()));
			FileCleanupQueue.getInstance().enqueue(decompressedFilePath, new Date(System.currentTimeMillis()));
			
			log.debug("Added file '" + compressedFilePath + "' to file cleanup queue");
			log.debug("Added file '" + decompressedFilePath + "' to file cleanup queue");
		}
		
	}
		
	
	/**
	 * Takes a map of files (absolute path) and standard paths that exists
	 * in the temp directory and ingest each one into the repository.
	 */
	private void ingestFilesIntoRepository(Map<String, String> fileList) {
		
		String tenantId = UserDataProperties.getInstance().getString(UserDataConstants.RTWS_TENANT_ID);
		String tenantPassword = RtwsConfig.getInstance().getString(RtwsPropertiesConstants.TENANT_PASSWORD);
		ContentManagementDao dao = DaoManager.getContentManagementDao();
		
		for (String key : fileList.keySet()) {
			FileInputStream fis = null;
			
			try {
				String path = fileList.get(key);
				File file = new File(key);
				fis = new FileInputStream(file);
				
				dao.addContent(tenantId, tenantPassword, file.getName(), fis, Visibility.Public.toString().toLowerCase(), path);
			} catch (Exception ex) {
				log.error("Fail to ingest files into repository. Message: " + ex.getMessage(), ex);
			} finally {
				if (fis != null) { 
					try { fis.close(); } catch (IOException e) { }
				}
			}
		}
		
	}
	
	/**
	 * Invoke the tenant manager repository rest service to retrieve all contents into our temp directory. 
	 */
	private String retrieveContentIntoTempDir(String serviceUrl, String userId, String password) {
		
		BufferedInputStream bis = null;
		BufferedOutputStream bos = null;
		
		try {
			Client client = Client.create(JerseyClientConfig.getInstance().getInternalConfig(120000, 900000));
			WebResource resource = client.resource(serviceUrl).queryParam("userId", userId).queryParam("password", password);
			
			ClientResponse response = resource.get(ClientResponse.class);

			if (response.getClientResponseStatus().compareTo(ClientResponse.Status.OK) == 0) {
				
				// The contents that is retrieved is bundled into a zip file. Give
				// the file a unique name to avoid collision.
				
				String tempDirPath = FileUtil.makeTempDir("snapshots");
				String uniqueName = "snapshot-" + Calendar.getInstance().getTime().getTime();
				String snapshotPath = tempDirPath + '/' + uniqueName + ".zip";
				
				// Make the temp directory if it doesn't exist
				
				FileUtil.makeFileParentDir(snapshotPath);
				
				// Take the response stream and write it out to a file
				
				bis = new BufferedInputStream(response.getEntity(InputStream.class));
				bos = new BufferedOutputStream(new FileOutputStream(snapshotPath), BUFFER_SIZE);
				
				int count;
				byte [] data = new byte[BUFFER_SIZE];
				while ((count = bis.read(data, 0, BUFFER_SIZE)) != -1) {
					bos.write(data, 0, count);
				}
				
				bos.flush();
				
			   	return snapshotPath;
			}
		} catch (Exception ex) {
			log.error("Fail to retrieve content into temp directory. Message: " + ex.getMessage(), ex);
		} finally {
			if (bos != null) {
				try { bos.close(); } catch (IOException ioe) { }
			}
			
			if (bis != null) {
				try { bis.close(); } catch (IOException ioe) { }
			}
		}
		
		return null;
		
	}
	
	/**
	 * Given the start path of a directory, build a list of files to ingest.
	 * The list will be a map of file (absolute path) to repo-relative paths. The
	 * path specifies where to ingest file in the repository.  Only those files in a StandardPath
	 * location will be included.
	 */
	private Map<String, String> buildFileListToIngest(String startPath) {
		
		Map<String, String> fileList = new HashMap<String, String>();
		
		File startFile = new File(startPath);
		
		if (startFile.exists()) {
			buildFileListToIngestHelper(startFile, fileList);
		}
		
		return fileList;
		
	}
	
	/**
	 * Helper method for the buildFileListToIngest method.
	 */
	private void buildFileListToIngestHelper(File file, Map<String, String> fileList) {
		
		if (file.isDirectory()) {
			File [] children = file.listFiles();
			for (File child : children) {
				buildFileListToIngestHelper(child, fileList);
			}
		} else {
			String unixStyleParentDirPath = FilenameUtils.separatorsToUnix(file.getParentFile().getAbsolutePath());
			if(StringUtils.isNotBlank(unixStyleParentDirPath)) {
				for (StandardPath path : StandardPath.values()) {
					int currStdPathIndex = unixStyleParentDirPath.indexOf(path.toString());
					if(currStdPathIndex >= 0) {
						fileList.put(file.getAbsolutePath(), unixStyleParentDirPath.substring(currStdPathIndex));
					}
				}
			}
		}
		
	}

}