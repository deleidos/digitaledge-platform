	@Test
	public void testTrickleOfData() throws Exception {
		JMSTransactionalMessageSender sender = null;
		try {
			sender = new JMSTransactionalMessageSender(session, producer);
			sender.start();

			// run this twice just to really check the timer
			for (int i=0; i<2; i++) {
				// send 20 with a sleep call after each
				List<JMSMessageWrapper> messages = createMessages(20);
				for (JMSMessageWrapper msg : messages) {
					sender.send(msg);
					Thread.sleep(1000);
				}
				
				// after 20 secs (20 * 1000) we shouldn't see any messages
				int recievedMsgCount = consumeMessages();
				assertEquals(0, recievedMsgCount);
	
				// wait another 10 seconds and we should see all messages committed by timer
				Thread.sleep(10000);
	
				recievedMsgCount = consumeMessages();
				assertEquals(messages.size(), recievedMsgCount);
			}
		} finally {
			if (sender != null)
				sender.stop();
		}
	}
	
	@Test
	public void testChangeBatchSize() throws Exception {
		JMSTransactionalMessageSender sender = null;
		try {
			sender = new JMSTransactionalMessageSender(session, producer);
			sender.start();

			// send 20, which is less than default batch size of 100
			List<JMSMessageWrapper> messages = createMessages(20);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			// we shouldn't see any yet
			int recievedMsgCount = consumeMessages();
			assertEquals(0, recievedMsgCount);
			
			// drop the batch size to 20 and send 1 more, should trigger commit
			sender.setMaxMessagesPerTransaction(20);
			messages = createMessages(1);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			Thread.sleep(1000);
			
			// now we should get all 21
			recievedMsgCount = consumeMessages();
			assertEquals(21, recievedMsgCount);
			
			// now bump up the batch size and send
			sender.setMaxMessagesPerTransaction(500);
			messages = createMessages(400);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			// we shouldn't see any yet
			recievedMsgCount = consumeMessages();
			assertEquals(0, recievedMsgCount);
			
			messages = createMessages(100);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			Thread.sleep(2000);

			// now we should get all 500
			recievedMsgCount = consumeMessages();
			assertEquals(500, recievedMsgCount);
						
		} finally {
			if (sender != null)
				sender.stop();
		}
	}
	
	@Test
	public void testChangeTimerLength() throws Exception {
		JMSTransactionalMessageSender sender = null;
		try {
			
			sender = new JMSTransactionalMessageSender(session, producer);
			sender.start();

			// send 20, which is less than default batch size of 100
			List<JMSMessageWrapper> messages = createMessages(20);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			// we shouldn't see any yet
			int recievedMsgCount = consumeMessages();
			assertEquals(0, recievedMsgCount);
			
			Thread.sleep(sender.getMaxTimeBeforeCommit() + 2000);
			
			// now we should get all 20
			recievedMsgCount = consumeMessages();
			assertEquals(messages.size(), recievedMsgCount);
			
			// now bump down the timer interval
			sender.setMaxTimeBeforeCommit(10000);
			messages = createMessages(20);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}
			
			// we shouldn't see any yet
			recievedMsgCount = consumeMessages();
			assertEquals(0, recievedMsgCount);
			
			Thread.sleep(sender.getMaxTimeBeforeCommit() + 2000);
			
			// now we should get all 20
			recievedMsgCount = consumeMessages();
			assertEquals(messages.size(), recievedMsgCount);
						
		} finally {
			if (sender != null)
				sender.stop();
		}
	}
	
	@Test
	public void testThreadSafety() throws Exception {
		JMSTransactionalMessageSender sender = null;
		try {
			
			// set the time interval really low and blast messages through the time window
			// make sure we get all messages and no exceptions, locks, etc
			sender = new JMSTransactionalMessageSender(session, producer);
			sender.setMaxTimeBeforeCommit(1000);
			sender.start();

			// send normally through the time iterations
			JMSMessageWrapper msg = createMessages(1).get(0);
			long startTime = System.currentTimeMillis();
			int messagesSent = 0;
			while (System.currentTimeMillis() - startTime < 2500) {
				sender.send(msg);
				messagesSent++;
			}
			
			// sleep through 1 more timer and check that we got all messages
			Thread.sleep(sender.getMaxTimeBeforeCommit() + 2000);
			
			int recievedMsgCount = consumeMessages();
			assertEquals(messagesSent, recievedMsgCount);
			
			
			// send immediately through time iterations
			startTime = System.currentTimeMillis();
			messagesSent = 0;
			while (System.currentTimeMillis() - startTime < 2500) {
				sender.send(msg);
				messagesSent++;
			}
			
			// sleep through 1 more timer and check that we got all messages
			Thread.sleep(sender.getMaxTimeBeforeCommit() + 2000);
			
			recievedMsgCount = consumeMessages();
			assertEquals(messagesSent, recievedMsgCount);
						
		} finally {
			if (sender != null)
				sender.stop();
		}
	}
