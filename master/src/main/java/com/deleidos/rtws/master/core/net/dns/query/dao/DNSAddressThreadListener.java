package com.deleidos.rtws.master.core.net.dns.query.dao;

import java.io.IOException;

import org.apache.commons.lang.StringUtils;
import org.apache.log4j.Logger;

import com.amazonaws.services.ec2.model.Instance;
import com.deleidos.rtws.commons.cloud.beans.Process;
import com.deleidos.rtws.commons.cloud.platform.ServiceInterface;
import com.deleidos.rtws.commons.config.RtwsConfig;
import com.deleidos.rtws.commons.net.dns.rest.DNSRestClient;
import com.deleidos.rtws.commons.util.env.SystemEnvUtil;
import com.deleidos.rtws.master.core.dao.ProcessDao;

/**
 * This class will wait for a started nodes DNS Name or IP Address to change.  When this change happens it will update the Master Node's
 * processes.xml file. If the address does not change in 20 minutes, the thread will give up and terminate.
 * 
 * This class is immutable and therefore thread-safe because its state cannot change.  There also no getters so that the class level variables
 * cannot be modified.  This makes the class thread-safe.
 * 
 */

public class DNSAddressThreadListener implements Runnable {
	/** Default Check Time in case one is not specified */
	private static final long DefaultDelayTime = 60000; // 60 seconds.
	/** Time to wait to see before checking for an update. */
	private final long checkForAddressUpdateDelayTime;
	/** Time for thread to stop waiting for  the address to change */
	private static final long DefaultTerminationTime = 1200000;  // 20 minutes
	/** Time the thread will wait for the address or DNS name to change*/
	private final long terminationTime;
	/** Address which will be updated when it changes */
	private final Process processToWatch;
	protected Logger log = Logger.getLogger(DNSAddressThreadListener.class);
	/** Cloud Service */
	private final ServiceInterface service;
	/** Place to store the updated information */
	private final ProcessDao dao;
	/** The maximum number of times to try and bind the elastic IP to a dns entry*/
	private int maxRetry;
	/** */
	private DNSRestClient dnsRestClient;
	private boolean byPass = false;
	
	public DNSAddressThreadListener(ServiceInterface service, ProcessDao dao, Process processToWatch) {
		this.service = service;
		this.dao = dao;
		this.processToWatch = processToWatch;
		this.checkForAddressUpdateDelayTime = DefaultDelayTime;
		this.terminationTime = DefaultTerminationTime;
		
		String retryCount = RtwsConfig.getInstance().getString("dns.retry.count");
		if(retryCount != null){
			maxRetry = Integer.parseInt(retryCount);
		}
		else{
			maxRetry = 60;  //defaults to retry for 1 hour at intervals of a minute
		}
		
		dnsRestClient = new DNSRestClient();
	}
	
	
	public DNSAddressThreadListener(ServiceInterface service, ProcessDao dao, Process processToWatch, 
			long checkForAddressUpdateDelayTime, long terminationTime) {
		this.service = service;
		this.dao = dao;
		this.processToWatch = processToWatch;
		this.checkForAddressUpdateDelayTime = checkForAddressUpdateDelayTime;
		this.terminationTime = terminationTime;
		
		String retryCount = RtwsConfig.getInstance().getString("dns.retry.count");
		if(retryCount != null){
			maxRetry = Integer.parseInt(retryCount);
		}
		else{
			maxRetry = 60;  //defaults to retry for 1 hour at intervals of a minute
		}
		
		dnsRestClient = new DNSRestClient();
	}
	
	
	public void run()  {
		log.debug("In dns address thread listener");
		// Check address to watch, if it is null, return DNS Address Listener Exception.
		if (processToWatch == null) {
			log.error("Thread terminated because there is no address to watch" );
			return;
		}
		
		log.debug("Process to watch not null");
		
		// Get the Start Time of the thread.
		long startTime = System.currentTimeMillis();
		
		
		String instanceId = processToWatch.getId();
		
		String addressToWatch = null;
		//if not in vpc, or running a NAT
		if(SystemEnvUtil.isVpcEnabled() == false && SystemEnvUtil.isNatConfigured() == false){
			addressToWatch = processToWatch.getPublicDnsName();
		}
		else{
			addressToWatch = processToWatch.getPublicIpAddress();
		}
		
		// If the address has not changed, check to see if termination time has been exceeded.
		// Check the current time with the start time plus the termination time
		while (System.currentTimeMillis() < (startTime + terminationTime)) {
			// Check to see if address has changed, by running Describe Instance
			Instance instance = service.describeInstance(instanceId);
			if (instance != null) {
				log.debug("Instance not null");
				String latestAddress = null;
				//if not in vpc, or running a NAT
				if(SystemEnvUtil.isVpcEnabled() == false && SystemEnvUtil.isNatConfigured() == false){
					latestAddress = instance.getPublicDnsName();
				}
				else{
					latestAddress = instance.getPublicIpAddress();
					//if latest address is empty or loopback then set it to the address to watch force another iteration/check
					if(StringUtils.isBlank(latestAddress) == true || "127.0.0.1".equals(latestAddress) == true){
						latestAddress = addressToWatch;
					}
					else{
						//check to see if IP is already an elasticIP, revisit after 1.1, case for when addressToWatch is the elasticIP,
						//then the elasticIP is compared to latest which is the same value and loop never exists.  So if latestaddress is an
						//elastic IP then byPass check
						byPass = true;
					}
				}
				
				log.debug(String.format("Comparing address %s to %s", addressToWatch, latestAddress));
				if (!(addressToWatch.equals(latestAddress)) || byPass == true) {
					// If the address is updated, then call back to the Master Node to write to processes.xml file.
					updateAndWriteProcess(processToWatch, latestAddress);
					// terminate because address has been updated.
					log.info(String.format("Stopping the thread for [instanceId,publicDns]: [%s,%s]", 
							instance.getInstanceId(), latestAddress));  //added instance id for VPC case
					break;
				} 
				else {
					// Wait for the delay time to pass.
					try {
						Thread.sleep(checkForAddressUpdateDelayTime);
					} catch(InterruptedException ie) {
						// Do nothing, do not mind if the thread is interrupted during sleep.
					}
				}
			} else {
				// Instance is not found, so there is no way to check the address.
				log.error("Address checking error for instance: " + instanceId + ", instance not found.");
				// Stop the thread, it is no longer needed.
				break;
			}
		}
	}
	
	private void updateAndWriteProcess(Process process, String updatedAddress) {
		Process storedProcess =  null;
		//race condition not always going to find id on first try
		for(int i = 0; i < 5; i++){
			storedProcess = dao.findById(process.getId());
			
			if(storedProcess == null){
				// Wait for the delay time to pass.
				try {
					Thread.sleep(2000);
				} catch(InterruptedException ie) {
					// Do nothing, do not mind if the thread is interrupted during sleep.
				}
			}
			else{
				break;
			}
		}
		
		// If stored process is not found, log the error and return.
		if (storedProcess == null) {
			log.error("Cannot update public DNS Address for instance because " + process.getId() + " was not found ");
			return;
		}
		
		// Set the Public Dns Name and store the process.
		storedProcess.setPublicDnsName(updatedAddress);
		// Set the Public Dns Name to incoming process, will be used by the Internet DNS.
		process.setPublicDnsName(updatedAddress);
		
		//if dns not set, using IP then update process, this occurs when in VPC or using nat
		if(SystemEnvUtil.isVpcEnabled() == true || SystemEnvUtil.isNatConfigured() == true){
			storedProcess.setPublicIpAddress(updatedAddress);
			process.setPublicIpAddress(updatedAddress);
		}
		
		try {
			dao.store(storedProcess);
		} catch (IOException ioe) {
			log.error("Unable to store the updated process " + process.getId());
		}
		
		if (process.getAllocateInternetAddress() == true) {
			// Allocate the persistent address
			String ipAddress = process.getPublicIpAddress();
			
			// Check if this is an elastic IP address and update
			if (process.getPersistentIpAddress() != null) {
				ipAddress = process.getPersistentIpAddress();
			}
			
			String dnsEntry = null;
			if(StringUtils.isNotBlank(process.getPersistentDnsName())){
				dnsEntry = process.getPersistentDnsName();
			}
			else if(StringUtils.isNotBlank(process.getInternetAddress())){
				dnsEntry = process.getInternetAddress();
			}
			else{
				log.warn("A public DNS entry name was not provided.");
			}
			
			boolean success = false;
			try{
				int attemptNum = 0;
				
				while(attemptNum < maxRetry && success == false){
					log.info(String.format("Attempt:  %s binding %s to %s", attemptNum, ipAddress, dnsEntry));
					// Wait for the delay time to pass.
					try {
						Thread.sleep(60000);  //sleep for a minute
					} catch(InterruptedException ie) {
						// Do nothing, do not mind if the thread is interrupted during sleep.
					}
					
					success = dnsRestClient.bindRestAddEntry(dnsEntry, ipAddress, true);
					
					attemptNum++;
					
					//logs the last error that occurred every 10 attempts, default 10 minutes
					if(attemptNum % 10 == 0){
						log.error(dnsRestClient.getLastError());
					}
				}
			}
			catch(Exception e){
				log.error(e.toString(), e);
			}
			
			//maximum attempts exceeded
			if(success == false){
				log.error(String.format("Unable to bind IP address %s to fqdn name %s, max number of tries exceeded.", ipAddress, dnsEntry));
				log.error(dnsRestClient.getLastError());
			}
			else{
				log.info(String.format("Success: bound %s to %s", ipAddress, dnsEntry));
			}
			
		}
	}
}
