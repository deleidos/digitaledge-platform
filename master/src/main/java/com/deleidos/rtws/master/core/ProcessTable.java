package com.deleidos.rtws.master.core;

import java.util.Collection;
import java.util.LinkedList;
import java.util.TreeMap;

import com.deleidos.rtws.commons.cloud.beans.Process;

/**
 * Container used to keep track of a list of processes (organized by number).
 */
public class ProcessTable {

	/** The list of processes. */
	private TreeMap<Integer, Slot> table = new TreeMap<Integer, Slot>();

	/**
	 * Constructor.
	 */
	public ProcessTable() {
		super();
	}

	/**
	 * Return the list of currently active processes. Note that if other threads are currently adding or removing
	 * entries from the table, results may be inconsistent. Slots that are pending insertion are not included in the
	 * result, while slots that are pending deletion are.
	 */
	public synchronized Collection<Process> list() {
		LinkedList<Process> buffer = new LinkedList<Process>();
		for (Slot slot : table.values()) {
			Process process = slot.value();
			if (process != null) {
				buffer.add(process);
			}
		}
		return buffer;
	}

	/**
	 * Adds the given processes to this table, inserting into the position indicated by it's process number.
	 */
	public synchronized void insert(Process process) {
		Integer index = process.getNumber();
		if (!table.containsKey(index)) {
			insert(index, new Slot(process));
		} else {
			throw new IllegalArgumentException("Process table slot not empty: " + index);
		}
	}

	protected synchronized void insert(int index, Slot entry) {
		table.put(index, entry);
	}

	/**
	 * Reserves the next available slot in the table, allowing for multiple threads to added entries without contending
	 * for the same resulting process number. Also provides a transactional mechanism for rolling back the insert.
	 */
	public synchronized Reservable reserve() {
		int index = 1;
		while (index <= table.size()) {
			if (table.containsKey(index)) {
				index++;
			} else {
				break;
			}
		}
		Slot placeholder = new Slot(index);
		table.put(index, placeholder);
		notifyAll();
		return placeholder;
	}

	/**
	 * Removes the given process from this table. The process to be removed is identified using the process number from
	 * the give process object.
	 */
	public synchronized Process remove(Process process) {
		return remove(process.getNumber());
	}

	protected synchronized Process remove(int index) {
		Slot result = table.remove(index);
		return (result == null) ? null : result.value();
	}

	/**
	 * Marks the highest numbered slot in the table for removal. This allows multiple threads to remove entries from the
	 * table without attempting to remove the same one. Also provides a transactional mechanism for rolling back the
	 * attempted eviction.
	 */
	public synchronized Evictable evict() {
		while (!table.isEmpty()) {
			for (int index : table.descendingKeySet()) {
				Slot slot = table.get(index);
				if (slot.lock()) { return slot; }
			}
			try {
				wait();
			} catch (InterruptedException e) {
				// Ignore;
			}
		}
		return null;
	}

	/**
	 * Returns the number of processes currently in this table.
	 */
	public synchronized int size() {
		return table.size();
	}

	/**
	 * Wakes up any threads that are waiting for a pending insert to complete, or a pending deletion to rollback.
	 */
	protected synchronized void wake() {
		notifyAll();
	}

	/**
	 * Transaction wrapper for committing/rolling back an insert into a reserved slot in the table.
	 */
	public static interface Reservable {
		public int index();

		public void fill(Process process);

		public void release();
	}

	/**
	 * Transaction wrapper for invoking/undoing a deletion from the table.
	 */
	public static interface Evictable {
		public int index();

		public Process value();

		public Process clear();

		public void release();
	}

	/**
	 * Inner class used to represent a slot in the process table and to manage concurrent transaction against it.
	 */
	public class Slot implements Reservable, Evictable {

		private final int index;
		private Process process;
		private boolean pending;

		/**
		 * Constructor; Reserves a new slot for an insertion.
		 */
		protected Slot(int index) {
			this.index = index;
			this.pending = true;
		}

		/**
		 * Constructor; Lock the given process' slot for eviction.
		 */
		protected Slot(Process process) {
			this.index = process.getNumber();
			this.process = process;
			this.pending = false;
		}

		/**
		 * The index of this slot.
		 */
		public int index() {
			return index;
		}

		/**
		 * The process currently stored in this slot.
		 */
		public Process value() {
			return process;
		}

		/**
		 * Sets a value into this slot.
		 */
		public void fill(Process process) {
			int number = process.getNumber();
			if (!pending) {
				throw new IllegalStateException("Transaction in inconsistent state.");
			} else if (number != index) {
				throw new IllegalArgumentException("Invalid process number '" + number + "'");
			} else {
				this.process = process;
			}
		}

		/**
		 * Clears this slot.
		 */
		public Process clear() {
			if (!pending) {
				throw new IllegalStateException("Transaction in inconsistent state.");
			} else try {
				return process;
			} finally {
				process = null;
			}
		}

		/**
		 * Rolls back the current operation on this slot.
		 */
		public void release() {
			if (process == null) {
				remove(index);
			}
			synchronized (this) {
				pending = false;
			}
			wake();
		}

		/**
		 * Attempts to set this slot's status to pending, marking it for exclusive access; or returns false if it is
		 * already locked by another thread.
		 */
		public synchronized boolean lock() {
			if (!pending) {
				return pending = true;
			} else {
				return false;
			}
		}

	}

}
