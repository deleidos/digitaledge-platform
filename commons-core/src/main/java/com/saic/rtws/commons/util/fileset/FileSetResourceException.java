package com.saic.rtws.commons.util.fileset;

/**
 * Exception that when raised indicates a likely bug or unanticipated condition in the code. 
 */
public class FileSetResourceException extends RuntimeException {
	
	private static final long serialVersionUID = -7221396522128247763L;

	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 */
	public FileSetResourceException(final String message) {
		super(message);
	}
	
	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 * @param cause Exception object
	 */
	public FileSetResourceException(final String message, final Exception cause) {
		super(message, cause);
	}
}
