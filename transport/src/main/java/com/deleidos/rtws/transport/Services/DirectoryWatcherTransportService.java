package com.deleidos.rtws.transport.Services;

import java.io.File;
import java.io.FileInputStream;
import java.io.IOException;
import javax.xml.bind.DatatypeConverter;

import org.apache.commons.io.FileUtils;
import org.apache.commons.io.IOUtils;

import com.deleidos.rtws.commons.util.CompoundMessageGenerator;
import com.deleidos.rtws.commons.util.FilenamePreservingCompoundMessageGenerator;
import com.deleidos.rtws.core.framework.Description;
import com.deleidos.rtws.core.framework.UserConfigured;
import com.deleidos.rtws.splitter.FileSplitter;
import com.deleidos.rtws.transport.AbstractTransportService;
import com.deleidos.rtws.transport.Services.utils.DirectoryWatcher;
import com.deleidos.rtws.transport.model.Encoding;

@Description("A transport service which periodically checks a local or remote file system for files that are ready to transport.")
public class DirectoryWatcherTransportService extends AbstractTransportService {

	private String watchedDirectory;
	private int checkInterval;
	private int fileSendDelay;
	private String remoteServer;
	private String remoteShareName;
	private String preserveFileName = "false";
	
	private DirectoryWatcher watcher;
	private boolean stopped = false;

	private CompoundMessageGenerator<String> compoundMsgGenerator = new FilenamePreservingCompoundMessageGenerator();

	public String getPreserveFileName() {
		return preserveFileName;
	}

	@UserConfigured(value = "false", flexValidator = { "RegExpValidator expression=(true|false)" }, description = "Specifies whether or not the file name is included in the message sent for processing by the ingest pipeline.  This is useful if using the Unstructured File Parser, and you would like to include the source file name in the data model's output.  Note, this only applicable if either Base16 or Base64 encoding is chosen.")
	public void setPreserveFileName(String preserveFileName) {
		this.preserveFileName = preserveFileName;
	}

	final private String ENCODE_CONTENT_DEFAULT = "None";
	final private String ENCODE_CONTENT_REGEX = "^(" + ENCODE_CONTENT_DEFAULT + "|Base16|Base64)$";
	private Encoding contentEncoding = Encoding.None;

	public Encoding getContentEncoding() {
		return contentEncoding;
	}

	@UserConfigured(value = "None", flexValidator = { "RegExpValidator expression=" + ENCODE_CONTENT_REGEX }, description = "Specifies whether the transport should encode the files prior to submitting them for processing. None means that not encoding will be performed. Base16 is the hexadecimal encoding")
	public void setContentEncoding(Encoding contentEncoding) {
		this.contentEncoding = contentEncoding;
	}

	@UserConfigured(description = "The interval in which this transport will poll " + "the directory specified for data to transmit.", value = "500", flexValidator = { "NumberValidator minValue=0 maxValue="
			+ Integer.MAX_VALUE })
	public void setCheckInterval(String val) {
		checkInterval = Integer.parseInt(val);
	}

	public String getCheckInterval() {
		return checkInterval + "";
	}

	@UserConfigured(value = "/usr/local/data", description = "The local directory to poll for data to " + 
			"transmit into the system.  If remoteServer and remoteShareName are defined, this directory will " +
			"be created and used as the local mount point (value must be unique when using multiple remote transports).", flexValidator = { "StringValidator minLength=3 maxLength=63" })
	public void setWatchedDirectory(String val) {
		watchedDirectory = val;
	}

	public String getWatchedDirectory() {
		return watchedDirectory;
	}

	public void setFileSendDelay(String val) {
		fileSendDelay = Integer.parseInt(val);
	}

	public String getFileSendDelay() {
		return fileSendDelay + "";
	}

	@UserConfigured(value = "", description = "The IP address or domain name of the remote NFS server.", flexValidator = { "StringValidator required=false minLength=0 maxLength=253" })
	public void setRemoteServer(String remoteServer) {
		this.remoteServer = remoteServer;
	}

	@UserConfigured(value = "", description = "The name of the shared directory on the remote NFS server. "
			+ "The watched-directory is used as the local mount point to this shared directory.", flexValidator = { "StringValidator required=false minLength=0 maxLength=65" })
	public void setRemoteShareName(String remoteShareName) {
		this.remoteShareName = remoteShareName;
	}

	public void execute() {
		if (!remoteServer.isEmpty() && !remoteShareName.isEmpty()) {
			mountRemoteShare();
		}
		clearNonFatalError();
		log.info("Watching for files in " + watchedDirectory);
	
		watcher = new DirectoryWatcher(watchedDirectory, checkInterval);
		watcher.start();
		
		while (!stopped) {
			File file = watcher.getNextFile();
			if (file != null)
				processFile(file);
		}
	}

	public void terminate() {
		if (watcher != null) {
			watcher.stop();
		}
		if (!remoteServer.isEmpty() && !remoteShareName.isEmpty()) {
			unMountRemoteShare();
		}
		stopped = true;
	}

	private void mountRemoteShare() {
		boolean mounted = false;
		long lastMountAttemptTime = 0;
		long timeBetweenMountAttempts = 30000;
		while (!mounted && !stopped) {
			if (System.currentTimeMillis() - lastMountAttemptTime > timeBetweenMountAttempts) {
				lastMountAttemptTime = System.currentTimeMillis();
				try {
					File mountDir = new File(watchedDirectory);
					if (!mountDir.exists() && !mountDir.mkdirs()) {
						log.error("Failed to make local mount directory " + watchedDirectory + ", aborting remote share mount");
					}
		
					Process process = null;
					String mountCommand = String.format("sudo mount -t nfs %s:%s %s", remoteServer, remoteShareName, watchedDirectory);
					log.info("Mounting remote share with command: " + mountCommand);
		
					process = new ProcessBuilder("/bin/bash", "-c", mountCommand).start();
					int retVal = process.waitFor();
					if (retVal == 0) {
						log.info("Mount command succeeded, now watching on directory " + watchedDirectory);
						mounted = true;
						clearNonFatalError();
					} else {
						log.error("Mount command failed with the following error: " +
								IOUtils.toString(process.getErrorStream(), "UTF-8"));
					}
				} catch (Exception e) {
					log.error("Unexpected Exception while mounting remote server share " + e.toString(), e);
				}
				if (!mounted) {
					log.warn("Will retry mount command in " + timeBetweenMountAttempts/1000 + " seconds");
					setNonFatalErrorMessage("Error mounting remote server share.  Will retry until mount succeeeds.");
				}
			}
			if (!mounted) {
				try { Thread.sleep(1000); } catch (InterruptedException igonore) { }
			}
		}
	}

	private void unMountRemoteShare() {
		try {
			Process process = null;
			String umountCommand = String.format("sudo umount %s", watchedDirectory);
			log.info("Unmounting remote share with command: " + umountCommand);

			process = new ProcessBuilder("/bin/bash", "-c", umountCommand).start();
			int retVal = process.waitFor();
			if (retVal != 0) {
				log.error("The umount command failed with the following error: " +
						IOUtils.toString(process.getErrorStream(), "UTF-8"));
			}
		} catch (Exception e) {
			log.error("Unexpected Exception while umounting remote server share", e);
		}
	}
	
	private void processFile(File file) {
		FileInputStream stream = null;
		try {
			if (file.canRead()) {
				WakeableSleepHelper sleepHelper = new WakeableSleepHelper(fileSendDelay);
				while (sleepHelper.continueSleeping(stopped)); // breaks when time to wake up
				log.info("Processing file: " + file.getAbsolutePath());

				switch (contentEncoding) {
				case None:
					SetFileNameProperty(file.getName());
					int numRecords = 0;
					stream = new FileInputStream(file);
					getSplitter().setInputStream(stream);
					setHeader(getSplitter().parseHeaders());

					String record = getSplitter().split();

					while (record != null && !stopped) {
						SendJMSMessage(record);
						record = getSplitter().split();
						numRecords++;
					}

					stream.close();
					stream = null;
					Reset();
					
					// Note: Currently doing 1.2 release testing, so not going to make an 
					// extensive change to the splitter framework. If we want to fix this 
					// cast class issue we would put the getApproximateNumBlankLines() in 
					// an interface so each splitter would have it. Fix this in 1.2.1+ release.
					if (getSplitter() != null && getSplitter() instanceof FileSplitter) {
						log.info(String.format("Finished processing file: %s  number of records submitted: %s  approximate number of blank lines: %s", file.getAbsolutePath(), numRecords,((FileSplitter)getSplitter()).getApproximateNumBlankLines()));
					} else {
						log.info(String.format("Finished processing file: %s  number of records submitted: %s", file.getAbsolutePath(), numRecords));
					}
					break;
				case Base16:
					if (Boolean.valueOf(preserveFileName)) {
						SendJMSRecord(compoundMsgGenerator.generate(file.getName().getBytes("UTF-8"), FileUtils.readFileToByteArray(file),contentEncoding.toString()));
					} else {
						SendJMSRecord(DatatypeConverter.printHexBinary(FileUtils.readFileToByteArray(file)));
					}
					break;
				case Base64:
					if (Boolean.valueOf(preserveFileName)) {
						SendJMSRecord(compoundMsgGenerator.generate(file.getName().getBytes("UTF-8"), FileUtils.readFileToByteArray(file),contentEncoding.toString()));
					} else {
						SendJMSRecord(DatatypeConverter.printBase64Binary(FileUtils.readFileToByteArray(file)));
					}
					break;
				default:
					log.warn("Invalid value for contentEncoding.  Doing nothing...");
					break;
				}
			} else {
				log.debug("File " + file.getAbsolutePath() + " can not be read by current user, will be skipped.");
			}
		} catch (Exception e) {
			log.error("Error enquing file " + file.getAbsolutePath(), e);
		} finally {
			try {
				if (stream != null)
					stream.close();
			} catch (IOException ignore) {
			}
		}
	}
}