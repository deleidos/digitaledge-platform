package com.deleidos.rtws.webapp.repository.dao;

import java.util.ArrayList;
import java.util.List;

import javax.jcr.LoginException;
import javax.jcr.Node;
import javax.jcr.Repository;
import javax.jcr.RepositoryException;
import javax.jcr.security.Privilege;

import org.apache.jackrabbit.api.security.user.User;
import org.apache.jackrabbit.core.security.principal.EveryonePrincipal;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import com.deleidos.rtws.commons.util.repository.JcrSystemRepository;
import com.deleidos.rtws.commons.util.repository.RepositoryCredentials;
import com.deleidos.rtws.commons.util.repository.StandardPath;
import com.deleidos.rtws.commons.util.repository.AbstractSystemRepository.Visibility;
import com.deleidos.rtws.webapp.repository.exception.RollbackException;
import com.deleidos.rtws.webapp.repository.exception.TenantMangementException;
import com.deleidos.rtws.webapp.repository.model.TenantPrivatePaths;

/**
 * DAO implementation used to perform tenant management operations.
 */
public class TenantManagementDao {
	
	/** The logger */
	private static final Logger log = LoggerFactory.getLogger(TenantManagementDao.class);

	/** The jcr repository. */
	private Repository repository;
	
	/**
	 * Constructor.
	 */
	public TenantManagementDao() {
		super();
	}
	
	/**
	 * Set the jcr repository.
	 */
	public void setRepository(Repository repository) {
		this.repository = repository;
	}
	
	/**
	 * Determine if a tenant already exist in the repository. 
	 */
	public boolean tenantExist(String userId, String password, String tenantName) {
		
		RepositoryCredentials credentials = new RepositoryCredentials(userId, password);
		JcrSystemRepository jcr = new JcrSystemRepository();
		jcr.setRepository(repository);
		jcr.setCredentials(credentials);
		
		if (jcr.findUser(tenantName) != null) {
			return true;
		}
		
		return false;
		
	}
	
	/**
	 * Create a new tenant in the jcr repository.
	 */
	public void createTenant(String userId, String password, String tenantName, String tenantPassword) throws Exception {
		
		RepositoryCredentials credentials = new RepositoryCredentials(userId, password);
		JcrSystemRepository jcr = new JcrSystemRepository();
		jcr.setRepository(repository);
		jcr.setCredentials(credentials);
		
		boolean rollback = false;
		
		try {
			// Check to see if the tenant user already exists.
			
			if (tenantExist(userId, password, tenantName)) {
				throw new TenantMangementException("Tenant '" + tenantName + "' already exist");
			}
			
			rollback = true;
			
			// Create the tenant user

			User user = jcr.createUser(tenantName, tenantPassword);

			log.info("Created tenant user '" + user.getID() + "'");
			
			// Create the tenant's public and private area.
		
			jcr.createUserStructures(credentials, tenantName);
		
			// Create the ACL for the tenant's public and private area. The tenant gets ALL privileges
			// to its public and private area while everyone else gets READ privilege to the tenant's
			// public area.
		
			String [] privileges = { Privilege.JCR_ALL };
		
			List<JcrSystemRepository.ACLEntry> privateAclEntries = new ArrayList<JcrSystemRepository.ACLEntry>();
			privateAclEntries.add(new JcrSystemRepository.ACLEntry(EveryonePrincipal.getInstance(), privileges, false));
			privateAclEntries.add(new JcrSystemRepository.ACLEntry(user.getPrincipal(), privileges, true));
			
			// By default all user gets READ privilege thus we don't need to set it here.
			
			List<JcrSystemRepository.ACLEntry> publicAclEntries = new ArrayList<JcrSystemRepository.ACLEntry>();
			publicAclEntries.add(new JcrSystemRepository.ACLEntry(user.getPrincipal(), privileges, true));

			// Time to apply the privileges.
			
			Node [] nodes = jcr.getUsersNodes(tenantName);
		
			for (Node node : nodes) {
				if (node.getPath().contains(Visibility.Private.toString().toLowerCase())) {
					jcr.setAccessControlPolicy(node, privateAclEntries);
				
					log.info("Private privileges set on node " + node.getPath() + " for user '" + user.getID() + "'");
				}
			
				if (node.getPath().contains(Visibility.Public.toString().toLowerCase())) {
					jcr.setAccessControlPolicy(node, publicAclEntries);
				
					log.info("Public privileges set on node " + node.getPath() + " for user '" + user.getID() + "'");
				}
			}
		
			// Create the tenant's initial paths in the repository.
		
			jcr.setCredentials(new RepositoryCredentials(tenantName, tenantPassword));
		
			for (StandardPath path : StandardPath.values()) {
				jcr.createPath(Visibility.Private, path.name());
				jcr.createPath(Visibility.Public, path.name());
			
				log.info("Created path " + path + " for user '" + user.getID() + "'");
			}
			
			for ( TenantPrivatePaths privatePath : TenantPrivatePaths.values()){
				String pathToCreate = String.format("services/%s", privatePath.name().toLowerCase());
				jcr.createPath(Visibility.Private, pathToCreate);
				log.info("Created private path " + pathToCreate + " for user '" + user.getID() + "'");
			}
		} catch(Exception ex) {
			if (rollback) {
				throw new RollbackException("ERROR: Fail to create tenant.", ex);
			}
			
			throw ex;
		}
		
	}
	
	/**
	 * Removes a tenant from the jcr repository.
	 */
	public void deleteTenant(String userId, String password, String tenantName) throws LoginException, RepositoryException, TenantMangementException {
		
		RepositoryCredentials credentials = new RepositoryCredentials(userId, password);
		JcrSystemRepository jcr = new JcrSystemRepository();
		jcr.setRepository(repository);

		// Delete the tenant areas in the repository

		jcr.setCredentials(credentials);
		jcr.deleteUserStructures(credentials, tenantName);
		
		// Delete the tenant from user management

		jcr.setCredentials(credentials);
		jcr.deleteUser(tenantName);
		
	}
	
	/**
	 * Change the password of a tenant user.
	 */
	public void changePassword(String userId, String password, String tenantId, String tenantPassword) {
		
		RepositoryCredentials credentials = new RepositoryCredentials(userId, password);
		JcrSystemRepository jcr = new JcrSystemRepository();
		jcr.setRepository(repository);
		jcr.setCredentials(credentials);
		
		jcr.changeUserPassword(tenantId, tenantPassword);
		
	}
	
	/**
	 * Return a list of tenants in the repository.
	 */
	public List<String> listAllTenants(String userId, String password) throws TenantMangementException {
		
		List<String> tenants = null;
		
		try {
			RepositoryCredentials credentials = new RepositoryCredentials(userId, password);
		
			JcrSystemRepository jcr = new JcrSystemRepository();
			jcr.setRepository(repository);
			jcr.setCredentials(credentials);
		
			tenants = jcr.retrieveAllUserIds();
		}
		catch (Exception ex) {
			throw new TenantMangementException("ERROR: Fail to list all tenants.", ex);
		}
		
		return tenants;
		
	}
	
}