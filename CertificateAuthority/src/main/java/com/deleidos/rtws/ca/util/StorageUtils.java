	public static InputStream retrieveKeystore() throws StorageUtilsException, IOException {
		synchronized(CommonResource.getInstance().getCertFile()){
			try{
				File keyStore = File.createTempFile("tmpKeystore", "jks");
				keyStore.deleteOnExit();
				CommonResource.getInstance().createTemporaryPublicKeypair();
				CertificateConversion.convertPEMToDERProvidePasswd(new File(CAProperties.getTmpPublicCert()),CAProperties.getTmpPublicKey());
				KeystoreUtil.addKeyAndCertToKeyStore(CAProperties.getRootCAPassword(), CAProperties.getTenantAlias(), 
											keyStore.getAbsolutePath(), CAProperties.getTmpKeyDer(), CAProperties.getTmpCertDer(), true);
				
				return new FileInputStream(keyStore);
			}finally{
				CommonResource.getInstance().cleanupTemporaryKeypair();
			}
		}
	}
	
	public static List<String> listFiles() throws StorageUtilsException {
		
		List<String> files = new ArrayList<String>();
		String cmd = String.format("/usr/bin/sudo %s %s %s", CERT_STORAGE_SCRIPT, Action.list.toString(), CAProperties.getPublicPkiDir());
		
		logger.debug(cmd);
		
		BufferedReader br = null;
		
		try {
			br = new BufferedReader(new InputStreamReader(forkStream(cmd)));
			String line = null;
		
			while((line = br.readLine()) != null){
				files.add(line);
			}
		} catch (Exception ex) {
			logger.debug("Error listing files.", ex);
			throw new StorageUtilsException("Error listing files.", ex);
		} finally {
			if (br != null) {
				 try { br.close(); } catch (Exception ignore) { }
			}
		}
		
		return files;
		
	}
	
