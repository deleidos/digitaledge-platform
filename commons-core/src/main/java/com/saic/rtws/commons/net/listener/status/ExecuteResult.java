package com.saic.rtws.commons.net.listener.status;

public class ExecuteResult {
	
	private String command;
	private ExecuteState state;
	private String message;
	
	public ExecuteResult(String command, ExecuteState state) {
		this.command = command;
		this.state = state;
	}
	
	public ExecuteResult(String command, ExecuteState state, String message) {
		this.command = command;
		this.state = state;
		this.message = message;
	}
	
	public String getCommand() {
		return this.command;
	}
	
	public String getMessage() {
		return this.message;
	}
	
	public ExecuteState getState() {
		return this.state;
	}
	
}