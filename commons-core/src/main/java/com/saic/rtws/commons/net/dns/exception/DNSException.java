package com.saic.rtws.commons.net.dns.exception;

public class DNSException extends Exception {
	/** Serial Version UID. */
	private static final long serialVersionUID = 2474415L;
	

	/** 
	 * Constructor. 
	 */
	public DNSException() {
		super();
	}
	
	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 */
	public DNSException(String message) {
		super(message);
	}
	
	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 * @param cause Exception object
	 */
	public DNSException(String message, Throwable cause) {
		super(message, cause);
	}
	
	/** 
	 * Constructor. 
	 * @param cause Exception object
	 */
	public DNSException(Throwable cause) {
		super(cause);
	}
}
