	@Override
	public StandardResponse<?> create(String accountId, String domain, String cfgVersion,
			String swVersion, String accountEmail, String imageId, String keypairName,
			String instanceType, String securityGroup, String mountDevice, String iaasServiceName,
			String aZone, String subnetId) {

		try {
			ApiAuthUtils.assertTmsOrTenantFromUserData();
			AccountSystemDao dao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
			AccountSystem system = dao.getAccountSystem(domain);

			Node node = RestClientManager.getSystemServiceClient().getNodeWithoutVolumes(
					system.getMasterInstanceId());

			if (node == null || node.getState() == State.Terminated) {
				String manifestFile = MANIFEST_DIR + domain + "/master.ini";
				String storageEndpoint = EndpointUtil.getStorageEndpoint();
				String gatewayIpAddress = subnetId != null ? RestClientManager
						.getServiceInterface().self().getPublicIpAddress() : RestClientManager
						.getServiceInterface().self().getPrivateIpAddress();
				String bucketName = UserDataBuilder.createBucketName(Integer.parseInt(accountId));
				String masterSetupKey = RetrieveHashUtil.getHash(String.format("%s%s%s", domain,
						accountEmail, imageId));

				UserDataBuilder masterUserData = new UserDataBuilder();
				masterUserData.setAccessKey(UserDataUtil.getAccessKey())
						.setSecretKey(UserDataUtil.getSecretKey()).setMountDevice(mountDevice)
						.setMountMode("s3cmd").setManifestFile(manifestFile)
						.setSwVersion(swVersion).setDnsAddress(DNS_ADDRESS)
						.setTenantId(UserDataUtil.getTenantId()).setDomain(domain)
						.setDomainSuffix(UserDataUtil.getDomainSuffix()).setBucketName(bucketName)
						.setStorageEndpoint(storageEndpoint).setGatewayIpAddress(gatewayIpAddress)
						.setSetupKey(masterSetupKey);

				CreateSystemRequest cRequest = new CreateSystemRequest();
				cRequest.setImageId(imageId);
				cRequest.setInstanceType(instanceType);
				cRequest.setAzone(aZone);
				cRequest.setSecurity(securityGroup);
				cRequest.setKeyPair(keypairName);
				cRequest.setCfgVersion(cfgVersion);
				cRequest.setSWVersion(swVersion);
				cRequest.setDomain(domain);
				cRequest.setSubnetId(subnetId);
				cRequest.setIaasServiceName(iaasServiceName);
				cRequest.setUserData(masterUserData);

				CreateNatRequest nRequest = null;
				if (subnetId != null) {
					nRequest = new CreateNatRequest();
					nRequest.setImageId(imageId);
					nRequest.setDomain(domain);
					nRequest.setAzone(aZone);
					nRequest.setKeyPair(keypairName);
					nRequest.setNextRequest(cRequest);

					String natSetupKey = RetrieveHashUtil.getHash(String.format("%s%s%s", domain,
							accountEmail, imageId));

					UserDataBuilder natUserData = new UserDataBuilder();
					natUserData.setAccessKey(UserDataUtil.getAccessKey())
							.setSecretKey(UserDataUtil.getSecretKey()).setMountDevice(mountDevice)
							.setMountMode("s3cmd")
							.setManifestFile(MANIFEST_DIR + domain + "/nat.ini")
							.setSwVersion(swVersion).setDnsAddress(DNS_ADDRESS)
							.setTenantId(UserDataUtil.getTenantId()).setDomain(domain)
							.setDomainSuffix(UserDataUtil.getDomainSuffix())
							.setBucketName(bucketName).setStorageEndpoint(storageEndpoint)
							.setGatewayIpAddress(gatewayIpAddress).setSetupKey(natSetupKey)
							.setIsNat(Boolean.TRUE);

					nRequest.setUserData(natUserData);
				}

				AbstractSystemRequest request = (nRequest != null) ? nRequest : cRequest;

				if (QueueManager.getSystemRequestQueue().addRequest(request)) {
					Cluster cluster = ClusterCache.getInstance().getCluster(domain);
					cluster.setStatus(ClusterStatus.createStartingStatus());

					ClusterCache.getInstance().clearErrorReport(domain);

					String tenantName = UserDataProperties.getInstance().getString(
							UserDataProperties.RTWS_TENANT_ID);
					BusinessMetricsDao.addEvent(domain, tenantName, SystemEventMessage.CREATED,
							System.currentTimeMillis());

					PropertiesResponse response = new PropertiesResponse();
					response.setStandardHeaderCode(200);
					response.setProperty("Status", "true");
					return response;
				}

				throw new SystemServiceException("Create system '" + domain + "' is in progress.");
			}

			throw new SystemServiceException("System '" + domain + "' already exists.");
		} catch (Exception ex) {
			logger.error("create - Exception: " + ex.getMessage(), ex);

			ErrorResponse response = new ErrorResponse();
			response.setStandardHeaderCode(500);
			response.setMessage(ex.getMessage());

			return response;
		}
	}

	@Override
	public StandardResponse<?> start(String accountId, String domain, String cfgVersion,
			String swVersion, String keypairName, String aZone, String subnetId) {

		try {
			ApiAuthUtils.assertTmsOrTenantFromUserData();
			AccountSystemDao dao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
			AccountSystem system = dao.getAccountSystem(domain);

			Node node = RestClientManager.getSystemServiceClient().getNodeWithoutVolumes(
					system.getMasterInstanceId());

			if (State.Stopped == node.getState()) {
				StartSystemRequest sRequest = new StartSystemRequest();
				sRequest.setDomain(domain);
				sRequest.setSWVersion(swVersion);
				sRequest.setCfgVersion(cfgVersion);
				sRequest.setSubnetId(subnetId);

				CreateNatRequest nRequest = null;
				if (subnetId != null) {
					nRequest = new CreateNatRequest();
					nRequest.setDomain(domain);
					nRequest.setAzone(aZone);
					nRequest.setKeyPair(keypairName);

					SystemSubnetMappingDao subnetDao = new SystemSubnetMappingDao(
							EmbeddedH2DataAccessUtility.getInstance());
					SystemSubnetMapping mapping = subnetDao.get(domain);

					String natSetupKey = RetrieveHashUtil.getHash(String.format("%s%s%s", domain,
							swVersion, subnetId));

					String userData = mapping.getNatUserData();
					userData = UserDataBuilder.modifySWVersion(userData, swVersion);
					userData = UserDataBuilder.modifySetupKey(userData, natSetupKey);

					UserDataBuilder natUserData = new UserDataBuilder(userData);

					nRequest.setUserData(natUserData);
					nRequest.setNextRequest(sRequest);
				}

				AbstractSystemRequest request = (nRequest != null) ? nRequest : sRequest;

				if (QueueManager.getSystemRequestQueue().addRequest(request)) {
					Cluster cluster = ClusterCache.getInstance().getCluster(domain);
					cluster.setStatus(ClusterStatus.createStartingStatus());

					ClusterCache.getInstance().clearErrorReport(domain);

					PropertiesResponse response = new PropertiesResponse();
					response.setStandardHeaderCode(200);
					response.setProperty("Status", "true");
					return response;
				}

				throw new SystemServiceException("Start system '" + domain + "' is in progress.");
			}

			throw new SystemServiceException("System '" + domain
					+ "' master instance is not in a STOPPED state.");
		} catch (Exception ex) {
			logger.error("start - Exception: " + ex.getMessage(), ex);

			ErrorResponse response = new ErrorResponse();
			response.setStandardHeaderCode(500);
			response.setMessage(ex.getMessage());

			return response;
		}

	}

	@Override
	public StandardResponse<?> updateConfig(String domain, String cfgVersion) {

		try {
			ApiAuthUtils.assertTmsOrTenantFromUserData();
			AccountSystemDao dao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
			AccountSystem system = dao.getAccountSystem(domain);

			Node node = RestClientManager.getSystemServiceClient().getNodeWithoutVolumes(
					system.getMasterInstanceId());

			if (State.Running == node.getState()) {
				UpdateSystemConfigRequest updateSysCfgRequest = new UpdateSystemConfigRequest();
				updateSysCfgRequest.setDomain(domain);
				updateSysCfgRequest.setCfgVersion(cfgVersion);

				SyncRequest syncRequest = new SyncRequest();
				syncRequest.setDomain(domain);
				syncRequest.setNextRequest(updateSysCfgRequest);

				if (QueueManager.getSystemRequestQueue().addRequest(syncRequest)) {
					Cluster cluster = ClusterCache.getInstance().getCluster(domain);
					cluster.setStatus(ClusterStatus.createUpdatingStatus());

					ClusterCache.getInstance().clearErrorReport(domain);

					PropertiesResponse response = new PropertiesResponse();
					response.setStandardHeaderCode(200);
					response.setProperty("Status", "true");
					return response;
				}

				throw new SystemServiceException("Update system '" + domain
						+ "' configuration is in progress.");
			}

			throw new SystemServiceException("System '" + domain
					+ "' master instance is not in a RUNNING state.");
		} catch (Exception ex) {
			logger.error("updateIngest - Exception: " + ex.getMessage(), ex);

			ErrorResponse response = new ErrorResponse();
			response.setStandardHeaderCode(500);
			response.setMessage(ex.getMessage());

			return response;
		}
	}

