package com.deleidos.rtws.master.core.dao;

import java.io.File;
import java.io.FileInputStream;
import java.io.FileNotFoundException;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.util.Arrays;
import java.util.Collection;
import java.util.HashMap;
import java.util.HashSet;
import java.util.LinkedList;
import java.util.Map;

import javax.xml.bind.JAXBContext;
import javax.xml.bind.JAXBElement;
import javax.xml.bind.JAXBException;
import javax.xml.bind.Marshaller;
import javax.xml.bind.Unmarshaller;
import javax.xml.namespace.QName;
import javax.xml.transform.stream.StreamSource;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.cloud.beans.Process;
import com.deleidos.rtws.commons.cloud.platform.StorageInterface;
import com.deleidos.rtws.commons.util.env.SystemEnvUtil;
import com.deleidos.rtws.commons.util.file.TransactedFile;

/**
 * Data access object used to store and retrieve process meta-data.
 */
public class FileProcessDao implements ProcessDao {

	/** The default bean classes that can be persisted by this DAO. */
	private static final Class<?>[] DEFAULT_BEANS = { Process.class, Process[].class };

	/** A full in memory cache of the objects maintained by this DAO. */
	private Map<String, Process> cache = new HashMap<String, Process>();

	/** The bean data types that this DAO is capable of persisting. */
	private Class<?>[] beanClasses = DEFAULT_BEANS;

	/** The file location where data is stored. */
	private TransactedFile file;
	
	/** The service that will be used to make a shadow copy of the file. */
	private StorageInterface storage;
	
	private Logger log = Logger.getLogger(getClass());
	
	/**
	 * Constructor.
	 */
	protected FileProcessDao(File file, StorageInterface storage, Class<?>... classes) {
		this.file = createTransationWrapper(file);
		this.storage = storage;
		setBeanClasses(classes);
	}

	/**
	 * Sets a list of classes objects that are used to represent process meta-data. By default, the Process class is
	 * assumed; however, if the virtualization service needs to save additional meta-data beyond the default fields, it
	 * should do so by extending Process. In that case, this DAO need to know the class object being used in order to
	 * dynamically store the additional meta-data.
	 */
	private void setBeanClasses(Class<?>... list) {
		HashSet<Class<?>> classes = new HashSet<Class<?>>();
		classes.addAll(Arrays.asList(DEFAULT_BEANS));
		classes.addAll(Arrays.asList(list));
		beanClasses = classes.toArray(new Class<?>[classes.size()]);
	}

	/**
	 * Determines whether the given process is already in persistent storage.
	 */
	public boolean contains(Process process) {
		String ip = process.getPersistentIpAddress();
		if(ip != null && findByIp(ip) != null) { 
			return true;
		}
		String dns = process.getPersistentDnsName();
		if(dns != null && findByDns(dns) != null) {
			return true;
		}
		return findById(process.getId()) != null;
	}
	
	/**
	 * Retrieves the process with the given id.
	 */
	public synchronized Process findById(String id) {
		Process result = cache.get(id);
		return (result == null) ? null : result.clone();
	}

	/**
	 * Retrieves the process with the given "persistent" IP address.
	 */
	public synchronized Process findByIp(String ip) {
		for (Process process : cache.values()) {
			String candidate = process.getPersistentIpAddress();
			if (candidate != null && candidate.equals(ip)) {
				return (process == null) ? null : process.clone();
			}
		}
		return null;
	}

	/**
	 * Retrieves the process with the given "persistent" FQDN.
	 */
	public synchronized Process findByDns(String name) {
		for (Process process : cache.values()) {
			String candidate = process.getPersistentDnsName();
			if (candidate != null && candidate.equals(name)) {
				return (process == null) ? null : process.clone();
			}
		}
		return null;
	}

	/**
	 * Retrieves the processes with the given group name.
	 */
	public synchronized Collection<Process> findByGroup(String group) {
		LinkedList<Process> list = new LinkedList<Process>();
		for (Process process : cache.values()) {
			String candidate = process.getGroup();
			if (candidate != null && candidate.equals(group)) {
				list.add(process.clone());
			}
		}
		return list;
	}

	/**
	 * Retrieves all stored processes.
	 */
	public synchronized Collection<Process> findAll() {
		Collection<Process> result = new LinkedList<Process>();
		for(Process process : cache.values()) {
			result.add(process.clone());
		}
		return result;
	}
	
	/**
	 * Finds all entries in the cache that are share any identifying name with the given process.  
	 */
	private synchronized Collection<Process> findDuplicates(Process bean) {
		
		String id = bean.getId();
		String ip = bean.getPersistentIpAddress();
		String dns = bean.getPersistentDnsName();
		
		Collection<Process> result = new LinkedList<Process>();
		for(Process process : cache.values()) {
			if(id != null && id.equals(process.getId())) {
				result.add(process);
			} else if(ip != null && ip.equals(process.getPersistentIpAddress())) {
				result.add(process);
			} else if(dns != null && dns.equals(process.getPersistentDnsName())) {
				result.add(process);
			}
		}
		
		return result;
		
	}
	
	/**
	 * Loads the persisted process meta-data from the configured storage file.
	 */
	public synchronized void open() throws IOException {

		InputStream stream = null;

		try {

			// Configure an XML marshaller for the bean classes being stored.
			JAXBContext context = createXmlContext();
			Unmarshaller marshaller = context.createUnmarshaller();

			// Create a stream to read the configured storage file.
			stream = createStreamToLoadCache();
			StreamSource source = new StreamSource(stream);

			// Load the data and replace the in memory cache.
			index(marshaller.unmarshal(source, Process[].class).getValue());

		} catch (JAXBException e) {
			throw new IOException(e);

		} catch (FileNotFoundException e) {
			// Ignore; assume there no data to load.
			cache.clear();
			
		} finally {
			try {stream.close();} catch (Exception ignore) { }
		}

	}

	/**
	 * Adds the given Process object to the set of persisted processes or updates it if it is already there.
	 */
	public synchronized void store(Process bean) throws IOException {
		Collection<Process> duplicates = findDuplicates(bean);
		if(duplicates.size() > 1) {
			throw new IOException("Too many matching process '" + bean.getId() + "', unable to determine which to update.");
		} else {
			for(Process duplicate : duplicates) {
				cache.remove(duplicate.getId());
			}
			cache.put(bean.getId(), bean.clone());
			try {
				store();
			} catch (IOException e) {
				cache.remove(bean.getId());
				for(Process duplicate : duplicates) {
					cache.put(duplicate.getId(), duplicate);
				}
				throw e;
			} catch (RuntimeException e) {
				cache.remove(bean.getId());
				for(Process duplicate : duplicates) {
					cache.put(duplicate.getId(), duplicate);
				}
				throw e;
			}
		}
	}

	/**
	 * Removes the given Process object from the set of persisted processes.
	 */
	public synchronized void remove(Process bean) throws IOException {
		Collection<Process> duplicates = findDuplicates(bean);
		for(Process duplicate : duplicates) {
			cache.remove(duplicate.getId());
		}
		try {
			store();
		} catch (IOException e) {
			for(Process duplicate : duplicates) {
				cache.put(duplicate.getId(), duplicate);
			}
			throw e;
		} catch (RuntimeException e) {
			for(Process duplicate : duplicates) {
				cache.put(duplicate.getId(), duplicate);
			}
			throw e;
		}
	}

	/**
	 * Cleans up any resources held by this DAO.
	 */
	public synchronized void close() throws IOException {
		store();
	}

	/**
	 * Replaces the current in memory cache with the give list of processes.
	 */
	private void index(Process[] list) {
		cache.clear();
		for (Process process : list) {
			cache.put(process.getId(), process);
		}
	}

	/**
	 * Saves the current in memory cache to disk.
	 */
	private void store() throws IOException {

		OutputStream stream = null;
		boolean success = false;

		try {

			// Lock the file and prepare it for a write transaction.
			file.lock();
			file.init(false);
			
			// Configure an XML marshaller for the bean classes being stored.
			JAXBContext context = createXmlContext();
			Marshaller marshaller = context.createMarshaller();

			// Create a stream that overwrites the configured storage file.
			stream = createStreamToStoreCache();

			// Build the XML wrapper for the list of processes.
			Process[] list = cache.values().toArray(new Process[cache.size()]);
			JAXBProcessList rootTag = new JAXBProcessList("processes", list);

			// Store the data.
			marshaller.setProperty("jaxb.formatted.output", Boolean.TRUE);
			marshaller.marshal(rootTag, stream);
			
			// Good to go.
			success = true;
			
		} catch (JAXBException e) {
			throw new IOException(e);
		} finally {
			
			// Make sure the stream is closed (before any commit/revert).
			try {
				stream.close();
			} catch (Exception e) {
				// Ignore.
			}
			
			try {
				// If nothing went wrong, commit the changes.
				if(success) {
					file.commit();
					
					// The process file was modified, save a shadow copy of it.
					
					storeProcessFile();
					
				// Otherwise, revert the changes. 
				} else {
					file.revert();
				}
				
			// Always!, release the lock.
			} finally {
				file.release();
			}
			
		}

	}

	/**
	 * Creates an XML context for marshalling the configured bean types.
	 */
	protected JAXBContext createXmlContext() throws JAXBException {
		return JAXBContext.newInstance(beanClasses);
	}

	/**
	 * Creates an output stream for writing data to the configured storage file.
	 */
	protected OutputStream createStreamToStoreCache() throws IOException {
		return new FileOutputStream(file.getTempFile());
	}

	/**
	 * Creates an output stream for reading data from the configured storage file.
	 */
	protected InputStream createStreamToLoadCache() throws IOException {
		return new FileInputStream(file);
	}

	/**
	 * Create a wrapper object to manage writes to the give file transactionally.
	 */
	protected TransactedFile createTransationWrapper(File file) {
		return new TransactedFile(file);
	}
	
	/**
	 * Store a shadow copy of the process file.
	 */
	protected void storeProcessFile() {
		
		if (SystemEnvUtil.checkShadowCopyEnvExist()) {
			if (storage != null) {
				try {
					// Broken UNTIL I talk to ToNY.
					String fileKey = SystemEnvUtil.buildProcessesFileKey(SystemEnvUtil.getDomain(), file.getName());
					storage.storeFile(SystemEnvUtil.getShadowCopyBucket(), fileKey, file);
				} catch(Exception ex) {
					log.error(ex.getMessage(),ex);
				}
			}
		}
		
	}
	
	/**
	 * Internal class used to represent the root tag for JAXB marshalling. This class is primarily used to reduce the
	 * "wordiness" of the marshalling code implemented above by providing a constructor with fewer arguments and by
	 * filling in the type parameter of the super class.
	 */
	private static class JAXBProcessList extends JAXBElement<Process[]> {

		private static final long serialVersionUID = -1924882222495958300L;

		public JAXBProcessList(String name, Process[] value) {
			super(new QName(name), Process[].class, value);
		}

	}

}
