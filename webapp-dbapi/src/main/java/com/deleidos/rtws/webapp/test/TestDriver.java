	private static void buildCSVString() {
		// String NEWLINE = System.getProperty("line.separator");
		String NEWLINE = "\n";
		StringBuilder b = new StringBuilder();
		// int,string,int
		b.append("COL,COL2");
		for (int i = 0; i < 10; i++) {
			b.append(NEWLINE);
			b.append("'value = ").append(i).append("',").append(i);
		}
		csvFile = b.toString();
	}

