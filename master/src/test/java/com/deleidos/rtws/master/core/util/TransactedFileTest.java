package com.deleidos.rtws.master.core.util;

import static org.junit.Assert.*;

import java.io.File;
import java.io.FileOutputStream;

import org.junit.After;
import org.junit.AfterClass;
import org.junit.Before;
import org.junit.BeforeClass;
import org.junit.Test;

import com.deleidos.rtws.commons.util.file.TransactedFile;

import static org.mockito.Mockito.*;

public class TransactedFileTest {

	private static File path = new File("./build/junit/resource/com.deleidos.rtws.master.core.util/TransactedFileTest");
	
	private static final int INITIAL_SIZE = 10;
	
	private File original = new File(path, "test.txt");
	
	@BeforeClass
	public static void setUpBeforeClass() throws Exception {
		path.mkdirs();
	}

	@AfterClass
	public static void tearDownAfterClass() throws Exception {
	}

	@Before
	public void setUp() throws Exception {
		original.delete();
		writeToFile(original, INITIAL_SIZE);
	}

	@After
	public void tearDown() throws Exception {
		original.delete();
	}

	@Test
	public void testConstructor() throws Exception {
		
		TransactedFile file = new TransactedFile(original);
		File temp = file.getTempFile();
		
		assertTrue("Transacted file does not point to the same path as the original.", original.getCanonicalPath().equals(file.getCanonicalPath()));
		assertFalse("Temporary file points to the same path as the original.", original.equals(temp));
		
		try {
			new TransactedFile(null);
			fail("Null reference did not throw an exception.");
		} catch(NullPointerException e) {
			// Expected.
		}
		
		try {
			new TransactedFile(new File(path, ".bogus.txt"));
			fail("Reference to hidden file did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
		try {
			new TransactedFile(new File(path, "bogus.lck"));
			fail("Reference to lock file did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
		try {
			new TransactedFile(path);
			fail("Reference to directory did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
		try {
			File parent = mock(File.class);
			when(parent.canWrite()).thenReturn(false);
			File mock = mock(File.class);
			when(mock.getName()).thenReturn("mock.txt");
			when(mock.isDirectory()).thenReturn(false);
			when(mock.getParentFile()).thenReturn(parent);
			new TransactedFile(mock);
			fail("Reference to file in a read-only directory did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
		try {
			File parent = mock(File.class);
			when(parent.canWrite()).thenReturn(true);
			File writeonly = mock(File.class);
			when(writeonly.getName()).thenReturn("mock.txt");
			when(writeonly.isDirectory()).thenReturn(false);
			when(writeonly.getParentFile()).thenReturn(parent);
			when(writeonly.exists()).thenReturn(true);
			when(writeonly.canRead()).thenReturn(false);
			when(writeonly.canWrite()).thenReturn(true);
			new TransactedFile(writeonly);
			fail("Reference to non-readable file did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
		try {
			File parent = mock(File.class);
			when(parent.canWrite()).thenReturn(true);
			File readonly = mock(File.class);
			when(readonly.getName()).thenReturn("mock.txt");
			when(readonly.isDirectory()).thenReturn(false);
			when(readonly.getParentFile()).thenReturn(parent);
			when(readonly.exists()).thenReturn(true);
			when(readonly.canRead()).thenReturn(true);
			when(readonly.canWrite()).thenReturn(false);
			new TransactedFile(readonly);
			fail("Reference to read-only file did not throw an exception.");
		} catch(IllegalArgumentException e) {
			// Expected.
		}
		
	}
	
	@Test
	public void testInitWithCopy() throws Exception {
		
		TransactedFile file = new TransactedFile(original);
		file.init(true);
		
		File temp = file.getTempFile();
		assertFalse("Temp file has the same name as the original.", original.getName().equals(temp.getName()));
		assertTrue("Original file's contents not copied.", temp.length() == INITIAL_SIZE);
		
	}

	@Test
	public void testInitWithoutCopy() throws Exception {
		
		TransactedFile file = new TransactedFile(original);
		file.init(false);
		
		File temp = file.getTempFile();
		assertFalse("Temp file has the same name as the original.", original.getName().equals(temp.getName()));
		assertFalse("Temp file is not empty.", temp.exists());
		
	}

	@Test
	public void testCommit() throws Exception {
		
		TransactedFile file = new TransactedFile(original);
		file.init(false);
		
		File temp = file.getTempFile();
		writeToFile(temp, 20);
		file.commit();
		assertTrue("Original file not updated.", original.length() == 20);
		assertFalse("Temp file not removed.", temp.exists());
		
	}

	@Test
	public void testRevert() throws Exception {
		
		TransactedFile file = new TransactedFile(original);
		file.init(false);
		
		File temp = file.getTempFile();
		writeToFile(temp, 20);
		file.revert();
		assertTrue("Original file changed.", original.length() == INITIAL_SIZE);
		assertFalse("Temp file not removed.", temp.exists());
		
	}

	@Test
	public void testLock() {
		// Not sure how to test this!
	}

	@Test
	public void testRelease() {
		// Not sure how to test this!
	}

	/**
	 * Writes the specified number of random bytes to the given file.
	 */
	private void writeToFile(File file, int bytes) throws Exception {
		FileOutputStream stream = null;
		try {
			stream = new FileOutputStream(file);
			for(int i = 0; i < bytes; i++) {
				stream.write('A' + (i % 24));
			}
		} finally {
			try {stream.close();} catch (Exception ignore) { }
		}
	}
}
