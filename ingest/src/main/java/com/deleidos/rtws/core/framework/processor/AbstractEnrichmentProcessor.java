package com.deleidos.rtws.core.framework.processor;

import java.io.IOException;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import net.sf.json.JSONArray;
import net.sf.json.JSONObject;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.util.DataModelBasedNames;
import com.deleidos.rtws.commons.util.JsonPath;
import com.deleidos.rtws.core.framework.AbstractProcessor;
import com.deleidos.rtws.core.framework.EnrichmentDefinition;
import com.deleidos.rtws.core.framework.EnrichmentProperty;
import com.deleidos.rtws.core.framework.UserConfigured;
import com.deleidos.rtws.core.framework.parser.CanonicalFormat;

/**
 * Base class for processors capable of performing data enrichment. Provides a configurable way to define what data
 * elements from a model should be used as input for an enrichment, in what field the result should be stored, and
 * whether to retain or remove the input fields once used.
 * 
 * To perform the actual enrichment, extending classes need only implement the buildEnrichedElement() method. The
 * process of extracting the input parameters from and inserting the result back into the data record is handled
 * automatically by this base class.
 * 
 * There are two ways to configure this class. The setSchemas() method can be invoked to explicitly specify the schema
 * specific enrichment actions, or the enrichment actions can be implicitly loaded from the "enriched model"
 * configuration file. If no actions are explicitly defined, then when a record is processed, the model name is pulled
 * from the standard header and is used to load the appropriate "enriched model" file. The schema definition provided
 * therein is then scanned for any enrichment actions associated with this processor. Extending classes must implement
 * the getType() method to declare the type of enrichment that should be handled.
 */

@EnrichmentDefinition(
	type="abstract_enrichment",
	description="The abstract enrichment processor.",
	properties = {
		@EnrichmentProperty(
			name="override",
			description="Property to over-write the field if it exists.",
			type="boolean"
		),
		@EnrichmentProperty(
			name="extractPath",
			description="Specifies a JSON path of a subset of the result from the enrichment instead of the entire result object.",
			type="java.lang.String"
		),
		@EnrichmentProperty(
			name="removeParameters",
			description="Remove the input parameter fields.",
			type="boolean"
		)
	}
)
public abstract class AbstractEnrichmentProcessor extends AbstractProcessor {

	private static Logger log = Logger.getLogger(AbstractEnrichmentProcessor.class);

	/** Pattern that matches a single field element with a JSON path expression. */
	private static final Pattern JSON_PATH_ELEMENT = Pattern.compile("(\\w+)(\\[(\\*?|\\d*)\\])?");

	/** The JSON path expression representing the root of JSON object tree. */
	private static final String JSON_ROOT_PATH = "";

	/** A cache of known enrichment definitions. */
	private EnrichmentModelMappingCache models = new EnrichmentModelMappingCache();

	private static final String wildcard = "[*]";
	/**
	 * Constructor.
	 */
	public AbstractEnrichmentProcessor() {
		super();
	}

	/**
	 * Defines the data model specific enrichment actions this processor should perform. This method allows the
	 * enrichment rules of this instance to be configured explicitly. It's use is optional; omitting it will then
	 * attempt to load the enrichment rules from the "enriched model" file (see
	 * DataModelBasedNames.getEnrichedFilename()).
	 */
	@UserConfigured(value = "com.deleidos.rtws.core.framework.processor.EnrichmentModelMappingCache")
	public final void setEnrichments(EnrichmentModelMappingCache cache) {
		models = cache;
	}

	/**
	 * Extending classes may override as needed.
	 */
	public void initialize(){}

	/**
	 * Extending classes may override as needed.
	 */
	public void dispose() {
		// Nothing to do.
	}

	/**
	 * Declares the type of enrichment performed by this processor. Extending classes must implement this method to
	 * identify which fields in the "enriched model" configuration files are intended to be handled by this enrichment
	 * process.
	 * 
	 * @return the processor type
	 */
	public abstract String getType();

	/**
	 * Performs the actual enrichment operation. Extending classes must provide the enrichment algorithm specific
	 * implementation.
	 * 
	 * @param action
	 *            The definition of the enrichment to be performed.
	 * @param parameters
	 *            Extracted values for the configured parameter fields.
	 *            
	 * @return the Object built by enrichment
	 */
	public abstract Object buildEnrichedElement(EnrichmentAction action, ParameterList parameters);

	/**
	 * Processes the given record. Determines the enrichments that are applicable to the model identified in the
	 * record's header and delegates processing for each.
	 * 
	 * @param record
	 * 			the record to be processed and enriched
	 */
	@Override
	public final void processInternal(JSONObject record) {
		DataModelBasedNames model = CanonicalFormat.getModel(record);
		if (model == null) {
			log.warn("Unable to determine data model; header missing from record.");
		} else try {
			EnrichmentFieldMappingCache actions = models.get(model);
			if (actions != null) {
				processRecursive(record, record, JSON_ROOT_PATH, actions);
			} else {
				log.debug("No enrichment definitions defined for model '" + model.getFullModelName() + "'.");
			}
		} catch (IOException e) {
			log.error("Unable to load enriched model definition '" + model.getFullModelName() + "'.", e);
		}
	}

	/**
	 * Recursively traverses a JSON object tree looking for elements where enrichments should be inserted as child
	 * elements.
	 * 
	 * @param root
	 *            The root object of the JSON object tree.
	 * @param element
	 *            The element to be inspected (the current element in the traversal).
	 * @param path
	 *            The JSON path expression defining the path between the root and the current element.
	 * @param actions
	 *            The set of defined enrichment actions.
	 */
	private void processRecursive(JSONObject root, Object element, String path, EnrichmentFieldMappingCache actions) {
		if (element instanceof JSONArray) {
			// Loop over each array element and recurse.
			JSONArray array = (JSONArray) element;
			for (int index = 0; index < array.size(); index++) {
				processRecursive(root, array.get(index), buildPath(path, index), actions);
			}
		} else if (element instanceof JSONObject) {
			// Apply any applicable enrichments to this object.
			JSONObject object = (JSONObject) element;
			enrich(root, object, path, actions);
			// Loop over each child element and recurse.
			for (Object field : object.keySet()) {
				processRecursive(root, object.get(field), buildPath(path, field.toString()), actions);
			}
		} else {
			// Primitive objects cannot have child elements added to them.
		}
	}

	/**
	 * Performs any enrichment actions that are defined to be inserted at the given path position.
	 * 
	 * @param root
	 *            The root object of the JSON object tree.
	 * @param object
	 *            The JSON object into which enriched results should be inserted.
	 * @param path
	 *            The JSON path expression defining the path between the root and the current element.
	 * @param actions
	 *            The set of defined enrichment actions.
	 */
	private void enrich(JSONObject root, JSONObject object, String path, EnrichmentFieldMappingCache actions) {

		// Remove the array indexes from the current path before looking up the applicable actions. Since
		// the actions apply to all the elements of an array, the indexes are not stored in the cache key.
		String key = EnrichmentFieldMappingCache.removeArrayParameters(path);

		// Lookup the enrichment actions defined for this element.
		if (actions.containsKey(key)) {
			for (EnrichmentAction action : actions.get(key)) {
				if (!action.getType().equals(getType())) {
					// Ignore any actions that do not represent the algorithm implemented by this subclass.
				} else if (object.containsKey(action.getFieldName()) && !action.getOverride()) {
					// Ignore the action if the field already has a value set, and it should not be overridden.
				} else {

					// Extract the list of field values which are expected as parameters to this 
					// enrichment action. The path for each field will appear as an absolute path.
					// However, any array element indexes should be missing (because the actual
					// number of elements in the parsed data can vary). Instead, the path implies
					// a reference that is (at least partially) relative to the current element in
					// the tree. To resolve this, any array positions in the current path need to
					// be substituted into the defined path.
					String[] parameterFields = mergePaths(path, action.getParameters());
					Object[] values = buildCompositeKey(root, parameterFields);
					action.setRootObject(root);
					try {
						// Add the result to the current path element.
						Object result = buildEnrichedElement(action, new ParameterList(values));
						if (result != null) {
							String extract = action.getExtractField();
							if((extract == null) || (extract.length() == 0)) {
								object.put(action.getFieldName(), result);
							} else if(result instanceof JSONObject) {
								object.put(action.getFieldName(), JsonPath.simpleLookup((JSONObject)result, extract, false));
							} else {
								log.warn("Unable to extract sub-field from primitive type for field '" + path + "." + action.getFieldName() + "'.");
							}
							if (action.getRemoveParameters()) {
								remove(root, parameterFields);
							}
						}
					} catch (Exception e) {
						log.warn("Unable to perform dimensional enrichment for field '" + path + "." + action.getFieldName() + "'.", e);
					}

				}
			}
		}

	}

	/**
	 * Removes the given fields from the given JSON Object.
	 * 
	 * @param root
	 * 			the JSON Object from which to remove the fields
	 * @param fields
	 * 			the fields to remove from the JSON Object
	 */
	private void remove(JSONObject root, String[] fields) {
		for (String path : fields) {
			String parent = JsonPath.parent(path);
			String child = JsonPath.child(path);
			Object object = JsonPath.simpleLookup(root, parent, false);
			if (object instanceof JSONObject) {
				((JSONObject) object).remove(child);
			}
		}
	}

	/**
	 * Append the field name into the given path.
	 * 
	 * @param parent
	 *            The path upon which to be appended.
	 * @param child
	 *            The field to be appended.
	 *            
	 * @return the path with the child appended
	 */
	private static String buildPath(String parent, String child) {
		if (parent == null || parent.isEmpty()) {
			return child;
		} else {
			return parent + "." + child;
		}
	}

	/**
	 * Append an array index to the given path.
	 * 
	 * @param parent
	 *            The path upon which to be appended
	 * @param index
	 *            The array index to be appended
	 * 
	 * @return the path with the index appended
	 */
	private static String buildPath(String parent, int index) {
		return parent + "[" + index + "]";
	}

	/**
	 * Builds an array of field values extracted from the given path reference.
	 * 
	 * @param object
	 *            The object from which to extract field values.
	 * @param keyFields
	 *            The paths indicating the fields to be extracted.
	 *            
	 * @return an Object array containing the values of the composite key
	 */
	private static Object[] buildCompositeKey(JSONObject object, String[] fields) {
		int count = fields.length;
		Object[] values = new Object[count];
		for (int i = 0; i < count; i++) {
			if (fields[i].contains(wildcard)) {
				values[i] = JsonPath.query(object, fields[i]);
			} else {
				values[i] = JsonPath.simpleLookup(object, fields[i], false);
			}	
		}
		return values;
	}

	/**
	 * Merges two paths, substituting array indexes from a concrete path into the related array elements in an abstract
	 * path. The two path do not need to be exactly the same, but they do need to share a prefix. Substitutions are made
	 * only to the leading fields that are the same in both paths.
	 * 
	 * <blockquote>
	 * 
	 * <pre>
	 * Simple example:
	 * Concrete path: order.items[3].product
	 * Abstract path: order.items[].product.serial
	 * Returned path: order.items[3].product.serial
	 * 
	 * Complex example:
	 * Concrete path: order.items[3].vendors[1].address
	 * Abstract path: order.items[].customer.address.zipcode
	 * Returned path: order.items[3].customer.address.zipcode
	 * </pre>
	 * 
	 * </blockquote>
	 * 
	 * @param real
	 *            The concrete path from which to extract array indexes.
	 * @param template
	 *            The abstract path to be filled in with the extracted indexes.
	 *            
	 * @return the merged path
	 */
	private static String mergePath(String real, String template) {
		Matcher actualMatcher = JSON_PATH_ELEMENT.matcher(real);
		Matcher templateMatcher = JSON_PATH_ELEMENT.matcher(template);
		int start = 0;
		int end = 0;
		// Scan the paths from left to right, one field at a time, looking for array
		// fields that require relative path substitution (those with empty indexes).
		while (actualMatcher.find() && templateMatcher.find()) {
			String actualField = actualMatcher.group(1);
			String templateField = templateMatcher.group(1);
			String templateIndex = templateMatcher.group(3);
			if (templateIndex != null && !templateIndex.isEmpty() && !templateIndex.equals("*")) {
				// If this element of the template contains a numeric index, then no
				// substitution is required. Further substitutions are not allowed.
				break;
			} else if (actualField.equals(templateField)) {
				// If this element's array index is blank (i.e. requires substitution)
				// and the field name in the template matches the current position in
				// real path, slide the prefix marker position forward.
				end = actualMatcher.end();
				start = templateMatcher.end();
			} else {
				// If this element's field name does not match the template, then this
				// is the point at which the paths diverge, stop searching.
				break;
			}
		}
		return real.substring(0, end) + template.substring(start);
	}

	/**
	 * Performs a path merge on a list of template paths.
	 * 
	 * @param currentField
	 *            The concrete path from which to extract array indexes.
	 * @param templates
	 *            The list of abstract path to be filled in with the extracted indexes.
	 * 
	 * @return a String array of the merged paths
	 */
	private static String[] mergePaths(String currentField, String[] templates) {
		int count = templates.length;
		String[] realtiveFields = new String[count];
		for (int i = 0; i < count; i++) {
			realtiveFields[i] = mergePath(currentField, templates[i]);
		}
		return realtiveFields;
	}

}
