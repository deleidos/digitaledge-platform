	@Override
	public StandardResponse<?> broadcast(String domain, String jsonDef) {

		try {
			AccountSystemDao dao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
			AccountSystem system = dao.getAccountSystem(domain);

			String swVersion = system.getSwVersionId();

			if (!swVersion.startsWith("rtws-nightly") && swVersion.compareTo("1.1.3") < 0) {
				throw new SystemServiceException(
						"Broadcast command functionality is supported on system built with software version 1.1.3+.");
			}

			Cluster cluster = ClusterCache.getInstance().getCluster(domain);
			if (cluster.getStatus().getState() != ClusterState.OK
					&& cluster.getStatus().getState() != ClusterState.Warning) {
				throw new SystemServiceException(String.format(
						"Failed to broadcast command because system %s is in an invalid state.",
						domain));
			}

			String result = BroadcastCommandClient.getInstance().send(domain, jsonDef);
			JSONObject jsonResponse = JSONObject.fromObject(result);

			String status = null;
			if (jsonResponse.has("status")) {
				status = jsonResponse.getString("status");
			}

			if (status == null || status.equals("Error")) {
				throw new SystemServiceException(String.format(
						"Error occurred while broadcasting command to system %s.", domain));
			}

			String token = null;
			if (jsonResponse.has("message")) {
				String message = jsonResponse.getString("message");
				StringTokenizer st = new StringTokenizer(message);
				if (st.countTokens() == 0) {
					throw new SystemServiceException(String.format(
							"No return token found after sending broadcast command to system %s.",
							domain));
				} else if (st.countTokens() == 1) {
					token = message.trim();
				} else {
					throw new SystemServiceException(message);
				}
			}

			PropertiesResponse response = new PropertiesResponse();
			response.setStandardHeaderCode(200);
			response.setProperty("Status", "true");
			response.setProperty("Token", token);

			return response;
		} catch (Exception ex) {
			logger.error("broadcast - Exception: " + ex.getMessage(), ex);

			ErrorResponse response = new ErrorResponse();
			response.setStandardHeaderCode(500);
			response.setMessage(ex.getMessage());

			return response;
		}

	}

