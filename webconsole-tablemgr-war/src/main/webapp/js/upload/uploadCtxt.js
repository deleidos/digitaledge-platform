var uploadModule = angular.module("uploadModule");

uploadModule.controller("uploadController", function($scope, requestData, urlUtils, uploadProgressService) {
	// Constants
	$scope.PROMPT_STATE = "prompt";
  $scope.UPLOADING_STATE = "uploading";
  
  $scope.STATUS_TYPE_INFO = "alert-info";
  $scope.STATUS_TYPE_SUCCESS = "alert-success";
  $scope.STATUS_TYPE_ERROR = "alert-error";
	
  // Scope instance variables
  $scope.state = $scope.PROMPT_STATE;
	$scope.uploadPercent = -1;
	$scope.importPercent = -1;
	
	$scope.statusMsg = null;
	$scope.statusMsgType = null;
	
	$scope.isUploadSuccessful = false;
	
	$scope.serverRootPath = urlUtils.getServerRootPath();
	// Using 250 MiB as the default.
	$scope.serverMaxUploadBytes = 250 * 1024 * 1024;
	if(requestData && requestData.maxUploadBytes && typeof requestData.maxUploadBytes === "string")
	{
		var tmpMaxUpload = Number(requestData.maxUploadBytes);
		if(isNaN(tmpMaxUpload) == false)
		{
			$scope.serverMaxUploadBytes = tmpMaxUpload;
		}
	}
	
	$scope.opTrackingToken = null;
	if(requestData && requestData.opTrackingToken && typeof requestData.opTrackingToken === "string")
	{
		$scope.opTrackingToken = requestData.opTrackingToken;
	}
	
	// Form variables
	$scope.targetDbHost = null;
	if(requestData && requestData.targetDbHost && typeof requestData.targetDbHost === "string")
	{
		$scope.targetDbHost = requestData.targetDbHost;
	}
	$scope.targetDbPort = null;
	if(requestData && requestData.targetDbPort && typeof requestData.targetDbPort === "string")
	{
		//NaN is handled later
		$scope.targetDbPort = Number(requestData.targetDbPort);
	}
	$scope.schema = null;
	if(requestData && requestData.schema && typeof requestData.schema === "string")
	{
		$scope.schema = requestData.schema;
	}
	$scope.table = null;
	if(requestData && requestData.table && typeof requestData.table === "string")
	{
		$scope.table = requestData.table;
	}
	$scope.destLabel = null;
	if($scope.table)
	{
		$scope.destLabel = (($scope.schema) ? $scope.schema + "." : "") + $scope.table;
	}
	
	var supportedFileExtensions = ["csv", "zip"];
	
	var extDisplay = "(";
	for(var index=0; index < supportedFileExtensions.length; index++) {
		if(index > 0) {
			extDisplay += ", ";
		}
		extDisplay += "*." + supportedFileExtensions[index]; 
	}
	extDisplay += ")";
	$scope.supportedFileExtensionDisplay = extDisplay;
	
	
	$scope.filename = null;
	$scope.fileBytes = null;
	$scope.fileErrorMsg = null;
	
	$scope.mockUpload = {
			intervalId: null,
			startTimestamp: -1
	};
	
	// Listeners
	$scope.$on("fileUploadChange", function(event, changeDetails) {
		$scope.filename = changeDetails.filename;
		$scope.fileBytes = changeDetails.fileBytes;
		validateUploadForm(true);
	});
	
	$scope.$on("uploadStatus", function(event, importStatusEvent) {
		if(importStatusEvent.status() == com.deleidos.rtws.tablemgr.event.OpStatus.SUCCESS)
		{
			$scope.importPercent = 100;
			
			setUploadFinalStatus(true, null);
		}
		else if(importStatusEvent.status() == com.deleidos.rtws.tablemgr.event.OpStatus.FAILURE)
		{
			$scope.importPercent = -1;
			
			var msg = "Failed to import data to " + $scope.destLabel;
			if(importStatusEvent.userMsg())
			{
				msg += "\n<br /><strong>Details:</strong>&nbsp;&nbsp;" + importStatusEvent.userMsg();
			}
			
			setUploadFinalStatus(false, msg);
		}
		else if(importStatusEvent.status() == com.deleidos.rtws.tablemgr.event.OpStatus.IN_PROGRESS)
		{
			var percent = importStatusEvent.percentComplete();
			if(isNaN(percent) || percent < 0)
			{
				percent = -1;
			}
			else if(percent > 100)
			{
				percent = 99;
			}

			$scope.importPercent = percent;
			
			$scope.statusMsgType = null;
			$scope.statusMsg = null;
			
			setTimeout(function() {
				uploadProgressService.getUploadStatus($scope, importStatusEvent.trackingToken());
			}, 5000);
		}
	});
	
	var toHumanReadable = function(bytes, base, forceDecimalPrefixes) {
		if(!bytes || angular.isNumber(bytes) === false || bytes < 0)
		{
			throw Error("Unsupported bytes value");
		}
		
		var tmpBase = null;
		if(typeof base === "undefined")
		{
			tmpBase = 2;
		}
		else
		{
			tmpBase = Number(base);
		}
		
		if(isNaN(tmpBase) || (tmpBase != 2 && tmpBase != 10))
		{
			throw Error("Unsupported Base");
		}
		
		var unit = (tmpBase == 2 ? 1024 : 1000);
    if(bytes < unit) {
    	return bytes + " B";
    }
    
    var exp = Math.floor(Math.log(bytes) / Math.log(unit));
    
    var useDecimalPrefixes = (base == 10 || (base == 2 && forceDecimalPrefixes === true));
    var unitsSymbol = (useDecimalPrefixes  ? "kMGTPE" : "KMGTPE").charAt(exp-1) + (useDecimalPrefixes ? "" : "i") + "B";
    
    return Number(bytes / Math.pow(unit, exp)).toFixed(1) + " " + unitsSymbol;
	}
	
	// Methods
	
	var validateUploadForm = function(scriptInit){
		var isUserInitiated = (typeof scriptInit === "undefined");
		
		var result = true;
		
		if(($scope.targetDbPort != null && isNaN($scope.targetDbPort)) || $scope.schema == null || $scope.table == null) {
			result = false;
			
			if(isUserInitiated && window && window.alert) {
				alert("Unknown Upload Location.  Please close the dialog and try again.");
			}
		}
		
		if($scope.opTrackingToken == null) {
			result = false;
			
			if(isUserInitiated && window && window.alert) {
				alert("Upload is misconfigured.  Please close the dialog and try again.");
			}
		}
		
		$scope.fileErrorMsg = null;
		if(!$scope.filename) {
			result = false;
			
			$scope.fileErrorMsg = "Please select a File to upload";
		}
		else {
			// Type Check
			var ext = null;
			
			var extStartIndex = $scope.filename.lastIndexOf(".");
			if(extStartIndex > 0 && extStartIndex < ($scope.filename.length - 1)) {
				ext = $scope.filename.substring(extStartIndex + 1).toLowerCase();
			}
			
			var isValidExt = false;
			for(var index=0; index < supportedFileExtensions.length; index++) {
				if(ext === supportedFileExtensions[index]) {
					isValidExt = true;
					break;
				}
			}
			
			if(isValidExt == false) {
				result = false;
				$scope.fileErrorMsg = "Invalid File Type.  Allowed extensions are " + $scope.supportedFileExtensionDisplay;
			}
			else
			{
				// File Size Check
				
				// Allowing fileBytes == null in case a browser cannot access file size
				if(isNaN($scope.serverMaxUploadBytes) == false && $scope.fileBytes != null && $scope.fileBytes > 0 && $scope.fileBytes > $scope.serverMaxUploadBytes)
				{
					result = false;
					
					$scope.fileErrorMsg = "File Size (" + toHumanReadable($scope.fileBytes, 2, true) + ") is too large.  Max Upload Size is " + toHumanReadable($scope.serverMaxUploadBytes, 2, true);
				}
			}
		}
		
		return result;
	};
	
	$scope.isUploadInProgress = function() {
		return ($scope.state === $scope.UPLOADING_STATE);
	}
	
	$scope.upload = function(){
		if(validateUploadForm())
		{
			$scope.state = $scope.UPLOADING_STATE;
			$scope.uploadPercent = -1;
			$scope.isUploadSuccessful = false;
			$scope.statusMsgType = null;
			$scope.statusMsg = null;
			
			// Mock Upload
			/*$scope.mockUpload.intervalId = setInterval(function(){
				$scope.$apply(onMockUploadInterval);
			}, 1000);*/
			
			// Real Upload
			uploadFile();
		}
	}
	
	function uploadFile() {
		var $uploadForm = jQuery("#uploadForm");
		
		var options = {
				context: $scope,
				url: $scope.serverRootPath + "/json/upload/import/" + encodeURIComponent($scope.schema) + "/" + encodeURIComponent($scope.table),
				dataType: "json",
				accepts: {
					json: "application/json",
					text: "*/*"
				},
				timeout: 0,
				uploadProgress: onUploadProgress,
				success: onUploadSuccess,
				error: onUploadFailure
		};
		
		$uploadForm.ajaxSubmit(options);
	}

	function onUploadProgress(event, position, total, percentComplete) {
	    $scope.$apply(function(){
	        if (percentComplete && percentComplete >= 0 && percentComplete <= 100) {
	            $scope.uploadPercent = percentComplete;
	        } else {
	            $scope.uploadPercent = -1;
	        }
	    })
	}

	function onUploadSuccess(data, textStatus, jqXHR){
		$scope.$apply(function(){
			
			if(data.jobId && angular.isString(data.jobId) && data.jobId.length > 0 && data.state && angular.isString(data.state))
			{
				if(data.state === "SUCCESS")
				{
					setUploadFinalStatus(true, null);
				}
				else if(data.state === "FAILURE")
				{
					setUploadFinalStatus(false, (data.details ? data.details : "Failed to upload your file '" + $scope.filename + "'"));
				}
				else
				{
					$scope.statusMsgType = null;
					$scope.statusMsg = null;
					
					var percent = -1;
					if(data.percentComplete)
					{
						var percent = Number(data.percentComplete);
						if(isNaN(percent) == false && percent > 0) {
							percent = Math.min(percent, 99);
						}
					}
					
					$scope.importPercent = Math.max(0, percent);
					
					setTimeout(function() {
						uploadProgressService.getUploadStatus($scope, data.jobId);
					}, 2000);
				}
			}
			else
			{
				setUploadFinalStatus(false, "Failed to parse the upload response." + (data.details ? ("  Details: " + data.details) : ""));
			}
		});
	};
	
	function onUploadFailure(jqXHR, textStatus, errorThrown) {
		$scope.$apply(function(){
			var failureMsgDetails = "";
			
			if(jqXHR)
			{
				if(jqXHR.responseText && typeof jqXHR.responseText === "string" && jqXHR.responseText.indexOf("<html>") == -1)
				{
					failureMsgDetails = jqXHR.responseText;
				}
				else if("error" !== jqXHR.statusText)
				{
					failureMsgDetails = jqXHR.statusText + " (" + jqXHR.status + ")";
				}
			}
			
			var failureMsg = "Failed to Upload the File.";
			if(failureMsgDetails)
			{
				failureMsg += "<br /><strong>Details: </strong>" + failureMsgDetails;
			}
			
			setUploadFinalStatus(false, failureMsg);
		});
	}
	
	function setUploadFinalStatus(success, userMsg) {
		$scope.state = $scope.PROMPT_STATE;
		$scope.uploadPercent = -1;
		$scope.importPercent = -1;
		$scope.isUploadSuccessful = success;
		
		if(userMsg)
		{
			$scope.statusMsgType = (success ? $scope.STATUS_TYPE_SUCCESS : $scope.STATUS_TYPE_ERROR);
			$scope.statusMsg = userMsg;
		}
		else
		{
			$scope.statusMsgType = null;
			$scope.statusMsg = null;
		}
		
		if(success)
		{
			setTimeout(function(){
				$scope.closeDialog();
			}, 250);
		}
	}
	
	function onMockUploadInterval($scope) {
		var currTime = new Date().getTime();
		
		if($scope.mockUpload.startTimestamp == -1)
		{
			$scope.mockUpload.startTimestamp = currTime;
			$scope.uploadPercent = 0;
		}
		else
		{
			var secondsDelta = Math.floor((currTime - $scope.mockUpload.startTimestamp) / 1000);
			$scope.statusMsgType = $scope.STATUS_TYPE_INFO;
			$scope.statusMsg = "It's been " + secondsDelta + " seconds";
			$scope.uploadPercent = Math.min(100, (secondsDelta * 10));
		}
		
		if($scope.uploadPercent == 100)
		{
			window.clearInterval($scope.mockUpload.intervalId);
			$scope.mockUpload.intervalId = null;
			$scope.mockUpload.startTimestamp = -1;
			
			$scope.uploadPercent = -1;
			$scope.state = $scope.PROMPT_STATE;
			
			$scope.statusMsgType = $scope.STATUS_TYPE_SUCCESS;
			$scope.statusMsg = "Successfully updated test file!";
		}
	}
});

uploadModule.factory("uploadProgressService",
		[
		 "$http", "urlUtils", "$log", function($http, urlUtils, $logger){
			var impl = new Object();
			
			impl.getUploadStatus = function($scope, uploadOpTrackingToken) {
				$http({
					method: "GET",
					url: urlUtils.getServerRootPath() + "/proxy/rtws/tenantDbApi/rest/status/" + encodeURIComponent(uploadOpTrackingToken)})
					.success(function(data, status, headers, config) {
						var event = null;
						if(data.state === "SUCCESS")
						{
							event = new com.deleidos.rtws.tablemgr.upload.ImportStatusEvent(com.deleidos.rtws.tablemgr.event.OpStatus.SUCCESS, uploadOpTrackingToken, (data.details ? data.details : null), 100);
						}
						else
						{
							event = new com.deleidos.rtws.tablemgr.upload.ImportStatusEvent(com.deleidos.rtws.tablemgr.event.OpStatus.IN_PROGRESS, uploadOpTrackingToken, (data.details ? data.details : null), data.percentComplete);
						}
						 
						$scope.$broadcast("uploadStatus", event);
					})
					.error(function(data, status, headers, config) {
						// Service returns 500 if the op was an error
						var userMsg = data.details;
						if(userMsg)
						{
							userMsg = userMsg.replace(/^s+/g, "").replace(/s+$/g, "");
							if(userMsg.length > 0)
							{
								var sqlDetailsExtractPattern = /;\s*SQL\s?STATEMENT:\s*.*$/mi;
								var extractionResults = sqlDetailsExtractPattern.exec(userMsg);
								if(extractionResults != null)
								{
									userMsg = userMsg.substring(0, extractionResults.index);
								}
							}
							else
							{
								userMsg = null;
							}
						}
						$scope.$broadcast("uploadStatus", new com.deleidos.rtws.tablemgr.upload.ImportStatusEvent(com.deleidos.rtws.tablemgr.event.OpStatus.FAILURE, uploadOpTrackingToken, userMsg, -1));
					});
			};
			
			return impl;
		}
	]);

uploadModule.directive('jqueryDialogHandler', function() {
  return {
    link: function($scope, element, attributes) {
    	jQuery(element.ownerDocument).ready(
  				function($jQuery) {
  					var $dialog = null;
  					
  					var onDialogBeforeClose = function(event, ui) {
  						if($scope.isUploadInProgress()) {
  							return false;
  						}
  						else {
  							var status = ($scope.isUploadSuccessful ? "SUCCESS" : "CANCELLED");
  							FABridge.tableMgrFlexAjaxBridge.root().dataImportComplete(status, $scope.opTrackingToken);
  							$jQuery(this).unbind("dialogbeforeclose", onDialogBeforeClose);
  							return true;
  						}
  					};
  					
  					$scope.closeDialog = function() {
  						if(!$dialog) {
  							$dialog = $jQuery(element).closest(".ui-dialog-content");
  						}
  						
  						if($dialog && $dialog.length == 1) {
  							$dialog.dialog("close");
  						}
  					};
  					
  					$jQuery(element).parent().bind("dialogbeforeclose", onDialogBeforeClose);
  				});
    }
  }
});