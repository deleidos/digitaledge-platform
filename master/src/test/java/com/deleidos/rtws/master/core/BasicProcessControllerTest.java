	@Test
	public void testSync() throws Exception {

		ProcessGroup group = new ProcessGroup();
		group.setName("test");
		group.setDefinition(new ProcessDefinition());
		group.setAllocationPolicy(new ProcessAllocationPolicy());

		Process runningProcess = new Process();
		runningProcess.setPersistentDnsName("host1.test.com");
		runningProcess.setPrivateIpAddress("127.0.0.1");
		Process stoppedProcess = new Process();
		stoppedProcess.setPersistentDnsName("host2.test.com");
		stoppedProcess.setPrivateIpAddress("127.0.0.2");

		ProcessDao dao = mock(ProcessDao.class);
		DnsClient dns = mock(DnsClient.class);
		ProcessTable table = mock(ProcessTable.class);
		ServiceInterface provider = mock(ServiceInterface.class);
		ManagementContext jmx = mock(ManagementContext.class);

		when(dao.findByGroup(anyString())).thenReturn(
				Arrays.asList(runningProcess, stoppedProcess));
		when(provider.status(runningProcess)).thenReturn(ProcessState.RUNNING);
		when(provider.status(stoppedProcess)).thenReturn(ProcessState.UNKNOWN);

		BasicProcessController controller = new BasicProcessController();
		controller.setProcessGroup(group);
		controller.setProcessDao(dao);
		controller.setDnsClient(dns);
		controller.setProcessTable(table);
		controller.setServiceInterface(provider);
		controller.setManagementContext(jmx);
		controller.sync();

		verify(dao).findByGroup("test");
		verify(provider).status(same(runningProcess));
		verify(provider).status(same(stoppedProcess));
		verify(dns, times(1)).bind(anyString(), anyString());
		verify(dns).bind(runningProcess.getPersistentDnsName(),
				runningProcess.getPrivateIpAddress());
		verify(table).insert(same(runningProcess));
		verify(table).remove(same(stoppedProcess));
		verify(jmx).manage(same(runningProcess));
		verify(jmx).release(same(stoppedProcess));

	}

