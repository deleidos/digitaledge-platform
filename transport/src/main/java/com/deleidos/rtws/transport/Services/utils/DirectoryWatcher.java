package com.deleidos.rtws.transport.Services.utils;

import java.io.File;
import java.io.IOException;
import java.io.RandomAccessFile;
import java.nio.channels.FileChannel;
import java.util.HashMap;
import java.util.Iterator;
import java.util.Map;
import java.util.Map.Entry;
import java.util.Timer;
import java.util.TimerTask;
import java.util.concurrent.BlockingQueue;
import java.util.concurrent.LinkedBlockingQueue;
import java.util.concurrent.TimeUnit;

import org.apache.commons.io.monitor.FileAlterationListener;
import org.apache.commons.io.monitor.FileAlterationListenerAdaptor;
import org.apache.commons.io.monitor.FileAlterationMonitor;
import org.apache.commons.io.monitor.FileAlterationObserver;
import org.apache.log4j.Logger;

/**
 * Watches a given directory for file creation/changes.  Files are collected
 * and made available via getNextFile().  getNextFile() blocks until a file is available
 * and completely finished copying into the watchDirectory.
 * 
 */
public class DirectoryWatcher {

	private static final Logger log = Logger.getLogger(DirectoryWatcher.class);
	
	// this value must be greater than the largest time interval between onFileChange events
	// when copying large files into the watched directory (see FileListener notes below)
	// so far 2 seconds seems like plenty of time, change events appear to happen sub second
	private static final long FILE_CHANGE_TIME_THRESHOLD = 2000;
	
	private String watchedDirectory;
	private int checkInterval;
	private FileAlterationMonitor monitor;
	private BlockingQueue<File> fileQueue = new LinkedBlockingQueue<File>();
	private Map<File, Long> fileMap = new HashMap<File, Long>();
	private Timer timer;
	private boolean stopped = false;
	
	public DirectoryWatcher(String watchedDirectory, int checkInterval) {
		this.watchedDirectory = watchedDirectory;
		this.checkInterval = checkInterval;
	}
	
	public void start() {
		FileAlterationObserver observer = new FileAlterationObserver(watchedDirectory);
		monitor = new FileAlterationMonitor(checkInterval);
		FileAlterationListener listener = new FileListener();
		observer.addListener(listener);
        monitor.addObserver(observer);
        
        timer = new Timer();
		timer.scheduleAtFixedRate(new FileChecker(), FILE_CHANGE_TIME_THRESHOLD, FILE_CHANGE_TIME_THRESHOLD);
		
		try {
			monitor.start();
			stopped = false;
		} catch (Exception e) {
			log.error("DirectoryWatcher start error", e);
		}
	}

	public void stop() {
		try {
			stopped = true;
			monitor.stop();
			timer.cancel();
		} catch (Exception e) {
			log.error("DirectoryWatcher stop error", e);
		}
	}
	
	public File getNextFile() {
		File file = null;
		try {
			while (file == null && !stopped) {
				file = fileQueue.poll(1000, TimeUnit.MILLISECONDS);
			}
			return file;
		} catch (InterruptedException e) {
			return null;
		}
	}
	
	private class FileListener extends FileAlterationListenerAdaptor {
		private FileListener() {
		}
		
		// large file copies trigger one onCreate and multiple onChange calls in rapid succession
		// this listener just tracks the file that caused the event and the time it happened in a map
		// another thread will scan this map of files and look for ones that haven't changed in some
		// threshold, that's how we know that the file is done copying
		// we use the filePart trick to filter out the tmp files created during remote copies
		public void onFileChange(File file) {
			if (!isFilePart(file)) {
				synchronized (fileMap) {
					fileMap.put(file, System.currentTimeMillis());
				}
			}
		}

		public void onFileCreate(File file) {
			if (!isFilePart(file)) {
				synchronized (fileMap) {
					fileMap.put(file, System.currentTimeMillis());
				}
			}
		}
		
		private boolean isFilePart(File file) {
			// remote file copies like from scp create <filename>.filepart files
			// we don't want to process those
			String path = file.getAbsolutePath();
			return path.endsWith("filepart");
		}
	}
	
	private class FileChecker extends TimerTask {
		@Override
		public void run() {
			synchronized (fileMap) {
				long currentTime = System.currentTimeMillis();
				Iterator<Map.Entry<File, Long>> it = fileMap.entrySet().iterator();
				while (it.hasNext()) {
				    Entry<File, Long> fileEntry = it.next();
				    File file = fileEntry.getKey();
					if (currentTime - fileEntry.getValue() > FILE_CHANGE_TIME_THRESHOLD &&
						isCopyComplete(file)) {
				    	// this file is done copying, remove from the map and add to the queue
						log.info("File changed: " + file.getAbsolutePath());
						fileQueue.add(file);
				    	it.remove();
				    }
				}
			}
		}
		
		// used as one final check to make sure the file copy is complete and we can get
		// exclusive access to the file
		private boolean isCopyComplete(File file) {
			FileChannel channel = null;
			while (channel == null) {
				try {
					channel = new RandomAccessFile(file, "r").getChannel();
				} catch (IOException ioe) {
					log.warn(file.getAbsolutePath() + " still copying");
					channel = null;
				} finally {
					if (channel != null) {
						try { channel.close(); } catch (IOException e) { log.warn("Failed to close channel", e); }
					}
				}
			}
			return (channel != null);
		}
	}

}
