package com.deleidos.rtws.systemcfg.parser;

import java.io.ByteArrayInputStream;
import java.io.FileNotFoundException;
import java.io.FileOutputStream;
import java.io.InputStream;
import java.io.OutputStream;
import java.io.StringWriter;

import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;
import javax.xml.transform.OutputKeys;
import javax.xml.transform.Transformer;
import javax.xml.transform.TransformerException;
import javax.xml.transform.TransformerFactory;
import javax.xml.transform.dom.DOMSource;
import javax.xml.transform.stream.StreamResult;

import org.apache.log4j.LogManager;
import org.apache.log4j.Logger;
import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.Node;
import org.w3c.dom.NodeList;
import org.w3c.dom.Text;

public class XMLParser {
	
	private static final Logger logger = LogManager.getLogger(XMLParser.class);
	
	private Document doc;
	private boolean nameSpaceAware;
	
	/**
	 * Default constructor.
	 */
	public XMLParser(){
		nameSpaceAware = false;
	}
	
	public XMLParser(boolean nameSpaceAware){
		this.nameSpaceAware = nameSpaceAware;
	}
	
	/**
	 * Read xml file into a document object.
	 * 
	 * @param xmlFile
	 */
	public void readXmlFile(String xmlFile){
		readXmlFileFromStream(getClass().getResourceAsStream(xmlFile));
	}
	
	/**
	 * Read xml string into a document object.
	 * 
	 * @param xml
	 */
	public void readXmlFileFromStream(InputStream is){
		try{
			DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
			
			if(nameSpaceAware == true){
				dbFactory.setNamespaceAware(true);
			}
			DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
			
			//working copy of the xml file
			doc = dBuilder.parse(is);
			doc.getDocumentElement().normalize();
		}
		catch(Exception e){
			e.printStackTrace();
		}
	}
	
	/**
	 * Get a {@link NodeList} from {@link Document}.
	 * 
	 * @param tagName
	 * @return {@link NodeList}
	 */
	public NodeList getElementList(String tagName){
		NodeList nList = doc.getElementsByTagName(tagName);
		return nList;
	}
	
	/**
	 * Get a {@link Element} from a {@link Document}
	 * 
	 * @param tagName
	 * @return {@link Element}
	 */
	public Element getElement(String tagName){
		NodeList nList = doc.getElementsByTagName(tagName);
		Element element = null;
		if(nList.getLength() > 0){
			element = (Element) nList.item(0);
		}
		
		return element;
	}
	
	/**
	 * Add a {@link Node} to the current {@link Document}.
	 * 
	 * @param tagName
	 * @param node
	 */
	public void addElement(String tagName, Node node){
		NodeList nList = doc.getElementsByTagName(tagName);
		if(nList.getLength() > 0){
			Node n = nList.item(0);
			n.appendChild(node);
		}
	}
	
	/**
	 * Create a new {@link Node} using the {@Document} to create it.
	 * 
	 * @param name
	 * @return{@link Element}s
	 */
	public Element createElement(String name){
		return doc.createElement(name);
	}
	
	/**
	 * Import node into root document element.
	 * 
	 * @param node
	 * @param deep
	 */
	public void importNode(Node node, boolean deep){
		doc.getDocumentElement().appendChild(doc.importNode(node, deep));
	}
	
	/**
	 * Import node into document, append to child element.
	 * 
	 * @param node the node to add to document
	 * @param deep
	 * @param element  the element to append the node to in th edocument
	 * @return
	 */
	public Element importNode(Node node, boolean deep, Element element){
		element.appendChild(doc.importNode(node, deep));
		return element;
	}
	
	/**
	 * Removes the given node from the root document element.
	 * 
	 * @param node
	 */
	public void removeNode(Node node) {
		Node prev = node.getPreviousSibling();
		if (prev != null && 
		    prev.getNodeType() == Node.TEXT_NODE &&
		    prev.getNodeValue().trim().length() == 0) {
			doc.getDocumentElement().removeChild(prev);
		}
		doc.getDocumentElement().removeChild(node);
	}
	
	/**
	 * Creates a new TextNode containing a tab char
	 * 
	 * @return a new TextNode representing the tab char
	 */
	public Text createTabNode() {
		return doc.createTextNode("\t");
	}
	
	/**
	 * Get document element for document.
	 * 
	 * @return {@link Element}
	 */
	public Element getDocumentElement(){
		return doc.getDocumentElement();
	}
	
	/**
	 * Write out the current {@link Document} to a file.
	 * @param outFile the output file
	 * @throws Exception 
	 */
	public void writeXmlFile(String outFile){
			OutputStream os=null;
			try {
				os = new FileOutputStream(outFile);
			} catch (FileNotFoundException e) {
				logger.error(e.toString(), e);
				return;
			}
			
			writeXmlFileTo(os);
	}
	
	/**
	 * Write out the current {@link Document} to a file.
	 * @param os the {@link OutputStream}
	 * @throws Exception 
	 */
	public void writeXmlFileTo(OutputStream os){
		try{
			// Use a Transformer for output
			TransformerFactory tFactory = TransformerFactory.newInstance();
			Transformer transformer = tFactory.newTransformer();
			//.getDomConfig().setParameter("format-pretty-print", Boolean.TRUE);
			

			transformer.setOutputProperty(OutputKeys.METHOD, "xml");
			transformer.setOutputProperty(OutputKeys.INDENT, "yes");
			transformer.setOutputProperty("{http://xml.apache.org/xslt}indent-amount", "4");
			
			doc.getDocumentElement().normalize();
			
			DOMSource source = new DOMSource(doc);
			StreamResult result = new StreamResult(os);
			transformer.transform(source, result);
			
			os.close();
		}
		catch(Exception e ){
			logger.error(e.toString(), e);
		}
	}
	
	/**
	 * Converts the given Element to String
	 * 
	 * @param element
	 * @return
	 */
	public static String elementToString(Element element) {
		StringWriter buffer = new StringWriter();
		try {
			TransformerFactory transFactory = TransformerFactory.newInstance();
			Transformer transformer = transFactory.newTransformer();
			
			transformer.setOutputProperty(OutputKeys.OMIT_XML_DECLARATION, "yes");
			
				transformer.transform(new DOMSource(element),
				      new StreamResult(buffer));
		} catch (TransformerException e) {
			logger.error("elementToString failed", e);
		}
		String str = buffer.toString();
		return str;
	}
	
	/**
	 * Takes the given XML String and creates a new DOM Element
	 * 
	 * @param xml
	 * @return
	 */
	public static Element stringToElement(String xml) {
		XMLParser tmpParser = new XMLParser(true);
		InputStream is = new ByteArrayInputStream(xml.getBytes());
		tmpParser.readXmlFileFromStream(is);
		return tmpParser.getDocumentElement();
	}
	
}
	
	