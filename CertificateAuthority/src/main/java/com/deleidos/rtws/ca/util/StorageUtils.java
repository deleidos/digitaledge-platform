	public static InputStream decryptAndRetrieveCertificate() throws StorageUtilsException, IOException, InterruptedException, IllegalBlockSizeException, BadPaddingException, InvalidKeyException, NoSuchAlgorithmException, InvalidKeySpecException, NoSuchPaddingException {
		InputStream is = null;
		try {
			String password = PasswordBuilder.getInstance().getPassword();
		
			is = retrieveCertificate();
			byte [] encrypted = IOUtils.toByteArray(is);
			byte [] plaintext = decrypt(encrypted, password);
			is.close();
			return new ByteArrayInputStream(plaintext);
		} catch (Exception ex) {
			logger.debug("Error decrypting and retrieving certificate.", ex);
			throw new StorageUtilsException("Error decrypting and retrieving certificate.", ex);
		} finally {
			if (is != null)
				try {
					is.close();
				} catch (IOException e) {
					logger.error(e);
				}
		}
		
	}
	
