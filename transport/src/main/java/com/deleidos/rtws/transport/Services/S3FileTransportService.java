import com.deleidos.rtws.transport.Services.utils.S3TransportUtils;

@Description("A transport that transmits files from a specified S3 bucket into the system.")
public class S3FileTransportService extends AbstractTransportService {

	private Logger logger = LoggerFactory.getLogger(getClass());

	/** The Constant DEFAULT_POLLING_INTERVAL. */
	private static final long DEFAULT_POLLING_INTERVAL = 0;

	/** The keep polling. */
	private boolean keepPolling = true;

	/** The polling interval. */
	private String pollingInterval = null;
	
	/** The tar processing interval. */
	private String tarProcessingInterval = null;

	/** The used polling interval. */
	private long usedPollingInterval = DEFAULT_POLLING_INTERVAL;
	
	/** The used tar processing interval. */
	private long usedTarProcessingDelay = DEFAULT_POLLING_INTERVAL;

	/** The bucket name where files are located. */
	private String bucketName;
	
	/** The path inside the bucket to where the files are located. */
	private String bucketPath;

	/** Whether the file should be removed from the bucket after it has been transported. */
	private String shouldDeleteSource;

	/** The S3 storage implementation. */
	private StorageInterface storage;
	
	private boolean wait = false;
	
	private boolean noReprocess;
	
	private String proceedFileName;
	
	private Map<String,Long> reprocessMap;
	
	private boolean processLocal = false;
	
	private final static String LOCAL_DIR = "/mnt/rdafs/transport/s3/";
	
	private S3TransportUtils util = new S3TransportUtils();

	public String getPollingInterval() {
		return pollingInterval;
	}

	@UserConfigured(description = "The number of milliseconds between each poll of the S3 bucket for data to transmit. Use 0 to poll only once.",
			value = "0", flexValidator = { "NumberValidator minValue=0 maxValue="
			+ Integer.MAX_VALUE })
	public void setPollingInterval(String pollingInterval) {
		// make static analysis tools happy
		this.pollingInterval = pollingInterval;

		long tmp = DEFAULT_POLLING_INTERVAL;
		try {
			tmp = Long.valueOf(pollingInterval);
		} catch (NumberFormatException e) {
			log.error("Ignoring invalid input: " + pollingInterval, e);
			tmp = DEFAULT_POLLING_INTERVAL;
		}

		this.usedPollingInterval = tmp;
	}
	
	@UserConfigured(description = "The number of milliseconds between each processing of a tar file.",
			value = "0", flexValidator = { "NumberValidator minValue=0 maxValue=" + Integer.MAX_VALUE })
	public void setTarProcessingInterval (String tarProcessingInterval) {
		// make static analysis tools happy
		this.tarProcessingInterval = tarProcessingInterval;

		long tmp = DEFAULT_POLLING_INTERVAL;
		try {
			tmp = Long.valueOf(tarProcessingInterval);
		} catch (NumberFormatException e) {
			log.error("Ignoring invalid input: " + tarProcessingInterval, e);
			tmp = DEFAULT_POLLING_INTERVAL;
		}

		this.usedTarProcessingDelay = tmp;
	}	

	public String getBucketName() {
		return bucketName;
	}

	@UserConfigured(value = "yourS3BucketName", description = "The S3 bucket to poll for data to "
			+ "transmit into the system.", flexValidator = { "StringValidator minLength=3 maxLength=63" })
	public void setBucketName(String val) {
		bucketName = val;
	}

	public String getBucketPath() {
		return bucketPath;
	}
	
	@UserConfigured(value = "", description = "The path in the bucket to poll the data for.")
	public void setBucketPath(String val) {
		bucketPath = val;
		
		// Make sure the path does not start with the '/'
		if(bucketPath.startsWith("/")) {
			bucketPath = bucketPath.substring(1);
		}
		
		// Make sure if a path is specified, to include the '/' on the end
		if(!bucketPath.isEmpty() && !bucketPath.endsWith("/")) {
			bucketPath += "/";
		}
	}
	
	@UserConfigured(value = "false", description = "If true, do not reprocess unchanged files.",
			flexValidator = { "RegExpValidator expression=True|False" })
	public void setNoReprocess(boolean val) {
		noReprocess = val;
	}

	@UserConfigured(value = "false", description = "If true, sleep until a SYSTEM_NAME.RTWS_DOMAIN.proceed file exists in top level of bucket.",
			flexValidator = { "RegExpValidator expression=True|False" })
	public void setWait(boolean wait) {
		this.wait = wait;
	}

	@UserConfigured(value = "false",
					description = "If true, download the file locally before processing it. After processing is complete, " +
								"the local copy will be deleted.",
					flexValidator = { "RegExpValidator expression=True|False" })
	public void setProcessLocal(boolean processLocal) {
		this.processLocal = processLocal;
	}
	
	public boolean getNoReprocess(){
		return noReprocess;
	}

	public String getShouldDeleteSource() {
		return shouldDeleteSource;
	}

	@UserConfigured(description = "(Yes or No) to configure the tranport to remove the "
			+ "files from the S3 bucket after submission.", value = "No", flexValidator = { "RegExpValidator expression=Yes|No" })
	public void setShouldDeleteSource(String shouldDeleteSource) {
		this.shouldDeleteSource = shouldDeleteSource;
	}

	public StorageInterface getStorageInterface() {
		// See if the storage interface is currently configured,
		// if not, configure it from the interfaces configuration
		if(storage == null) {
			storage = InterfaceConfig.getInstance().getStorageInterface();
		}
		
		return storage;
	}

	public void setStorageInterface(StorageInterface val) {
		this.storage = val;
	}

	public void terminate() {
		keepPolling = false;
		try {
			getStorageInterface().removeFile(bucketName, proceedFileName);
		} catch (StorageException e) {
			e.printStackTrace();
		}
	}
	
	public void execute() {
		
		reprocessMap = new HashMap<String,Long>();
		
		proceedFileName = UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN) + ".proceed";

		logger.info("Checking for files in bucket '{}' under path '{}'", bucketName, bucketPath);

		while (keepPolling) {

			try {
				
				while (wait) {
					logger.debug(String.format("Waiting for %s to exist. Sleeping for 1 minute.", proceedFileName));
					List<String> contents = getStorageInterface().listFiles(bucketName);
					for (String o : contents) {
						if (o.contentEquals(proceedFileName)) {
							wait = false;
							logger.info("Found file in S3: " + proceedFileName);
						}
					}
					try { Thread.sleep(60000);} catch (InterruptedException e){}// do nothing
				}
				
				if (logger.isDebugEnabled())
					logger.debug("Polling bucket '{}' under path '{}'", bucketName, bucketPath);

				List<String> contents = getStorageInterface().listFiles(bucketName);
				for (String o : contents) {
					if(o.startsWith(bucketPath) &&
					   !o.endsWith("/") &&	// Ignore processing the bucket path itself
					   !o.substring(bucketPath.length()).contains("/")) {	// Ignore sub-directories
						if (noReprocess) {
							long updated = storage.getFileLastModified(bucketName, o);
							String fullFileKey = bucketName + "/" + o;
							if (reprocessMap.containsKey(fullFileKey)) {
								long stored = reprocessMap.get(fullFileKey);
								if (updated <= stored) {
									continue;
								}
							} else {
								reprocessMap.put(fullFileKey, updated);
								// go on to keep processing the file
							}
						}
						
						logger.info("Processing file: " + o);

						if (o.endsWith(".tar")) {
							try {
								if(processLocal) {
									try {Thread.sleep(usedTarProcessingDelay);} catch (InterruptedException e) {}
									String fileName = util.downloadFile(getStorageInterface(), bucketName, o, LOCAL_DIR);
									util.processTarFile(fileName, this, this.getSplitter(), bundler);
									deleteLocalFile(fileName);
								}
								else {
									util.processTarFile(o, bucketName, getStorageInterface(), this, this.getSplitter(), bundler);
								}
							} catch (Exception e) {
								if(e instanceof StorageException) {
									throw (StorageException)e;
								}
								
								logger.error("There was an error processing the Tar file '" + o + "': " + e.getMessage(), e);
								
								// Write the file as an error to the top level of the bucket
								StringBuilder prefix = new StringBuilder(UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN));
								prefix.append("_");
								prefix.append(o);
								prefix.append("_");
								prefix.append(getCurrentTimestamp());
								
								String suffix = ".error";

								String errorFileName = prefix.append(suffix).toString();
								
								try {
									File errorFile = File.createTempFile(prefix.toString(), suffix);

									logger.info("Writting error file " + errorFileName + " to S3 bucket: " + bucketName);
									getStorageInterface().storeFile(bucketName, errorFileName, errorFile);
								} catch (IOException ioe) {
									logger.error("Error creating Temporary file " + errorFileName, ioe);
								}
								
							}
						} else {
							enqueue(o);
						}

						// Delete file from S3 if required
						if ("yes".equalsIgnoreCase(shouldDeleteSource)) {
							getStorageInterface().removeFile(bucketName, o);
						}
					}
				}

				if (logger.isDebugEnabled())
					logger.debug("Finished processing bucket: '{}'", bucketName);
			} catch (StorageException e) {
				logger.error("There was an error retrieving a file from S3 : " + e.getMessage(), e);
			}

			try {
				// Wait for next go around....
				if (usedPollingInterval > 0) {
					WakeableSleepHelper sleepHelper = new WakeableSleepHelper(usedPollingInterval);
					while (sleepHelper.continueSleeping(!keepPolling)); // breaks when time to wake up
				}
				else
					keepPolling = false;

			} catch (InterruptedException e) {
				logger.error(e.getMessage(), e);
			}

			// TDD: Allow stopping
			if (Boolean.getBoolean("RTWS_TEST_MODE")) {
				logger.info("In test mode, quiting...");
				keepPolling = false;
			}
		}
		// processing is finished
		setInactive(this);
		log.debug("S3FileTransport Done.");
	}

	/**
	 * Download and enqueue.
	 * 
	 * @param fName
	 *            the f name
	 */
	private void enqueue(String fName) {
		InputStream stream = null;
		String localFileName = null;
		int bufferSize = 8096;
		StringBuilder buffer = new StringBuilder(bufferSize);

		try {
			if(processLocal) {
				localFileName = util.downloadFile(getStorageInterface(), bucketName, fName, LOCAL_DIR);
				stream = new FileInputStream(localFileName);
			}
			else {
				stream = getStorageInterface().getFile(bucketName, fName);
			}

			if (stream != null) {
				SetFileNameProperty(fName);
				getSplitter().setInputStream(stream);
				setHeader( getSplitter().parseHeaders() );

				String record = getSplitter().split();
				while (record != null && keepPolling) {
					if (record.length() > 0) {
						buffer.append(record);
						buffer.append("\n");
					}

					if (buffer.length() >= bufferSize) {

						// Send Message
						logger.debug("sending:" + buffer.toString());
						SendJMSMessage(buffer.toString());

						// reset buffer
						buffer.delete(0, buffer.length());
					}

					record = getSplitter().split();
				}

				// Flush remaining buffer
				SendJMSMessageAndFlush(buffer.toString());	
				Reset();
				
				logger.info("Finished processing file: {}/{} ", bucketName, fName);

			} else {
				logger.error("Unable to read and transport: '{}' from bucket: .'{}'",
					fName, bucketName);
			}

		} catch (StorageException e) {
			logger.error("There was an error retrieving a file from S3 : " + e.getMessage(), e);
		} catch (FileNotFoundException e) {
			logger.error("Unable to find local file : " + e.getMessage(), e);
		} catch (IOException e) {
			logger.error("There was an error accessing local file : " + e.getMessage(), e);
		} finally {
			try {
				if (stream != null)
					stream.close();
				if(processLocal && localFileName != null) 
					deleteLocalFile(localFileName);
			} catch (IOException e) {
				logger.error(e.getMessage(), e);
			}

		}

	}

	private void deleteLocalFile(String fileName) {
		File file = new File(fileName);
		
		if(file.exists()) {				
			file.delete();
		
			log.debug("Deleted file '" + file.getAbsolutePath() + "'.");
		}
	}
	
	private String getCurrentTimestamp() {
		DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd_HH-mm-ss");
		String dateString = dateFormat.format(new Date());
		
		return dateString;
	}

}
