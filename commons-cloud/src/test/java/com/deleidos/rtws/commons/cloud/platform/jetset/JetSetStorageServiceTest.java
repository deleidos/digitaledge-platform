	@Test
	public void testLoadStorageServiceFromConfigFile() {
		File configFile = new File(basedir + "/src/test/resources/storage.jetsettest.xml");
		Assert.assertTrue(configFile.exists());
		 
		try {
			 // Configure an XML marshaller for the classes referenced in the configuration file.
			JAXBContext context = JAXBContext.newInstance(configClasses);
			Unmarshaller marshaller = context.createUnmarshaller();
			 
			 // Create a stream to read the configuration file.
			InputStream stream  = PropertiesUtils.loadResource(configFile.getAbsolutePath());
			StreamSource source = new StreamSource(stream);
			TestMarshalWrapper wrapper = marshaller.unmarshal(source, TestMarshalWrapper.class).getValue(); 
			StorageInterface storage = wrapper.getStorageInterface();
			Assert.assertNotNull(storage);
			Assert.assertTrue(storage instanceof JetSetStorageService);
		} catch (Exception ex) {
			Assert.fail("Marshalling failed with exception: " + ex.getMessage());
		}
	}
	
	@Test
	public void testOneUpFilename() {
		String[] ex = {};
		String result = service.nextOneUpFileKey("testing.txt", Arrays.asList(ex));
		Assert.assertEquals("testing.txt", result);

		String[] ex1 = {"testing.txt"};
		result = service.nextOneUpFileKey("testing.txt", Arrays.asList(ex1));
		Assert.assertEquals("testing-1.txt", result);

		String[] ex2 = {"testing", "testing.txt"};
		result = service.nextOneUpFileKey("testing", Arrays.asList(ex2));
		Assert.assertEquals("testing-1", result);

		String[] ex3 = {"testing", "testing.txt"};
		result = service.nextOneUpFileKey("testing.jpg", Arrays.asList(ex3));
		Assert.assertEquals("testing.jpg", result);

		String[] ex4 = {"testing.txt", "testing-1.txt", "testing-2.txt", "testing-3.txt"};
		result = service.nextOneUpFileKey("testing.txt", Arrays.asList(ex4));
		Assert.assertEquals("testing-4.txt", result);

		String[] ex5 = {"testing", "testing.txt", "testing_2012.txt", "testing-1.txt", "testing-2.txt"};
		result = service.nextOneUpFileKey("testing.txt", Arrays.asList(ex5));
		Assert.assertEquals("testing-3.txt", result);

		String[] ex6 = {"testing", "testing.txt", "testing.jpg", "testing_2012.txt", "testing-1.txt", "testing-2.txt"};
		result = service.nextOneUpFileKey("testing.jpg", Arrays.asList(ex6));
		Assert.assertEquals("testing-1.jpg", result);

		String[] ex7 = {"testing", "testing.txt", "testing.jpg", "testing_2012.txt", "testing-1.txt", "testing-2.txt"};
		result = service.nextOneUpFileKey("test.txt", Arrays.asList(ex7));
		Assert.assertEquals("test.txt", result);

		String[] ex8 = {"testing", "testing.txt", "testing.jpg", "testing_2012.txt", "testing-1.txt", "testing-2.txt", "test.txt"};
		result = service.nextOneUpFileKey("test.txt", Arrays.asList(ex8));
		Assert.assertEquals("test-1.txt", result);
	}

