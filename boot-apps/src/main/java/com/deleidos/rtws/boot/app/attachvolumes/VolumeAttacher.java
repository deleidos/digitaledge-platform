public class VolumeAttacher {
	
	public static final Logger logger = LogManager.getLogger(VolumeAttacher.class);
	private final String CONFIG_FILE = "filepath";
	private Marshaller marshaller;
	private CommandLine line;
	private Options options;
	
	private AttacherDefinition definition;
	/** Utility used to discover an AMI instances own properties. */
	private IntrospectionClient introspectionClient = new IntrospectionClient();
	
	public VolumeAttacher(String [] args) throws VolumeAttacherException{
		options = buildOptions();
		
		try {
			CommandLineParser parser = new PosixParser();
			this.line = parser.parse(options, args);
			
			marshaller = new Marshaller();
		} catch (Exception pe) {
			throw new VolumeAttacherException(pe.getMessage(), pe);
		}
		
		checkRequiredOptions(options);
	}
	
	
	/**
	 * Executes the volume attacher, attaches volumes using configuration passed in.
	 */
	public void execute(){
		try{
			//load configuration file
			readConfigFile(line.getOptionValue(CONFIG_FILE));
			
			ServiceInterface service = definition.getServiceInterface();
			StorageInterface storage = definition.getStorageInterface();
			
			//create key for volumes.xml file
			String fileKey = SystemEnvUtil.buildProcessesFileKey(SystemEnvUtil.getDomain(), new File("volumes.xml").getName());
			
			//check if bucket exists
			if(storage.bucketExists(SystemEnvUtil.getShadowCopyBucket())){
				System.out.println("Storage bucket exists.");
			}
			else{
				System.out.println("Bucket does not exist, stopping execution.");
				System.exit(-1);
			}
		
			//check if file exists in bucket
			if(storage.fileExists(SystemEnvUtil.getShadowCopyBucket(), fileKey)){
				logger.debug("Bucket and volume file exist;");
				String id = introspectionClient.getId();  //instance id
				
				//store volumes from bucket to temporary file
				TempFile tempFile = new TempFile(new File("volumes.xml"));
				tempFile.deleteOnExit();  //cleanup file after finished
				storage.getFile(SystemEnvUtil.getShadowCopyBucket(), fileKey, tempFile);
				
				AttacherVolumes volumes = (AttacherVolumes) (Unmarshaller.unmarshal(AttacherVolumes.class, 
																new InputStreamReader(new FileInputStream(tempFile))));
				
				//for each volume listed in file try to reattach
				for(Volume volume : volumes.getVolumes()){
					
					Volume current = null;
					try{
						current = service.listVolume(volume.getId());
					}
					catch(Exception e){
						logger.error(e.toString(), e);
					}
					
					if(current != null){
						//check if already attached
						if("in-use".equals(current.getState())){
							//nothing to do
						}
						//reattach
						else if("available".equals(current.getState())){
							logger.info(String.format("Re-Attaching volume:  %s ", volume.getId()));
							AttachVolumeResult  attachResult = service.attachVolume(volume, id);
							volume.setSize(current.getSize());
							volume.setDevice(attachResult.getAttachment().getDevice());
							volume.setId(attachResult.getAttachment().getVolumeId());
						}
						//in unknown state
						else{
							logger.info("State is:  " + current.getState());
						}
					}
					//volume does not exist create
					else{
						volume.setSize((float) definition.getVolumeSize());
						
						Instance instance = service.describeInstance(id);
						String zone = instance.getPlacement().getAvailabilityZone();
						
						CreateVolumeResult volumeResult = service.createVolume(volume, zone);
						
						String out = String.format("Creating then attaching volume id:  %s, instance id:  %s, device:  %s", 
								volumeResult.getVolume().getVolumeId(), id, volumeResult.getVolume().getAttachments());
						logger.debug(out);
						
						//create volume to replace lost volume
						Volume tmp = new Volume();
						tmp.setDevice(volume.getDevice());
						tmp.setId(volumeResult.getVolume().getVolumeId());
						tmp.setSize((float) volumeResult.getVolume().getSize());
						
						
						AttachVolumeResult attachVolume = service.attachVolume(tmp, id);
						//update volume
						volume.setDevice(attachVolume.getAttachment().getDevice());
						volume.setId(attachVolume.getAttachment().getVolumeId());
						volume.setCreateTime(volumeResult.getVolume().getCreateTime());
					}
				}//end for
				
				//update volumes file to bucket
				TempFile saveFile = createVolumeFile(volumes);
				storage.storeFile(SystemEnvUtil.getShadowCopyBucket(), fileKey, saveFile);
				tempFile.delete();
				
				//update rtwsrc file
				updateRTWSRCFile(volumes);
			}
			//volumes file does not exist in bucket create new volumes based on configuration values
			else{
				createNewAttach(fileKey, service, storage);
			}
			
			try {
				// Note: This sleep was placed here to help alleviate the load on the controller
				// so we don't hit the describe volume service too often.
				Thread.sleep(5000);
			} catch (InterruptedException e) {
				// ignore exception
			}
			
			int state = 0;
			int retryCount = 0;
			
			while(state != 1 && retryCount < 5){
				//check that the volumes are attached and in a ready state
				state = checkVolumesAttached(service, storage);
				retryCount++;
				Thread.sleep(8000);  //sleep 8 seconds then retry
			}
		}
		catch(Exception e){
			logger.error(e.toString(), e);
		}
	}
	
	/**
	 * Creates new volumes based on configuration parameters.
	 * 
	 * @param fileKey String bucket file key
	 * @param service {@link ServiceInterface}
	 * @param storage {@link StorageInterface}
	 * @throws Exception
	 */
	private void createNewAttach(String fileKey, ServiceInterface service, StorageInterface storage) throws Exception{
		logger.debug("Creating new file for volume:  " + fileKey);
		
		AttacherVolumes volumes = new AttacherVolumes();
		String id = introspectionClient.getId();  //instance id
		
		//create volumes for the volumecount in the configuration
		for(int i = 0; i < definition.getVolumeCount(); i++){
			
			Volume volume = new Volume();
			volume.setSize((float) definition.getVolumeSize());
			
			Instance instance = service.describeInstance(id);
			String zone = instance.getPlacement().getAvailabilityZone();
			
			CreateVolumeResult volumeResult = service.createVolume(volume, zone);
			
			String out = String.format("Creating then attaching volume id:  %s, instance id:  %s, device:  %s", 
					volumeResult.getVolume().getVolumeId(), id, volumeResult.getVolume().getAttachments());
			logger.debug(out);
			
			String volumeId = volumeResult.getVolume().getVolumeId();
			
			try {
				// Note: This sleep was placed here to help alleviate the load on the controller
				// so we don't hit the describe volume service too often.
				Thread.sleep(5000);
			} catch (InterruptedException e) {
				// ignore exception
			}
			
			int retry = 20;
			while (retry > 0) {
				System.out.println("Attaching volume '" + volumeId + "' to instance '" + id + "' attempt #" + retry);
				
				// Note: In euca-land, using the service.listVolume(volumeId) method returns a null 
				// value because euca returns all the available volumes for a given account.
				
				Volume checkVolume = service.listVolume(volumeId);
				if (checkVolume != null && checkVolume.getState().equals("available")) {
					
					AttachVolumeResult attachVolume = service.attachVolume(volumeResult, id, definition.getMountDevice(), String.valueOf(i + 1));
					//reset volume
					volume = new Volume();
					volume.setDevice(attachVolume.getAttachment().getDevice());
					volume.setId(attachVolume.getAttachment().getVolumeId());
					volume.setSize((float) definition.getVolumeSize());
					volume.setCreateTime(volumeResult.getVolume().getCreateTime());
					
					volumes.addVolume(volume);
					
					System.out.println("Attached volume '" + volumeId + "' to instance '" + id + "'.");
					
					break;
				}
				
				try {
					Thread.sleep(8000);
				} catch (InterruptedException e) {
					// ignore exception
				}
				
				retry--;
			}
		}
		
		//update volumes file to bucket
		TempFile tempFile = createVolumeFile(volumes);
		storage.storeFile(SystemEnvUtil.getShadowCopyBucket(), fileKey, tempFile);
		tempFile.delete();
		
		//update rtwsrc file
		updateRTWSRCFile(volumes);
	}
	
	private int checkVolumesAttached(ServiceInterface service, StorageInterface storage){
		int returnState = 1;
		
		//create key for volumes.xml file
		String fileKey = SystemEnvUtil.buildProcessesFileKey(SystemEnvUtil.getDomain(), new File("volumes.xml").getName());
		try{
			//check if bucket exists
			if(storage.bucketExists(SystemEnvUtil.getShadowCopyBucket())){
				System.out.println("Storage bucket exists.");
			}
			else{
				System.out.println("Bucket does not exist, stopping execution.");
				System.exit(-1);
			}
		
			//check if file exists in bucket
			if(storage.fileExists(SystemEnvUtil.getShadowCopyBucket(), fileKey)){
				logger.debug("Bucket and volume file exist;");
				
				//store volumes from bucket to temporary file
				TempFile tempFile = new TempFile(new File("volumes.xml"));
				storage.getFile(SystemEnvUtil.getShadowCopyBucket(), fileKey, tempFile);
				
				AttacherVolumes volumes = (AttacherVolumes) (Unmarshaller.unmarshal(AttacherVolumes.class, 
																new InputStreamReader(new FileInputStream(tempFile))));
				
				if(volumes != null){
					for(Volume volume : volumes.getVolumes()){
						
						//get current volume state
						Volume current = null;
						try{
							current = service.listVolume(volume.getId());
						}
						catch(Exception e){
							logger.error(e.toString(), e);
							returnState = 0;
						}
						
						if(current != null){
							//check if already attached
							if("in-use".equals(current.getState())){
								//nothing to do
							}
							//in unknown state
							else{
								logger.info("State is:  " + current.getState());
								returnState = 0;
							}
						}
						else{
							logger.error(String.format("Unable to find volume %d to check attached state.", volume.getId()));
						}
					}
				}//end if
			}
			//volumes file does not exist in bucket
			else{
				logger.error("The volumes.xml file does not exist in RTWS_BUCKET_NAME, problem checking volume attached state.");
			}
		}
		catch(Exception e){
			logger.error(e.toString(), e);
			returnState = 0;
		}
		
		return returnState;
	}
	
	/**
	 * Creates a volumes.xml file.
	 * 
	 * @param volumes {@link AttacherVolumes} volumes object
	 * @return {@link TempFile}
	 * @throws Exception
	 */
	public TempFile createVolumeFile(AttacherVolumes volumes) throws Exception {
		TempFile tempFile = null;
		try{
			tempFile = new TempFile(new File("volumes.xml"));
			
			OutputStreamWriter writer = new OutputStreamWriter(new FileOutputStream(tempFile));
		
			marshaller.setWriter(writer);
			marshaller.marshal(volumes);
			marshaller.setProperty("org.exolab.castor.indent", "true");  //format output xml file
			
			writer.close();
		}catch(Exception e){
			logger.error(e.toString(), e);
			throw e;
		}
		
		return tempFile;
	}
	
	/**
	 * Read configuration file.
	 * 
	 * @param inputFile String file name
	 */
	public void readConfigFile(String inputFile){
		InputStreamReader reader = null;
		 definition = null;
		try{
			File f = new File(inputFile);
			System.out.println("Input file:  " + f.exists());
			reader = new InputStreamReader(new FileInputStream(inputFile));
			definition = (AttacherDefinition) (Unmarshaller.unmarshal(AttacherDefinition.class, reader));
			definition.intializeInterfaces();
			reader.close();
		}catch(Exception e){
			logger.error(e.toString(), e);
			try {
				reader.close();
			} catch (IOException ioe) {
				logger.error(e.toString(), e);
			}
		}
	}
	
	
	/**
	 * Build command line supported options.
	 */
	private Options buildOptions() {
		
		Options options = new Options();
		
		options.addOption(CONFIG_FILE, true, "path to configuration file");
		
		return options;
	}
	
	/**
	 * Checks that required parameters were passed in.
	 * @param options {@link Options}
	 * @throws VolumeAttacherException
	 */
	private void checkRequiredOptions(Options options) throws VolumeAttacherException {
		if (! line.hasOption(CONFIG_FILE)) {
			HelpFormatter formatter = new HelpFormatter();
			formatter.printHelp("com.deleidos.rtws.boot.app.attachvolumes.VolumeAttacher", options);
			System.exit(1);
		}
	}
	

	private void updateRTWSRCFile(AttacherVolumes volumes){
		try{
			if (volumes == null || volumes.getVolumes().size() == 0) {
				return;
			}
			
			//open /etc/rtwsrc, copy to memory remove RTWS_RAID_DEVICES, then write the file
			
			StringBuilder rtwsrc = new StringBuilder("");
			
			BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream("/etc/rtwsrc")));
			
			String line = null;
			
			while((line = br.readLine()) != null){
				if(line.contains("RTWS_RAID_DEVICES=")){
					//skip line will be updated later
				}
				else{
					rtwsrc.append(line);
					rtwsrc.append("\n");
				}
			}
			
			br.close();
			
			BufferedWriter bw = new BufferedWriter(new FileWriter("/etc/rtwsrc"));
			
			StringBuilder sb = new StringBuilder("RTWS_RAID_DEVICES=");
			for(int i = 0; i < volumes.getVolumes().size(); i++){
				sb.append(volumes.getVolumes().get(i).getDevice());
				
				if((i + 1) < volumes.getVolumes().size()){
					sb.append(",");
				}
			}
			
			sb.append("\n");
			
			bw.write(rtwsrc.toString());  //original /etc/rtwsrc file minus raid devices
			bw.write(sb.toString());  //raid devices
			
			bw.flush();
			bw.close();
		}
		catch(Exception e){
			logger.error(e.toString(), e);
		}
	}
	
	/**
	 * Main method for volume attacher.
	 * @param args
	 */
	public static void main(String [] args){
		try {
			
			logger.debug("Good found properties file");
			
			String runAttacher = System.getProperty("RTWS_RUN_ATTACHER");
			
			boolean run = false;
			
			try{
				run = Boolean.parseBoolean(runAttacher.toLowerCase());
			}
			catch(Exception e){
				e.printStackTrace();
				run = false;
			}
			
			if(runAttacher != null && run == true){
				VolumeAttacher attacher;
				attacher = new VolumeAttacher(args);
				attacher.execute();
				
				System.out.println("Finished attaching/creating volumes for instance.");
			}
			else{
				System.out.println("Not running volume attacher, system property RTWS_RUN_ATTACHER is not set.");
			}
			
			
		} catch (Exception e) {
			e.printStackTrace();
		}	
	}
}
