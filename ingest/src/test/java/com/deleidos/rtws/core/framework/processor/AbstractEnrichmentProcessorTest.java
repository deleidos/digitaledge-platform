	public static void configure(AbstractEnrichmentProcessor processor, String outputField, String extractField, boolean override, boolean remove, String... inputFields) {
		
		EnrichmentAction action = new EnrichmentAction();
		action.setField(outputField);
		action.setType(processor.getType());
		action.setOverride(override);
		action.setExtractField(extractField);
		action.setRemoveParameters(remove);
		action.setParameters(inputFields);

		EnrichmentFieldMappingCache fields = new EnrichmentFieldMappingCache();
		fields.setModelName(DUMMY_MODEL_TYPE);
		fields.setModelVersion("1.0");
		fields.setFields(new EnrichmentAction[] { action });

		EnrichmentModelMappingCache models = new EnrichmentModelMappingCache();
		models.setModels(new EnrichmentFieldMappingCache[] { fields });

		processor.setEnrichments(models);
		
	}
	
