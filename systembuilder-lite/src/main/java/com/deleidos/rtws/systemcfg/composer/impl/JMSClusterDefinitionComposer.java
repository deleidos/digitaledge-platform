	/**
	 * Create small node with combined internal and external nodes.
	 * 
	 * @param config
	 */
	private void createCombinedNode(SystemConfig config){
		for(int i = 1; i <= this.numNodes; i++){
			String fileName = combinedTemplate;
			
			//add cursor type to queue and subscriber
			Element element = parser.getElement("pendingSubscriberPolicy");
			element.appendChild(createCursor(false, CursorType.VM_CURSOR, true));
			
			element = parser.getElement("pendingQueuePolicy");
			element.appendChild(createCursor(true, CursorType.VM_CURSOR, true));
			
			parser.writeXmlFile(this.outputDir + fileName);
			parser.readXmlFile(this.templateFile);  //reset
		}
	}
	
	/**
	 * Create additional jms nodes that are not the standard external
	 * and internal jms cluster.
	 */
	private void createNonStandardNode(){
		for (String additionalTemplate : additionalTemplates) {
			if (additionalTemplate.equals("/activemq-pubsub-server.xml")) {
				/*
				  create and add authorization entries
				 	1. enable anonymous login but only allow it to read topics
					2. users and admins group can read/write/create queues and topics
					
				 <authorizationPlugin>
            		<map>
            			<authorizationMap>
            				<authorizationEntries>
                        		<authorizationEntry queue=">" read="admins" write="admins" admin="admins" />
                            	<authorizationEntry topic=">" read="anonymous,admins" write="admins" admin="admins" />
                            	<authorizationEntry topic="ActiveMQ.Advisory.Consumer.Topic.>" read="anonymous,admins" write="anonymous,admins" admin="anonymous,admins" />
                        	</authorizationEntries>
                    	</authorizationMap>
                	</map>
             	 </authorizationPlugin>
				*/
				Element authElement = parser.getElement("simpleAuthenticationPlugin");
				authElement.setAttribute("anonymousAccessAllowed", "true");
				
				Element authQueueEntry = parser.createElement("authorizationEntry");
				authQueueEntry.setAttribute("queue", ">");
				authQueueEntry.setAttribute("read", "admins");
				authQueueEntry.setAttribute("write", "admins");
				authQueueEntry.setAttribute("admin", "admins");
				
				Element authTopicEntry = parser.createElement("authorizationEntry");
				authTopicEntry.setAttribute("topic", ">");
				authTopicEntry.setAttribute("read", "anonymous,admins");
				authTopicEntry.setAttribute("write", "admins");
				authTopicEntry.setAttribute("admin", "admins");
				
				Element authAdvisoryTopicEntry = parser.createElement("authorizationEntry");
				authAdvisoryTopicEntry.setAttribute("topic", "ActiveMQ.Advisory.Consumer.Topic.>");
				authAdvisoryTopicEntry.setAttribute("read", "anonymous,admins");
				authAdvisoryTopicEntry.setAttribute("write", "anonymous,admins");
				authAdvisoryTopicEntry.setAttribute("admin", "anonymous,admins");
				
				Element authEntries = parser.createElement("authorizationEntries");
				authEntries.appendChild(authQueueEntry);
				authEntries.appendChild(authTopicEntry);
				authEntries.appendChild(authAdvisoryTopicEntry);
				
				Element authMap = parser.createElement("authorizationMap");
				authMap.appendChild(authEntries);
				
				Element map = parser.createElement("map");
				map.appendChild(authMap);
				
				Element authPlugin = parser.createElement("authorizationPlugin");
				authPlugin.appendChild(map);
				
				Element plugins = parser.getElement("plugins");
				plugins.appendChild(authPlugin);
				
				// add cursor type to queue and subscriber
				Element element = parser.getElement("pendingSubscriberPolicy");
				element.appendChild(createCursor(false, CursorType.VM_CURSOR, true));
				
				element = parser.getElement("pendingQueuePolicy");
				element.appendChild(createCursor(true, CursorType.VM_CURSOR, true));
				
				parser.writeXmlFile(this.outputDir + additionalTemplate);
			}
			parser.readXmlFile(this.templateFile);  //reset
		}
	}
	
	/**
	 * Creates the JMS node properties for the combined node case.
	 * 
	 * @param config {@link SystemConfig}
	 */
	private void createCombinedNodeProperties(SystemConfig config){
		//external properties
		
		StringBuilder connURL = new StringBuilder();
		
		//node name properties 
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = combinedNodeName.replace("@", config.getSystemDomain());
			properties.setProperty(extPropName.replace('?', (char)(i + 48)), nodeValue);
			connURL.append(failOverNode.replace("@", nodeValue) + ",");
			
			//specifying only the first jms ext node as the node to receive alerts
			if (i == 1) {
				String alertsURL = connURL.toString().substring(0, connURL.length() - 1);
				alertsURL+= "?jms.prefetchPolicy.all=3?wireFormat.maxInactivityDuration=300000?jms.useCompression=true";
				properties.setProperty(alertsURLPropName, alertsURL);
			}
		}
		connURL.replace(connURL.length() - 1, connURL.length(), "");
		
		//jmx properties
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = combinedNodeName.replace("@", config.getSystemDomain());
			properties.setProperty(extJMXPropName.replace('?', (char)(i + 48)), jmxURL.replace("?", nodeValue));
		}
		
		//connection url
		properties.setProperty(extURLPropName, connURL.toString());
		
		//internal properties
		connURL = new StringBuilder("");
		
		//node name properties 
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = combinedNodeName.replace("@", config.getSystemDomain());
			properties.setProperty(intPropName.replace('?', (char)(i + 48)), nodeValue);
			connURL.append(failOverNode.replace("@", nodeValue) + ",");
		}
		
		connURL.replace(connURL.length() - 1, connURL.length(), "");
		
		//jmx properties
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = combinedNodeName.replace("@", config.getSystemDomain());
			properties.setProperty(intJMXPropName.replace('?', (char)(i + 48)), jmxURL.replace("?", nodeValue));
		}
		
		//connection url
		properties.setProperty(intURLPropName, connURL.toString());
	}

	
	/**
	 * Creates the JMS node properties.
	 * 
	 * @param config {@link SystemConfig}
	 * @param context {@link SystemContext}
	 */
	private void createProperties(SystemConfig config, SystemContext context){
		if(context.getSystemSize().getCombineIntExtFlag() == false){
			createInternalNodeProperties(config);
			createExternalNodeProperties(config);
		}
		//if combined flag true
		else{
			createCombinedNodeProperties(config);
		}
	}
