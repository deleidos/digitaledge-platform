	/**
	 * Get the number of bits for an instance size.
	 * 
	 * @param instanceTypes
	 * @param instanceSize
	 * @return number of bits for instance
	 */
	public static int getBitsForInstanceSize(ArrayList<InstanceType> instanceTypes, String instanceSize){
		int defaultNum = 0;
		for(InstanceType type : instanceTypes){
			if(type.getInstanceType().equals(instanceSize)){
				return type.getNumBits();
			}
		}

		return defaultNum;
	}

	/**
	 * Get image id based on user and system context configurations.
	 * 
	 * @param images
	 * @param serviceName
	 * @param region
	 * @param swVersionId
	 * @param instanceTypes
	 * @param storage
	 * @param instanceSize
	 * @return String image type
	 * @throws ComposerException 
	 */
	public static String getImageType(ArrayList<MachineImage> images, String serviceName, String region, String swVersionId, 
			ArrayList<InstanceType> instanceTypes, String storage, String instanceSize, String tenantId)
					throws ComposerException {

		if (images == null || images.size() <= 0) {
			throw new ComposerException("No images available in MACHINE_IMAGES table! Contact your system administrator.");
		}

		String imageType = null;

		//determine how to throw error if no image found
		boolean serviceFound=false;
		boolean regionFound=false;
		boolean swVersionFound=false;
		boolean tenantIdFound=false;
		boolean storageTypeFound=false;
		int bits = getBitsForInstanceSize(instanceTypes, instanceSize);
		for(MachineImage image : images){
			if(imageType==null){
				//does the IAAS service name match requested service name.
				if(image.getIaasServiceName().equals(serviceName)){
					serviceFound = true;
					//does the IAAS region match requested region
					if(image.getIaasRegion().equals(region)){
						regionFound=true;
						//if list of images software version id matches the requested version
						if(image.getSWVersionId().equals(swVersionId)){
							swVersionFound=true;
							//does this image belong to this tenant
							if(image.getTenantId().equals(tenantId)) {
								tenantIdFound=true;
								if(storage.equals("instance")){
									storageTypeFound=true;
									if(bits == 64){
										imageType = image.getMI64BitInstance();
									}
									else{
										//unknown bit size
										//throw new ComposerException("No match in INSTANCE_TYPES table for type [" + instanceSize + "] [" + bits + "] bits! Contact your system administrator.");
									}
								}								
								//else{
								//unknown storage type
								//throw new ComposerException("No images matching instance type [" + storage + "]! Contact your system administrator.");
								//}
							}
						}//end if
						//else {
						//throw new ComposerException("No images matching software version [" + swVersionId + "]! Contact your system administrator.");
						//}
					}// end if
					//else {
					//throw new ComposerException("No images matching region [" + region + "]! Contact your system administrator.");
					//}
				}// end if
				//else {
				//throw new ComposerException("No images matching service name [" + serviceName + "]! Contact your system administrator.");
				//}
			}//end if check for loop end (image found)
		}//end for
		if(imageType==null)
			//determine which exception to throw.
			if(!serviceFound){
				throw new ComposerException("No images matching service name [" + serviceName + "]! Contact your system administrator.");
			}else if(!regionFound){
				throw new ComposerException("No images matching region [" + region + "]! Contact your system administrator.");
			}else if(!swVersionFound){
				throw new ComposerException("No images matching software version [" + swVersionId + "]! Contact your system administrator.");
			}else if(!tenantIdFound){
				throw new ComposerException("No images matching tenantId [" + tenantId + "]! Contact your system administrator.");
			}else if(!storageTypeFound){
				throw new ComposerException("No images matching instance type [" + storage + "]! Contact your system administrator.");
			}else{//bit type not found
				throw new ComposerException("No match in INSTANCE_TYPES table for type [" + instanceSize + "] [" + bits + "] bits! Contact your system administrator.");
			}

		return imageType;
	}

