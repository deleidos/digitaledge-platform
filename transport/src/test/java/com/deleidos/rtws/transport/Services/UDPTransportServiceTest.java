	public void setUp() throws Exception {
		startUdpTransport();
	}

	@Test
	public void testTransport() {
		testDataPacketClass();

		sendUdpPackets();
		
		try {
			Thread.sleep(2*1000);
		} catch (InterruptedException ignore) {
		}
	}
	
	private void testDataPacketClass() {
		for( int i = 0; i < packetsToSend; i++ ) {
			DataPacket dataPacket1 = new DataPacket(i,getRandomString(maxPacketSize));
			assertEquals(i,dataPacket1.getPacketId());
			assertTrue(dataPacket1.isHashValid());
			byte[] packet = dataPacket1.getBytes();
			DataPacket dataPacket2 = new DataPacket(packet);
			assertTrue(dataPacket2.isHashValid());
			assertEquals(dataPacket1.getPacketId(),dataPacket2.getPacketId());
			
			byte[] hash1 = dataPacket1.getHash();
			byte[] hash2 = dataPacket2.getHash();
			if ( hash1 != null && hash2!= null )
				assertEquals(Hex.encodeHexString(dataPacket1.getHash()),Hex.encodeHexString(dataPacket2.getHash()));
			else if ( hash1 == null && hash2 != null)
				assertFalse(true);
			else if ( hash1 != null && hash2 == null)
				assertFalse(true);
			assertEquals(dataPacket1.getData(),dataPacket2.getData());
		}
	}

	@After
	public void tearDown() throws Exception {
		stopUdpTransport();
		executeTrasportThread.join(10*1000);

		reportStatistics(System.currentTimeMillis());
		
		assertEquals(packetsToSend, packetCount.get());
	}
	
	void reportStatistics(long ended) {
		ended = System.currentTimeMillis();
		long delta = ended - started;
		if ( delta == 0 )
			delta = 1;
		long count = packetBytesCount.get();
		log.info(String.format("On %d cores processed %d bytes in %d ms i.e. %.2f Mb/sec", 
				Runtime.getRuntime().availableProcessors(), count, delta, (count/1024./1024.)/(delta/1000.0)));
	}
	
	private void startUdpTransport() {
		try {
			assertNull(udpTransport);
			udpTransport = new UDPTransportService();
			udpTransport.setMessageProcessorClass("com.deleidos.rtws.transport.Services.UDPTransportServiceTest$TestUDPMessageProcessor");
			udpTransport.setEncoding(Encoding.Base16);
			maxPacketSize = udpTransport.getMaxPacketSize();
			sendPort = udpTransport.getListenPort();
			executeTrasportThread = new Thread(new ExecuteUDPTransportService(udpTransport));
			executeTrasportThread.start();
		} catch (Exception ex ) {
			log.error("Couldn't execute UDP transport service", ex);
			assertFalse(true);
		}
	}
	
	private void stopUdpTransport() {
		if ( udpTransport != null ) {
			try {
				udpTransport.terminate();
				udpTransport = null;
			} catch (Exception ex ) {
				log.error("Couldn't terminate UDP transport service", ex);
				assertFalse(true);
			}			
		}
	}
	
	private void sendUdpPackets() {
		InetAddress ipAddress = null;
		try {
			ipAddress = Inet6Address.getByName("localhost");
		} catch (UnknownHostException ex) {
			log.error("Couldn't get local host by name", ex);
			assertFalse(true);
		}
		DatagramSocket socket = null;
		started = System.currentTimeMillis();
		try {
			socket = new DatagramSocket();
			for( int i = 0; i < packetsToSend; i++ ) {
				DataPacket dataPacket = new DataPacket(i,getRandomString(maxPacketSize));
				byte[] packet = dataPacket.getBytes();
				DatagramPacket datagramPacket = new DatagramPacket(packet, packet.length, ipAddress, sendPort);
				socket.send(datagramPacket);
				try {
					Thread.sleep(10);
				} catch (InterruptedException igonore) {
				}
			}
		} catch (IOException ex) {
			log.error("Couldn't send datagram packet", ex);
			assertFalse(true);
		} finally {
			if ( socket != null ) {
				if ( !socket.isClosed() )
					socket.close();
				socket = null;
			}
		}		
	}
	
	private String getRandomString(int maxBytesLenth) {
		// RandomStringUtils uses full range of UNICODE so we could get multi-byte characters.
		// Thus, to limit the total number of bytes we divide by 4 to make sure that we won't exceed the limit and reserve room for ID, SIZE, and HASH
		int randomSize = (int)(Math.random()*(maxBytesLenth/4-((Integer.SIZE/Byte.SIZE)*2+HASH_SIZE)));
		return RandomStringUtils.random(randomSize);
	}

