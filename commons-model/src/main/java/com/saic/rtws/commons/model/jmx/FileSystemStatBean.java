package com.saic.rtws.commons.model.jmx;

public class FileSystemStatBean {

	private String devName;
	private String dirName;
	private String sysTypeName;
	private String fsTypeName;
	private long fsReads;
	private long fsReadBytes;
	private long fsWrites;
	private long fsWriteBytes;
	
	public FileSystemStatBean(String devName, String dirName, String sysTypeName, String fsTypeName,
			long fsReads, long fsReadBytes, long fsWrites, long fsWriteBytes) {
		this.devName = devName;
		this.dirName = dirName;
		this.sysTypeName = sysTypeName;
		this.fsTypeName = fsTypeName;
		this.fsReads = fsReads;
		this.fsReadBytes = fsReadBytes;
		this.fsWrites = fsWrites;
		this.fsWriteBytes = fsWriteBytes;
	}

	public String getDevName() {
		return devName;
	}

	public String getDirName() {
		return dirName;
	}

	public String getSysTypeName() {
		return sysTypeName;
	}

	public String getFsTypeName() {
		return fsTypeName;
	}

	public long getFsReads() {
		return fsReads;
	}

	public long getFsReadBytes() {
		return fsReadBytes;
	}

	public long getFsWrites() {
		return fsWrites;
	}

	public long getFsWriteBytes() {
		return fsWriteBytes;
	}
	
}
