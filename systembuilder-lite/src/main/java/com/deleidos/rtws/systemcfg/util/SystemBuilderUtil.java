	/**
	 * Given an ingestapi inputParameter or systemParameter, generate an XML
	 * string for that parameter containing that parameter's default value.
	 * 
	 * Examples:
	 * 
	 * <pre>
	 * param="interval" json=null
	 * result={@code<interval></interval>}
	 * </pre>
	 * 
	 * <pre>
	 * param="header" json={"default": "HEADER", "type": "java.lang.String"}
	 * result={@code<header>HEADER</header>}
	 * </pre>
	 * 
	 * <pre>
	 * param="object" json={"default": "java:package.ClassName", "type": "package.ClassName"}
	 * result={@code<object xsi:type="java:package.ClassName"></object>}
	 * </pre>
	 * 
	 * @param param
	 *            the parameter name
	 * @param json
	 *            a JSONObject representing the contents of the parameters, used
	 *            to fill in the contents of the XML element. May be null which
	 *            will simply not fill in a XML value
	 * @return a string containing an XML element
	 */
	public static String createXmlElementFromParameter(String param, JSONObject json) {
		StringBuffer element = new StringBuffer("");

		if (StringUtils.isNotBlank(param)) {
			element.append("<").append(param);
			if (json != null && json.containsKey("default")) {
				String value = json.getString("default");
				// Hopefully a temporary hack because this nullifies any dependency
				// injection advantages we may have gained by injecting Java objects
				if (value.startsWith("java:")) {
					int whitepos = -1;
					for (int i=5; i < value.length(); i++) {
						if (Character.isWhitespace(value.charAt(i))) {
							whitepos = i;
							break;
						}
					}
					if (whitepos > 0) {
						element.append(" xsi:type=\"").append(value.substring(0, whitepos)).append("\">");
						element.append(value.substring(whitepos+1));
					} else {
						element.append(" xsi:type=\"").append(value).append("\">");
					}
				}
				else {
					element.append(">").append(json.getString("default"));
				}
			}
			else {
				element.append(">");
			}
			element.append("</").append(param).append(">");
		}

		return element.toString();
	}

	/**
	 * Will return the "default" value of a JSON object with a specified key
	 * inside the "systemParameters" object of a larger JSON object.
	 * 
	 * @param key
	 *            the key of a JSON system parameter object
	 */
	public static String getSystemParameterValue(JSONObject json, String key) {
		String result = null;

		try {
			JSONObject systemParams = json.optJSONObject("systemParameters");
			if (systemParams != null && key != null) {
				Iterator<?> nodeit = systemParams.keys();
				while (nodeit.hasNext()) {
					if (key.equals((String)nodeit.next())) {
						JSONObject jp = systemParams.getJSONObject(key);
						if (jp != null && jp.containsKey("default")) {
							result = jp.getString("default");
						}
					}
				}
			}
		} catch (JSONException e) {
			e.printStackTrace();
		}

		return result;
	}

	public static Properties convert(List<Property> properties) {
		Properties props = new Properties();
		
		if (properties != null) {
			for (Property property : properties) {
				props.put(property.getName(),property.getValue());
			}
		}
		
		return props;
	}
