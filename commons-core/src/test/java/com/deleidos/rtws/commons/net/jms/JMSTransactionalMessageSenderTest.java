	@Test
	public void testSendBatchSizeAndTimerCommit() throws Exception {
		JMSTransactionalMessageSender sender = null;
		try {
			sender = new JMSTransactionalMessageSender(session, producer);
			sender.start();

			// send 110 normally
			List<JMSMessageWrapper> messages = createMessages(JMSTransactionalMessageSender.DEFAULT_MESSAGES_PER_TRANSACTION + 10);
			for (JMSMessageWrapper msg : messages) {
				sender.send(msg);
			}

			// wait just a second and we should get back 100 messages
			Thread.sleep(1000);

			int recievedMsgCount = consumeMessages();
			assertEquals(JMSTransactionalMessageSender.DEFAULT_MESSAGES_PER_TRANSACTION, recievedMsgCount);

			// now wait for the final 10 committed by the timer
			Thread.sleep(JMSTransactionalMessageSender.DEFAULT_TIME_BEFORE_COMMIT
					+ JMSTransactionalMessageSender.DEFAULT_TIMER_INTERVAL + 2000);

			recievedMsgCount = consumeMessages();
			assertEquals(10, recievedMsgCount);

		} finally {
			if (sender != null)
				sender.stop();
		}
	}
	
