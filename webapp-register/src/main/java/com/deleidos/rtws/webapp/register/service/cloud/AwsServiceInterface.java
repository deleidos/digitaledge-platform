	public Instance describeInstance(String instanceId) {
		
		AmazonEC2Client client = factory.getAmazonEC2Client();
		
		DescribeInstancesRequest request = new DescribeInstancesRequest();
		request.setInstanceIds(Collections.singleton(instanceId));
		
		DescribeInstancesResult result = client.describeInstances(request);
		List<Reservation> reservations = result.getReservations();
		
		if (! reservations.isEmpty()) {
			List<Instance> instances = reservations.get(0).getInstances();
			if (! instances.isEmpty()) {
				return  instances.get(0);
			}
		}
		
		return null;
		
	}
	
	public Set<String> listInstanceIds(String vpcId) {
		
		AmazonEC2Client client = factory.getAmazonEC2Client();
		
		DescribeInstancesRequest request = new DescribeInstancesRequest();
		
		ArrayList<Filter> filters = new ArrayList<Filter>();
		
		Filter filter = new Filter();
		filter.setName("vpc-id");
		filter.setValues(Collections.singleton(vpcId));
		filters.add(filter);
		
		request.withFilters(filters);
		
		DescribeInstancesResult result = client.describeInstances(request);
		List<Reservation> reservations = result.getReservations();
		
		HashSet<String> ids = new HashSet<String>();
		
		for (Reservation reservation : reservations) {
			for (Instance instance : reservation.getInstances()) {
				ids.add(instance.getInstanceId());
			}
		}
		
		return ids;
		
	}
	
	public String createInstance(String imageId, String instanceType, String azone, String keyPair, String securityGroupId, String subnetId, String userData) {
		
		AmazonEC2Client client = factory.getAmazonEC2Client();
		
		RunInstancesRequest request = new RunInstancesRequest();
		request.setImageId(imageId);
		request.setInstanceType(instanceType);
		request.setPlacement(new Placement().withAvailabilityZone(azone));
		request.setKeyName(keyPair);
		request.setSecurityGroupIds(Collections.singleton(securityGroupId));
		request.setMinCount(1);
		request.setMaxCount(1);
		
		if (subnetId != null) {
			request.setSubnetId(subnetId);
		}
		
		if (userData != null) {
			request.setUserData(new String(Base64.encodeBase64(userData.getBytes())));
		}
		
		RunInstancesResult result = client.runInstances(request);
		Instance instance = result.getReservation().getInstances().get(0);

		return instance.getInstanceId();
		
	}
	
