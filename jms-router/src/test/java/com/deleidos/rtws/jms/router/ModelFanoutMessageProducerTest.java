public class ModelFanoutMessageProducerTest {
	
	static final String jsonFile = "/src/test/resources/full_records.json";
	private List<String> records = null;
	
	private static String basedir;
	
	private static boolean isVerbose = false;
	
	static
	{
	    Logger rootLogger = Logger.getRootLogger();
	    rootLogger.setLevel(Level.INFO);
	    rootLogger.addAppender(new ConsoleAppender(new PatternLayout("%-6r [%p] %c - %m%n")));
	}

	private static Logger log = Logger.getLogger(ModelFanoutMessageProducerTest.class);
	
	@BeforeClass
	public static void init() {
		basedir = System.getProperty("basedir", ".");
		Properties testProperties = System.getProperties();
		testProperties.setProperty("RTWS_CONFIG_DIR", basedir + "/../commons-systems/src/systems/com.deleidos.rtws.localhost");
		System.setProperties(testProperties);
	}

	@Before
	public void setUp() {
		// load data
		InputStream in = null;
		try {
			in = new FileInputStream(new File(basedir + jsonFile));
		} catch (FileNotFoundException e) {
			assertTrue( false );
			return;
		}
		String recordsObjString = null;
		try {
			recordsObjString = IOUtils.toString(in, "UTF-8");
		} catch (IOException e) {
			assertTrue( false );
			return;
		}
		JSONObject record = (JSONObject)JSONSerializer.toJSON(recordsObjString);
		
		JSONArray jsonRecords = record.getJSONArray("records");
		if ( jsonRecords == null ) {
			assertTrue( false );
			return;
		}
		
		int count = jsonRecords.size();
		records = new ArrayList<String>(count);
		for( int i = 0; i < count; i++ ) {
			JSONObject jsonRecord = jsonRecords.getJSONObject(i);
			records.add(jsonRecord.toString());
			if ( isVerbose )
				System.out.println(jsonRecord.toString(3));
		}

	}
	
	@Test
	public void testNoModelName()  {
		String raw_records[] = {
				"OrderKey|OrderStatus|OrderPrice|OrderDate|OrderPriority|Clerk|ShipPriority|OrderComment|LineNumber|Quantity|ExtendedPrice|Discount|Tax|ReturnFlag|LineStatus|ShipDate|CommitDate|ReceiptDate|ShipInstructions|ShipMode|LineItemComment|CustomerName|CustomerNation|PartName|PartMfgr|PartType|SupplierName|SupplierNation",
				"1282|F|85655.22|29-FEB-92|4-NOT SPECIFIED|Clerk#000000168|0|he quickly special packages. furiously final re|1|14|19816.86|.04|.02|R|F|29-JUN-92|05-APR-92|21-JUL-92|TAKE BACK RETURN|REG AIR|ecial deposit|Customer#000115915|PERU|lemon smoke ivory red chocolate|Manufacturer#3|STANDARD BURNISHED BRASS|Supplier#000002494|CANADA",
				"1282|F|85655.22|29-FEB-92|4-NOT SPECIFIED|Clerk#000000168|0|he quickly special packages. furiously final re|2|10|10581.2|.09|.06|R|F|10-APR-92|16-APR-92|01-MAY-92|DELIVER IN PERSON|SHIP|r theodolite|Customer#000115915|PERU|metallic saddle light green dark|Manufacturer#4|PROMO POLISHED STEEL|Supplier#000004134|UNITED STATES",

		};
		for( String record : raw_records ) {
			String modelName = ModelFanoutMessageProducer.getDataModelName(record);
			assertNull(modelName);
		}
	}
	
	@Test
	public void testGetModelName()  {
		for( String record : records )
			testGetModelName(record);
	}
	
	@Test
	public void testPerformanceRegex()  {
		final int batchSize = 2000;
		long started = System.currentTimeMillis();
		for( int i = 0; i < batchSize; i++ ) {
			for( String record : records ) {
				String modelName = ModelFanoutMessageProducer.getDataModelName(record);
				assertNotNull(modelName);
			}
		}
		long ended = System.currentTimeMillis();
		log.info(String.format("Regex: Extacted model name from %6d records in %4d ms", batchSize*records.size(), ended - started));
	}
	
	@Test
	public void testPerformanceJson()  {
		final int batchSize = 10;
		long started = System.currentTimeMillis();
		for( int i = 0; i < batchSize; i++ ) {
			for( String record : records ) {
				String modelName = getDataModelNameViaJson(record);
				assertNotNull(modelName);
			}
		}
		long ended = System.currentTimeMillis();
		log.info(String.format("JSON : Extacted model name from %6d records in %4d ms", batchSize*records.size(), ended - started ));
	}
	
	private void testGetModelName(String record)  {
		String modelName = ModelFanoutMessageProducer.getDataModelName(record);
		assertEquals(getDataModelNameViaJson(record), modelName);
		assertEquals("rfc822email_v0.10.zip", modelName);
	}
	
	private String getDataModelNameViaJson(String jsonString) {
		JSONObject record = (JSONObject) JSONSerializer.toJSON(jsonString);
		JSONObject header = (JSONObject) JsonPath.simpleLookup(record, "standardHeader", false);
		String modelName = header.getString("modelName");
		String modelVersion = header.getString("modelVersion");
		return ModelFanoutMessageProducer.getDataModelName(modelName, modelVersion);
	}
	
	@Test
	public void misc(){
		String text = "h";
		System.out.println(String.format("Sending message head: %s ...\n", text.substring(0, (text.length()/2))));
	}
	
}
