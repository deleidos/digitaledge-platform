	public void terminateGatewayInstance(String instanceId, AccountForm form, GatewayData gateway) throws ClientException {
		
		String accessKey = form.getAccessKey();
		String secretKey = form.getSecretKey();
		String endpoint = gateway.getAzone().getServiceEndpoint().getURLString();
		
		AwsServiceInterface serviceInterface = getAwsServiceInterface(accessKey, secretKey, endpoint);
		
		Instance instance = serviceInterface.describeInstance(instanceId);
		if (instance != null && ! instance.getState().getName().equals("terminated")) {
			serviceInterface.terminateInstance(instanceId);
			
			Timestamp expiration = new Timestamp(System.currentTimeMillis() + TimeUtil.FIVE_MINUTES);
			
			while (! TimeUtil.hasExpired(expiration)) {
				instance = serviceInterface.describeInstance(instanceId);
				
				if (instance.getState().getName().equals("terminated")) {
					return;
				}
				
				try { 
					Thread.sleep(TimeUtil.FIVE_SECONDS);
				} catch (Exception ex) {
					// ignore
				}
			}
			
			throw new ClientException("Failed to stop gateway instance, retries exhausted.");
		}
		
	}
	
	protected AwsServiceInterface getAwsServiceInterface(String accessKey, String secretKey, String endpoint) {
		
		AwsConnectionFactory factory = new AwsConnectionFactory(accessKey, secretKey);
		
		if (endpoint != null) {
			factory.setServiceEndpoint(endpoint);
		}
		
		AwsServiceInterface serviceInterface = new AwsServiceInterface();
		serviceInterface.setConnectionFactory(factory);
		
		return serviceInterface;
		
	}

	public void openSecurityGroupRulesFromTMS(AccountForm form,
			Map<String, String> groups) {

		AwsServiceInterface service = new AwsServiceInterface();
		AwsConnectionFactory value = new AwsConnectionFactory(RtwsrcProperties
				.getInstance().getString(RtwsrcProperties.RTWS_ACCESS_KEY),
				RtwsrcProperties.getInstance().getString(
						RtwsrcProperties.RTWS_SECRET_KEY));
		value.setServiceEndpoint("https://ec2.amazonaws.com/");
		service.setConnectionFactory(value);
		String[] tmsSgs = { "internal.tms", "webapp.tms" };
		
		String group = groups.get(DigitalEdgeSecurityGroup.WEBAPP_SG_NAME);
		
		for (String tmsSg : tmsSgs) {
			if (!form.getNetworkType().equals(NetworkType.VPC)) {
				service.createFirewallRuleByUserIdGroup(tmsSg, "tcp", 80, 80,
						form.getIaasServiceNumber(), group);
				service.createFirewallRuleByUserIdGroup(tmsSg, "tcp", 443, 443,
						form.getIaasServiceNumber(), group);
				service.createFirewallRuleByUserIdGroup(tmsSg, "tcp", 8161,
						8161, form.getIaasServiceNumber(), group);
			}
		}
	}

