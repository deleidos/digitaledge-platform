package com.saic.rtws.commons.model.tmsdb;

public class ProcessGroupConfig {

	private String processGroupName;
	private String securityGroup;
	private String vpcSubnetPlacement;
	private String defaultInstanceType;
	private String instanceStorage;
	private String manifestFilename;
	private String configPermissions;
	private String publicDomainName;
	private String internalDomainName;
	private String managementInterfaces;
	private String fixedWebapps;
	private String ingestConfigFilename;
	
	/**
	 * Default constructor.
	 */
	public ProcessGroupConfig(){
		
	}
	
	/**
	 * Set process group name.
	 * 
	 * @param processGroupName
	 */
	public void setProcessGroupName(String processGroupName){
		this.processGroupName = processGroupName;
	}
	
	/**
	 * Get process group name.
	 * 
	 * @return
	 */
	public String getProcessGroupName(){
		return processGroupName;
	}
	
	/**
	 * Set security group
	 * 
	 * @param securityGroup String value
	 */
	public void setSecurityGroup(String securityGroup){
		this.securityGroup = securityGroup;
	}
	
	/**
	 * Get security group.
	 * 
	 * @return String value
	 */
	public String getSecurityGroup(){
		return securityGroup;
	}
	
	/**
	 * Set the vpc subnet placement preference.
	 * 
	 * @param vpcSubnetPlacement String value, possible choices are [public/private]
	 */
	public void setVpcSubnetPlacement(String vpcSubnetPlacement){
		this.vpcSubnetPlacement = vpcSubnetPlacement;
	}
	
	/**
	 * Get the vpc subnet placement preference.
	 * 
	 * @return String value
	 */
	public String getVpcSubnetPlacement(){
		return vpcSubnetPlacement;
	}
	
	/**
	 * Set default instance type.
	 * 
	 * @param defaultInstanceType String value
	 */
	public void setDefaultInstanceType(String defaultInstanceType){
		this.defaultInstanceType = defaultInstanceType;
	}
	
	/**
	 * Get default instance type.
	 * 
	 * @return String value
	 */
	public String getDefaultInstanceType(){
		return defaultInstanceType;
	}
	
	/**
	 * Set instance storage type.
	 * 
	 * @param instanceStorage String value
	 */
	public void setInstanceStorage(String instanceStorage){
		this.instanceStorage = instanceStorage;
	}
	
	/**
	 * Get instance storage type.
	 * 
	 * @return String value
	 */
	public String getInstanceStorage(){
		return instanceStorage;
	}
	
	/**
	 * Set manifest file name.
	 * 
	 * @param manifestFilename String value
	 */
	public void setManifestFilename(String manifestFilename){
		this.manifestFilename = manifestFilename;
	}
	
	/**
	 * Get manifest file name.
	 * 
	 * @return String value
	 */
	public String getManifestFilename(){
		return manifestFilename;
	}
	
	/**
	 * Set configuration permissions.
	 * 
	 * @param configPermissions String value
	 */
	public void setConfigPermissions(String configPermissions){
		this.configPermissions = configPermissions;
	}
	
	/**
	 * Get configuration permissions.
	 * 
	 * @return String value
	 */
	public String getConfigPermissions(){
		return configPermissions;
	}
	
	/**
	 * Set public domain name.
	 * 
	 * @param publicDomainName String value
	 */
	public void setPublicDomainName(String publicDomainName){
		this.publicDomainName = publicDomainName;
	}
	
	/**
	 * Get public domain name.
	 * 
	 * @return String value
	 */
	public String getPublicDomainName(){
		return publicDomainName;
	}
	
	/**
	 * Set internal domain name.
	 * 
	 * @param internalDomainName String value
	 */
	public void setInternalDomainName(String internalDomainName){
		this.internalDomainName = internalDomainName;
	}
	
	/**
	 * Get internal domain name.
	 * 
	 * @return String value
	 */
	public String getInternalDomainName(){
		return internalDomainName;
	}
	
	/**
	 * Set management interfaces.
	 * 
	 * @param managementInterfaces String value
	 */
	public void setManagementInterfaces(String managementInterfaces){
		this.managementInterfaces = managementInterfaces;
	}
	
	/**
	 * Get management interfaces.
	 * 
	 * @return String value
	 */
	public String getManagementInterfaces(){
		return managementInterfaces;
	}
	
	/**
	 * Set fixed webapps.
	 * 
	 * @param fixedWebapps String value
	 */
	public void setFixedWebapps(String fixedWebapps){
		this.fixedWebapps = fixedWebapps;
	}
	
	/**
	 * Get fixed webapps.
	 * 
	 * @return String value
	 */
	public String getFixedWebapps(){
		return fixedWebapps;
	}
	
	/**
	 * Get ingest configuration file name.
	 * 
	 * @param ingestConfigFilename String value
	 */
	public void setIngestConfigFilename(String ingestConfigFilename){
		this.ingestConfigFilename = ingestConfigFilename;
	}
	
	/**
	 * Set ingest configuration file name.
	 * 
	 * @return
	 */
	public String getIngestConfigFilename(){
		return ingestConfigFilename;
	}
}
