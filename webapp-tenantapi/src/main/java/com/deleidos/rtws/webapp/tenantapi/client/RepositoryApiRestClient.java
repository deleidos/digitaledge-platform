	public List<ProcessItem> retrieveProcessFile(String host) {
		
		WebResource resource = getClient().resource(buildBaseUrl(host));
		resource = resource.path("process/retrieve/all");

		String response = resource.accept(MediaType.APPLICATION_JSON).get(String.class);
		
		JSONObject json = JSONObject.fromObject(response);
		
		ArrayList<ProcessItem> items = new ArrayList<ProcessItem>();
		
		@SuppressWarnings("rawtypes")
		Iterator nodeit = json.getJSONArray("node").iterator();
		while (nodeit.hasNext()) {
			JSONObject node = (JSONObject) nodeit.next();
			
			ProcessItem item = new ProcessItem();
			
			if (node.has(ID_KEY)) {
				item.setInstanceId(node.getString(ID_KEY));
			}
			
			if (node.has(DOMAIN_KEY)) {
				item.setDomain(node.getString(DOMAIN_KEY));
			}
			
			if (node.has(HOST_KEY)) {
				item.setHost(node.getString(HOST_KEY));
			}
			
			if (node.has(GROUP_KEY)) {
				item.setGroup(node.getString(GROUP_KEY));
			}
			
			if (node.has(DNS_NAME_KEY)) {
				item.setDnsName(node.getString(DNS_NAME_KEY));
			}
			
			if (node.has(PRIVATE_IP_KEY)) {
				item.setPrivateIP(node.getString(PRIVATE_IP_KEY));
			}
			
			if (node.has(PERSISTENT_IP_KEY) && ! node.getJSONObject("PERSISTENT_IP_KEY").isNullObject()) {
				item.setPermanentIP(node.getString(PERSISTENT_IP_KEY));
			}
		
			item.setTimestamp(new Date());
			
			items.add(item);
		}
		
		return items;
		
	}
	
	public Map<String, ProcessItem> retrieveProcessFileByNode(String host) {
		
		List<ProcessItem> items = retrieveProcessFile(host);
		
		HashMap<String, ProcessItem> processMap = new HashMap<String, ProcessItem>();
		
		for (ProcessItem item : items) {
			processMap.put(item.getInstanceId(), item);
		}
		
		return processMap;
		
	}
	
	public Map<String, AllocationPolicy> retrieveServicesFile(String host) {
		
		WebResource resource = getClient().resource(buildBaseUrl(host));
		resource = resource.path("services/retrieve/all");
		
		String response = resource.accept(MediaType.APPLICATION_JSON).get(String.class);
		
		JSONObject json = JSONObject.fromObject(response);
		
		HashMap<String, AllocationPolicy> allocationPolicies = new HashMap<String, AllocationPolicy>();
		
		@SuppressWarnings("rawtypes")
		Iterator policyit = json.getJSONArray("allocationPolicy").iterator();
		while (policyit.hasNext()) {
			JSONObject policy = (JSONObject) policyit.next();
			
			AllocationPolicy allocationPolicy = new AllocationPolicy();
			
			if (policy.has(MIN_KEY)) {
				allocationPolicy.setMin(policy.getInt(MIN_KEY));
			}
			
			if (policy.has(MAX_KEY)) {
				allocationPolicy.setMax(policy.getInt(MAX_KEY));
			}
			
			if (policy.has(GROUP_KEY)) {
				allocationPolicies.put(policy.getString(GROUP_KEY), allocationPolicy);
			}
		}
		
		return allocationPolicies;
		
	}
	
