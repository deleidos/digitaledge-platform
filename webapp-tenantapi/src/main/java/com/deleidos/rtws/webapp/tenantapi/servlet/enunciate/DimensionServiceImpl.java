package com.deleidos.rtws.webapp.tenantapi.servlet.enunciate;

import java.text.ParseException;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.List;

import javax.ws.rs.Path;
import javax.ws.rs.WebApplicationException;
import javax.ws.rs.core.Response;
import javax.ws.rs.core.Response.ResponseBuilder;
import javax.ws.rs.core.Response.Status;

import net.sf.json.JSONObject;

import org.apache.commons.lang.StringUtils;
import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.model.dimension.DimensionTable;
import com.deleidos.rtws.commons.model.tmsdb.DbConnDetails;
import com.deleidos.rtws.webapp.tenantapi.dao.DaoManager;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterDimTableDependencies;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterState;
import com.deleidos.rtws.webapp.tenantapi.parse.DimensionCacheParser;

@Path("dimensions")
public class DimensionServiceImpl implements DimensionService {
	
	private static final Logger LOGGER = Logger.getLogger(DimensionServiceImpl.class);
	private static final List<ClusterState> NON_LOCKING_SYSTEM_STATES = Arrays.asList(ClusterState.New, ClusterState.Down, ClusterState.Deleting);

	@Override
	public List<DbConnDetails> getConnections(String tenantId) {
		if(StringUtils.isBlank(tenantId)) {
			throw new WebApplicationException(Status.BAD_REQUEST);
		}
		return new ArrayList<DbConnDetails>(DaoManager.getDimensionDao().getConnections(tenantId));
	}

	@Override
	public List<DimensionTable> getConnectionDimensionTables(Long connId, List<String> tableNames) {
		if(connId == null) {
			throw new WebApplicationException(Status.BAD_REQUEST);
		}
		return new ArrayList<DimensionTable>(DaoManager.getDimensionDao().getConnectionDimensionTables(connId, tableNames));
	}

	@Override
	public JSONObject updateDimCacheConfig(
			String tenantId, 
			Long connId,
			String schema, 
			String table, 
			String cacheConfigJson) {
		DimensionTable cacheConfig = null;
		
		List<String> badRequestErrors = new ArrayList<String>();
		
		if(StringUtils.isBlank(tenantId)) {
			badRequestErrors.add("tenantId must be provided");
		}
		if(connId == null) {
			badRequestErrors.add("connId must be provided");
		}
		if(StringUtils.isBlank(schema)) {
			badRequestErrors.add("schema must be provided");
		}
		if(StringUtils.isBlank(table)) {
			badRequestErrors.add("table must be provided");
		}
		
		if(StringUtils.isBlank(cacheConfigJson)){
			badRequestErrors.add("cacheConfigJson must be provided");
		} else {
			try {
				cacheConfig = DimensionCacheParser.parseCacheConfigJson(cacheConfigJson);
				
				List<String> cacheConfigErrorMsgs = cacheConfig.validate();
				if(cacheConfigErrorMsgs != null && cacheConfigErrorMsgs.size() > 0)
				{
					badRequestErrors.addAll(cacheConfigErrorMsgs);
				}
			} catch(ParseException e) {
				String tmpErrorMsg = e.getMessage();
				if(StringUtils.isBlank(tmpErrorMsg)) {
					tmpErrorMsg = "Invalid cacheConfigJson format";
				}
				badRequestErrors.add(tmpErrorMsg);
			}
		}
		
		if(badRequestErrors.size() == 0) {
			badRequestErrors.addAll(getDependentSystemDomains(tenantId, table));
		}
		
		if(badRequestErrors != null && badRequestErrors.size() > 0) {
			LOGGER.warn("Invalid data provided to Update Table Dimension Cache Config\n" + StringUtils.join(badRequestErrors, "\n"));
			ResponseBuilder responseBuilder = Response.status(Response.Status.BAD_REQUEST);
			JSONObject object = new JSONObject();
			object.element("errorMsgs", badRequestErrors);
			responseBuilder.entity(object.toString());
			throw new WebApplicationException(responseBuilder.build());
		}
		
		String errorMsg = null;
		try {
			if(DaoManager.getDimensionDao().setDimensionCacheConfig(connId, cacheConfig)) {
				JSONObject object = new JSONObject();
				object.put("operationStatus", "success");
				return object;
			} else {
				errorMsg = "Failed to set the dim cache config";
			}
		} catch(Exception e) {
			errorMsg = e.getMessage();
		}
		
		if(errorMsg == null) {
			errorMsg = "An Unknown error occurred while saving the dim cache config";
		}
		
		LOGGER.error(errorMsg);
		ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
		JSONObject errorObj = new JSONObject();
		errorObj.put("operationStatus", "failure");
		errorObj.put("errorMsg", errorMsg);
		responseBuilder.entity(errorObj.toString());
		throw new WebApplicationException(responseBuilder.build());
	}

	@Override
	public JSONObject deleteDimCacheConfig(
			String tenantId, 
			Long connId, 
			String schema, 
			String table) {
		List<String> badRequestErrors = new ArrayList<String>();

		if(StringUtils.isBlank(tenantId)) {
			badRequestErrors.add("tenantId must be provided");
		}
		if(connId == null) {
			badRequestErrors.add("connId must be provided");
		}
		if(StringUtils.isBlank(schema)) {
			badRequestErrors.add("schema must be provided");
		}
		if(StringUtils.isBlank(table)) {
			badRequestErrors.add("table must be provided");
		}

		if(badRequestErrors.size() == 0) {
			badRequestErrors.addAll(getDependentSystemDomains(tenantId, table));
		}

		if(badRequestErrors.size() > 0)
		{
			LOGGER.warn("Invalid data provided to Delete Table Dimension Cache Config\n" + StringUtils.join(badRequestErrors, "\n"));
			ResponseBuilder responseBuilder = Response.status(Response.Status.BAD_REQUEST);
			JSONObject object = new JSONObject();
			object.element("errorMsgs", badRequestErrors);
			responseBuilder.entity(object.toString());
			throw new WebApplicationException(responseBuilder.build());
		}

		String errorMsg = null;
		try {
			if(DaoManager.getDimensionDao().dropDimensionCacheConfig(connId, schema, table)) {
				LOGGER.info("Removed cache config for dimension " + schema + "." + table);

				JSONObject object = new JSONObject();
				object.put("operationStatus", "success");
				return object;
			} else {
				errorMsg = "Failed to delete the dim cache config";
			}
		} catch(Exception e) {
			errorMsg = e.getMessage();
		}
		
		if(errorMsg == null) {
			errorMsg = "An Unknown error occurred while deleting the dim cache config";
		}
		
		LOGGER.error(errorMsg);
		ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
		JSONObject errorObj = new JSONObject();
		errorObj.put("operationStatus", "failure");
		errorObj.put("errorMsg", errorMsg);
		responseBuilder.entity(errorObj.toString());
		throw new WebApplicationException(responseBuilder.build());
	}

	/**
	 * Get a list of dimension tables currently in use by one tenant's systems.
	 */
	private List<String> getDependentSystemDomains(
			String tenantId, 
			String table) {
		List<String> errors = new ArrayList<String>();

		try {
			ArrayList<String> depDomains = new ArrayList<String>();
			Collection<ClusterDimTableDependencies> dependencies = new SystemsServiceImpl().getSystemsDimTableDependencies(tenantId, Arrays.asList(table.trim()));
			if(dependencies != null && dependencies.size() > 0) {
				for(ClusterDimTableDependencies currDependencies : dependencies) {
					if(StringUtils.isNotBlank(currDependencies.getDomain())) {
						// Some system statuses (e.g. down, deleting) should not cause a lock
						if(currDependencies.getStatus() == null || currDependencies.getStatus().getState() == null || ! NON_LOCKING_SYSTEM_STATES.contains(currDependencies.getStatus().getState())) {
							depDomains.add(currDependencies.getDomain().trim());
						}
					}
				}
			}

			if(depDomains.size() > 0) {
				errors.add("This enrichment is in use and cannot be modified.  To modify the enrichment, please stop the following systems: " + StringUtils.join(depDomains, ", "));
			}
		} catch(WebApplicationException webAppException) {
			String msg = "The system could not determine if this enrichment is being used within any currently running systems.";
			msg += "  Please try again and notify your system administer if the problem persists.";
			errors.add(msg);
		}

		return errors;
	}

}
