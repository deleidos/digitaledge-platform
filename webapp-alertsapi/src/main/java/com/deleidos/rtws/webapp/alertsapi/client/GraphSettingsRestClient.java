	/**
	 * Get the configuration XML for one location from the database
	 * 
	 * @param dir
	 *            the location of the configuration in the database
	 * @return the configuration XML as a String
	 */
	@SuppressWarnings("rawtypes")
	public String getConfigFile(String dir) {
		try {
			int lastBackslash = dir.lastIndexOf("\\");
			String prefix = lastBackslash == -1 ? "" : dir.substring(0, lastBackslash);
			String name = lastBackslash == -1 ? dir : dir.substring(lastBackslash+1);
			String where = "PREFIX='" + prefix + "' AND NAME='" + name + "'";

			WebResource resource = getClient().resource(DBAPI_BASE_URL);
			resource = resource.path("json/select")
				.path(DB_HOST) // database
				.path(GRAPHSETTINGS_DB_SCHEMA) // schema
				.path(GRAPHSETTINGS_DB_TABLE) // table
				.path("XML") // columns
				.path(where); // where expression

			String response = resource.get(String.class);

			// Build return String from REST response
			String results = null;
			JSONObject json = JSONObject.fromObject(response);

			if (json.has("Operation Status")) {
				if ("FAILED".equals(json.getString("Operation Status"))) {
					throw new RestClientException("Retrieve failed - " + json.getString("Message"));
				}
			}

			Iterator rowit = json.getJSONArray("result").iterator();
			while (rowit.hasNext()) {
				JSONObject row = (JSONObject) rowit.next();
				if (row.has("XML")) {
					results = row.getString("XML");
				}
			}

			return results;
		}
		catch (UniformInterfaceException uie) {
			ClientResponse cr = uie.getResponse();
			logger.warn("Received [" + cr.getStatus() + "] response from DB - " + uie.getMessage());
			return null;
		}
		catch (JSONException je) {
			logger.warn("Malformed JSON from DB - " + je.toString());
			return null;
		}
		catch (Throwable e) {
			logger.error(e);
			return null;
		}
	}

	/**
	 * Get all graph settings locations from the database.
	 * 
	 * @return Collection of graph settings locations in the database 
	 */
	@SuppressWarnings("rawtypes")
	public Collection<String> getLocations() {
		try {
			// Send GET to DB API
			WebResource resource = getClient().resource(DBAPI_BASE_URL);
			resource = resource.path("json/select")
				.path(DB_HOST) // database
				.path(GRAPHSETTINGS_DB_SCHEMA) // schema
				.path(GRAPHSETTINGS_DB_TABLE) // table
				.path("PREFIX,NAME"); // columns

			String response = resource.get(String.class);

			// Build return collection from REST response
			Collection<String> results = new LinkedList<String>();
			JSONObject json = JSONObject.fromObject(response);

			if (json.has("Operation Status")) {
				if ("FAILED".equals(json.getString("Operation Status"))) {
					throw new RestClientException("Retrieve failed - " + json.getString("Message"));
				}
			}

			Iterator rowit = json.getJSONArray("result").iterator();
			while (rowit.hasNext()) {
				JSONObject row = (JSONObject) rowit.next();

				// Output from each row is in the form "PREFIX\NAME"
				String prefix = "";
				String name = "";
				if (row.has("PREFIX")) {
					prefix = row.getString("PREFIX");
				}
				if (row.has("NAME")) {
					name = row.getString("NAME");
				}

				results.add(prefix + "\\" + name);
			}

			return results;
		}
		catch (UniformInterfaceException uie) {
			ClientResponse cr = uie.getResponse();
			logger.warn("Received [" + cr.getStatus() + "] response from DB - " + uie.getMessage());
			return null;
		}
		catch (JSONException je) {
			logger.warn("Malformed JSON from DB - " + je.toString());
			return null;
		}
		catch (Throwable e) {
			logger.error(e);
			return null;
		}
	}

