	public static void addTrustedCertsToKeystore(File trustStore1, String trustPass1, String trustStore2, String trustPass2){
		try{
			//load trustore to copy certs from
			KeyStore tks = KeyStore.getInstance("JKS", "SUN");
			tks.load(new FileInputStream(trustStore1), trustPass1.toCharArray());
			
			//load truststore to copy in new entries
			KeyStore ks = KeyStore.getInstance("JKS", "SUN");
			ks.load(new FileInputStream(trustStore2), trustPass2.toCharArray());
			
			Enumeration<String> aliases = tks.aliases();
			
			//load each cert from truststore and save to another
			while(aliases.hasMoreElements()){
				String alias = aliases.nextElement();
				//load cert
				ks.setCertificateEntry(alias, tks.getCertificate(alias));
			}
			
			ks.store(new FileOutputStream(trustStore2), trustPass2.toCharArray());
		} catch(Exception e){
			log.error(e.toString(), e);
		}
	}
	
	public static void buildInternalChain(File trustStore, String trustStorePassword, String keystore, String keyStorePassword){
		try{
			//load trustore to copy certs from
			KeyStore tks = KeyStore.getInstance("JKS", "SUN");
			tks.load(new FileInputStream(trustStore), trustStorePassword.toCharArray());
			
			//load keystore to update certificate chain path
			KeyStore ks = KeyStore.getInstance("JKS", "SUN");
			ks.load(new FileInputStream(keystore), keyStorePassword.toCharArray());
			
			List<Certificate> internalChain = new ArrayList<Certificate>();
			List<Certificate> nonDupChain = new ArrayList<Certificate>();
			
			Enumeration<String> aliases = tks.aliases();
			
			//go through all trusted certs and get the internal ones only
			while(aliases.hasMoreElements()){
				String currentAlias = aliases.nextElement();
				//only internal
				if(!currentAlias.contains("external") && !currentAlias.equals("server")){
					internalChain.add(tks.getCertificate(currentAlias));
				}
			}
			
			//update internal cert chain
			Certificate [] currentChain = ks.getCertificateChain(CAProperties.getPrivateDomainAlias());
			
			//go through internal certs and make sure they do not conflict with any cert already in chain
			//case for adding same cert to chain twice
			for(Certificate intCert : internalChain){
				if(currentChain != null){
					for(int i = 0; i < currentChain.length; i++){
						if(intCert.hashCode() != currentChain[i].hashCode()){
							nonDupChain.add(intCert);
						}
						else{
							break;
						}
					}
				}
			}
			
			//figure out new certificate chain size for internal cert
			Certificate [] updatedChain = null;
			if(currentChain != null){
				updatedChain = new Certificate[currentChain.length + nonDupChain.size()];
			}
			else{
				updatedChain = new Certificate[nonDupChain.size()];
			}
			
			//add current chain to new chain
			int index = 0;
			if(currentChain != null){
				for(int i = 0; i < currentChain.length; i++){
					updatedChain[index] = currentChain[i];
					index++;
				}
			}
			
			//add non dup internal certs to the certificates chain
			for(Certificate cert : nonDupChain){
				updatedChain[index] = cert;
				index++;
			}
			//create a new private key entry from the existing private key that was created without a chain
			PrivateKeyEntry newEntry = new PrivateKeyEntry((PrivateKey) ks.getKey(CAProperties.getPrivateDomainAlias(), keyStorePassword.toCharArray()), updatedChain);
			//reset the private alias cert entry with the new updated chain
			ks.setEntry(CAProperties.getPrivateDomainAlias(), newEntry, new KeyStore.PasswordProtection(keyStorePassword.toCharArray()));
			//write out the keystore again
			ks.store(new FileOutputStream(keystore), keyStorePassword.toCharArray());
		}catch(Exception e){
			log.error(e.toString(), e);
		}
	}
	
	public static void addTrustedCertsToKeystoreInitialize(File trustStore1, String trustPass1, String trustStore2, String trustPass2){
		try{
			KeyStore ks = KeyStore.getInstance("JKS", "SUN");
			
			File tmp = new File(trustStore2);
			if(tmp.exists() == false){
				//initialize/clear keystore
				ks.load(null, trustPass2.toCharArray());
				ks.store(new FileOutputStream(tmp), trustPass2.toCharArray());
				log.debug(String.format("Initializing keystore-file:  %s", tmp.getCanonicalPath()));
			}
			
			
			KeystoreUtil.addTrustedCertsToKeystore(trustStore1, trustPass1, trustStore2, trustPass2);
		}
		catch(Exception e){
			log.error(e.toString(), e);
		}
	}
	
	private static InputStream fullStream(String fileName) throws IOException{
		//TODO: fix this?  The javadoc says allocating an array based on .available() shouldn't be done
		//if trying to get whole array
        FileInputStream fis = new FileInputStream(fileName);
        DataInputStream dis = new DataInputStream(fis);
        byte[] bytes = new byte[dis.available()];
        dis.readFully(bytes);
        ByteArrayInputStream bais = new ByteArrayInputStream(bytes);
        
        return bais;
	}
	
	private static InputStream fullStream(byte [] values) throws IOException{
       InputStream is = new ByteArrayInputStream(values);
       
       return is;
	}
