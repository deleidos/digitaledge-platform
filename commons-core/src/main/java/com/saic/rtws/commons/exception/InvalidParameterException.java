package com.saic.rtws.commons.exception;

/** Exception for instance of invalid parameter.
 */
public class InvalidParameterException extends Exception {
	
	/** Unique serial version. */
	private static final long serialVersionUID = 54841354792146L;
	
	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 */
	public InvalidParameterException(final String message) {
		super(message);
	}
	
	/** 
	 * Constructor. 
	 * @param message Message to be returned
	 * @param cause Exception object
	 */
	public InvalidParameterException(final String message, final Exception cause) {
		super(message, cause);
	}
}
