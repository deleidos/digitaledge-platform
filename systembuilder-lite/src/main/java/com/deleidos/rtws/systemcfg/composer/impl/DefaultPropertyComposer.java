/**
 * Generates the default properties needed for all composers, the creation of the RTWSCommon properties file.
 * 
 * @author LUCECU
 *
 */
public class DefaultPropertyComposer implements DefinitionComposer{

	private RtwsProperties properties = null;
	private static Logger logger = LogManager.getLogger(DefaultPropertyComposer.class);
	/** The Jersey REST client */
	private Client client;
	private String tenantId;
	private final String DATABASE = "database";
	private final String LDAP = "ldap";
	private final String ACCOUNTS = "accounts";
	private final String EUCA_ONLY_ANCHOR = "anchor-euca-public-ip";
	private String EUCA_CLOUD_IP = "UNKNOWN";
	
	@Override
	public void initialize() throws InitializationException {
		String templateFile = null;
		String baseDir = (String) RtwsConfig.getInstance().getConfiguration().getProperty("sysbuilder.template.basedir");
		
		//initialize template file locations
		//template file property sets, for lookup in composer later
		templateFile = String.format("%s/%s", baseDir, "default.properties");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.service.default.properties", templateFile);
		
		this.client = Client.create(JerseyClientConfig.getInstance().getInternalConfig());
		
		if (UserDataProperties.getInstance().getString(UserDataProperties.RTWS_STORAGE_ENDPOINT) != null)
			this.EUCA_CLOUD_IP = UserDataProperties.getInstance().getString(UserDataProperties.RTWS_STORAGE_ENDPOINT).split("/")[2].split(":")[0];
		
	}

	@Override
	public void dispose() {
		//not implemented
	}

	@Override
	public void writeFile(String version, String fileName) {
		//not implemented
	}

	/**
	 * Loads default configuration/templates for composer.
	 */
	@Override
	public void loadDefaults(String version, SystemConfig config, SystemContext systemContext, RtwsProperties properties) 
								throws MarshalException, DefaultConfigurationException {
		//properties param is null, use this.properties to set new values
		String file = RtwsConfig.getInstance().getString("sysbuilder.service.default.properties");
		
		this.properties = new RtwsProperties();
		
		//intialize with defaults for system
		this.properties.put("system.release", config.getSoftwareVersion());
		this.properties.put("system.domain", config.getSystemDomain());
		
		if (config.getApplianceDomain() != null)
			this.properties.put("system.applianceDomain", config.getApplianceDomain());
		
		//create niamod
		//String tmp [] = config.getDomain().split(".");
		StringBuilder niamod = new StringBuilder(config.getSystemDomain());
		/*for(int i = tmp.length - 1; i >=0; i--){
			niamod.append(tmp[i] + ".");
		}*/
		
		this.properties.put("system.niamod", niamod.reverse().toString());
		this.properties.put("system.region", config.getRegion());
		
		//load rest from file with property inheritance
		try{
			InputStream is = getClass().getResourceAsStream(file);
			this.properties.load(is);
			is.close();
			this.properties.encryptCredentials(config.getSystemDomain());
		}catch(Exception e){
			e.printStackTrace();
			throw new DefaultConfigurationException("Error during composer load defaults", e);
		}
	}

	/**
	 * Generates default properties for RTWSCommon properties.
	 */
	@Override
	public ComposerStatus compose(SystemConfig config, SystemContext systemContext) throws ComposerException{
		ComposerStatus status = ComposerStatus.done;
		
		try{
			generateConnectorProperties();
			generateAnchorProperties(config);
			generateUserProperties(config);
			generateInterfaceProperties(systemContext);
			generateProperties(config);
			scrubProperties(config);
				
		}
		catch(ComposerException ce){
			status = ComposerStatus.error;
			throw ce;
		}
		catch(Exception e){
			logger.error(e.toString(), e);
			e.printStackTrace();
			status = ComposerStatus.error;
			throw new ComposerException(e.toString(), e);
		}
		
		return status;
	}

	private void scrubProperties(SystemConfig config) {
		// Remove those properties not applicable for appliance systems
		String [] scrubPrefix = {"webapp.dnsapi", "webapp.caapi", 
								 "webapp.scheduleapi", "webapp.repository", 
								 "webapp.accounts", "webapp.authapi",								
								 "webapp.ingestapi", "webapp.tenantapi",
								 "webapp.dbapi"};
		List<String> scrubList = new ArrayList<String>();
		if (config.getApplianceEnabled()) {
			Enumeration<Object> keys = this.properties.keys();
			while(keys.hasMoreElements()) {
				String key = (String) keys.nextElement();
				for (String scrub : scrubPrefix) {
					if (key.startsWith(scrub))
						scrubList.add(key);					
				}
			}

			for (String scrub : scrubList) {
				this.properties.remove(scrub);
				logger.info(String.format("Removed propery not required for appliance {%s}: {%s}",
							scrub, config.getApplianceDomain()));
			}
		}
	}

	private void generateConnectorProperties(){
		this.properties.put("rtws.default.webapp.port", RtwsConfig.getInstance().getString("rtws.default.webapp.port"));
		this.properties.put("rtws.internal.webapp.port", RtwsConfig.getInstance().getString("rtws.internal.webapp.port"));
		this.properties.put("rtws.internal.webapp.unsecured.port", RtwsConfig.getInstance().getString("rtws.internal.webapp.unsecured.port"));
	}
	
	private void generateUserProperties(SystemConfig config){
		if(config.getProperties() != null){
			for(Property property : config.getProperties()){
				this.properties.put(property.getName(), property.getValue());
			}
		}
	}
	
	private void generateAnchorProperties(SystemConfig config) throws MalformedURLException, ComposerException{
		JSONObject anchor = getAnchorNodeInformation(this.tenantId);
		//h2.connection.url=jdbc:h2:tcp://auth-tms-dev.deleidos.com:8161/commondb
		//h2.dim.connection.url=jdbc:h2:tcp://auth-tms-dev.deleidos.com:8161/commondb;SCHEMA_SEARCH_PATH=DIMENSIONS
		//h2.app.connection.url=jdbc:h2:tcp://auth-tms-dev.deleidos.com:8161/commondb;SCHEMA_SEARCH_PATH=APPLICATION
		//ldap.provider.url=ldap://auth-tms-dev.deleidos.com:10389
		//webapp.accounts.url.host=auth-tms-dev.deleidos.com
		
		if (config.getApplianceEnabled()) {
			this.properties.put("h2.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;MULTI_THREADED=1;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", "APPLIANCE_DB_HOST"));
			this.properties.put("h2.dim.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;SCHEMA_SEARCH_PATH=DIMENSIONS;MULTI_THREADED=1;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", "APPLIANCE_DB_HOST"));
			this.properties.put("h2.app.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;SCHEMA_SEARCH_PATH=APPLICATION;MULTI_THREADED=1;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", "APPLIANCE_DB_HOST"));
			
		} else {
			this.properties.put("h2.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;MULTI_THREADED=1;LOCK_TIMEOUT=300000;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", anchor.getString(DATABASE)));
			this.properties.put("h2.dim.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;SCHEMA_SEARCH_PATH=DIMENSIONS;MULTI_THREADED=1;LOCK_TIMEOUT=300000;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", anchor.getString(DATABASE)));
			this.properties.put("h2.app.connection.url", String.format("jdbc:h2:tcp://%s:8161/commondb;SCHEMA_SEARCH_PATH=APPLICATION;MULTI_THREADED=1;LOCK_TIMEOUT=300000;MAX_OPERATION_MEMORY=268435456;MAX_COMPACT_TIME=60000", anchor.getString(DATABASE)));
		}
		this.properties.put("ldap.provider.url", String.format("ldap://%s:10389", anchor.getString(LDAP)));
		this.properties.put("webapp.accounts.url.host", anchor.getString(ACCOUNTS));
		this.properties.put("webapp.gatewayapi.url.host", anchor.getString(ACCOUNTS));
		
		//this.properties.put("tenant.dns.url", String.format("http://%s/dnsapi", anchor.getString(DATABASE)));
		
		//case for euca only, when external dns is not enabled public IP is used for external access
		if("EUC".equals(config.getServiceProvider()) && config.getExternalDnsEnabled() == false){
			this.properties.put("cas.service.url.host", anchor.getString(EUCA_ONLY_ANCHOR));
			
		}
		else{
			this.properties.put("cas.service.url.host", anchor.getString(ACCOUNTS));
		}
		
		//gateway/anchor host
		this.properties.put("tenant.gateway.host", anchor.getString(DATABASE));
		
		if("EUC".equals(config.getServiceProvider())) {
			this.properties.put("ntp.server", EUCA_CLOUD_IP);
		} else {
			this.properties.put("ntp.server", "0.amazon.pool.ntp.org,1.amazon.pool.ntp.org,2.amazon.pool.ntp.org,3.amazon.pool.ntp.org");
		}
	}
	
	/**
	 * Get dimension table information from Tenant API.  Builds URL to Dimension table in database.  Creates dimension table
	 * datasource.
	 * Default access for unit testing
	 * 
	 * @param tenantId String value
	 * @return List<Object> 
	 * @throws MalformedURLException
	 * @throws ComposerException 
	 */
	JSONObject getAnchorNodeInformation(String tenantId) throws MalformedURLException, ComposerException{
		String url = String.format("%s%s%s", RtwsConfig.getInstance().getString("webapp.tenantapi.url.path"),
				"/json/instance/anchor/", tenantId);
		WebResource resource = this.client.resource(url);
		String response = resource.get(String.class);
		
		logger.debug(resource.getURI().toURL().toString());
		
		JSONObject anchor = JSONObject.fromObject(response);
		logger.debug(anchor.toString(4));
		
		if(anchor.getString("ldap") == null){
			throw new ComposerException("Tenant does not have a anchor instance or with an incorrect anchor id stored in the database");
		}
		else if(anchor.getString("database") == null){
			throw new ComposerException("Tenant does not have a anchor instance or with an incorrect anchor id stored in the database");
		}
		else{
			//no more checks to perform on the returned object
		}
		
		return anchor;
	}
	
	private void generateInterfaceProperties(SystemContext systemContext) {
		String tempValue;
		int port;
		
		tempValue = this.properties.getProperty("rtws.path.home");
		this.properties.put("rtws.interfaces.config.path", String.format("%s/%s", tempValue, "conf"));
		
		// Service Interface
		if((tempValue = systemContext.getAvailabilityZone().getPropertiesFile()) != null) {
			this.properties.put("rtws.service.interface.propertiesFile", tempValue);
		}
		if((tempValue = systemContext.getAvailabilityZone().getServiceEndpoint().getURLString()) != null) {
			this.properties.put("rtws.service.interface.endpoint", tempValue);
		}
		
		// Storage Interface
		if((tempValue = systemContext.getAvailabilityZone().getPropertiesFile()) != null) {
			this.properties.put("rtws.storage.interface.propertiesFile", tempValue);
		}
		if((tempValue = systemContext.getAvailabilityZone().getStorageEndpoint().getEndpoint()) != null) {
			this.properties.put("rtws.storage.interface.endpoint", tempValue);
		}
		
		if((port = systemContext.getAvailabilityZone().getStorageEndpoint().getPort()) != 0) {
			this.properties.put("rtws.storage.interface.portNumber", String.valueOf(port));
		}
		if((tempValue = systemContext.getAvailabilityZone().getStorageEndpoint().getVirtualpath()) != null) {
			if (tempValue.trim().length() > 0) {
				this.properties.put("rtws.storage.interface.virtualPath", tempValue);
			}
		}
	}
	
	private void generateProperties(SystemConfig config){
		UserDataProperties udProps = UserDataProperties.getInstance();
		String domain = udProps.getString(UserDataProperties.RTWS_DOMAIN);
		String defaultNodeFQDN = "default." + domain;
		this.properties.put("webapp.repository.tenant.manager.url.host", defaultNodeFQDN);
		
		if (config.getApplianceEnabled()) {
			this.properties.put("rtws.cloud.provider", "APPLIANCE");
		} else {
			this.properties.put("rtws.cloud.provider", config.getServiceProvider());
		}
		
		this.properties.put("device.name.prefix", RtwsConfig.getInstance().getString("device.name.prefix"));
		
		if (config.getVpcEnabled()) {
			this.properties.put("rtws.vpc.enabled", Boolean.TRUE.toString());
		}
		//used for A&A
		this.properties.put("rtws.tmsTenantId", UserDataProperties.getInstance().getString(UserDataProperties.RTWS_TENANT_ID));
		
		// Let apps know whether or not the system is an auto scaling one
		this.properties.put("can.scale", Boolean.toString(config.getAutoScale()));		
	}
	
	/**
	 * Set the tenant id.
	 * 
	 * @param tenantId String value
	 */
	public void setTenantId(String tenantId){
		this.tenantId = tenantId;
	}
	
	/**
	 * Get properties.
	 */
	@Override
	public Properties getProperties() {
		return properties;
	}

}
