package com.deleidos.rtws.transport.Services;

import java.io.IOException;
import java.lang.reflect.Constructor;
import java.lang.reflect.InvocationTargetException;
import java.net.DatagramPacket;
import java.net.DatagramSocket;
import java.net.SocketException;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.LinkedBlockingQueue;
import java.util.concurrent.ThreadPoolExecutor;
import java.util.concurrent.TimeUnit;

import org.apache.log4j.Logger;

import com.deleidos.rtws.core.framework.Description;
import com.deleidos.rtws.core.framework.UserConfigured;
import com.deleidos.rtws.transport.AbstractTransportService;
import com.deleidos.rtws.transport.model.Encoding;

@Description("Transports UDP packets sent to a configured port")
public class UDPTransportService extends AbstractTransportService {

	private static final Logger log = Logger.getLogger(UDPTransportService.class);
	
	final private int DEFAULT_MAX_PACKET_SIZE = 65535;	// theoretical max for IPv4 but IPv6 can be larger
	final private int DEFAULT_LISTEN_PORT     = 46751;	// just some unused port
	final private long TERMINATE_TIMEOUT_SECONDS = 15;	// shutdown timeout in seconds
	final private String DEFAULT_MESSAGE_PROCESSOR_CLASS = "com.deleidos.rtws.transport.Services.utils.SimpleUDPMessageProcessor";
	// To validate java class name we really want to use something like: ^([\p{L}_\p{Sc}][\p{L}\p{N}_\p{Sc}]*\.)*[\p{L}_\p{Sc}][\p{L}\p{N}_\p{Sc}]*$
	// to handle java supported UNICODE names but RegExpValidator does not seem to support those so we use ASCII-only version: ^([a-zA-Z_$][a-zA-Z\d_$]*\.)*[a-zA-Z_$][a-zA-Z\d_$]*$
	final private String REGEX_JAVA_CLASS_NAME = "^([a-zA-Z_$][a-zA-Z\\d_$]*\\.)*[a-zA-Z_$][a-zA-Z\\d_$]*$";
	
	final private String ENCODE_CONTENT_DEFAULT = "None";
	final private String ENCODE_CONTENT_REGEX   = "^(" + ENCODE_CONTENT_DEFAULT + "|Base16|Base64)$";
	
	private int listenPort    = DEFAULT_LISTEN_PORT;
	private int maxPacketSize = DEFAULT_MAX_PACKET_SIZE;
	private String messageProcessorClass = DEFAULT_MESSAGE_PROCESSOR_CLASS;
	private Encoding contentEncoding = Encoding.None;

	private ExecutorService executorSvc = null;
	private Constructor<?> messageProcessorCtor = null;
	
	private DatagramSocket socket = null;

	public int getListenPort() {
		return listenPort;
	}
	
	@UserConfigured(value = "" + DEFAULT_LISTEN_PORT, description = "IP port number on which this transport will receive packets", flexValidator = "NumberValidator minValue=0 maxValue=65535")
	public void setListenPort(int listenPort) {
		this.listenPort = listenPort;
	}
	
	public int getMaxPacketSize() {
		return maxPacketSize;
	}

	@UserConfigured(value = "" + DEFAULT_MAX_PACKET_SIZE, description = "Maximum datagram packet size", flexValidator = { "NumberValidator minValue=16" })
	public void setMaxPacketSize(int maxPacketSize) {
		this.maxPacketSize = maxPacketSize;
	}

	public String getMessageProcessorClass() {
		return messageProcessorClass;
	}

	@UserConfigured(value=DEFAULT_MESSAGE_PROCESSOR_CLASS,
			flexValidator = { "RegExpValidator expression=" + REGEX_JAVA_CLASS_NAME + " noMatchError=\"Please provide a valid java class name.\"" },
		    description="Name of the java class that will be used to process received datagram packets")
	public void setMessageProcessorClass(String messageProcessorClass) {
		this.messageProcessorClass = messageProcessorClass;
	}

	public Encoding getEncoding() {
		return this.contentEncoding;
	}
	
	public void setEncoding(Encoding contentEncoding) {
		this.contentEncoding = contentEncoding;
	}
	
	public String getEncodeContent() {
		return this.contentEncoding.toString();
	}

	@UserConfigured(value=ENCODE_CONTENT_DEFAULT,
			flexValidator = { "RegExpValidator expression=" + ENCODE_CONTENT_REGEX },
		    description="Specifies whether the transport should encode received bytes before submitting them for processing. None means that no encoding will be performed. Base16 is the hexadecimal encoding")
	public void setContentEncoding(String contentEncoding) {
		try {
			this.contentEncoding = Encoding.valueOf(contentEncoding);
		}
		catch (IllegalArgumentException e) {
			log.error(String.format("Unexpected content encoding type '%s'", contentEncoding) );
			this.contentEncoding = Encoding.None;
		}
	}

	// execute() methods should continue processing messages until terminate() method is called
	public void execute() {
		log.info( "Starting UDPTransportService" );
		
		if ( executorSvc == null ) {
			int nThreads = Runtime.getRuntime().availableProcessors();
			executorSvc = new ThreadPoolExecutor(nThreads, nThreads,
							0L, TimeUnit.MILLISECONDS,
							new LinkedBlockingQueue<Runnable>(10*1024),
							new ThreadPoolExecutor.DiscardPolicy());
		}
		
		try {
			
			if ( messageProcessorCtor == null ) {
				Class<?> clazz = Class.forName(messageProcessorClass);
				if ( !Runnable.class.isAssignableFrom(clazz) ) {
					log.error(String.format("Message processor class '%s' does not implement Runnable", messageProcessorClass));
					return;
				}
				messageProcessorCtor = clazz.getConstructor(DatagramPacket.class, AbstractTransportService.class);
				log.info(String.format("Created constructor for message processor class '%s'", messageProcessorClass));
			}

			socket = new DatagramSocket(listenPort);
			
			DatagramPacket datagramPacket = null;
			while ( !executorSvc.isShutdown() ) {
				datagramPacket = new DatagramPacket(new byte[maxPacketSize], maxPacketSize);
				try {
					try {
						socket.receive(datagramPacket);
					} catch ( SocketException ex ) {
						if ( socket != null &&  !socket.isClosed() )
							log.error(String.format("Socket exception receiving packet on port %d", listenPort), ex);
						break;
					}
					Runnable messageProcessor = createMessageProcessorInstance(datagramPacket, this);
					if ( messageProcessor != null )
						executorSvc.submit(messageProcessor);
				} catch (IOException ex) {
					log.error(String.format("Error receiving packet on port %d", listenPort), ex);
				}
			}
			
			try {
				log.info(String.format("Awaiting worker threads termination with timeout %d sec", TERMINATE_TIMEOUT_SECONDS ));
				executorSvc.awaitTermination( TERMINATE_TIMEOUT_SECONDS, TimeUnit.SECONDS);
			} catch (InterruptedException ignored) {
				executorSvc.shutdownNow();
				try {
					log.info(String.format("Executing shutdown now with timeout %d sec", TERMINATE_TIMEOUT_SECONDS ));
					executorSvc.awaitTermination( TERMINATE_TIMEOUT_SECONDS, TimeUnit.SECONDS);
				} catch (InterruptedException ignored2) {
				}
			}
			
		} catch (SocketException ex) {
			log.error(String.format("Could not start datagram listener on port %d due to socket exception", listenPort), ex);
		} catch (SecurityException ex) {
			log.error(String.format("Could not start datagram listener on port %d due to security exception", listenPort), ex);
		} catch ( ClassNotFoundException ex ) {
			log.error(String.format("Could not find message processor class '%s'", messageProcessorClass), ex);
		} catch (NoSuchMethodException ex) {
			log.error(String.format("Message processor class '%s' does not have adeqate constructor", messageProcessorClass), ex);
		} finally {
			if ( socket != null && !socket.isClosed() )	{
				socket.close();
				socket = null;
			}
		}

		log.info( "Exiting UDPTransportService" );
	}

	public void terminate() {
		executorSvc.shutdown();
		if ( socket != null && !socket.isClosed() )
			socket.close();
	}
	
	private Runnable createMessageProcessorInstance(DatagramPacket datagramPacket, AbstractTransportService transportService) {
		try {
			return (Runnable)messageProcessorCtor.newInstance(datagramPacket, this);
		} catch (IllegalArgumentException ex) {
			log.error(String.format("Message processor class '%s' does not have adeqate constructor", messageProcessorClass), ex);
		} catch (InstantiationException ex) {
			log.error(String.format("Couldn't create message processor instance for class '%s' due to instantiation exception", messageProcessorClass), ex);
		} catch (IllegalAccessException ex) {
			log.error(String.format("Couldn't create message processor instance for class '%s' due to illegal access exception", messageProcessorClass), ex);
		} catch (InvocationTargetException ex) {
			log.error(String.format("Couldn't create message processor instance for class '%s' due to invocaiton target exception", messageProcessorClass), ex);
		}
		return null;
	}
}
