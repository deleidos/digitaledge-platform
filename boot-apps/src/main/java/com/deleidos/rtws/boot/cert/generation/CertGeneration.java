	private void initialize(String [] args){
		try{
			internalKeyPass = RtwsConfig.getInstance().getString("rtws.internal.keystore.password");
			
			//initialize sslconext
			sslContext = SSLContext.getInstance("TLS");
			sslContext.init(null, trustAllCerts, new SecureRandom());
		    
		    //host verifier that does not verifier, used for initial connection over 443
		    hostnameVerifier =  new HostnameVerifier() {
				@Override
				public boolean verify(String arg0, SSLSession arg1) {
					return true;
				}
		     };
		    
			ClientConfig config = new DefaultClientConfig();
			config.getProperties().put(ClientConfig.PROPERTY_CONNECT_TIMEOUT, 120000);
			config.getProperties().put(ClientConfig.PROPERTY_READ_TIMEOUT, 120000);
			//set a https setup that does not verifier cert, no certs at time of call
			config.getProperties().put(HTTPSProperties.PROPERTY_HTTPS_PROPERTIES, new HTTPSProperties(hostnameVerifier, sslContext));
			
			client = Client.create(config);
			
			certUtil = new CertUtils();
			
			introspectionClient = new IntrospectionClient();
			
			service = InterfaceConfig.getInstance().getServiceInterface();
		}
		catch(Exception e){
			logger.error(e.toString(), e);
		}
	}
	
	private void run(){
		try{
			//test api url before starting
			testAPIConnection();
			
			//create all csr
			CSR csr = new CSR();
			
			//save public cert
			//save self signed cert, get public cert, add cert to cacerts, generate external keystore/truststore
			if(getPublicCertStatus() == false){
				savePublicCert();  //save self signed key/cert
			}
			
			initializeLocalKeystores();
			
			//private domain as subject with an alternate subject name of the private ip
			String commonName = getCertificateDomain();
			
			//temporary fix need to revisit
			List<SubjectAltName> subjAltName = new ArrayList<SubjectAltName>();
			
			if(StringUtils.isBlank(getInstancePublicIp())){
				subjAltName.add(new SubjectAltName(commonName, SubjectAltName.TYPE.DNS));
				
				//do not add a IP to certificate if its GW, only DNS entries should be used
				if(isGateway() == false){
					subjAltName.add(new SubjectAltName(getInstancePrivateIp(), SubjectAltName.TYPE.IP));
				}
				if(SystemEnvUtil.isNatConfigured()){
					subjAltName.add(new SubjectAltName(UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN), SubjectAltName.TYPE.DNS));
				}
			}
			else{
				subjAltName.add(new SubjectAltName(commonName, SubjectAltName.TYPE.DNS));
				//do not add a IP to certificate if its GW, only DNS entries should be used
				if(isGateway() == false){
					subjAltName.add(new SubjectAltName(getInstancePrivateIp(), SubjectAltName.TYPE.IP));
					subjAltName.add(new SubjectAltName(getInstancePublicIp(), SubjectAltName.TYPE.IP));
				}
			}
			
			//always add loopback address for localhost to communicate with itsself on 8443, case for GW only having DNS no private IP that changes
			subjAltName.add(new SubjectAltName("127.0.0.1", SubjectAltName.TYPE.IP));
			
			if (SystemEnvUtil.isSingleTmsSystem()) {
				subjAltName.add(
						new SubjectAltName("api." + UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN), SubjectAltName.TYPE.DNS));
				subjAltName.add(
						new SubjectAltName("auth." + UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN), SubjectAltName.TYPE.DNS));
				subjAltName.add(
						new SubjectAltName("default." + UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN), SubjectAltName.TYPE.DNS));
				subjAltName.add(
						new SubjectAltName("master." + UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN), SubjectAltName.TYPE.DNS));
			}
			
			String request = certUtil.createRequest(commonName, CAProperties.getSigningEmail(), CAProperties.getTmpPrivateDomainKeyName(), subjAltName);
			
			csr.setPrivateDomainCsr(request);
			//csr.setUniquePrivateDomainFilename(String.format("%s_%s", commonName, CAProperties.getPrivateDomainAlias()));
			csr.setUniquePrivateDomainCn(commonName);
			csr.setSubjAltName(subjAltName);
			csr.setSetupKey(UserDataProperties.getInstance().getString(UserDataProperties.RTWS_SETUP_KEY));
			
			//can be done with http, only passing cert, key generated locally
			//process csr results
			if(!isGateway()){
				generateCerts(csr);
			}
			//sign root ica then sign cert to present from GW
			else if(isGateway()){
				//only generate on first startup
				if(doesICAExist() == false){
					generateCerts(csr, SIGN_TYPE.ca);
				}
				generateCerts(csr, SIGN_TYPE.cert);
			}
			else{
				logger.info("Unrecognized certificate signing case.");
			}
			
			//download trust chain for internal comms
			getTrustChain();
			
			//needs to be done over https, passing cert and key
			
			signPublicCert(getCertificateDomain());
			
			//download trust chain for external comms
			getPublicTrustChain();
			
			//copy truststore to default cacerts in java
			KeystoreUtil.addTrustedCertsToKeystore(new File(TRUST_STORE), CAProperties.getInternalTrustStorePassword(), certUtil.getDefaultKeystore(), CAProperties.getRootCAPassword());
			
			//still separate keystores are generated for activemq case, after resolved can now remove, jetty can use one keystore
			createInternalKeystore();
			createExternalKeystore();
			
			//fix/update chain for internal certificate after it is signed and the truststore can be downloaded
			KeystoreUtil.buildInternalChain(new File(TRUST_STORE), CAProperties.getInternalTrustStorePassword(), KEY_STORE, internalKeyPass);
		}catch(Exception e){
			logger.error(e.toString(), e);
		}
	}
	
	private void signPublicCert(String domain) throws Exception{
		//generate values used for public cert signing
		String uniqueFile = String.format("%s_public_domain", domain);
		String keyStore = String.format("%s%s", domain, CAProperties.getKeystorePublicSuffix());
		String setupKey = publicSetupKey;  //setup key is returned from signing internal cert, if internal validates then a hash can be retrieved for next op
		String cn = domain;
		
		//get cert/keypair for public use
		//not root ca or ica, only instances
		if(!isRoot() == true && ! new File(CAProperties.getPublicCertLocation()).exists() == true){
			getPublicCertInstance(cn, setupKey, uniqueFile, keyStore);
		}
		else{
			getPublicCertCA(cn, setupKey, uniqueFile, keyStore);
		}
		
		//common code between ca and nonca instances
		//get truststore, save to default truststore
		InputStream is = null;
		
		if(new File(CAProperties.getPublicCertLocation()).exists() && new File(CAProperties.getPublicKeyLocation()).exists()){
			is = StorageUtils.retrieveTruststore();
		}
		else{
			WebResource resource = this.client.resource(CAUrl.getCaApiUrl("retrieve_truststore"));
			logger.debug(resource.getURI().toString());
			
			ClientResponse response = resource.get(ClientResponse.class);
			is = response.getEntity(InputStream.class);
		}
		
		File tmpTrustStore = File.createTempFile("public_store_tmp", ".jks");
		tmpTrustStore.deleteOnExit();
		FileUtils.copyInputStreamToFile(is, tmpTrustStore);
		is.close();
		
		//copy public truststore to generated local truststore
		KeystoreUtil.addTrustedCertsToKeystore(tmpTrustStore, CAProperties.getRootCAPassword(), TRUST_STORE, CAProperties.getInternalTrustStorePassword());
	}
	
	/**
	 * Get public cert for non ca instances.
	 * @throws Exception 
	 */
	private void getPublicCertInstance(String domain, String setupKey, String uniqueFile, String keyStore) throws Exception{
		
		if(StringUtils.isBlank(domain) == false){
			WebResource resource = null;
			resource = this.client.resource(CAUrl.getCaApiUrl("sign_public"));
			
			Form form = new Form();
			form.add("domain", domain);
			form.add("hash", setupKey);
			form.add("certFileName", uniqueFile);
			form.add("certType", CERT_TYPE.external.toString());
			form.add("keyStore", keyStore);
			
			logger.debug(resource.getURI().toString());
			
			//testAPIConnection();  //test that the api node/connection is reachable
			
			ClientResponse response = resource.post(ClientResponse.class, form);
			if (response.getStatus() != 200) {
				String body = response.getEntity(String.class);
				logger.warn(String.format("CA API sign_public called returned error code %s with body %s", response.getStatus(), body));
			}
						
			InputStream is = response.getEntity(InputStream.class);
			
			File tmpFile = File.createTempFile("public_trust", "jks");
			FileUtils.copyInputStreamToFile(is, tmpFile);
			is.close();
			
			certUtil.copyCertBetweenKeystore(tmpFile.getAbsolutePath(), CAProperties.getInternalKeyPassword(), KEY_STORE, internalKeyPass, CAProperties.getPublicAlias(), TYPE.equal, false);
			//certUtil.addKeyAndCertToKeyStore(INTERNAL_KEY_PASS, CAProperties.getPublicAlias(), KEY_STORE, KEY_DER, CERT_DER, false);
		}
		else{
			logger.error("System property RTWS_DOMAIN must be set to get a public certificate for the domain.");
		}
	}
	
	/**
	 * Get public cert for root ca or intermediate ca.
	 * 
	 * 
	 * @throws InvalidKeyException
	 * @throws IllegalBlockSizeException
	 * @throws BadPaddingException
	 * @throws NoSuchAlgorithmException
	 * @throws InvalidKeySpecException
	 * @throws NoSuchPaddingException
	 * @throws StorageUtilsException
	 * @throws IOException
	 * @throws InterruptedException
	 */
	private void getPublicCertCA(String domain, String setupKey, String uniqueFile, String keyStore) throws InvalidKeyException, IllegalBlockSizeException, BadPaddingException, NoSuchAlgorithmException, InvalidKeySpecException, NoSuchPaddingException, StorageUtilsException, IOException, InterruptedException{
		InputStream is = certUtil.signPublicCert(domain, uniqueFile, CERT_TYPE.external, keyStore, domain, SIGN_TYPE.cert);
		
		File tmpFile = File.createTempFile("public_trust", "jks");
		FileUtils.copyInputStreamToFile(is, tmpFile);
		is.close();
		
		certUtil.copyCertBetweenKeystore(tmpFile.getAbsolutePath(), CAProperties.getInternalKeyPassword(), KEY_STORE, internalKeyPass, CAProperties.getPublicAlias(), TYPE.equal, false);
	}
	
	private boolean isRoot(){
		String rootCA = UserDataProperties.getInstance().getString("RTWS_ROOT_CA");
		
		if(StringUtils.isNotBlank(rootCA) == true &&  "true".equals(rootCA)){
			return true;
		}
		else{
			return false;
		}
	}
	
	private boolean isGateway(){
		String rootCA = UserDataProperties.getInstance().getString(UserDataProperties.RTWS_IS_GATEWAY);
		
		if(StringUtils.isNotBlank(rootCA) == true &&  "true".equals(rootCA)){
			return true;
		}
		else{
			return false;
		}
	}
	
	private boolean getPublicCertStatus() throws StorageUtilsException, InterruptedException, IOException{
		//true = public cert already exits
		//false = public cert doesn't exist, need to generate self signed cert
		
		boolean alreadyExists = false;
		List<String> hasToContain = StorageUtils.getStatusCheckList();
		
		
		if(isRoot() == true || new File(CAProperties.getPublicCertLocation()).exists() == true){
			List<String> files = StorageUtils.listFiles();
			
			for(String file : files){
				for(int i = 0; i < hasToContain.size(); i++){
					if(file.contains(hasToContain.get(i))){
						hasToContain.remove(i);
						break;
					}
				}
			}
		}
		else{
			WebResource resource = null;
			resource = this.client.resource(CAUrl.getCaApiUrl("list_files"));
			logger.debug(resource.getURI().toString());
			
			ClientResponse response = resource.get(ClientResponse.class);
			String list = response.getEntity(String.class);
			
			JSONArray files = JSONArray.fromObject(list);
			
			Iterator<?> nodeItr = files.iterator();
			while(nodeItr.hasNext()){
				String fileName = (String) nodeItr.next();
				
				for(int i = 0; i < hasToContain.size(); i++){
					if(fileName.contains(hasToContain.get(i))){
						hasToContain.remove(i);
						break;
					}
				}
			}
		}
		
		//found all required files
		if(hasToContain.size() == 0){
			alreadyExists = true;
		}
	
		return alreadyExists;
	}
	
