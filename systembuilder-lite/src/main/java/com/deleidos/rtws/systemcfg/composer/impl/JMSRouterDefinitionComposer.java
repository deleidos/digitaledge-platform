package com.deleidos.rtws.systemcfg.composer.impl;

import java.util.List;
import java.util.Properties;

import javax.xml.bind.MarshalException;

import org.apache.log4j.LogManager;
import org.apache.log4j.Logger;
import org.w3c.dom.Element;
import org.w3c.dom.Node;

import com.deleidos.rtws.commons.config.RtwsConfig;
import com.deleidos.rtws.commons.exception.DefaultConfigurationException;
import com.deleidos.rtws.commons.exception.InitializationException;
import com.deleidos.rtws.systemcfg.bean.ComposerStatus;
import com.deleidos.rtws.systemcfg.bean.DataSink;
import com.deleidos.rtws.systemcfg.bean.SystemContext;
import com.deleidos.rtws.systemcfg.composer.DefinitionComposer;
import com.deleidos.rtws.systemcfg.exception.ComposerException;
import com.deleidos.rtws.systemcfg.parser.XMLParser;
import com.deleidos.rtws.systemcfg.userinput.bean.SystemConfig;
import com.deleidos.rtws.systemcfg.util.RtwsProperties;
import com.deleidos.rtws.systemcfg.util.SystemBuilderUtil;

public class JMSRouterDefinitionComposer implements DefinitionComposer {
	
	private static Logger logger = LogManager.getLogger(JMSRouterDefinitionComposer.class);
	
	private RtwsProperties properties;
	private XMLParser externalParser;
	private XMLParser internalParser;
	
	private String templateFile;
	private String outputDir;
	private int numNodes;
	private String externalOutputFilename = "jms-router.external.xml";
	private String internalOutputFilename = "jms-router.internal.xml";
	
	private final String confDir = "/jms-router/";

	@Override
	public void initialize() throws InitializationException {
		externalParser = new XMLParser(true);
		internalParser = new XMLParser(true);
		
		String templateFile = null;
		String baseDir = (String) RtwsConfig.getInstance().getConfiguration().getProperty("sysbuilder.template.basedir");
		
		//initialize template file locations
		//template file property sets, for lookup in composer later
		//set template file in properties
		templateFile = String.format("%s/%s", baseDir, "jms-router-template.xml");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.jms.router.template", templateFile);
		
		//set default property composer property file template
		templateFile = String.format("%s/%s", baseDir, "default.properties");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.service.default.properties", templateFile);
	}

	@Override
	public void dispose() {
		//not implemented
	}

	@Override
	public void writeFile(String version, String fileName) {
		if (this.numNodes > 1) {
			externalParser.writeXmlFile(outputDir + externalOutputFilename);
			internalParser.writeXmlFile(outputDir + internalOutputFilename);
		}
	}

	@Override
	public void loadDefaults(String version, SystemConfig config,
			SystemContext systemContext, RtwsProperties properties)
			throws MarshalException, DefaultConfigurationException {
		this.properties = properties;  //initialize properties
		//get activemq.xml template file
		this.templateFile = RtwsConfig.getInstance().getString("sysbuilder.jms.router.template");
		externalParser.readXmlFile(templateFile);  //parse to dom object
		internalParser.readXmlFile(templateFile);  //parse to dom object
		
		//setup local compose values
		buildLocalConfig(config, systemContext);
	}
	
	/**
	 * Setup configuration values/parameters based on SystemConfig and SystemContext.
	 * 
	 * @param config
	 * @param systemContext
	 * @throws DefaultConfigurationException
	 */
	private void buildLocalConfig(SystemConfig config, SystemContext systemContext) throws DefaultConfigurationException{
		this.outputDir = config.getOutputConfDir() + confDir;
		SystemBuilderUtil.mkdir(this.outputDir);  //create directory
		if(systemContext.getSystemSize() != null){
			this.numNodes = systemContext.getSystemSize().getJmsInstanceCount();
		}
		else{
			logger.warn("No record was returned from the SystemSizing table for the user defined system size.");
			throw new DefaultConfigurationException("No record was returned from the SystemSizing table for the user defined system size.");
		}
	}

	@Override
	public ComposerStatus compose(SystemConfig config,
			SystemContext systemContext) throws ComposerException {
		
		ComposerStatus status = ComposerStatus.done;
		try {
			// only configuring the jms-router for systems with more than 1 jms node
			if (this.numNodes > 1) {
				// set the external queue name to the router queue so all messages go to this queue
				this.properties.setProperty("messaging.queue.external.name", this.properties.getProperty("messaging.queue.router.name"));
				
				// messages to the router queue from the transport are not persisted for performance
				this.properties.setProperty("messaging.delivery.persistent", "false");
				
				// compose both the external and internal configuration files
				composeRouter(externalParser, "external", properties.getProperty("messaging.queue.parse.name"));
				clearModelsCache(externalParser);
				
				// handle system with multiple sinks (queues)
				String[] queues = new String[config.getDataSinks().size()];
				int index = 0;
				for (DataSink dataSink : config.getDataSinks()) {
					queues[index++] = SystemBuilderUtil.generateDataModelQueueName(dataSink.getFqn());
				}
				composeRouter(internalParser, "internal", queues);
				configureModelsCache(internalParser, config);
			}
		}
		catch(Exception e){
			status = ComposerStatus.error;
			logger.error(e.toString(), e);
			throw new ComposerException(e.toString(), e);
		}
		return status;
	}
	
	private void composeRouter(XMLParser parser, String externalInternalID, String... destinationQueues) throws Exception {
		
		Element templateSource = parser.getElement("jms-source");
		String templateSourceString = XMLParser.elementToString(templateSource);
		parser.removeNode(templateSource);
		
		Element templateDest = parser.getElement("jms-destinations");
		String templateDestString = XMLParser.elementToString(templateDest);
		parser.removeNode(templateDest);
		
		String sourceString = templateSourceString.replace("#location#", externalInternalID);
		sourceString = properties.filterProperty(sourceString);
		Node sourceNode = XMLParser.stringToElement(sourceString);
		parser.importNode(parser.createTabNode(), true);
		parser.importNode(sourceNode, true);
		
		for (int i = 1; i <= this.numNodes; i++) {
			StringBuilder sb = new StringBuilder();
			for (String queueName: destinationQueues) {
				String queueString = String.format("%s%s%s", "<queues>", queueName, "</queues>");
				sb.append(queueString);
			}
			
			String destString = templateDestString.replace("#location#", externalInternalID).
				replace("#nodeNum#", String.valueOf(i)).
				replace("<queues/>", sb.toString());
			destString = properties.filterProperty(destString);
			Element destElement = XMLParser.stringToElement(destString);
			parser.importNode(destElement, true);
		}
	}
	
	private void configureModelsCache(XMLParser parser, SystemConfig config) {
		Element templateModelsCache = parser.getElement("models-cache");

		Element templateModels = parser.getElement("models");
		String templateModelsString = XMLParser.elementToString(templateModels);
		templateModelsCache.removeChild(templateModels);
		
		for(String dmName : config.getDataModels()) {
			StringBuilder sbQueues = new StringBuilder();
			for(DataSink dataSink : config.getDataSinks()){
				List<String> dataSinkModels = dataSink.getDataModels();
				if (dataSinkModels == null || dataSinkModels.contains(dmName) ) {
					String queueName = SystemBuilderUtil.generateDataModelQueueName(dataSink.getFqn());
					String queueString = String.format("%s%s%s", "<queues>", queueName, "</queues>");
					sbQueues.append(queueString);
				}
			}
			String modelsString = templateModelsString.replace("#modelName#", dmName).replace("<queues/>", sbQueues.toString());
			Element modelsElement = XMLParser.stringToElement(modelsString);
			parser.importNode(modelsElement, true, templateModelsCache);
		}

		// just to be consistent with the rest of the builder and have models-cache as the last element instead of second
		parser.removeNode(templateModelsCache);
		parser.importNode(templateModelsCache, true);
	}
	
	private void clearModelsCache(XMLParser parser) {
		Element templateModelsCache = parser.getElement("models-cache");
		parser.removeNode(templateModelsCache);
	}
	
	@Override
	public Properties getProperties() {
		return properties;
	}
	
}
