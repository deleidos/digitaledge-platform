import com.deleidos.rtws.webapp.register.client.DbApiRestClient;
import com.deleidos.rtws.webapp.register.client.GatewayApiRestClient;
import com.deleidos.rtws.webapp.register.client.RegisterApiRestClient;
import com.deleidos.rtws.webapp.register.client.RestClientException;
import com.deleidos.rtws.webapp.register.client.RestClientManager;
import com.deleidos.rtws.webapp.register.client.TenantApiRestClient;
import com.deleidos.rtws.webapp.register.form.AccountForm;
import com.deleidos.rtws.webapp.register.form.NetworkType;
import com.deleidos.rtws.webapp.register.model.AccountStatus;
import com.deleidos.rtws.webapp.register.model.DigitalEdgeSecurityGroup;
import com.deleidos.rtws.webapp.register.model.VpcNetwork;
import com.deleidos.rtws.webapp.register.model.VpcNetworkBuilder;
import com.deleidos.rtws.webapp.register.util.SubnetCalculator;

public abstract class AbstractNetworkService extends AbstractAccountService {
	
	private static final String ALL_CIDR_BLOCK = "0.0.0.0/0";
	private static final String INSTANCE_TENANCY = "default";
	private static final Integer MAX_SUBNET_PER_VPC = 10;
	private static final Integer MAX_ADDRESS_PER_SUBNET = 1024;
	
	private Logger logger = Logger.getLogger(this.getClass());
	
	protected VpcNetwork vpc;
	
	protected AbstractNetworkService(AccountForm form) {
		super(form);
		this.vpc = null;
	}
	
	protected void createNetwork() throws GatewayServiceException {
		
		checkParametersNotNull(this.form);
		
		if (this.vpc == null) {
			createVpcNetwork(form);
		}
		
	}
	
	protected void addNetworkDbEntry() throws GatewayServiceException, ClientException {
		
		checkParametersNotNull(this.form);
		if (this.vpc != null && this.vpc.getState() == VpcNetwork.State.New) {
			RestClientManager.getTenantApiClient().addVpcNetwork(this.vpc,form.getTenantId());
		}
		
	}
	
	@Override
	protected String launchGatewayInstance() throws GatewayServiceException {
		
		checkParametersNotNull(this.form, this.gateway);
		
		boolean inVpc = (form.getNetworkType() == NetworkType.VPC) ? true : false;
		
		if (inVpc) {
			String securityGroupId = RestClientManager.getCloudProviderClient().findVpcSecurityGroupId(form, 
					this.vpc.getVpcId(), DigitalEdgeSecurityGroup.VPC_WEBAPP_SG_NAME, endpoint.getEC2Endpoint());
			this.gateway.setSecurityGroupId(securityGroupId);
			this.gateway.setSubnetId(vpc.getPublicSubnetId());
		} else {
			String securityGroupId = 
					RestClientManager.getCloudProviderClient().findSecurityGroupId(form, DigitalEdgeSecurityGroup.WEBAPP_SG_NAME, endpoint.getEC2Endpoint());
			this.gateway.setSecurityGroupId(securityGroupId);
		}
		
		String instanceId = super.launchGatewayInstance();
		
		if (inVpc) {
			String elasticAddress = RestClientManager.getCloudProviderClient().allocateElasticAddress(form, endpoint.getEC2Endpoint());
			RestClientManager.getCloudProviderClient().associateElasticAddress(form, instanceId, elasticAddress, endpoint.getEC2Endpoint());
		}
		
		return instanceId;
		
	}
	
	private String getGatewayIp(String instanceid, AccountForm form) throws RestClientException{
		
		DbApiRestClient dbClient = RestClientManager.getDBApiClient();
		return dbClient.getGatewayDNS(form.getAccountId());
	}
	
	public void validateGatewayInstance(String instanceId, AccountForm form)
			throws GatewayServiceException {
		GatewayApiRestClient gatewayClient = RestClientManager
				.getGatewayApiClient();

		int timeout = 60 * 60 * 1000;// one hour timeout - 60 minutes *
										// 60seconds/minute * 1000ms/second
		long start = DateTime.now().getMillis();

		// try for an hour to connect to jetty on gw
		while (true) {
			long now = DateTime.now().getMillis();
			if ((now - start) > timeout)
				throw new GatewayServiceException(
						"Gateway failed to respond in timeout period.");
			try {
				String gwIp = getGatewayIp(instanceId, form);
				if(gwIp!=null && gwIp.length()>0){
					String accountSystems = gatewayClient.getAccountSystems(gwIp);
					if (accountSystems != null && accountSystems.length() > 0){
						break;// exits the loop, returning success
					}// else is null or empty, keep trying
				}//else no ip yet
			} catch (Exception e) {
				// didn't work yet, no problem
			}
			try{Thread.sleep(10*1000);}catch(Exception drop){}
		}
	}
	
	protected boolean isSwitchingFromVpcToDefaultNetwork(AccountForm form) throws GatewayServiceException {
		
		checkParametersNotNull(form);
		
		if (this.vpc != null) {
			// Previously the gateway instance was running in a vpc network.
			// The user wants to switch to running it in a default network.
			
			if (form.getNetworkType() != NetworkType.VPC) {
				return true;
			}
		}
		
		return false;
		
	}
	
	private boolean createVpcNetwork(AccountForm form) throws GatewayServiceException {
				
		if (this.vpc != null && this.vpc.getVpcId() != null) {
			return false;
		}
		
		VpcNetworkBuilder builder = new VpcNetworkBuilder();
		
		try {
			String CIDRBlock="10.0.0.0/16";
			String publicSubnet = "10.0.0.0/22";
			String privateSubnet = "10.0.200.0/22";
			//String vpcCidrBlock = SubnetCalculator.calculateCidrBlock(MAX_SUBNET_PER_VPC, MAX_ADDRESS_PER_SUBNET);
			String vpcId = RestClientManager.getCloudProviderClient().createVPC(form, this.endpoint.getEC2Endpoint(), CIDRBlock, INSTANCE_TENANCY);
			
			builder.setVpcId(vpcId); // vpc network created
			
			String internetGatewayId = RestClientManager.getCloudProviderClient().createInternetGateway(form, endpoint.getEC2Endpoint());
			
			builder.setInternetGatewayId(internetGatewayId); // internet gateway created
			
			RestClientManager.getCloudProviderClient().attachInternetGateway(form, internetGatewayId, vpcId, endpoint.getEC2Endpoint());
		
			//String subnetCidrBlock = SubnetCalculator.nextAvailableSubnet(Collections.<String>emptyList(), vpcCidrBlock, MAX_ADDRESS_PER_SUBNET);
			String publicSubnetId = RestClientManager.getCloudProviderClient().createPublicSubnet(form, vpcId, publicSubnet, endpoint.getEC2Endpoint());
			//String privateSubnetId = RestClientManager.getCloudProviderClient().createPrivateSubnet(form,vpcId,privateSubnet,endpoint.getEC2Endpoint());
			
			builder.setPublicSubnetId(publicSubnetId); // public subnet created
			
			String mainRouteTableId = RestClientManager.getCloudProviderClient().getVpcMainRouteTableId(form, vpcId, endpoint.getEC2Endpoint());
			if(mainRouteTableId==null){
				mainRouteTableId = RestClientManager.getCloudProviderClient().createVpcRouteTable(form,vpcId,endpoint.getEC2Endpoint());
			}
			
			RestClientManager.getCloudProviderClient().associateRouteTable(form, mainRouteTableId, publicSubnetId, endpoint.getEC2Endpoint());
			RestClientManager.getCloudProviderClient().createInternetGatewayRoute(form, ALL_CIDR_BLOCK, internetGatewayId, mainRouteTableId, endpoint.getEC2Endpoint());
			
			RestClientManager.getCloudProviderClient().createVpcSecurityGroups(form, vpcId, endpoint.getEC2Endpoint());
			
			this.vpc = builder.toVpcNetwork();
			
			return true;
		} catch (Exception ex) {
			logger.error("Fail to create VPC network for gateway instance.", ex);
			
			this.vpc = builder.toVpcNetwork();
			deleteVpcNetwork(form);
			
			throw new GatewayServiceException(ex.getMessage());
		}
		
	}
	
	private boolean deleteVpcNetwork(AccountForm form) throws GatewayServiceException {
		
		if (this.vpc == null || this.vpc.getVpcId() == null) {
			return false;
		}
			
		try {
			String mainRouteTableId = 
					RestClientManager.getCloudProviderClient().getVpcMainRouteTableId(form, this.vpc.getVpcId(), endpoint.getEC2Endpoint());
			
			if (mainRouteTableId != null) {
				RestClientManager.getCloudProviderClient().deleteRouteTable(form, mainRouteTableId, endpoint.getEC2Endpoint());
			}
		} catch (Exception ex) {
			logger.error("Fail to delete VPC route tables.", ex);
		}
		
		try {
			if (this.vpc.getPublicSubnetId() != null) {
				RestClientManager.getCloudProviderClient().deleteSubnet(form, this.vpc.getPublicSubnetId(), endpoint.getEC2Endpoint());
			}
		} catch (Exception ex) {
			logger.error("Fail to delete the VPC public subnets.", ex);
		}
		
		try {
			if (this.vpc.getInternetGatewayId() != null) {
				RestClientManager.getCloudProviderClient().detachInternetGateway(form, this.vpc.getInternetGatewayId(), this.vpc.getVpcId(), endpoint.getEC2Endpoint());
			}
		} catch (Exception ex) {
			logger.error("Fail to detach VPC internet gateway.", ex);
		}

		try {
			if (this.vpc.getInternetGatewayId() != null) {
				RestClientManager.getCloudProviderClient().deleteInternetGateway(form, this.vpc.getInternetGatewayId(), endpoint.getEC2Endpoint());
			}
		} catch (Exception ex) {
			logger.error("Fail to delete VPC internet gateway.", ex);
		}
			
		try {
			RestClientManager.getCloudProviderClient().deleteVpcSecurityGroups(form, this.vpc.getVpcId(), endpoint.getEC2Endpoint());
		} catch (Exception ex) {
			logger.error("Fail to delete VPC security groups.", ex);
		}
			
		try {
			RestClientManager.getCloudProviderClient().deleteVPC(form, this.vpc.getVpcId(), endpoint.getEC2Endpoint());	
		} catch (Exception ex) {
			logger.error("Fail to delete VPC network with public and private subnets.", ex);
			throw new GatewayServiceException("Fail to delete VPC '" + this.vpc.getVpcId() + "'.", ex);
		}
		
		return true;
		
	}
	
}