/**
 * Abstract base class for processing services.
 */
public abstract class AbstractTransportService implements TransportService, Initializable {

	protected static final Logger log = Logger.getLogger(AbstractTransportService.class);

	private String JMSURL;
	private String JMSQueue;
	private String JMSUser;
	private String JMSPassword;
	private boolean persistent = false;
	private String filenameKey = null;
	
	private Map<String,String> nonFatalErrorMap = null;
	
	/**
	 * CSV Header value appended to each JMS message.
	 */
	//This value must be set in user data, the csv header from the file parsed by the transport
	//is stripped from the file and not appended to the JMS message.  The splitter can be configured
	//with a line number value that will strip the header from all files.
	private String header;
	
	/**
	 * The input data format, send in JMS message header as Data.Format, used to select a data source parser.
	 */
	private String inputFormat;

	/**
	 * A format for records, to help with determining record boundaries in input data containing multiple records.
	 */
	private String recordFormat;

	/**
	 * For CSV record format, the number of lines in input data that contain the header.
	 */
	private int recordHeaderLines = 0;

	/**
	 * Splitter implementation to separate individual records in input data.
	 */
	private Splitter splitter;

	protected ActiveMQSslTransportFactory activeMQSslTransportFactory;
	protected RoundRobinJMSConnectionFactory connectionFactory;
	protected JMSBundler bundler;

	/**
	 * Constructor.
	 */
	public AbstractTransportService() {
		super();
		bundler = new JMSBundler();
		
	}

	@NotNull
	public String getInputFormat(){
		return inputFormat;
	}

	@UserConfigured(description = "The input data source format", value = "input-format")
	public void setInputFormat(String value){
		inputFormat = value;
	}
	
	public String getHeader(){
		return header;
	}
	
	public void setHeader(String value){
		
		header = value.trim();
		header+=System.getProperty("line.separator");
		bundler.setHeader(header);
	}

	@NotNull
	public String getRecordFormat() {
		return recordFormat;
	}

	@UserConfigured(description = "Input data record type, to help with determining record boundaries in input data containing multiple records.",
			value = "record-format")
	public void setRecordFormat(String recordFormat) {
		this.recordFormat = recordFormat;
		splitter = SplitterFactory.create(recordFormat);
		splitter.setNumHeaders(recordHeaderLines);
	}

	public int getRecordHeaderLines() {
		return recordHeaderLines;
	}

	@UserConfigured(description = "The number of lines in the input data that are header lines not to be treated as data",
			value = "0",
			flexValidator = { "NumberValidator minValue=0 maxValue=" + Integer.MAX_VALUE })
	public void setRecordHeaderLines(int recordHeaderLines) {
		this.recordHeaderLines = recordHeaderLines;
		if (splitter != null) {
			splitter.setNumHeaders(recordHeaderLines);
		}
	}

	@NotNull
	public Splitter getSplitter() {
		return splitter;
	}

	public void setSplitter(Splitter splitter) {
		this.splitter = splitter;
	}

	public void initialize() {
		if(activeMQSslTransportFactory != null){
			log.info("Starting SSL Transport Factory");
			try {
				activeMQSslTransportFactory.initialize();
			} catch (Exception e) {
				log.error(String.format("Error starting SSL transport:  %s.", e.toString()),e);
			}
		}

		// TODO Remove this bypass if/when the command listener is run in appliance mode
		if (SystemEnvUtil.isInApplianceMode()) {
			// noop
			nonFatalErrorMap = new HashMap<String,String>();
		} else {
			HazelcastInstance hazelcast = HazelcastHelper.instance.getHazelcastLocalClient();
			nonFatalErrorMap = hazelcast.getMap("monitor");
		}
		
		
		connectionFactory = new RoundRobinJMSConnectionFactory();
		connectionFactory.setBrokerURL(JMSURL);
		connectionFactory.setUserName(JMSUser);
		connectionFactory.setPassword(JMSPassword);
		connectionFactory.setAlwaysSyncSend(true);
		//= new RoundRobinJMSConnectionFactory(JMSUser, JMSPassword, JMSURL);
		bundler.setConnectionFactory(connectionFactory);
		bundler.setQueues(JMSQueue);
		if (persistent) {
			bundler.setDeliveryMode(DeliveryMode.PERSISTENT);
		} else {
			bundler.setDeliveryMode(DeliveryMode.NON_PERSISTENT);
		}
		Properties props = new Properties();
		props.put(RtwsConfig.getInstance().getConfiguration().getString("messaging.header.format"), inputFormat);
		bundler.setProperties(props);		
		bundler.initialize();
	}

	public void dispose(){
		bundler.dispose();
	}
	
	/**
	 * Adds the message to a a buffer and sends the message at the defined buffer size.
	 * @param msg
	 */
	public void SendJMSMessage(String msg){
		log.debug("adding message to bundler: "+msg);
		bundler.appendln(msg);
	}
	
	public void SendJMSMessageAndFlush(String msg){
		log.debug("adding message to bundler: "+msg);
		bundler.appendln(msg);
		bundler.flush();
	}
	
	/**
	 * Sends the message immediately, message is not bundled.
	 * @param record
	 */
	public void SendJMSRecord(String record){
		log.debug(String.format("Sending recording:  %s", record));
		bundler.sendRecord(record);
	}

	@SystemConfigured(value = "@messaging.external.connection.url@")
	public void setUrl(String value){
		JMSURL=value;
	}

	@NotNull
	public String getUrl()
	{
		return JMSURL;
	}

	@SystemConfigured(value = "@messaging.external.connection.user@")
	public void setUser(String value){
		JMSUser=value;
	}

	@NotNull
	public String getUser(){
		return JMSUser;
	}

	@SystemConfigured(value = "@messaging.external.connection.password@")
	public void setPassword(String value){
		JMSPassword = value;
	}

	@NotNull
	public String getPassword(){
		return JMSPassword;
	}

	@SystemConfigured(value = "@messaging.queue.external.name@")
	public void setQueue(String value){
		JMSQueue = value;
	}

	@NotNull
	public String getQueue(){
		return JMSQueue;
	}
	
    public void setActivemqSslTransportFactory(ActiveMQSslTransportFactory factory) {
    	activeMQSslTransportFactory = factory;
    }
    
    @SystemConfigured(value = "@messaging.delivery.persistent@")
    public void setPersistent(boolean value) {
		persistent = value;
	}

    public boolean getPersistent() {
		return persistent;
	}
    
    // Sleep for an interval with the option to wake up early
    // Useful when the main transport thread is being terminated
    protected class WakeableSleepHelper {
    	private long totalSleepTime = 0;
    	private long wakeTime = 0;
    	private long checkInterval = 1000;
    	
    	public WakeableSleepHelper(long totalSleepTime) {
    		this.totalSleepTime = totalSleepTime;
    		if (this.totalSleepTime < this.checkInterval) {
    			this.checkInterval = this.totalSleepTime;
    		}
    		this.wakeTime = System.currentTimeMillis() + this.totalSleepTime;
    	}
    	
    	public WakeableSleepHelper(long totalSleepTime, long checkInterval) {
    		this.totalSleepTime = totalSleepTime;
    		this.checkInterval = checkInterval;
    		if (this.totalSleepTime < this.checkInterval) {
    			this.checkInterval = this.totalSleepTime;
    		}
    		this.wakeTime = System.currentTimeMillis() + this.totalSleepTime;
    	}
    	
    	public boolean continueSleeping(boolean wakeFlag) throws InterruptedException {
    		if (wakeFlag || System.currentTimeMillis() > wakeTime) {
    			return false;
    		} else {
    			Thread.sleep(checkInterval);
				return true;
    		}
    	}
    }
    
    public void SetFileNameProperty(String name) {
    	if (filenameKey == null)
    		filenameKey = RtwsConfig.getInstance().getString("messaging.header.filename");
    	
		bundler.addProperty(filenameKey, name);		
	}
    
    public void Reset() {
    	bundler.flush();
    	bundler.clearProperty(filenameKey);
    }
    
    protected void clearNonFatalError(){
    	nonFatalErrorMap.remove(this.getClass().getSimpleName());
    }
    
    protected void setNonFatalErrorMessage(String message){
    	nonFatalErrorMap.put(this.getClass().getSimpleName(),message);
    }
    
    public void addMessageProperty(String key, String value){
    	bundler.addProperty(key, value);
    }
    
    /**
     * Creates a file on the file system in the transport logs directory to indicate the file is currently inactive.
     * 
     * @param transport		a reference to itself
     */
    protected void setInactive(AbstractTransportService transport) {
    	File inactiveFlagFile = getInactiveFlagFile(transport);
    	
    	try {
    		FileUtils.touch(inactiveFlagFile);
    	} catch (IOException e) {
    		log.error(String.format("Failed to touch transport inactive flag file %s.", inactiveFlagFile.getAbsolutePath()), e);
    	}

    }
    
    /**
     * Deletes the flag file indicating the transport is currently inactive in the transport logs directory that .
     * 
     * @param transport		a reference to itself
     */
    protected void setActive(AbstractTransportService transport) {
    	File inactiveFlagFile = getInactiveFlagFile(transport);
    	
    	
		if(inactiveFlagFile.exists()) {
			if(!inactiveFlagFile.delete()) {
	    		log.error(String.format("Failed to delete transport inactive flag file %s.", inactiveFlagFile.getAbsolutePath()));
			}
		}
    		
    }
    
    private File getInactiveFlagFile(AbstractTransportService transport) {
    	String filePath = String.format("%s/%s_%s_%s",
    										SoftwareUtil.TRANSPORT_LOGS_DIR,
    										SoftwareUtil.TRANSPORT_INACTIVE_LOGS_FILENAME, 
											transport.getClass().toString().replaceAll(" " , "_"),
											Integer.toHexString((System.identityHashCode(transport))));
    	
    	return new File(filePath);
    }
}
