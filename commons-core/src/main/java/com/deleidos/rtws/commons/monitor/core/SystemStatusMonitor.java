package com.deleidos.rtws.commons.monitor.core;

import java.io.IOException;
import java.util.ArrayList;
import java.util.HashSet;
import java.util.Properties;
import java.util.Set;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import javax.management.ObjectName;

import org.apache.commons.lang3.StringUtils;
import org.apache.log4j.Logger;

import com.amazonaws.auth.AWSCredentials;
import com.deleidos.rtws.commons.config.RtwsConfig;
import com.deleidos.rtws.commons.config.UserDataProperties;
import com.deleidos.rtws.commons.monitor.notification.SimpleEmailStatusNotifier;
import com.deleidos.rtws.commons.monitor.notification.StatusDetails;
import com.deleidos.rtws.commons.monitor.notification.StatusNotifier;
import com.deleidos.rtws.commons.net.jmx.JmxConnectionFactory;
import com.deleidos.rtws.commons.net.jmx.JmxConnectionFactoryImpl;
import com.deleidos.rtws.commons.net.jmx.JmxMBeanServerConnection;
import com.deleidos.rtws.commons.util.env.SystemEnvUtil;

public class SystemStatusMonitor extends ManagedMonitor {

	/** Factory used to connect to the master node's JMX server. */
	private JmxConnectionFactory connectionFactory = new JmxConnectionFactoryImpl();
	
	private Logger logger = Logger.getLogger(SystemStatusMonitor.class);
	
	Pattern pattern = Pattern.compile("\\[(.*?)\\]:");
	
	private MonitorStatus previousStatus = MonitorStatus.UNKNOWN;

	/**
	 * Instantiates a new system monitor with an immutable name.
	 *
	 * @param name
	 *            the immutable name
	 */
	public SystemStatusMonitor(String name) {
		super(name);

		setStartupPeriod(1000 * 60 * 20);		// 20 minute startup 
		
		notifiers.add(new SimpleEmailStatusNotifier());

		if (credentials == null)
			credentials = new AWSCredentials() {

				@Override
				public String getAWSSecretKey() {
					return UserDataProperties.getInstance().getString(UserDataProperties.RTWS_SECRET_KEY);
				}

				@Override
				public String getAWSAccessKeyId() {
					return UserDataProperties.getInstance().getString(UserDataProperties.RTWS_ACCESS_KEY);
				}
			};
	}

	/**
	 * This method allows the system status monitor to be set up with an error state and locked permanently. This is
	 * necessary because SystemStatusMonitor is currently placed in CommandListener which is started on each node. This
	 * results in a SystemStatusMonitor existing on every node in a system, when truly it should only live and function
	 * on the master node.
	 * 
	 * @param invalidationMessage
	 *            the message to set as the reason for invalidation
	 */
	public void invalidateMonitor(String invalidationMessage) {
		unlockState();
		removeAllErrors();
		addError(invalidationMessage);
		lockState();
	}

	/**
	 * Monitors the basic process controller monitors. This will give a status on all the process groups to make sure
	 * they are reporting a OK status.
	 * 
	 * In addition to the basic process controller monitors, the process group monitor for master must also be monitored
	 * individually since master is not controlled by a basic process controller.
	 */
	@Override
	public void monitor() {
		boolean statusOk = true;

		// Start with monitoring the master process group monitor
		if (!masterProcessGroupMonitorOk()) {
			statusOk = false;
		}

		// Monitor all the basic process controller monitor beans.
		if (!basicProcessControllerMonitorsOk()) {
			statusOk = false;
		}

		if (statusOk){
			setStatus(MonitorStatus.OK);
		} else {
			// only send notification(s) on state changes
			if (logger.isDebugEnabled())
				logger.debug(String.format("Current State: {%s}		Previous State: {%s}",getStatus(), previousStatus));
			
			if (getStatus() != previousStatus) {
				previousStatus = getStatus();
				RtwsConfig.getInstance().getConfiguration().setProperty("system.status", getStatus());
				for (StatusNotifier statusNotifier : notifiers) {
					Set<StatusDetails> details = new HashSet<StatusDetails>();
					String[] errors = getErrorMessages();
					if (errors != null) {
						for (String err : errors) {
							logger.info(err);
							try {	
								String pg = "";
								String eMsg = err.substring(StringUtils.ordinalIndexOf(err, "]: ", 3) + 2, err.length());
								int pos = 0;
								int wantedPos = 1;
								Matcher match = pattern.matcher(err);
								while (match.find()) {
									if (pos == wantedPos) 
										pg = match.group(1).replaceAll("processgroup-", "");

									pos++;
								}
								details.add(new StatusDetails(pg, eMsg));
							} catch (ArrayIndexOutOfBoundsException e) {
								logger.error(e);
								details.add(new StatusDetails("", err));
							} catch (NullPointerException e ) {
								logger.error(e);
							}finally {
							}
						}
					}
					statusNotifier.sendStatusNotification(credentials, getStatus(), details, getRecipients());
				}

				// reset
				RtwsConfig.getInstance().getConfiguration().setProperty("system.status", " ");
			}
		}
	}

	/**
	 * Monitor the process group monitor for master. This is required because there is no basic process controller that
	 * controls the master process.
	 *
	 * @param connection
	 *            the JMX connection to connect to the master process group monitor
	 * @return true, if the status for the master process group monitor was MonitoredStatus.OK
	 */
	private boolean masterProcessGroupMonitorOk() {
		JmxMBeanServerConnection connection = null;

		boolean statusOk = true;

		// TODO remove if decided to alter master to run on a single instance TMS
		if (SystemEnvUtil.isTmsSystem()) {
			setStatus(MonitorStatus.OK);
			return statusOk;
		}

		try {
			// Connect to the master node's JMX server for ProcessGroupMonitors
			((JmxConnectionFactoryImpl) connectionFactory).setPort(1098);
			connection = establishJmxConnection(connectionFactory);

			if (connection != null) {
				ObjectName pattern = ObjectName.getInstance("rtws.deleidos.com:type=ProcessGroupMonitor");

				// Get the status for each bean
				Set<ObjectName> availableMonitors = connection.queryNames(pattern, null);
				if (availableMonitors.size() == 0) {
					addError("No process group monitor found for master.");
					statusOk = false;
				} else {
					for (ObjectName monitor : availableMonitors) {
						MonitorStatus processGroupStatus = MonitorStatus.fromString((String) connection.getAttribute(
								monitor, "Status"));
						String monitorName = (String) connection.getAttribute(monitor, "Name");
						if (processGroupStatus != MonitorStatus.OK) {
							addErrors(monitorName, (String[]) connection.getAttribute(monitor, "ErrorMessages"));
							statusOk = false;
						} else {
							removeErrors(monitorName);
						}
					}
				}
			} else {
				// Cannot establish a connection
				addError("Unable to connect to master process group monitor.");
				statusOk = false;
			}

		} catch (Exception e) {
			// Something is wrong, we have an error
			recordExceptionAsError(e);
			statusOk = false;
		} finally {
			try {
				connection.close();
			} catch (Exception ignore) {
			}
		}

		return statusOk;
	}

	/**
	 * Monitor basic process controller monitors. Verifies that all known process groups have running basic process
	 * controller monitors.
	 *
	 * @param connection
	 *            the JMX connection to connect to the basic process controller monitors
	 * @return true, if the status for all the basic process controller monitors were MonitoredStatus.OK
	 */
	private boolean basicProcessControllerMonitorsOk() {
		JmxMBeanServerConnection connection = null;

		boolean statusOk = true;
		try {
			// Connect to the master node's JMX server.
			((JmxConnectionFactoryImpl) connectionFactory).setPort(1099);
			connection = establishJmxConnection(connectionFactory);

			if (connection != null) {
				// Check the names of the process group to verify we have monitors for each,
				// since each BasicProcessController controls one process group
				ArrayList<String> processGroupNames = new ArrayList<String>();
				ObjectName pattern = ObjectName.getInstance("rtws.deleidos.com:type=ProcessGroup,*");
				for (ObjectName processGroup : connection.queryNames(pattern, null)) {
					processGroupNames.add((String) connection.getAttribute(processGroup, "Name"));
				}

				if (processGroupNames.isEmpty()) {
					addError("No basic process controller monitors found.");
					statusOk = false;
					return statusOk;
				}

				// Get the BasicProcessControllers
				pattern = ObjectName.getInstance("rtws.deleidos.com:type=BasicProcessControllerMonitor,*");
				for (ObjectName monitor : connection.queryNames(pattern, null)) {
					MonitorStatus processGroupStatus = MonitorStatus.fromString((String) connection.getAttribute(
							monitor, "Status"));
					String monitorName = (String) connection.getAttribute(monitor, "Name");
					if (processGroupStatus != MonitorStatus.OK && processGroupStatus != MonitorStatus.STARTING) {
						addErrors(monitorName, (String[]) connection.getAttribute(monitor, "ErrorMessages"));
						statusOk = false;

						// Alert if this BasicProcessController is of an unknown process group
						if (!processGroupNames.contains(monitorName)) {
							addError(String.format("WARNING: Unknown process group %s causing %s status.", monitorName,
									MonitorStatus.ERROR.toString()));
						}
					} else {
						removeErrors(monitorName);
					}

					// Remove the monitorName from the known process group names to track
					// which monitors we have checked
					processGroupNames.remove(monitorName);
				}

				// Make sure we checked all the known process groups
				if (!processGroupNames.isEmpty()) {
					statusOk = false;
					for (String processGroupName : processGroupNames) {
						addError(String.format("No process controller monitor found for %s.", processGroupName));
					}
				}
			} else {
				// Cannot establish a connection
				addError("Unable to connect to basic process controller monitors in the cluster manager.");
				statusOk = false;
			}
		} catch (Exception e) {
			// Something is wrong, we have an error
			recordExceptionAsError(e);
			statusOk = false;
		} finally {
			try {
				connection.close();
			} catch (Exception ignore) {
			}
		}

		return statusOk;
	}

	private JmxMBeanServerConnection establishJmxConnection(JmxConnectionFactory connectionFactory) {
		int connectionRetryCount = 15; // Try multiple times to establish a JMX connection

		while (connectionRetryCount > 0) {
			try {
				return connectionFactory.getConnection();
			} catch (IOException ioe) {
				connectionRetryCount--;
			}
		}

		return null;
	}

	/**
	 * Record exception as error.
	 *
	 * @param e
	 *            the exception
	 */
	private void recordExceptionAsError(Exception e) {
		addError(String.format("Exception caught: %s", e.getMessage()));
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see com.deleidos.rtws.commons.management.ManagedBean#buildObjectNameKeys(java.util.Properties)
	 */
	@Override
	public void buildObjectNameKeys(Properties keys) {
		keys.put("type", "SystemStatusMonitor");
	}
}
