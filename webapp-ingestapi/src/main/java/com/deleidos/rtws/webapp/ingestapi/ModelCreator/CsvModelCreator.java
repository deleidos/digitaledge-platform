package com.deleidos.rtws.webapp.ingestapi.ModelCreator;

import java.io.File;
import java.io.InputStream;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

import net.sf.json.JSONArray;
import net.sf.json.JSONObject;

import org.joda.time.format.DateTimeFormatter;

import com.deleidos.rtws.splitter.TextlineWithQuotesSplitter;
import com.deleidos.rtws.webapp.ingestapi.servlet.enunciate.DataModelCreationServiceImpl;

public class CsvModelCreator extends ModelCreator{
	
	int numHeaders;
	String columnSeparator;
	
	public CsvModelCreator(String executionId, int numHeaders, int numRowsToRead, double decisionThreshold, String columnSeparator, InputStream is){
		super(executionId, numRowsToRead, decisionThreshold, is);
		this.numHeaders=numHeaders;
		this.columnSeparator=columnSeparator;	
	}
	
	
	@Override
	public void run() {
		try {
			DataModelCreationServiceImpl.statusMap.put(executionId,
					"PROCESSING: 0");
			Map<Integer, String> headers = null;
			Map<Integer, String> inputHeaders = null;

			TextlineWithQuotesSplitter splitter = new TextlineWithQuotesSplitter();

			splitter.setInputStream(is);
			splitter.setNumHeaders(numHeaders);

			String headerLn = splitter.parseHeaders();

			if (headerLn.length() > 0) {// should always happen
				headers = new HashMap<Integer, String>();
				inputHeaders = new HashMap<Integer, String>();
				decideKnownHeaders(headerLn, columnSeparator, headers,
						inputHeaders);
			} else {
				DataModelCreationServiceImpl.statusMap
						.put(executionId,
								"ERROR: First line (header line) has zero length.  CSV model creation requires a valid header.");
				return;// drop out early as error
			}

			// extractRowData(columnSeparator, rowData, splitter,numRowsToRead);

			// with the data read, we must now figure out what kind of column
			// each one is
			for (int ordinal : headers.keySet()) {
				String headerName = headers.get(ordinal);
				columnTypeInfo.put(headerName, new ColumnDecision());
				columnTypeInfo.get(headerName).setOrdinal(ordinal);
				columnTypeInfo.get(headerName).setColumnHeader(headerName);
			}
			String line = null;
			int numProcessed = 0;
			while ((line = splitter.split()) != null
					&& (++numProcessed) < numRowsToRead) {
				int processingPercent = (int) (((((double) numProcessed) / ((double) numRowsToRead))) * 100);
				DataModelCreationServiceImpl.statusMap.put(executionId,
						"PROCESSING: " + processingPercent);
				List<String> rowContents = extractRow(columnSeparator, line);
				processRow(rowContents, headers);
			}
			makeDecisions(columnTypeInfo, decisionThreshold);
			// type information read in. Now we must just pick the type.

			// for(Integer colNum : rowData.keySet()){
			// type and headers determined
			// we need to create the model json and zip
			try {
				createOutput(headers, inputHeaders);
				DataModelCreationServiceImpl.statusMap.put(executionId,
						"FINISHED");
			} catch (ModelProcessingException e) {
				DataModelCreationServiceImpl.statusMap.put(executionId,
						"ERROR: " + e.getMessage());
			}

		} catch (Exception catchAll) {
			DataModelCreationServiceImpl.statusMap.put(executionId, "ERROR: "+catchAll.getMessage());
			//will grab any unexpected errors and drop them on the status map
		}
		new ExpiredFileDelete(completedFile).start();// clean up in a bit...

	}
	
	private void createOutput(Map<Integer,String> headers,Map<Integer,String> inputHeaders) throws ModelProcessingException{
		JSONObject canonicalJSON = new JSONObject();
		JSONObject xlateJSON = new JSONObject();
		for(int i=0;i<headers.size();i++){
			String header = headers.get(i);
			ColumnDecision colDec = columnTypeInfo.get(header);
			String inHeader = inputHeaders.get(i);
		
			canonicalJSON.put(header, colDec.getFormatDecision());
			xlateJSON.put(header, "get("+inHeader+")");
			
		}
		JSONObject retVal = new JSONObject();
		retVal.put("canonical.json", canonicalJSON);
		retVal.put("xlate_generated.json", xlateJSON);
		retVal.put("enrichcfg.json", new JSONArray());
		
		retVal.put("datasources.json", generateDataSources(columnSeparator,headers));
		try{
			this.completedFile = createZipFile(retVal);
			this.completedJSON = retVal;
		}catch(Exception e){
			System.out.println("Error: writing generated zip file: "+e.getMessage());
			throw new ModelProcessingException("Writing generated zip file failed.");
		}
	}
	
	private void processRow(List<String> rowContents, Map<Integer, String> headers) {
		for (int i = 0; i < rowContents.size(); i++) {
			String rowEntry = rowContents.get(i);
			String header = headers.get(i);
			ColumnDecision colDec = columnTypeInfo.get(header);
			if (colDec == null) {
				DataModelCreationServiceImpl.statusMap.put(executionId,
						"ERROR: inconsistent number of rows found");
				return;
			}

			// number decision block
			boolean parsedAsNumber=false;
			try {
				if (rowEntry.length() == 0) {
					// zero length row - empty. 
					// mark it as a no-info for later use
					colDec.noInfo();
					parsedAsNumber=true;//empty, so not valid for date parsing anyway
				} else {
					Double.parseDouble(rowEntry);
					colDec.numberYes();
					//we know a number cannot be a date, so we can save some time
					parsedAsNumber=true;
					allNoDates(colDec);
				}
			} catch (Exception e) {
				colDec.numberNo();
			}
			// end number decision block

			// begin date decision block, if not a number
			if(!parsedAsNumber)
				decideDates(colDec,rowEntry);
			// end date decision block

		}
	}

	private JSONArray generateDataSources(String colSep,Map<Integer,String> headers){
		
		JSONArray out = new JSONArray();
		JSONObject generatedCSVParser = new JSONObject();
		
		
		
		JSONArray inFields = new JSONArray();
		for(int i=0;i<headers.size();i++){
			inFields.add(headers.get(i));
		}
		
		
		JSONArray parseParams = new JSONArray();
		
		//these values are mostly static...
		JSONObject stopOn = new JSONObject();
		stopOn.put("name", "stop-on-missing-field");
		stopOn.put("value", "true");
		stopOn.put("type", "boolean");
		parseParams.add(stopOn);
		
		JSONObject source = new JSONObject();
		source.put("name", "default-source");
		source.put("value", "UNKNOWN");
		source.put("type", "java.lang.String");
		parseParams.add(source);

		JSONObject delim = new JSONObject();
		delim.put("name", "delimiter");
		delim.put("value", colSep);
		delim.put("type", "java.lang.String");
		parseParams.add(delim);
		
		JSONObject mode = new JSONObject();
		mode.put("name","mode");
		mode.put("type", "java.lang.String");
		mode.put("value", "Parse");
		parseParams.add(mode);
		
		
		JSONObject label = new JSONObject();
		label.put("name", "default-access-label");
		label.put("value", "UNCLASSIFIED");
		label.put("type", "java.lang.String");
		parseParams.add(label);
		
		
		generatedCSVParser.put("parserParams", parseParams);
		generatedCSVParser.put("parser", "CsvParser");
		generatedCSVParser.put("inputFields", inFields);
		generatedCSVParser.put("name", "generated");
		//preserve ordering as in DME output files - for simplicity
		
		out.add(generatedCSVParser);
		return out;
	}
	
	private List<String> extractRow(String columnSeparator,String record){
		List<String> output = new ArrayList<String>();
		boolean currentlyQuoted=false;
		String[] parts = record.split(columnSeparator);
		String assembledColumn="";
		boolean fullyAssembled=false;
		for(int i=0;i<parts.length;i++){
			String thisPart=parts[i];
			if(currentlyQuoted){
				assembledColumn+=columnSeparator+thisPart;
				if(thisPart.endsWith("\"")){
					currentlyQuoted=false;
					fullyAssembled=true;
				}
			}else{
				if(thisPart.startsWith("\"") && thisPart.endsWith("\"")){
					assembledColumn=thisPart;
					fullyAssembled=true;
				}else if(thisPart.startsWith("\"")){
					fullyAssembled=false;
					currentlyQuoted=true;
					assembledColumn=thisPart;
				}else{
					assembledColumn=thisPart;
					fullyAssembled=true;
				}
			}
			
			if (fullyAssembled) {
				output.add(assembledColumn.replaceAll("\"",""));
			}
		}
		
		
		return output;
	}
	

	private Map<Integer,String> decideKnownHeaders(String headerLn,String columnSeparator,Map<Integer,String> headers, Map<Integer,String> inputHeaders){
		//we know our headers
		//we require that our headers have no commas or quotes.
		String[] headerList = headerLn.split(columnSeparator);
		for(int i=0;i<headerList.length;i++){
			String cleanedHeader = cleanHeaderName(headerList[i]);
			headers.put(i, cleanedHeader);
			inputHeaders.put(i,headerList[i].trim());
		}
		return headers;
	}
}
