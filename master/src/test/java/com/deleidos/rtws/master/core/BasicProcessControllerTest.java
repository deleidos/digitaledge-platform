	@Test(timeout = 1000)
	public void testDeallocateProcess() throws Exception {

		ManagementContext jmx = mock(ManagementContext.class);
		ServiceInterface provider = mock(ServiceInterface.class);
		doReturn(ProcessState.RUNNING).when(provider).status((Process) any());
		ShutdownHook shutdown = mock(ShutdownHook.class);

		Process process1 = new Process();
		process1.setPersistentDnsName("host1.test.com");
		process1.setNumber(1);

		Process process2 = new Process();
		process2.setPersistentDnsName("host2.test.com");
		process2.setNumber(2);

		ProcessGroup group = new ProcessGroup();
		group.setShutdownHook(shutdown);
		group.setAllocationPolicy(new ProcessAllocationPolicy());

		BasicProcessController controller = new BasicProcessController();
		controller.setServiceInterface(provider);
		controller.setManagementContext(jmx);
		controller.setProcessGroup(group);
		controller.getProcessTable().insert(process1);
		controller.getProcessTable().insert(process2);
		controller.deallocateProcess();

		InOrder order = inOrder(jmx, shutdown, provider);
		order.verify(shutdown).shutdown(process2.getPersistentDnsName());
		order.verify(provider).terminate(same(process2), eq(true), eq(false));
		order.verify(jmx).release(same(process2));

		assertNull(controller.getProcessTable().remove(2));

	}

