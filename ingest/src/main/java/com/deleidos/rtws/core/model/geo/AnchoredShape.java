package com.deleidos.rtws.core.model.geo;

import java.util.Arrays;

/**
 * Base class for shapes representing geo-spatial areas of interest. The shapes
 * represented are considered "anchored" in that they describe some area near or
 * around a logical focal point. In most cases the anchor point is the center of
 * a symmetrical area, but that is not required.
 */
public abstract class AnchoredShape implements Cloneable, Comparable<AnchoredShape> {
	
    public static final int UNKNOWN = 0;
    public static final int POINT = 1;
    public static final int CIRCLE = 2;
    public static final int ELLIPSE = 3;
    public static final int VECTOR = 4;
    public static final int GRID = 5;
    
    private int type;
    private double latitude;
    private double longitude;
    private double[] parameters;
    
    /**
     * Constructor... The parameters for this constructor are unusual but are
     * intended to aid in storing the shapes in a database as a simple list of
     * numeric values (minimizing record size optimizes the amount of disk reads
     * needed to satisfy queries).
     * 
     * Each AnchoredShape subclass is assigned an integer type code. The factory
     * uses this code to decide which subclass to instantiate when creating an
     * object from the list of numeric fields stored in the database.
     * 
     * The parameters list is a set of fields that describe the shape's area.
     * Since each type of shape is geometrically distinct, each subclass will
     * have a different parameter list. A circle can be described in terms of a
     * center point and radius, thus it would have one parameter. An ellipse is
     * described in terms of a center point, major/minor axis, and orientation;
     * thus, it would have three parameters.
     * 
     * Extending classes should provide two constructors. One protected
     * constructor like this one for use by the shape factory, which will accept
     * the latitude, longitude, and variable length parameter list (Note that
     * the type should be omitted as an argument and instead hard coded). The
     * other constructor should be public and should have and explicitly named
     * argument for each shape parameter.
     * 
     * @param type Code value representing the type of shape.
     * @param latitude Latitude of anchor point.
     * @param longitude Longitude of anchor point.
     * @param parameters Parameters describing shape characteristics/geometry.
     */
    protected AnchoredShape(int type, double latitude, double longitude, double... parameters) {
        
        // Set properties.
        this.type = type;
        this.latitude = latitude;
        this.longitude = longitude;
        this.parameters = parameters.clone();
        
        // Validate latitude.
        if(latitude < -90.0 || latitude > 90.0) {
            throw new IllegalArgumentException("Latitude '" + latitude + "' is outside valid range (+/-90.0).");
        }
        
        // Validate longitude.
        if(longitude < -180.0 || longitude > 180.0) {
            throw new IllegalArgumentException("Longitude '" + longitude + "' is outside valid range (+/-180.0).");
        }
        
    }
    
    /**
     * The type of shape.
     */
    public final int getType() {
        return type;
    }
    
    /**
     * The name of this shape.
     */
    public final String getTypeName() {
        return getClass().getSimpleName();
    }
    
    /**
     * The latitude of the shape's anchor point.
     */
    public final double getLatitude() {
        return latitude;
    }
    
    /**
     * The longitude of the shape's anchor point.
     */
    public final double getLongitude() {
        return longitude;
    }
    
    /**
     * The array of parameters describing the shape's geometric attributes.
     */
    public final double[] getParameters() {
        return parameters.clone();
    }
    
    /**
     * Gets the specified parameter value.
     */
    protected final double getParameter(int index) {
        return parameters[index];
    }
    
    /**
     * Gets the names of the shape attributes.
     * @return Parameter names array.
     */
    public abstract String[] getParameterNames();
    
    /**
     * Indicates whether the coverage area described by this shape is
     * bounded by the edge of this shape.
     * 
     * Shapes that are not considered bounded instead represent some
     * area near, but not necessarily inside the shapes boundaries.
     * For example, a shape may represent an object that is due north
     * of a given point, but it's distance is not know. Or it may
     * represent an object that is in the vicinity of a fixed point,
     * but not how close.
     */
    public abstract boolean isBounded();
    
    /**
     * Indicates whether the shape should scale when the view around
     * the area is zoomed in or out.
     */
    public abstract boolean isScalable();
    
    /**
     * Computes the area within the shape if possible.  Some unbounded shapes, 
     * such as a wedge, can be effectively bounded by the maximum distance of   
     * 64 timing advance rings.  Other shapse, such as a vicinity, are infinite.  
     * LOBs and vectors are NaN.
     */
    public abstract Double computeNominalCoverage();
    
    /**
     * Computes the smallest rectangular area that encloses this shape. The
     * returned array represents the upper left and lower right corner of the
     * bounding box, expressed as four doubles (lon, lat, lon, lat) in decimal
     * degrees.
     */
    public double[] computeMinimumBoundingRectangle() {
        
        double lonMin = Long.MAX_VALUE;
        double latMin = Long.MAX_VALUE;
        double lonMax = Long.MIN_VALUE;
        double latMax = Long.MIN_VALUE;
        
        double[] coordinates = computeOrdinates();
        for (int i = 0; i < coordinates.length; i++) {
            double value = coordinates[i];
            // If this index is a horizontal coordinate...
            if (i % 2 == 0) {
                lonMin = Math.min(value, lonMin);
                lonMax = Math.max(value, lonMax);
            // If this index is a vertical coordinate...
            } else {
                latMin = Math.min(value, latMin);
                latMax = Math.max(value, latMax);
            }
        }
        
        return new double[] { lonMin, latMin, lonMax, latMax };
        
    }
    
    /**
     * Computes the centroid of the shape, which is the average of the longitudes and 
     * latitudes of coordinate points that define the shape.
     * @return double [] longitude, latitude of the centroid
     */
    public double[] computeCentroid () {
    	double [] ordinates = computeOrdinates();
    	int divisor = ordinates.length / 2;
    	double longitudes = 0;
    	double latitudes = 0;
    	// get the totals for lons and lats
    	for (int i = 0; i < ordinates.length; i+=2) {
    		longitudes += ordinates[i];
    		latitudes += ordinates[i+1];
    	}
    	// return the pair of the averages
    	return new double [] {(longitudes/divisor), (latitudes/divisor)};
    }
    
    /**
     * Computes the furthest distance of any part of the shape
     * from the anchor point, in meters.
     * 
     * @return meters from centroid location or -1 if not known
     */
    public double getFurthestPointDistanceMeters() {
        // This default implementation returns -1;
        return -1;
    }
    
    /**
     * Normalizes the given angle of azimuth to values between -180 and 180.
     */
    protected static double normalizeAzimuth(double azimuth) {
        double modulo = azimuth % 360;
        int adjust = 360 * (int) (modulo / 180);
        azimuth = modulo - adjust;
        if (azimuth == -180) azimuth = 180;
        return azimuth;
    }
    
    /**
     * Computes the set of points used to represent this shape as a linear ring
     * of points that forms a closed polygon. Every two array elements
     * represents a longitude/latitude coordinate pair expressed in decimal
     * degrees. Points are listed in counter-clockwise order around the ring.
     * The first and last point must be the same in order to "close" the polygon.
     */
    public abstract double[] computeOrdinates();
    
    @Override
    public AnchoredShape clone() throws CloneNotSupportedException {
    	return (AnchoredShape) super.clone();
    }
    
    @Override
    public boolean equals(Object obj) {
        
        if (obj == this) {return true;}
        if (obj == null) {return false;}
        if (obj.getClass() != this.getClass()) {return false;}

        AnchoredShape that = (AnchoredShape) obj;
        if(this.type != that.type) {return false;}
        if(this.latitude != that.latitude) {return false;}
        if(this.longitude != that.longitude) {return false;}
        if(!Arrays.equals(this.parameters, that.parameters)) {return false;}
        
        return true;
        
    }
    
    @Override
    public int hashCode() {
        int hash = type;
        hash = (hash << 3) ^ (int)(latitude * 10000);
        hash = (hash << 3) ^ (int)(longitude * 10000);
        hash = (hash << 3) ^ Arrays.hashCode(parameters);
        return hash;
    }
    
    public int compareTo(AnchoredShape that) {
        if(this.type != that.type) {return this.type - that.type;} 
        if(this.latitude != that.latitude) {return (int)Math.signum(this.latitude - that.latitude);} 
        if(this.longitude != that.longitude) {return (int)Math.signum(this.longitude - that.longitude);} 
        if(this.parameters != that.parameters) {return Arrays.hashCode(this.parameters) - Arrays.hashCode(that.parameters);}
        return 0;
    }

	@Override
	public String toString() {
		StringBuilder result = new StringBuilder();
		result.append(getTypeName())
			  .append(" (")
			  .append(longitude)
			  .append(",")
			  .append(latitude)
			  .append(")");
		int i = 0;
		for (String name : getParameterNames()) {
			result.append(" ")
				  .append(name)
				  .append(":")
				  .append(getParameter(i++));
		}
		return result.toString();
	}
    
}
