package com.deleidos.rtws.webapp.dbapi.dao;

import java.util.Set;

import javax.sql.DataSource;

import net.sf.json.JSONArray;
import net.sf.json.JSONObject;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.dao.jdbc.DataAccessSession;
import com.deleidos.rtws.commons.dao.jdbc.DataAccessUtil;
import com.deleidos.rtws.webapp.dbapi.servlet.H2DBUtils;


/**
 * 
 *
 */
public class UpdateDao {

	private static Logger log = Logger.getLogger(UpdateDao.class);
	
	/** SQL used to insert data into the named table. */
	protected static final String insertIntoTableSql = "INSERT INTO ?.? ? VALUES ?;"; 
	
	/** SQL used to delete data into the named table. */
	protected static final String deleteFromTableSql = "DELETE FROM ?.? WHERE ?;";
	
	/** SQL used to update data into the named table. */
	protected static final String updateTableSql = "UPDATE ?.? SET ? WHERE ?;";
	
	/** Data source through which SQL operations should be performed. */
	private DataSource dataSource;

	/** Utility instance used to manage connections. */
	private DataAccessSession session;

	/**
	 * Constructor.
	 */
	public UpdateDao(String ip) {
		super();
		setDataSource(H2DBUtils.getDataSource(ip));
	}

	/**
	 * Data source through which SQL operations should be performed.
	 */
	public DataSource getDataSource() {

		return dataSource;
	}

	/**
	 * Data source through which SQL operations should be performed.
	 */
	public void setDataSource(DataSource dataSource) {
		this.dataSource = dataSource;
		this.session = DataAccessUtil.session(dataSource);
	}

	/**
	 * Return the list of tables.
	 */
	public int insertIntoTable(String type, String table, String jsonDefinition) {
		JSONObject definition = JSONObject.fromObject(jsonDefinition);
		JSONArray insert = definition.getJSONArray("insert");
		String allColumns = "";
		StringBuffer allValues = new StringBuffer();
	
		for(int i=0;i<insert.size();i++){
			JSONArray row = insert.getJSONArray(i);
			//JSONArray row = rows.getJSONArray("row");
			StringBuffer columns = new StringBuffer();
			StringBuffer values = new StringBuffer();
			String sep = "";
			for(int j=0;j<row.size();j++) {
				JSONObject columnDef = row.getJSONObject(j);
				
				if (columns.length() != 0) {
					sep = ", ";
				}
				else {
					columns.append("(");
					values.append("(");
				}
					
				Set<String> keys = columnDef.keySet();

				for (String key: keys) {
					columns.append(sep);
					columns.append(key);
					values.append(sep);
					Object val = columnDef.get(key);
					if(val instanceof String)
					{
						values.append("'");
						values.append((String)val);
						values.append("'");
					}
					else if(val instanceof JSONObject)
					{
						values.append("'");
						values.append(val.toString());
						values.append("'");
					}
					else
					{
						values.append(val);
					}
				}
			}
			columns.append(")");
			values.append(")");
			
			allColumns = columns.toString();
			if (allValues.length() != 0)
				allValues.append(", ");
			allValues.append(values);
		}
		
		String modifiedSql = insertIntoTableSql.replaceFirst("\\?", type).replaceFirst("\\?", table).replaceFirst("\\?", allColumns).replaceFirst("\\?", allValues.toString());
		
		return session.executeStatement(modifiedSql, null);	
	}
	
	/**
	 * Return the description of the named table.
	 */
	public int deleteFromTable(String type, String table, String where) {
		String modifiedSql = deleteFromTableSql.replaceFirst("\\?", type).replaceFirst("\\?", table).replaceFirst("\\?", where);
				
		return session.executeStatement(modifiedSql, null);
	}

	/**
	 * Return the description of the named table.
	 */
	public int updateTable(String type, String table, String where, String jsonDefinition) {
		JSONObject definition = JSONObject.fromObject(jsonDefinition);
		JSONArray update = definition.getJSONArray("update");
		StringBuffer set = new StringBuffer();
		String sep = "";
		
		for(int i=0;i<update.size();i++){
			JSONObject column = update.getJSONObject(i);
			if (set.length() != 0) {
				sep = ", ";
			}
			Set<String> keys = column.keySet();

			// Create the set portion of the SQL command
			for (String key: keys) {
				set.append(sep);
				set.append(key);
				set.append("=");
				Object val = column.get(key);
				if(val instanceof String)
				{
					set.append("'");
					set.append((String)val);
					set.append("'");
				}
				else if (val instanceof JSONObject) 
				{
					set.append("'");
					set.append(val.toString());
					set.append("'");
				}
				else
				{
					set.append(val);
				}
				//set.append(column.getString(key));
				
			}
		}
		String modifiedSql = updateTableSql.replaceFirst("\\?", type).replaceFirst("\\?", table).replaceFirst("\\?", set.toString()).replaceFirst("\\?", where);
				
		return session.executeStatement(modifiedSql, null);	
	}
}
