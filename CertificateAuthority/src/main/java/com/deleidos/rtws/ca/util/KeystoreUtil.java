	public static void addKeyAndCertToKeyStore(String keyPass, String alias, String keyStoreFile, String keyFile, String certificate, boolean initialize){
		try{
			KeyStore ks = KeyStore.getInstance("JKS", "SUN");
			if(initialize == true){
				//initialize/clear keystore
				ks.load(null, keyPass.toCharArray());
				ks.store(new FileOutputStream(keyStoreFile), keyPass.toCharArray());
				log.debug(String.format("Initializing keystore-file:  %s", keyStoreFile));
			}
			else{
				log.debug(String.format("Using existing keystore:  %s", keyStoreFile));
			}
			
			ks.load(new FileInputStream(keyStoreFile), keyPass.toCharArray());
			
			//load key
			InputStream is = fullStream(keyFile);
			byte [] key = new byte[is.available()];
			KeyFactory kf = KeyFactory.getInstance("RSA");
			is.read(key, 0, is.available());
			is.close();
			
			PKCS8EncodedKeySpec keysp = new PKCS8EncodedKeySpec ( key );
            PrivateKey privateKey = kf.generatePrivate (keysp);
            
            //load cert chain
            CertificateFactory certFactory = CertificateFactory.getInstance("X.509");
            InputStream certStream = fullStream(certificate);
            
            Collection<? extends Certificate> c = certFactory.generateCertificates(certStream);
            Certificate[] certs = new Certificate[c.toArray().length];
            
            if(c.size() == 1){
            	certStream = fullStream(certificate);
            	Certificate cert = certFactory.generateCertificate(certStream);
            	certs[0] = cert;
            }
            else{
            	certs = (Certificate []) c.toArray();
            }
            
            ks.setKeyEntry(alias, privateKey, keyPass.toCharArray(), certs);
            
            ks.store(new FileOutputStream(keyStoreFile), keyPass.toCharArray());
			
		}
		catch(Exception e){
			log.error(e.toString(), e);
		}
	}
	
	public static void addTrustedCertToKeystore(byte [] trustedCertificate, String keyPass, String keyStoreFile, String alias){
		try{
			KeyStore ks = KeyStore.getInstance("JKS", "SUN");
			
			if(new File(keyStoreFile).exists() == false){
				//initialize/clear keystore
				ks.load(null, keyPass.toCharArray());
				ks.store(new FileOutputStream(keyStoreFile), keyPass.toCharArray());
				log.debug(String.format("Initializing keystore-file:  %s", keyStoreFile));
			}
			
			ks.load(new FileInputStream(keyStoreFile), keyPass.toCharArray());
			
			 //load cert chain
            CertificateFactory certFactory = CertificateFactory.getInstance("X.509");
            InputStream certStream = fullStream(trustedCertificate);
			
			ks.setCertificateEntry(alias, certFactory.generateCertificate(certStream));
			
			ks.store(new FileOutputStream(keyStoreFile), keyPass.toCharArray());
		} catch(Exception e){
			log.error(e.toString(), e);
		}
	}
	
