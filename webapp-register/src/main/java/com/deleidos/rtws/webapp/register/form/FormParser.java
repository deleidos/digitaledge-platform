	protected final static String DOMAIN_FIELD_NAME = "domain";
	protected final static String EMAIL_ADDRESS_FIELD_NAME = "email";
	protected final static String EXECUTIONID_FIELD_NAME = "uuid";
	protected final static String AVAILABILITY_ZONE_FIELD_NAME = "availabilityZone";
	public final static String[] SECURITY_QUESTIONS_FIELD_NAMES = {"question1","question2","question3"};
