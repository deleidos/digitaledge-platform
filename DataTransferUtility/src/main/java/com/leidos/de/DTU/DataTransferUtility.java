package com.leidos.de.DTU;

import java.util.ArrayList;
import java.io.File;
import java.io.FileInputStream;
import java.io.UnsupportedEncodingException;
import java.net.URLDecoder;

import org.apache.commons.cli.BasicParser;
import org.apache.commons.cli.CommandLine;
import org.apache.commons.cli.CommandLineParser;
import org.apache.commons.cli.HelpFormatter;
import org.apache.commons.cli.Options;
import org.apache.commons.cli.ParseException;
import org.apache.log4j.ConsoleAppender;
import org.apache.log4j.Level;
import org.apache.log4j.Logger;
import org.apache.log4j.PatternLayout;

import com.deleidos.rtws.splitter.SplitterFactory;
import com.leidos.de.DTUgui.DTUmain;

/**
 * Hello world!
 * 
 */
public class DataTransferUtility {


  private static ArrayList<File> sourceFiles;


	public static void main(String[] args) {
		
		//configure log4j to start.
		ConsoleAppender consoleAppender = new ConsoleAppender();
		String PATTERN = "%d [%p|%c|%C{1}] %m%n";
		consoleAppender.setLayout(new PatternLayout(PATTERN)); 
		consoleAppender.setThreshold(Level.INFO);
		consoleAppender.activateOptions();
		//add appender to any Logger (here is root)
		Logger.getRootLogger().addAppender(consoleAppender);
		

		if (args.length == 0) {
			// no args - run in gui mode
			DTUmain.main(args);
		} else {//args present, run in CLI mode

			Options o = new Options();
			o.addOption(optionEnums.DESTINATION.getShortOpt(),
					optionEnums.DESTINATION.getLongOpt(), true,
					"Comma separated public ip addresses or dns names of jms external nodes.");
			o.addOption(optionEnums.TRUSTSTORE.getShortOpt(),
					optionEnums.TRUSTSTORE.getLongOpt(), true,
					"Path to trust store.");
			o.addOption(optionEnums.KEYSTORE.getShortOpt(),
					optionEnums.KEYSTORE.getLongOpt(), true,
					"Path to key store.");
			o.addOption(optionEnums.FILES.getShortOpt(),
					optionEnums.FILES.getLongOpt(), true,
					"Path to a file or files (directory) to send to JMS.");
			o.addOption(optionEnums.INPUTFORMAT.getShortOpt(),
					optionEnums.INPUTFORMAT.getLongOpt(), true,
					"Input format of data - the data source name from Data Model Editor.");
			o.addOption(optionEnums.MAPINPUTMODELS.getShortOpt(),
					optionEnums.MAPINPUTMODELS.getLongOpt(),true,
					"Use a filename to input model mapping (see documentation).  When selected, ignores the following flags: --"
					+optionEnums.SPLITTERTYPE.getLongOpt()+", --"+optionEnums.INPUTFORMAT.getLongOpt()+", and --"+optionEnums.HEADERS.getLongOpt());
			o.addOption(optionEnums.HEADERS.getShortOpt(),
					optionEnums.HEADERS.getLongOpt(), true,
					"The number of lines of header information in the file. (0 if not present)");
			o.addOption(optionEnums.SPLITTERTYPE.getShortOpt(),
					optionEnums.SPLITTERTYPE.getLongOpt(), true,
					"Type of recordFormat of the data.");
			o.addOption(optionEnums.RECURSIVE.getShortOpt(),
					optionEnums.RECURSIVE.getLongOpt(), false,
					"Recurse into subfolders if input is a directory.");
			o.addOption(optionEnums.BASE64ENCODE.getShortOpt(),
					optionEnums.BASE64ENCODE.getLongOpt(), false,
					"Base64 encode the resulting records.  Used for BinaryParser.");
			o.addOption(optionEnums.BATCH.getShortOpt(),
					optionEnums.BATCH.getLongOpt(), true,
					"Set the size of the JMS message batches.  Defaults to 100 if not set.");
			o.addOption(
					optionEnums.INTERACTIVE.getShortOpt(),
					optionEnums.INTERACTIVE.getLongOpt(),
					false,
					"Pause between JMS message batches (see -z, --batchsize) and files.  Waits for input before proceeding.");
			o.addOption(
					optionEnums.PROCESSZIP.getShortOpt(),
					optionEnums.PROCESSZIP.getLongOpt(),
					false,
					"Unzip and process zip files.");
			o.addOption(
					optionEnums.LISTEN.getShortOpt(),
					optionEnums.LISTEN.getLongOpt(),
					false,
					"Listen for messages on the Dead Letter Queue (DLQ).  Messages that go to the "
							+ "DLQ could not be parsed.  If this flag is selected, the DTU will not "
							+ "self-terminate when completed, but will listen on the DLQ, outputting "
							+ "messages, until it is killed.");
			o.addOption(
					optionEnums.LISTSPLITTERTYPES.getShortOpt(),
					optionEnums.LISTSPLITTERTYPES.getLongOpt(),
					false,
					"List the available types of recordFormats that can be used.  When passed in, the DTU ignores all other options.");
			o.addOption(optionEnums.TESTONLY.getShortOpt(),
					optionEnums.TESTONLY.getLongOpt(), false,
					"Test connections and files, but do not send any JMS messages.");
			o.addOption(
					optionEnums.MD5HASH.getShortOpt(),
					optionEnums.MD5HASH.getLongOpt(),
					false,
					"Calculate the MD5 hash of this file for verification that this is an untampered file.  When passed in, the DTU ignores all other options.");
			o.addOption(optionEnums.HELP.getShortOpt(),
					optionEnums.HELP.getLongOpt(), false, 
					"Show this help.");

			// keystore/truststore passwords are set by DE, and known, as are
			// connection parameters, port, etc.
			CommandLineParser parser = new BasicParser();
			CommandLine line = null;
			HelpFormatter fmt = new HelpFormatter();
			try {
				line = parser.parse(o, args);
			} catch (ParseException e) {
				System.out.println("Parsing command line failed with error: "
						+ e.getMessage());
				System.exit(1);
			}
			
			if(line.hasOption(optionEnums.HELP.getLongOpt())){
				fmt.printHelp("DataTransferUtility", o);
				System.exit(0);
			}

			if (line.hasOption(optionEnums.LISTSPLITTERTYPES.getLongOpt())) {
				System.out.println("Listing Available Splitter Types: ");
				String[] types = SplitterFactory.getSplitterTypes();
				for (String type : types) {
					System.out.println(type);
				}
				System.exit(0);
			}

			if (line.hasOption(optionEnums.MD5HASH.getLongOpt())) {
				System.out.println("Calculating hash of this JAR file...");
				String path = DataTransferUtility.class.getProtectionDomain()
						.getCodeSource().getLocation().getPath();
				String decodedPath = "";
				try {
					decodedPath = URLDecoder.decode(path, "UTF-8");
				} catch (UnsupportedEncodingException e) {
					// not going to fail...
					System.err.println("ERROR DECODING PATH!");
				}
				File thisJar = new File(decodedPath);
				if (thisJar.isDirectory()) {
					// debug situation - running from target/classes
					System.out.println("Cannot check MD5 hash from debug situation.  Please run on built jar.");
				} else {
					// it's the jar
					try {
						FileInputStream fis = new FileInputStream(thisJar);
						String md5 = org.apache.commons.codec.digest.DigestUtils
								.md5Hex(fis);
						System.out.println(md5);
					} catch (Exception e) {
					}

				}
				System.out.println(decodedPath);
				System.exit(0);
			}

			String rawDests = line
					.getOptionValue(optionEnums.DESTINATION.longOpt);
			if (rawDests == null || rawDests.isEmpty()) {
				System.out.println("Empty or missing destination parameter.");
				fmt.printHelp("DataTransferUtility", o);
				System.exit(1);
			}
			String[] urls = rawDests.split(",");

			String trustStoreLoc = line.getOptionValue(optionEnums.TRUSTSTORE
					.getLongOpt());
			String keyStoreLoc = line.getOptionValue(optionEnums.KEYSTORE
					.getLongOpt());

			if (trustStoreLoc == null || trustStoreLoc.isEmpty()) {
				System.out.println("Empty or missing trust store parameter.");
				fmt.printHelp("DataTransferUtility", o);
				System.exit(1);
			}
			if (keyStoreLoc == null || keyStoreLoc.isEmpty()) {
				System.out.println("Empty or missing key store parameter.");
				fmt.printHelp("DataTransferUtility", o);
				System.exit(1);
			}

			File trustStore = new File(trustStoreLoc);
			File keyStore = new File(keyStoreLoc);
			if (!trustStore.exists()) {
				System.out.println("Trust store not found at "
						+ trustStore.getAbsolutePath());
				System.exit(1);
			}
			if (!keyStore.exists()) {
				System.out.println("Key store not found at "
						+ keyStore.getAbsolutePath());
				System.exit(1);
			}

			String srcLoc = line.getOptionValue(optionEnums.FILES.getLongOpt());

			if (srcLoc == null || srcLoc.isEmpty()) {
				System.out
						.println("Empty or missing source file/directory parameter.");
				fmt.printHelp("DataTransferUtility", o);
				System.exit(1);
			}

			File sourceLocation = new File(srcLoc);
			if (!sourceLocation.exists() || !sourceLocation.canRead()) {
				System.out.println("File source location "
						+ sourceLocation.getAbsolutePath()
						+ " does not exist or cannot be read.");
				System.exit(1);
			}

			System.out.println(srcLoc);	
			sourceFiles = new ArrayList<File>();
  			sourceFiles.add(new File(srcLoc));



			// this one we'll just pass along, no way to verify
			String inputFormat = line.getOptionValue(optionEnums.INPUTFORMAT
					.getLongOpt());

			int headerCount = 0;
			if (line.hasOption(optionEnums.HEADERS.getLongOpt())) {
				try {
					String numHeaders = line.getOptionValue(optionEnums.HEADERS
							.getLongOpt());
					headerCount = Integer.parseInt(numHeaders);
				} catch (Exception e) {
					System.out.println("Could not convert header count ("
							+ line.getOptionValue(optionEnums.HEADERS
									.getLongOpt()) + ") to number.");
					System.exit(1);
				}
			}

			String numInBatch = line.getOptionValue(
					optionEnums.BATCH.getLongOpt(), "100");
			int batchSize = 100;
			try {
				batchSize = Integer.parseInt(numInBatch);
			} catch (Exception e) {
				// format exception
				System.out.println("Could not convert batch size ("
						+ numInBatch + ") to number.");
				System.exit(1);
			}

			String splitterType = line.getOptionValue(optionEnums.SPLITTERTYPE
					.getLongOpt());

			if (splitterType == null || splitterType.isEmpty()) {
				System.out.println("Empty or missing recordFormat parameter.");
				fmt.printHelp("DataTransferUtility", o);
				System.exit(1);
			}

			boolean r = line.hasOption(optionEnums.RECURSIVE.getLongOpt());
			boolean encode = line.hasOption(optionEnums.BASE64ENCODE
					.getLongOpt());
			boolean listen = line.hasOption(optionEnums.LISTEN.getLongOpt());
			boolean testOnly = line
					.hasOption(optionEnums.TESTONLY.getLongOpt());
			boolean interactive = line.hasOption(optionEnums.INTERACTIVE
					.getLongOpt());
			boolean processZip = line.hasOption(optionEnums.PROCESSZIP
					.getLongOpt());

			// using a marshaller, we'll have to create a pluggable splitter out
			// of
			// the xmlfile
			// xml file chosen for configuration because the variety of
			// splitters
			// forces
			// us to configure it very dynamically - more dynamically than can
			// be
			// done
			// with a simple command line.

			JMSUploader upload = new JMSUploader();
			upload.setDestination(urls);
			//upload.setFile(sourceLocation);
			upload.setFile(sourceFiles);
			upload.setInputFormat(inputFormat);
			upload.setNumHeaders(headerCount);
			upload.setSplitter(splitterType);
			upload.setStoreLocations(keyStore, trustStore);
			upload.setRecursive(r);
			upload.setBase64Encode(encode);
			upload.setListenDLQ(listen);
			upload.setTestOnly(testOnly);
			upload.setBatchSize(batchSize);
			upload.setInteractive(interactive);
			upload.setProcessZipFiles(processZip);
			upload.processCLI();
		}
	}

	private enum optionEnums {
		HELP("?","help"),
		DESTINATION("d", "destination"), 
		FILES("f", "files"), 
		BATCH("z", "batchSize"), 
		PROCESSZIP("p","zip"),
		INTERACTIVE("w", "interactive"), 
		RECURSIVE("r",	"recursive"), 
		HEADERS("h", "recordHeaderLines"), 
		INPUTFORMAT("i", "inputFormat"), 
		TRUSTSTORE("t", "trustStore"), 
		KEYSTORE("k", "keyStore"), 
		BASE64ENCODE("b", "encode"), 
		LISTEN("l", "listen"), 
		SPLITTERTYPE("s", "recordFormat"), 
		TESTONLY("c", "testConnection"), 
		MD5HASH("m", "md5"), 
		MAPINPUTMODELS("x","mapInputModels"),
		LISTSPLITTERTYPES("a", "availableRecordFormat");

		private String shortOpt;
		private String longOpt;

		private optionEnums(String s, String l) {
			shortOpt = s;
			longOpt = l;
		}

		public String getShortOpt() {
			return shortOpt;
		}

		public String getLongOpt() {
			return longOpt;
		}
	}
}
