	public String launchGatewayInstance(AccountForm form, GatewayData gateway) {
		
		String accessKey = form.getAccessKey();
		String secretKey = form.getSecretKey();
		String endpoint = gateway.getAzone().getServiceEndpoint().getURLString();
		
		AwsServiceInterface serviceInterface = getAwsServiceInterface(accessKey, secretKey, endpoint);
		
		String imageId = gateway.getImageId();
		String instanceType = form.getInstanceType();
		String azone = form.getAvailabilityZone();
		String keyPair = form.getKeyPair();
		String securityGroupId = gateway.getSecurityGroupId();
		String userData = gateway.getUserData().toString();
		String subnetId = gateway.getSubnetId();
		
		String instanceId = serviceInterface.createInstance(imageId, instanceType, azone, keyPair, securityGroupId, subnetId, userData);
		
		Timestamp expiration = new Timestamp(System.currentTimeMillis() + TimeUtil.TEN_MINUTES);
		
		do {
			try { Thread.sleep(5000); } catch (Exception ignore) { }
			
			Instance instance = serviceInterface.describeInstance(instanceId);
			
			if (instance != null && "running".equalsIgnoreCase(instance.getState().getName())) {
				break;
			}
		} while (! TimeUtil.hasExpired(expiration));
		
		return instanceId;
		
	}
	
