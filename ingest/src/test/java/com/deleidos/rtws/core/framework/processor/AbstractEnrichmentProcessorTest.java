	/**
	 * Verifies that if a null result was produced by an enrichment, that no element are added to
	 * the record, and that the parameter fields are left intact.
	 */
	@Test
	public void testNullResult() {
		
		JSONObject invoice = buildInvoice();
		
		EnrichmentModelMappingCache models = new EnrichmentModelMappingCache();
		
		AbstractEnrichmentProcessor proc = new AbstractEnrichmentProcessor() {
			public String getType() {return "lookup";}
			public JSONObject buildEnrichedElement(EnrichmentAction action, ParameterList parameters) {
				return null;
			}
		};
		
		proc.setEnrichments(models);
		proc.initialize();
		proc.process(invoice);
		
		JSONObject record = invoice;
		assertFalse("Root element changed when enrichment result was not performed.", record.containsKey("addressDetail"));
		assertTrue("Parameter field removed even though enrichment was not performed.", record.getJSONObject("store").containsKey("region"));
		
		record = invoice.optJSONArray("items").getJSONObject(0);
		assertFalse("Array element changed when enrichment result was not performed.", record.containsKey("itemDetail"));
		assertTrue("Parameter field removed even though enrichment was not performed.", record.containsKey("serialNumber"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(0);
		assertFalse("Nested array element changed when enrichment result was not performed.", record.containsKey("promotionDetail"));
		assertTrue("Parameter field removed even though enrichment was not performed.", record.containsKey("code"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(1);
		assertFalse("Nested array element changed when enrichment result was not performed.", record.containsKey("promotionDetail"));
		assertTrue("Parameter field removed even though enrichment was not performed.", record.containsKey("code"));
		
		record = invoice.optJSONArray("items").getJSONObject(1);
		assertFalse("Array element changed when enrichment result was not performed.", record.containsKey("itemDetail"));
		assertTrue("Parameter field removed even though enrichment was not performed.", record.containsKey("serialNumber"));
		
	}
	
