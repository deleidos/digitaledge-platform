	private void movePairToRootCAConfig(String keyFilePath, byte [] certificate){
		//copy key from tmp location to signing key location
		try{
			InputStream is = new FileInputStream(keyFilePath);
			OutputStream os = new FileOutputStream(CAProperties.getSigningKeyLocation());
			
			IOUtils.copy(is, os);
			is.close();
			os.close();
		}catch(Exception e){
			logger.error(e.toString(), e);
		}
		//check if key exists
		if(new File(keyFilePath).exists()){
			logger.debug("Key exists in config.");
		}
		else{
			logger.debug("Key does not exist in config.");
		}
		
		//output cert for signing config
		try{
			OutputStream os = new FileOutputStream(CAProperties.getSigningCertLocation());
			
			os.write(certificate);
			os.flush();
			os.close();
		} catch(Exception e){
			logger.error(e.toString(), e);
		}
	}
	
	public String readPublicCert(String certFile) throws IOException{
		StringBuilder bld = new StringBuilder();
		BufferedReader rdr = new BufferedReader(new FileReader(certFile));
		String ln = rdr.readLine();
		String newline = System.getProperty("line.separator");
		while(ln!=null){
			bld.append(ln);
			bld.append(newline);
			ln = rdr.readLine();
		}
		
		rdr.close();
		String b64EncodedCert = Base64.encodeBase64String(bld.toString().getBytes("UTF8"));
		
		return b64EncodedCert;
	}
	
