package com.deleidos.rtws.master.core;

import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.Map.Entry;

import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.dao.exception.DataAccessException;
import com.deleidos.rtws.commons.exception.InitializationException;
import com.deleidos.rtws.commons.net.jmx.JmxConnection;
import com.deleidos.rtws.master.core.beans.StatisticsCollector;
import com.deleidos.rtws.master.core.net.jmx.JmxStatisticsCollector;
import com.deleidos.rtws.master.core.net.jmx.LocalNodeFinder;

/**
 * StatisticsCollector implementation that uses the JMX data published by the master node to discover the currently
 * allocated members of the configured process group and sum the values for the configured set of MBean attributes from
 * those nodes.
 * 
 * This class provides the option to "sample" metrics rather that retrieve data from all the discovered nodes. This can
 * be advantageous for process groups that may have a large number of members as it limit the number of network
 * connections that need to be established in order to calculate the desired sum. Also, the returned sum is not the
 * simple sum over the sample, but rather that value extrapolated out the size of the process group. In other words,
 * what sum should have been if it were calculated over the whole process group.
 * 
 * Note: The extrapolation algorithm used simply multiplies out the measured value to the needed representative size.
 * This only provides accurate results assuming that all nodes average the same values.
 * 
 * This class also provides options to configure error handling behavior (FAIL, WARN, IGNORE). This is not the same as
 * logging level. This behavior controls how the aggregation of collected values is performed if some of the discovered
 * nodes cannot be contacted to retrieve remote attribute values. The default mode is IGNORE; which simply computes the
 * aggregate total without the data points that can't be retrieved. Thus providing a partial but possibly inaccurate
 * result. The FAIL mode immediately throws an exception without computing the aggregate at all. This is best for cases
 * where inaccurate results cannot be tolerated. The WARN mode is essentially the same as IGNORE except that the error
 * that caused the data retrieval to fail is logged instead of being overlooked.
 */
public class DiscoveryStatisticsCollector implements StatisticsCollector {

	private static final Logger log = Logger.getLogger(DiscoveryStatisticsCollector.class);

	private static final String FAIL = "FAIL";
	private static final String WARN = "WARN";
	private static final String IGNORE = "IGNORE";

	private LocalNodeFinder discovery = new LocalNodeFinder();

	private int port = 1099;
	private String connector = "jmxrmi";
	private String object;
	private String[] attributes = new String[0];

	private String errorHandling = IGNORE;
	private int samplePercent = 100;
	private int sampleMin = 7;

	public DiscoveryStatisticsCollector() {
		super();
	}

	public String getGroup() {
		return discovery.getGroup();
	}

	public void setGroup(String value) {
		discovery.setGroup(value);
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	public String getConnector() {
		return connector;
	}

	public void setConnector(String connector) {
		this.connector = connector;
	}

	public String getObjectName() {
		return object;
	}

	public void setObjectName(String object) {
		this.object = object;
	}

	public String[] getAttributes() {
		return attributes;
	}

	public void setAttributes(String[] value) {
		attributes = value;
	}

	public String getErrorHandling() {
		return errorHandling;
	}

	public void setErrorHandling(String errorHandling) {
		this.errorHandling = errorHandling;
	}

	public int getSamplePercent() {
		return samplePercent;
	}

	public void setSamplePercent(int samplePercent) {
		this.samplePercent = samplePercent;
	}

	public int getSampleMin() {
		return sampleMin;
	}

	public void setSampleMin(int sampleMin) {
		this.sampleMin = sampleMin;
	}

	public void initialize() throws InitializationException {
		// Nothing to do.
	}

	public void dispose() {
		// Nothing to do.
	}

	protected final String buildUrl(String host) {
		return "service:jmx:rmi:///jndi/rmi://" + host + ":" + port + "/" + connector;
	}

	/**
	 * Retrieve the configured bean attributes from the discovered nodes.
	 */
	public Map<String, Double> fetch() throws DataAccessException {

		List<String> nodes = discovery.findNodes();
		List<String> sample = sample(nodes, samplePercent, sampleMin);

		HashMap<String, Double> result = new HashMap<String, Double>();
		for (String node : sample) {
			JmxStatisticsCollector collector = new JmxStatisticsCollector();
			try {
				collector.setJmxConnection(new JmxConnection(buildUrl(node), object));
				collector.setAttributes(attributes);
				collector.initialize();
				merge(result, collector.fetch());
			} catch (DataAccessException e) {
				if (errorHandling == null) {
					log.debug("Unable to collect statistics from node '" + node + "'.", e);
				} else if (errorHandling.equalsIgnoreCase(FAIL)) {
					throw e;
				} else if (errorHandling.equalsIgnoreCase(WARN)) {
					log.warn("Unable to collect statistics from node '" + node + "'.", e);
				} else {
					log.debug("Unable to collect statistics from node '" + node + "'.", e);
				}
			} finally {
				collector.dispose();
			}
		}

		return extrapolate(result, nodes.size(), sample.size());

	}

	/**
	 * Add the given values to the running total.
	 */
	private static void merge(Map<String, Double> result, Map<String, Double> add) {
		for (String key : add.keySet()) {
			if (result.containsKey(key)) {
				double value = result.get(key) + add.get(key);
				result.put(key, value);
			} else {
				result.put(key, add.get(key));
			}
		}
	}

	/**
	 * Retrieve a subset of the given list that is the desired size (whichever is larger between the given percent and
	 * minimum).
	 */
	private static List<String> sample(List<String> list, int percent, int minimum) {
		if (percent == 100) {
			return list;
		} else {
			int computedSize = (int) Math.ceil(list.size() * percent / 100);
			int computedMin = Math.min(minimum, list.size());
			return list.subList(0, Math.max(computedSize, computedMin));
		}
	}

	/**
	 * Assuming the the given values represent a sum over a subset of a larger group, multiply them out to normalize
	 * their values to the hypothetical sum over the larger group.
	 */
	private static Map<String, Double> extrapolate(Map<String, Double> values, int total, int sample) {
		if (total == sample) {
			return values;
		} else {
			Map<String, Double> result = new HashMap<String, Double>();
			for (Entry<String, Double> entry : values.entrySet()) {
				double value = entry.getValue() * total / sample;
				result.put(entry.getKey(), value);
			}
			return result;
		}
	}

}
