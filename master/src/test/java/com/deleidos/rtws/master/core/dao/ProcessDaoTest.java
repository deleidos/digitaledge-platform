	/**
	 * This test verifies that objects in the dao's cache are not shared by reference outside the cache. In
	 * other words, if I ask the dao to save an object, and then I modify the object (outside the dao) and don't
	 * attempt to save the changes, the cached object should remain unchanged.
	 */
	@Test
	public void testIsolation() throws Exception {
		
		MockDao dao = new MockDao();
		dao.store(process);
		
		String originalId = process.getId();
		String originalIp = process.getPersistentIpAddress();
		String originalDns = process.getPersistentDnsName();
		
		Process myValue;
		Process daoValue;
		
		myValue = process;
		myValue.setPersistentDnsName("unsaved-edit1");
		daoValue = dao.findById(originalId);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findByIp(originalIp);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findByDns(originalDns);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findAll().iterator().next();
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		
		
		myValue = dao.findByDns(originalDns);
		myValue.setPersistentDnsName("unsaved-edit2");
		daoValue = dao.findById(originalId);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findByIp(originalIp);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findByDns(originalDns);
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		daoValue = dao.findAll().iterator().next();
		assertEquals("Modification of object outside cache changed the cached value.", originalDns, daoValue.getPersistentDnsName());
		
	}
	
