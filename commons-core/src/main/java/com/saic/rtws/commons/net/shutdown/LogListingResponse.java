package com.saic.rtws.commons.net.shutdown;

public class LogListingResponse {

	private String name;
	private String size;

	public String getName() {
		return name;
	}

	public String getSize() {
		return size;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setSize(String size) {
		this.size = size;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("LogListingResponse [name=");
		builder.append(name);
		builder.append(", size=");
		builder.append(size);
		builder.append("]");
		return builder.toString();
	}

}
