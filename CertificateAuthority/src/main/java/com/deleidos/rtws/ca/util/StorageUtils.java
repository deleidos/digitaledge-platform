	private static void copy(String filepath, String dirPath) throws StorageUtilsException, IOException, InterruptedException {
		
		String cmd = String.format("/usr/bin/sudo %s %s %s %s", CERT_STORAGE_SCRIPT, Action.copy.toString(), filepath, dirPath);
		logger.debug(cmd);
		fork(cmd);
		
	}
	
	private static synchronized String makeTempDir() {
		StringBuilder filepath = new StringBuilder();
		
		filepath.append(TMP_PKI_DIR).append("/cert-").append(UUID.randomUUID().toString());
		
		File tempDir = new File(filepath.toString());
		if (! tempDir.exists()) {
			tempDir.mkdirs();
		}
		
		return filepath.toString();
		
	}
	
	private static byte [] encrypt(byte [] data, String password) throws NoSuchAlgorithmException, InvalidKeySpecException, NoSuchPaddingException, InvalidKeyException, IllegalBlockSizeException, BadPaddingException, InvalidAlgorithmParameterException {
		
		SecretKeyFactory skf = SecretKeyFactory.getInstance(DEFAULT_ENCRYPT_METHOD);
	    PBEKeySpec keySpec = new PBEKeySpec(password.toCharArray());
	    SecretKey key = skf.generateSecret(keySpec);
	    
	    Cipher cipher = Cipher.getInstance(DEFAULT_ENCRYPT_METHOD);
	    cipher.init(Cipher.ENCRYPT_MODE, key, new PBEParameterSpec(salt, iterationCount));
	    
	    return Base64.encodeBase64(cipher.doFinal(data));
	    
	}
	
	private static byte [] decrypt(byte [] data, String password) throws NoSuchAlgorithmException, InvalidKeySpecException, NoSuchPaddingException, InvalidKeyException, IllegalBlockSizeException, BadPaddingException, InvalidAlgorithmParameterException {
	    
		SecretKeyFactory skf = SecretKeyFactory.getInstance(DEFAULT_ENCRYPT_METHOD);
	    PBEKeySpec keySpec = new PBEKeySpec(password.toCharArray());
	    SecretKey key = skf.generateSecret(keySpec);
	    
	    Cipher cipher = Cipher.getInstance(DEFAULT_ENCRYPT_METHOD);
	    cipher.init(Cipher.DECRYPT_MODE, key, new PBEParameterSpec(salt, iterationCount));
	   
	    return cipher.doFinal(Base64.decodeBase64(data));
	    
	}
	
