package com.deleidos.rtws.webapp.gatewayapi.common;

import java.util.List;
import java.util.Properties;

import org.apache.commons.lang.StringUtils;
import org.apache.log4j.Logger;

import com.deleidos.rtws.commons.config.UserDataProperties;
import com.deleidos.rtws.commons.model.tmsdb.AccountSystem;
import com.deleidos.rtws.commons.util.env.SystemEnvUtil;
import com.deleidos.rtws.webapp.gatewayapi.client.RestClientException;
import com.deleidos.rtws.webapp.gatewayapi.client.RestClientManager;
import com.deleidos.rtws.webapp.gatewayapi.dao.AccountSystemDao;
import com.deleidos.rtws.webapp.gatewayapi.dao.EmbeddedH2DataAccessUtility;
import com.deleidos.rtws.webapp.gatewayapi.model.Node;
import com.deleidos.rtws.webapp.gatewayapi.model.ProcessItem;

public class GWAccountSystemHelper extends Thread {
	
	private Logger logger = Logger.getLogger(this.getClass());
	
	@Override
	public void run() {

		String domain = UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN);

		if (StringUtils.isBlank(domain)) {
			logger.info("UserData '" + UserDataProperties.RTWS_DOMAIN + "' doesn't exist. Stopping GWAccountSystemHelper thread.");
			return;
		}
		
		AccountSystemDao accountDao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
		AccountSystem system = accountDao.getAccountSystem(domain);
		
		if (system == null && !SystemEnvUtil.isSingleTmsSystem()) {
			// Could not find the userdata specified system. This is the case for all tenant gateway node.
			
			logger.info("System '" + domain + "' not found. Stopping GWAccountSystemHelper thread.");
			return;
		}

		// System found - we're on tms! Let's update the account system table.
		
		while (true) {
			int counter=0;
			try {
				ProcessItem master = getMasterProcess(domain);
				if (master != null && master.getInstanceId() != null) {
					Node node = RestClientManager.getInstanceServiceClient().getNodeWithoutVolumes(master.getInstanceId());
					String userdata = RestClientManager.getInstanceServiceClient().getUserData(master.getInstanceId());
					Properties userDataProps = parseUserData(userdata);
					
					updateAccountSystemsEntry(domain, node, userdata, userDataProps);
					
					break;
				}
			} catch (Exception ex) {
				if(counter % 10 == 0){
					logger.warn("Fail to update account system table for '" + domain + "'.", ex);
				}
				counter++;
			}
			
			try { Thread.sleep(10000); } catch (Exception drop) { }
		}
		
		logger.info("Updated account system table for '" + domain + "'. Stopping GWAccountSystemHelper thread.");
		
	}
	
	private ProcessItem getMasterProcess(String domain) {
		
		String dns = String.format("master.%s", UserDataProperties.getInstance().getString(UserDataProperties.RTWS_DOMAIN));

		List<ProcessItem> processes = 
				RestClientManager.getRespositoryApiRestClient().retrieveProcessFile(dns);
			
		for (ProcessItem process : processes) {
			if (process.getGroup().equalsIgnoreCase("master")) {
				return process;
			}
		}
		
		return null;
		
	}
	
	public Properties parseUserData(String userData) {
		
		Properties userDataProps = new Properties();
		
		String [] keyValPairs = userData.split("[;]");
		for (int i = 0; i < keyValPairs.length; i++) {
			String [] keyValPair = keyValPairs[i].split("[=]");
			if (keyValPair.length == 2) {
				userDataProps.put(keyValPair[0], keyValPair[1]); 
			}
		}
		
		return userDataProps;
		
	}
	
	private void updateAccountSystemsEntry(
			String domain, 
			Node node, 
			String userData, 
			Properties userDataProps) throws RestClientException {
		
		String instanceId = node.getId();
		String privateIpAddress = node.getPrivateIpAddress();
		String version = userDataProps.getProperty(UserDataProperties.RTWS_SW_VERSION);

		if (domain == null || instanceId == null || privateIpAddress == null || version == null || userData == null) {
			throw new NullPointerException("A required property is missing.");
		}
		
		AccountSystemDao dao = new AccountSystemDao(EmbeddedH2DataAccessUtility.getInstance());
		AccountSystem system = dao.getAccountSystem(domain);
		
		if (system == null) {
			// TODO: fix config version and availability zone
			dao.insert(domain, version, "1.0", "");
			
			if (SystemEnvUtil.isSingleTmsSystem()) {
				logger.info("Updating system information for single instance tms.");
				system = dao.getAccountSystem(domain);
				dao.update(domain, instanceId, privateIpAddress, version, version, userData);
			}
		} else {
			dao.update(domain, instanceId, privateIpAddress, version, version, userData);
		}		
		
	}
	
}