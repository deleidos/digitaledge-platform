	@Test
	public void testFirewallGroupLifecycle() throws Exception {

		AwsServiceInterface service = new AwsServiceInterface();
		AwsConnectionFactory value = new AwsConnectionFactory(accessKey, secretKey);
		value.setServiceEndpoint(serviceEndpoint);
		service.setConnectionFactory(value);

		Map<String, FirewallGroup> fwgrps;
		FirewallGroup fwgrp;
		FirewallRule fwrule;

		fwgrps = service.listFirewallGroupsByName();
		Assert.assertNotNull(fwgrps);
		Assert.assertNull(fwgrps.get(FW_GROUP_JUNIT));

		// create security group
		service.createFirewallGroup(FW_GROUP_JUNIT, "description", null); // third argument is VPC-ID
		fwgrps = service.listFirewallGroupsByName();
		Assert.assertNotNull(fwgrps);
		Assert.assertNotNull(fwgrps.get(FW_GROUP_JUNIT));

		// should not see the "Already Created" msgs when called again
		service.createFirewallGroup(FW_GROUP_JUNIT, "description", null); // third argument is VPC-ID
		fwgrps = service.listFirewallGroupsByName();
		Assert.assertNotNull(fwgrps);
		Assert.assertNotNull(fwgrps.get(FW_GROUP_JUNIT));

		// create rule - ip
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(0, fwgrp.getRules().size());
		service.createFirewallRuleByIpPermission(FW_GROUP_JUNIT, "tcp", 9494, 9494, "192.168.0.0/16");
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		fwrule = fwgrp.getRules().get(0);
		Assert.assertEquals(9494, fwrule.getToPort().longValue());
		Assert.assertEquals("tcp", fwrule.getProtocol());

		Thread.sleep(5000); // Give time for AWS to register rules to see expected output when attempted to run again

		// should not get a InvalidPermission.Duplicate Error
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		service.createFirewallRuleByIpPermission(FW_GROUP_JUNIT, "tcp", 9494, 9494, "192.168.0.0/16");
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		fwrule = fwgrp.getRules().get(0);
		Assert.assertEquals(9494, fwrule.getToPort().longValue());
		Assert.assertEquals("tcp", fwrule.getProtocol());

		// delete rule - ip
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		service.deleteFirewallRuleByIpPermission(FW_GROUP_JUNIT, "tcp", 9494, 9494, "192.168.0.0/16");
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(0, fwgrp.getRules().size());

		// create rule - userid
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(0, fwgrp.getRules().size());
		service.createFirewallRuleByUserIdGroup(FW_GROUP_JUNIT, "tcp", 9595, 9595, userId, userIdGroup);
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		fwrule = fwgrp.getRules().get(0);
		Assert.assertEquals(9595, fwrule.getToPort().longValue());
		Assert.assertEquals("tcp", fwrule.getProtocol());

		// delete rule - userid
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(1, fwgrp.getRules().size());
		service.deleteFirewallRuleByUserIdGroup(FW_GROUP_JUNIT, "tcp", 9595, 9595, userId, userIdGroup);
		fwgrps = service.listFirewallGroupsByName();
		fwgrp = fwgrps.get(FW_GROUP_JUNIT);
		Assert.assertEquals(0, fwgrp.getRules().size());

		// delete security group
		service.deleteFirewallGroup(FW_GROUP_JUNIT);
		fwgrps = service.listFirewallGroupsByName();
		Assert.assertNotNull(fwgrps);
		Assert.assertNull(fwgrps.get(FW_GROUP_JUNIT));
	}

