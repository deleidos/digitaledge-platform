	private final static String USERS_OU = "users";
	
	public static void main(String[] args) {
		
		if(args.length != 2) {
			logger.error("Usage: LdapDomainChange <old-domain> <new-domain>");
			System.exit(1);
		}
		
		changeDomain(args[0], args[1]);
	}
	
	public static void changeDomain(String oldDomain, String newDomain) {
		String contextUrl = RtwsConfig.getInstance().getString("ldap.provider.url");
		
		boolean removeOldDomainPartition = true;
		
		// If the old domain exists, then we want to copy everything from it to the new domain.
		if(dnExists(contextUrl, oldDomain)) {
			
			try {
				copyUsers(contextUrl, oldDomain, newDomain);
				
				try {
					// Delete the old domain so that next time this runs, it will fail
					// the initial existence check and the domain change won't run again.
					deleteDn(contextUrl, oldDomain);
				} catch (Exception e) {
					logger.warn("Failed to delete old domain [" + oldDomain + "] form LDAP.");
				}
				
			} catch (Exception e) {
				logger.error("Failed to copy all users from domain [" + oldDomain + "] to domain [" + newDomain + "].", e);
				
				// We failed to copy the users, make sure to not remove the old partition
				removeOldDomainPartition = false;
			}
		}
		
		// Always remove the old partition because we created a new one or a new one always exists,
		// We always remove the old partition because it must be in the server.xml. The only reason
		// we don't want to delete this partition is if we tried to create a new context and copying
		// the users failed.
		if(removeOldDomainPartition) {
			deleteApacheDSPartition(oldDomain);
		}
		
	}
	
	private static boolean dnExists(String contextUrl, String searchDomain) {
		String searchDn = buildDn(searchDomain);
		String usersRdn = "ou=" + USERS_OU;
		
		try {
			logger.debug("Checking if DN [" + searchDn + "] exists...");

			DirContext searchCtx = getBaseDirContext(contextUrl, searchDn);
			
			searchCtx.search(usersRdn, null);
			
			logger.debug("DN exists.");
			// If we reach here, the search was successful with this DN so it must exists
			return true;
		} catch (Exception e) {
			logger.debug("DN does not exist.");
			
			// We failed to find a name using that DN so it must not exist
			return false;
		}
	}
	
	private static void copyUsers(String contextUrl, String sourceDomain, String destinationDomain) throws NamingException  {
		String sourceDn = buildDn(sourceDomain);
		String destinationDn = buildDn(destinationDomain);
		
		logger.debug("Copying users from [" + sourceDn + "] to [" + destinationDn + "]...");
		
		String usersRdn = RtwsConfig.getInstance().getString("ldap.search.usersRdn");
		
		DirContext sourceCtx = getBaseDirContext(contextUrl, sourceDn);
		DirContext destinationCtx = getBaseDirContext(contextUrl, usersRdn + "," + destinationDn);

		NamingEnumeration<SearchResult> sourceUsers = sourceCtx.search(usersRdn, null);

		while (sourceUsers.hasMoreElements()) {
			try {
				SearchResult sourceUser = sourceUsers.next();
				 
				// Copy all the user fields
				String uid = (String)sourceUser.getAttributes().get(LdapConstants.UID).get();
				String password = new String((byte[])sourceUser.getAttributes().get(LdapConstants.USER_PASSWORD).get());
				String description = (String)sourceUser.getAttributes().get(LdapConstants.DESCRIPTION).get();
				String cn = (String)sourceUser.getAttributes().get(LdapConstants.CN).get();
				String sn = (String)sourceUser.getAttributes().get(LdapConstants.SN).get();
				
				// If a user has not update their user information to include security questions, we will use the old
				// version of the user context which does not include the mail or info attributes.  This is done because
				// the webapp-accounts will detect the user has no security questions configured when they attempt to login
				// next and will be forced to create security questions.
				Attribute mailAttr = sourceUser.getAttributes().get(LdapConstants.MAIL);
				if(mailAttr != null) {
					// New user format with mail and info attributes.
					String mail = (String)mailAttr.get();
					String info = (String)sourceUser.getAttributes().get(LdapConstants.INFO).get();
					
					UserContext destinationUser = new UserContext(uid, password, description, cn, sn, mail, info);
					destinationCtx.bind(LdapConstants.UID + "=" + uid, destinationUser);
				}
				else {
					// Use the old user format
					OldUserContext destinationUser = new OldUserContext(uid, password, description, cn, sn);
					destinationCtx.bind(LdapConstants.UID + "=" + uid, destinationUser);
				}
				
				logger.debug("Copied user [" + uid + "] from [" + sourceDn + "] to [" + destinationDn + "]");
			} catch (NameAlreadyBoundException e) {
				// This user already exists in the new Domain, no need to copy. This applies because the 
				// init-directory.ldif initializes the domain with the Admin so we can ignore the copy.
				continue;
			}
		}
	}
	
	private static void deleteDn(String contextUrl, String oldDomain) throws NamingException {
		String oldDn = buildDn(oldDomain);
		oldDn = LdapConstants.nameToLdapFormat(oldDn);
		
		logger.debug("Unbinding all chilren from " + oldDn + "...");
		// Unbind all the children of the DN
		recursiveChildrenUnbind(contextUrl, oldDn);
		logger.debug("Children unbound.");
		
		logger.debug("Unbinding context [" + oldDn +"] from root...");
		// Remove the domain from the root
		DirContext rootCtx = getBaseDirContext(contextUrl, "");
		rootCtx.unbind(oldDn);
		logger.debug("Context [" + oldDn + "] unbound");
		
		// Restart ApacheDS without the old partition
		deleteApacheDSPartition(oldDomain);
	}
	
	private static void recursiveChildrenUnbind(String contextUrl, String contextName) throws NamingException {
		DirContext context = getBaseDirContext(contextUrl, contextName);
		
		NamingEnumeration<NameClassPair> children = context.list("");
		
		while(children.hasMoreElements()) {
			NameClassPair child = children.next();
			String childName = child.getName();
			String childDn = childName + "," + contextName;
			

			logger.debug("Detected child [" + childDn + "].");
			// Make sure all the children unbind their children	
			recursiveChildrenUnbind(contextUrl, childDn);
			
			// Unbind the child
			context.unbind(childName);
			logger.debug("Child [" + childDn + "] unbound.");
		}
	}

	private static boolean deleteApacheDSPartition(String domain) {
		StringBuilder command = new StringBuilder("/usr/local/apacheds/delete_partition.sh");
		command.append(" ").append(domain);
		
		Process process = null;

		try {
			logger.debug("Executing delete partition command '/bin/bash -c " + command.toString() + "'");

			process = new ProcessBuilder("/bin/bash", "-c", command.toString()).start();
			synchronized (process) {
				process.waitFor(); // Let the script finish running before we get the exit value.
			}

			int exitValue = process.exitValue();

			logger.debug("Delete partition completed with exit value " + exitValue);

			if (exitValue == 0) {
				return true;
			}
		} catch (Exception e) {
			logger.error("Failed to delete [" + domain + "] partition from ApacheDS.", e);
		}

		return false;
	}
	
	private static DirContext getBaseDirContext(String contextUrl, String baseDn) throws NamingException {
		Hashtable<String, String> env = new Hashtable<String, String>();
	
		RtwsConfig config = RtwsConfig.getInstance();
		
		String providerUrl = contextUrl;
		String authentication = config.getString("ldap.authentication");
		String principal = config.getString("ldap.principal");
		String credentials = config.getString("ldap.credentials");
	
		baseDn = LdapConstants.nameToLdapFormat(baseDn);
		principal = LdapConstants.nameToLdapFormat(principal);
		
		StringBuilder url = new StringBuilder();
		url.append(providerUrl).append('/').append(baseDn);
	
		env.put(Context.INITIAL_CONTEXT_FACTORY, LdapConstants.LDAP_CONTEXT_FACTORY);
		env.put(Context.PROVIDER_URL, url.toString());
		env.put(Context.SECURITY_AUTHENTICATION, authentication);
		env.put(Context.SECURITY_PRINCIPAL, principal);
		env.put(Context.SECURITY_CREDENTIALS, credentials);
	
		return new InitialDirContext(env);
		
	}
	
	private static String buildDn(String domain) {
		StringBuilder dn = new StringBuilder();
		dn.append("dc=").append(domain).append("|dc=com");
		return dn.toString(); 
	}

