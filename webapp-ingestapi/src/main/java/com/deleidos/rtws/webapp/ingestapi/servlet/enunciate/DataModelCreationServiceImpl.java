	public DataModelCreationServiceImpl(){
		
		logger.debug("Initializing DataModelCreationServiceImpl");
		
		
		synchronized (this) {
			if(statusMap==null){
				statusMap = new ConcurrentHashMap<String, String>();
			}
			if(modelCreationExecutor==null){
				modelCreationExecutor = Executors.newFixedThreadPool(20);
				//modelCreationExecutor = Executors.newSingleThreadExecutor();
			}
			if(creatorMap==null){
				creatorMap = new ConcurrentHashMap<String, ModelCreator>();
			}
		}
	}

	@Override
	public String createDataModelCSV(int numHeaders, int numRowsToRead, double decisionThreshold,
			String columnSeparator, InputStream sourceStream) throws ServiceException {
		
		
		columnSeparator =  Pattern.quote(columnSeparator);
		
		String executionId = UUID.randomUUID().toString();
		
		InputStream is;
		try {
			is = readFile(sourceStream);
		} catch (IOException e) {
			throw new ServiceException("Error streaming data", e);
		}

		if (numHeaders != 1) {
			statusMap.put(executionId, "ERROR: CSV data model creation requires exactly one (1) header line.");
		} else {

			statusMap.put(executionId, "NOT YET STARTED");

			ModelCreator creator = new CsvModelCreator(executionId,
					numHeaders, numRowsToRead, decisionThreshold,
					columnSeparator, is);
			creatorMap.put(executionId, creator);

			modelCreationExecutor.execute(creator);
		}
		return executionId;
	}
	

	@Override
	public String createDataModelJSON(int numRowsToRead,
			double decisionThreshold, InputStream sourceStream) throws ServiceException {

		InputStream is;
		try {
			is = readFile(sourceStream);
		} catch (IOException e) {
			throw new ServiceException("Error streaming data", e);
		}
		
		String executionId = UUID.randomUUID().toString();
		statusMap.put(executionId, "NOT YET STARTED");
		JSONModelCreator creator = new JSONModelCreator(executionId,numRowsToRead,decisionThreshold,is);
		creatorMap.put(executionId, creator);
		modelCreationExecutor.execute(creator);

		return executionId;
	}

