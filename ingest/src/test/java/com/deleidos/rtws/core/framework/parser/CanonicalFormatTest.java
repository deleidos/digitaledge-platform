package com.deleidos.rtws.core.framework.parser;

import java.io.File;
import java.io.IOException;
import java.util.ArrayList;
import org.apache.commons.io.FileUtils;

import static org.junit.Assert.*;

import org.junit.After;
import org.junit.Before;
import org.junit.Test;

import com.deleidos.rtws.commons.config.PropertiesUtils;
import com.deleidos.rtws.core.framework.parser.CanonicalFormat;
import com.deleidos.rtws.core.framework.parser.CanonicalFormat.DataTypes;
import com.deleidos.rtws.core.framework.processor.EnrichmentFieldMappingCache;

public class CanonicalFormatTest {
	
	private final String validCanonicalJsonString =
		"{ " +
		"\"string1\"  : \"string\", " +
		"\"number1\"  : \"number\", " +
		"\"datetime\" : \"datestring MM/dd/yy HH:mm:ss\", " +
		"\"dateonly\" : \"datestring MM/dd/yy\", " +
		"\"timeonly\" : \"datestring HH:mm:ss\", " +
		"\"array1\" : [ \"number\" ], " +
		"\"obj1\"  : { " +
		"  \"obj1str1\" : \"string\", " +
		"  \"nestobj1\" : { " +
		"    \"nestobj1-str1\" : \"string\" " +
		"    }, " +
		"  \"obj1str2\" : \"string\" " +
		"  }, " +
		"\"array2\" : [ { " +
		"  \"str1-nestobj1-a2\" : \"string\", " +
		"  \"num1-nestobj2-a2\" : \"number\", " +
		"  \"nestobj2\" : { " +
		"    \"nestobj2-num1\" : \"number\" " +
		"    } " +
		"  } ], " +
		"\"endstring\" : \"string\" " +
		" } ";

	private final String invalidCanonicalJsonString =
		"{ " +
		"\"string1\"  : \"strng\", " +
		"\"number1\"  : \"nuber\", " +
		"\"datetime\" : \"datime MM/dd/yy HH:mm:ss\", " +
		"\"dateonly\" : \"datestring\", " +
		"\"timeonly\" : \"datestring XX:mm:ss\", " +
		"\"array1\" : [ \"\" ], " +
		"\"obj1\"  : { " +
		"  \"obj1str1\" : \"strng\", " +
		"  \"nestobj1\" : { " +
		"    \"nestobj1-str1\" : \"    \" " +
		"    }, " +
		"  \"obj1str2\" : \"		\" " +
		"  }, " +
		"\"array2\" : [ { " +
		"  \"str1-nestobj1-a2\" : \"453452256\", " +
		"  \"num1-nestobj2-a2\" : \"--=-=-=-=\", " +
		"  \"nestobj2\" : { " +
		"    \"nestobj2-num1\" : \"()()()\" " +
		"    } " +
		"  } ], " +
		"\"endstring\" : \"string\" " +
		" } ";

	private File validFile;
	private File invalidFile;
	
	private CanonicalFormat validCanonical;
	private CanonicalFormat invalidCanonical;
	private String basedir;

	/**
	 * @throws java.lang.Exception
	 */
	@Before
	public void setUp() throws Exception {
		if (validCanonical == null) {
			validFile = File.createTempFile("valid_canonical_json", null);
			validFile.deleteOnExit();
			FileUtils.writeStringToFile(validFile, validCanonicalJsonString);
			validCanonical = new CanonicalFormat(validFile);
		}

		if (invalidCanonical == null) {
			invalidFile = File.createTempFile("invalid_canonical_json", null);
			invalidFile.deleteOnExit();
			FileUtils.writeStringToFile(invalidFile, invalidCanonicalJsonString);
			invalidCanonical = new CanonicalFormat(invalidFile);
		}
		
		if (basedir == null) {
			basedir = System.getProperty("basedir", "");
		}
	}

	/**
	 * @throws java.lang.Exception
	 */
	@After
	public void tearDown() throws Exception {
	}

	@Test
	public void testValidate() throws Exception {
		ArrayList<String> errorMessages;
		errorMessages = new ArrayList<String>();
		assertTrue( validCanonical.validate(errorMessages) );
		assertTrue( errorMessages.size() == 0);
		assertFalse( invalidCanonical.validate(errorMessages) );
		assertTrue( errorMessages.size() == 12);
	}
	
	@Test
	public void testIsNumberString() throws Exception {
		assertTrue(CanonicalFormat.isNumberString("0"));
		assertTrue(CanonicalFormat.isNumberString("-1"));
		assertTrue(CanonicalFormat.isNumberString("1"));
		assertTrue(CanonicalFormat.isNumberString("-22"));
		assertTrue(CanonicalFormat.isNumberString("22"));
		assertTrue(CanonicalFormat.isNumberString("0.0"));
		assertTrue(CanonicalFormat.isNumberString("-1.0"));
		assertTrue(CanonicalFormat.isNumberString("1.0"));
		assertTrue(CanonicalFormat.isNumberString("-22.0"));
		assertTrue(CanonicalFormat.isNumberString("22.0"));
		assertTrue(CanonicalFormat.isNumberString("0.0e1"));
		assertTrue(CanonicalFormat.isNumberString("0.0E1"));
		assertTrue(CanonicalFormat.isNumberString("1.0e2"));
		assertTrue(CanonicalFormat.isNumberString("1.0e+2"));
		assertTrue(CanonicalFormat.isNumberString("1.0E-2"));
		assertTrue(CanonicalFormat.isNumberString("-1.2e12"));
		assertTrue(CanonicalFormat.isNumberString("1.2e+12"));
		assertTrue(CanonicalFormat.isNumberString("1.2E-12"));
		
		assertFalse(CanonicalFormat.isNumberString("xxxx"));
		assertFalse(CanonicalFormat.isNumberString("0xABCD"));
		assertFalse(CanonicalFormat.isNumberString("*&%2$%32^%2%^3$"));
		assertFalse(CanonicalFormat.isNumberString("FOOBAR"));
	}
	
	@Test
	public void testIsDateTimeString() throws Exception {
		assertTrue(CanonicalFormat.isDateTimeString("MM/dd/yy HH:mm:ss", "01/02/10 08:09:10"));
		assertFalse(CanonicalFormat.isDateTimeString("asldjfasadfasd", "01/02/10 08:09:10"));
		assertFalse(CanonicalFormat.isDateTimeString("MM/dd/yy HH:mm:ss", "asdfasfwefasdf"));
	}
	
	@Test
	public void testGetRawValue() throws Exception {
		assertTrue(validCanonical.getRawValue("string1").equals("string"));
		assertTrue(validCanonical.getRawValue("number1").equals("number"));
		assertTrue(validCanonical.getRawValue("datetime").equals("datestring MM/dd/yy HH:mm:ss"));
		assertTrue(validCanonical.getRawValue("obj1.obj1str1").equals("string"));
		assertTrue(validCanonical.getRawValue("obj1.nestobj1.nestobj1-str1").equals("string"));
		assertTrue(validCanonical.getRawValue("array2[20].num1-nestobj2-a2").equals("number"));
		assertTrue(validCanonical.getRawValue("array2[20].nestobj2.nestobj2-num1").equals("number"));
		assertTrue(validCanonical.getRawValue("array1[10]").equals("number"));
		
		assertNull(validCanonical.getRawValue("garbage")); 			  // Path does not exist
		assertNull(validCanonical.getRawValue("obj1.nestobj1"));	  // References an object
		assertNull(validCanonical.getRawValue("array2[1].nestobj2")); // References an object through an array
	}
	
	@Test
	public void testGetDataType() throws Exception {
		assertEquals(validCanonical.getDataType("string1"), DataTypes.string );
		assertEquals(validCanonical.getDataType("number1"), DataTypes.number );
		assertEquals(validCanonical.getDataType("datetime"), DataTypes.datestring );
		assertNull(validCanonical.getDataType("garbage"));   // Path does not exist
		assertNull(invalidCanonical.getDataType("string1")); // Invalid data type string
	}
	
	@Test
	public void testGetParameterString() throws Exception {
		assertTrue(validCanonical.getParameterString("datetime").equals("MM/dd/yy HH:mm:ss"));
		assertTrue(validCanonical.getParameterString("dateonly").equals("MM/dd/yy"));
		assertTrue(validCanonical.getParameterString("timeonly").equals("HH:mm:ss"));
		assertNull(validCanonical.getParameterString("string1")); // no parameter string
		assertNull(validCanonical.getDataType("garbage"));   // Path does not exist
	}
	
	@Test
	public void testSalesCanonicalConfig() {
		testFileSyntax(basedir + File.separator + "/src/test/resources/models/canonical_sales_v1.0.json");
	}
	
	@Test
	public void testSalesEnrichedConfig() {
		testFileSyntax(basedir + File.separator + "/src/test/resources/models/enriched_sales_v1.0.json");
		testEnrichmentSyntax(basedir + File.separator + "/src/test/resources/models/enriched_sales_v1.0.json");
	}
	
	@Test
	public void testTransporationCanonicalConfig() {
		testFileSyntax(basedir + File.separator + "/src/test/resources/models/canonical_transportation_v1.0.json");
	}
	
	@Test
	public void testTransporationEnrichedConfig() {
		testFileSyntax(basedir + File.separator + "/src/test/resources/models/enriched_transportation_v1.0.json");
		testEnrichmentSyntax(basedir + File.separator + "/src/test/resources/models/enriched_transportation_v1.0.json");
	}
	
	@Test
	public void testDnsModel() throws Exception {
		File f = new File(basedir + File.separator + "src/test/resources/dns_input_model.json");
		CanonicalFormat inputModel = new CanonicalFormat(f);
		DataTypes dataType = inputModel.getDataType("dns.questions[*]");
		assertNotNull(dataType);
	}
	
	private void testFileSyntax(String file) {
		try {
			new CanonicalFormat(PropertiesUtils.loadResource(file));
		} catch(IOException e) {
			fail(e.getMessage());
		}
	}
	
	private void testEnrichmentSyntax(String file) {
		try {
			new EnrichmentFieldMappingCache(new CanonicalFormat(PropertiesUtils.loadResource(file)));
		} catch(Exception e) {
			fail(e.getMessage());
		}
	}
	
}
