/**
 * 
 * Extends the ManagementContext base class to allow RemoteManagedBeans to communicate with other beans on other JMX
 * Servers.  It is designed to run in its own JVM and to allow the display of operating system statistics.  It provides the 
 * invoke methods which allow the Managed Beans to call methods on remote JMX Beans.
 * 
 * @author rainerr
 *
 */

public class ExternalJmxServer extends ManagementContext {

	/** Logger. */
	private static final Logger log = Logger.getLogger(ExternalJmxServer.class);
	
	/** List of Managed Beans. */
	private List<ManagedBean> managedBeans;
	
	/** Constructor. */
	public ExternalJmxServer() {
		super();
	}
	
	/**
	 * Constructor.
	 * @param managedBeans List of Managed Beans
	 */
	public void setManagedBeans(List<ManagedBean> managedBeans) {
		this.managedBeans = managedBeans;
	}
	
	/**
	 * Get list of managed beans.
	 * @return List
	 */
	@XmlElementWrapper(name="managedBeans")
	@XmlElement(name="diskInfoMXBean")
	public List<ManagedBean> getManagedBeans() {
		return managedBeans;
	}
	
	
	
	/**
	 * Allows a method to be invoked on a bean on another server.  An active connection to the MBeanServer must be supplied.  This connection
	 * must be open and active.  The ObjectName must be supplied to specify the bean where the method is defined.  Finally, a list of parameters
	 * must be supplied.
	 * 
	 * @param mbsc - MBeanServerConnection to connect to another bean server.
	 * @param objectName - ObjectName of the bean to be called.
	 * @param methodName - Name of the method to call.
	 * @param paramList - A list of parameters used by the method.
	 * @param sigTypeList - Parameter types from the paramList.
	 * 
	 * @return Any type of Object.
	 * 
	 */
	
	public static Object invoke(MBeanServerConnection mbsc, ObjectName objectName, String methodName, List<?> paramList, List<String> sigTypeList) throws MethodInvocationException {
		Object result = null;
		
		try {
			if(mbsc == null) {
				throw new MethodInvocationException("MBean Server Connection cannot be null");
			}
			if( objectName == null) {
				throw new MethodInvocationException("ObjectName cannot be null");
			}
			if( paramList == null) {
				result = mbsc.invoke(objectName, methodName, null, null);
			} else {
				result = mbsc.invoke(objectName, methodName, paramList.toArray(), sigTypeList.toArray(new String[sigTypeList.size()]));
			}
		} catch( ReflectionException re) {
			log.error("Reflection Exception Invoking method " + methodName, re);
			throw new MethodInvocationException("Reflection Exception Invoking method " + methodName, re);
		} catch( InstanceNotFoundException infe) {
			log.error("Instance Not Found Invoking method " + methodName, infe);
			throw new MethodInvocationException("Instance Not Found Invoking method " + methodName, infe); 
		} catch( MBeanException mbe) {
			log.error("MBean Exception Invoking method " + methodName, mbe);
			throw new MethodInvocationException("MBean Exception Invoking method " + methodName, mbe);
		} catch( IOException ioe) {
			log.error("IOException invoking method " + methodName, ioe);
			throw new MethodInvocationException("IOException invoking method " + methodName, ioe);
		} catch( Exception ex) {
			log.error("An Exception Occured Invoking method " + methodName, ex);
		}
		return result;
	}
	
	/**
	 * Keeps the Jmx Server running and the JVM still running.	
	 */
	
	
	public void startJmxServer() {
		
		try {
			for(;;) {
				// Must keep Server Alive.
				Thread.sleep(10000);
			} 
		} catch(Exception ex) {
			log.error("Error: " + ex.getMessage());
		}
	}
	
	/**
	 * Will load the meta data file for configuring the Apache Modeler.
	 * 
	 * @param metaDataFileName - Name of the meta data file.
	 */
	
	public void loadMetadata(String metaDataFileName) {
		// Functionality de-supported.
	}
	
	/**
	 * This method will build the local JMX URL in which another JMX server can call back on.  There
	 * is no standard way to get the local JMX URL.  This method will grab the port and the JMX Server
	 * Name and put it in an RMI URL format.
	 * 
	 */
	public String buildLocalJmxUrl() throws UnknownHostException {
		StringBuffer urlBuffer = new StringBuffer("service:jmx:rmi://");
		
		String  rmiPort = System.getProperty("com.deleidos.rtws.management.rmiPort");
		String jmxPort = System.getProperty("com.deleidos.rtws.management.jmxPort");
		urlBuffer.append(InetAddress.getLocalHost().getHostName() + ":");
		urlBuffer.append(jmxPort);
		urlBuffer.append("/jndi/rmi://" + InetAddress.getLocalHost().getHostName() + ":");
		urlBuffer.append(rmiPort);
		urlBuffer.append("/jmxrmi");
		
		log.info(urlBuffer.toString());
		return urlBuffer.toString();
	}
	
	public void initialize() throws JmxException {
		super.initialize();
		List<String> objectNameStrings = new ArrayList<String>();
		
		
		try {
			if (managedBeans != null) {
			
				for( ManagedBean managedBean : managedBeans) {
					//DiskInfoMBean diskInfoMBean = (DiskInfoMBean) managedBean;
					register(managedBean);
					ObjectName objectName = buildObjectName(managedBean);
					//this.register(objectName, diskInfoMBean);
					objectNameStrings.add(objectName.getCanonicalName());
				}
			} 
		} catch( MalformedObjectNameException mone) {
			log.error("Unable to create Object Name for a remote bean ", mone);
		} catch( Exception ex) {
			log.error("Error: " + ex.getMessage());
		} 
		
		String jmxUrl = null;
		try {
			jmxUrl = buildLocalJmxUrl();
			log.info("JMX URL to connect to: "+ jmxUrl);
		} catch(UnknownHostException uhe) {
			log.error("Unable to build Jmx Url during initialization", uhe);
		}
	}
	
	public static Object invoke(MBeanServerConnection mbsc, ObjectName objectName, String methodName) {
		return invoke(mbsc, objectName, methodName, null, null);
	}
	
	/**
	 * Print a usage statement.
	 */
	protected static void usage() {
		log.error("Usage: runjmxserver configfile bindingfile");
	}
	
	public static void main(String[] args) {
		InputStream stream = null;
		ExternalJmxServer externalJmxServer = null;
		
		// Verify that a command line parameter was passed.
		if((args.length < 2) || (args.length > 3)) { // 2 or 3 arguments
			usage();
			System.exit(1);
		}
		
		String configFile = args[0];
		String bindingFile = args[1];
				
		try {
			// Configure an XML marshaller for the classes referenced in the configuration file.
			Class<?>[] configClasses = BindingContextLoader.loadBindingContext(bindingFile);
			JAXBContext context = JAXBContext.newInstance( configClasses );
			Unmarshaller marshaller = context.createUnmarshaller();
	
			// Create a stream to read the configuration file.
			stream = PropertiesUtils.loadResource(configFile);
			StreamSource source = new StreamSource(stream);
	
			// Load the configuration data.
			externalJmxServer = marshaller.unmarshal(source, ExternalJmxServer.class).getValue(); 
			externalJmxServer.initialize(); 
			externalJmxServer.startJmxServer();
		} catch (Exception ex) {
			ex.printStackTrace();
			System.out.println("Error Marshalling JMX Server: " + ex.getMessage());
		} finally {
			try {stream.close();} catch (Exception ignore) { }
		}
	}


}
