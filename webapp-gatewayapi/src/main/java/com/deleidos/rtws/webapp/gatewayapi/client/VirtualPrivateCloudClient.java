	public void deleteRouteTable(String vpcId, String routeTableId, boolean isMain) throws RestClientException {		
		try {
			RouteTable routeTable = describeRouteTable(vpcId, routeTableId);
				
			for (Route route : routeTable.getRoutes()) {
				if (! "local".equals(route.getGatewayId())) {
					deleteRoute(routeTable.getRouteTableId(), route.getDestinationCidrBlock());
				}
			} 
				
			for (String associationId : routeTable.getAssociationIds()) {
				try {
					disassociateRouteTable(associationId);
				} catch (Exception ex) {
					logger.debug(String.format("Fail to disassociate %s for route table %s.", associationId, routeTable.getRouteTableId()));
				}
			}

			if (! isMain) {
				RestClientManager.getServiceInterface().deleteRouteTable(routeTableId);
			}
		} catch (Exception ex) {
			String message = String.format("Fail to delete route table %s for vpc %s.", routeTableId, vpcId);
			logger.error(message, ex);
			throw new RestClientException(message, ex);
		}
	}
	
