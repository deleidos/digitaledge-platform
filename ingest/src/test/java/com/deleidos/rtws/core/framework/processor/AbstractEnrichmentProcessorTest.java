	/**
	 * Verifies that under normal conditions, correct enrichment values are added to the record in
	 * the expected locations within the JSON tree. Also verifies that parameter fields are removed
	 * or retained as specified in the enrichment rules.
	 */
	@Test
	public void testProcess() {
		
		JSONObject invoice = buildInvoice();

		EnrichmentAction addressEnrichment = new EnrichmentAction();
		addressEnrichment.setType("lookup");
		addressEnrichment.setField("addressDetail");
		addressEnrichment.setParameters(new String[]{"zipcode"});
		addressEnrichment.setRemoveParameters(true);
		
		EnrichmentAction itemEnrichment = new EnrichmentAction();
		itemEnrichment.setType("lookup");
		itemEnrichment.setField("items[*].itemDetail");
		itemEnrichment.setParameters(new String[]{"items[].serialNumber"});
		itemEnrichment.setRemoveParameters(true);

		EnrichmentAction promotionEnrichment = new EnrichmentAction();
		promotionEnrichment.setType("lookup");
		promotionEnrichment.setField("items[*].promotions[*].promotionDetail");
		promotionEnrichment.setParameters(new String[]{"store.region", "items[].promotions[].code"});
		promotionEnrichment.setRemoveParameters(false);
		
		EnrichmentFieldMappingCache fields = new EnrichmentFieldMappingCache();
		fields.setModelName(DUMMY_MODEL_TYPE);
		fields.setModelVersion("1.0");
		fields.setFields(new EnrichmentAction[]{addressEnrichment, itemEnrichment, promotionEnrichment});
		
		EnrichmentModelMappingCache models = new EnrichmentModelMappingCache();
		models.setModels(new EnrichmentFieldMappingCache[]{fields});
		
		AbstractEnrichmentProcessor proc = new AbstractEnrichmentProcessor() {
			public String getType() {return "lookup";}
			public JSONObject buildEnrichedElement(EnrichmentAction action, ParameterList parameters) {
				if(parameters.equalsArray("12345")) {return address;}
				if(parameters.equalsArray("11111")) return(item1);
				if(parameters.equalsArray("22222")) return(item2);
				if(parameters.equalsArray("EAST", "p5151")) return(promotion1);
				if(parameters.equalsArray("EAST", "p6161")) return(promotion2);
				fail("Enrichment invoked with values that are not expected as parameters '" + parameters + "'");
				return null;
			}
		};
		
		proc.setEnrichments(models);
		proc.initialize();
		proc.process(invoice);
		
		JSONObject record = invoice;
		assertTrue("Enrichment not performed for root element.", record.containsKey("addressDetail"));
		assertEquals("Incorrect value found in enchiched field.", "12345", record.getJSONObject("addressDetail").optString("zipcode"));
		assertFalse("Parameter field retained when directoed to remove.", record.containsKey("zipcode"));
		assertTrue("Parameter field removed when directed to retain.", record.getJSONObject("store").containsKey("region"));
		
		record = invoice.optJSONArray("items").getJSONObject(0);
		assertTrue("Enrichment not performed for array element.", record.containsKey("itemDetail"));
		assertEquals("Incorrect value found in enchiched field.", "Cheap Item", record.getJSONObject("itemDetail").optString("name"));
		assertFalse("Enrichment not performed for nested array element.", record.containsKey("promotionDetail"));
		assertFalse("Parameter field retained when directoed to remove.", record.containsKey("serialNumber"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(0);
		assertTrue("Enrichment not performed for nested array element.", record.containsKey("promotionDetail"));
		assertEquals("Incorrect value found in enchiched field.", "yellow tag", record.getJSONObject("promotionDetail").optString("description"));
		assertTrue("Parameter field removed when directed to retain.", record.containsKey("code"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(1);
		assertTrue("Enrichment not performed for nested array element.", record.containsKey("promotionDetail"));
		assertEquals("Incorrect value found in enchiched field.", "2 for 1", record.getJSONObject("promotionDetail").optString("description"));
		assertTrue("Parameter field removed when directed to retain.", record.containsKey("code"));
		
		record = invoice.optJSONArray("items").getJSONObject(1);
		assertTrue("Enrichment not performed for array element.", record.containsKey("itemDetail"));
		assertEquals("Incorrect value found in enchiched field.", "Expensive Item", record.getJSONObject("itemDetail").optString("name"));
		assertFalse("Parameter field retained when directoed to remove.", record.containsKey("serialNumber"));
		
	}

	/**
	 * Verifies that if an enrichment throws an exception, that enrichments are still attempted for subsequent fields.
	 */
	@Test
	public void testProcessWithException() {
		
		JSONObject invoice = buildInvoice();

		EnrichmentAction addressEnrichment = new EnrichmentAction();
		addressEnrichment.setType("lookup");
		addressEnrichment.setField("addressDetail");
		addressEnrichment.setParameters(new String[]{"zipcode"});
		addressEnrichment.setRemoveParameters(true);
		
		EnrichmentAction itemEnrichment = new EnrichmentAction();
		itemEnrichment.setType("lookup");
		itemEnrichment.setField("items[*].itemDetail");
		itemEnrichment.setParameters(new String[]{"items[].serialNumber"});
		itemEnrichment.setRemoveParameters(true);

		EnrichmentAction promotionEnrichment = new EnrichmentAction();
		promotionEnrichment.setType("lookup");
		promotionEnrichment.setField("items[*].promotions[*].promotionDetail");
		promotionEnrichment.setParameters(new String[]{"store.region", "items[].promotions[].code"});
		promotionEnrichment.setRemoveParameters(false);
		
		EnrichmentFieldMappingCache fields = new EnrichmentFieldMappingCache();
		fields.setModelName(DUMMY_MODEL_TYPE);
		fields.setModelVersion("1.0");
		fields.setFields(new EnrichmentAction[]{addressEnrichment, itemEnrichment, promotionEnrichment});
		
		EnrichmentModelMappingCache models = new EnrichmentModelMappingCache();
		models.setModels(new EnrichmentFieldMappingCache[]{fields});
		
		AbstractEnrichmentProcessor proc = new AbstractEnrichmentProcessor() {
			public String getType() {return "lookup";}
			public JSONObject buildEnrichedElement(EnrichmentAction action, ParameterList parameters) {
				if(parameters.equalsArray("12345")) {throw new RuntimeException();}
				if(parameters.equalsArray("11111")) {return(item1);}
				if(parameters.equalsArray("22222")) {return(item2);}
				if(parameters.equalsArray("EAST", "p5151")) {return(promotion1);}
				if(parameters.equalsArray("EAST", "p6161")) {return(promotion2);}
				return null;
			}
		};
		
		proc.setEnrichments(models);
		proc.initialize();
		proc.process(invoice);
		
		JSONObject record = invoice;
		assertTrue("Parameter field removed when enrichment failed.", record.getJSONObject("store").containsKey("region"));
		
		record = invoice.optJSONArray("items").getJSONObject(0);
		assertTrue("Enrichment not performed for subsequent array element following exception.", record.containsKey("itemDetail"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(0);
		assertTrue("Enrichment not performed for subsequent nested array element following exception.", record.containsKey("promotionDetail"));
		
		record = invoice.optJSONArray("items").getJSONObject(0).getJSONArray("promotions").getJSONObject(1);
		assertTrue("Enrichment not performed for subsequent nested array element following exception.", record.containsKey("promotionDetail"));
		
		record = invoice.optJSONArray("items").getJSONObject(1);
		assertTrue("Enrichment not performed for subsequent array element following exception.", record.containsKey("itemDetail"));
		
	}

	@Test
	public void testOveride() {
		
		JSONObject record = buildBasicRecord();
		
		AbstractEnrichmentProcessor proc = new AbstractEnrichmentProcessor() {
			public String getType() {return "whatever";}
			public JSONObject buildEnrichedElement(EnrichmentAction action, ParameterList parameters) {
				return new JSONObject().element("name", "new");
			}
		};
		
		record.put("field", new JSONObject().element("name", "old"));
		configure(proc, "field", null, false, false);
		proc.initialize();
		proc.process(record);
		proc.dispose();
		
		assertEquals("Field value replaced when override was disabled.", "old", record.getJSONObject("field").getString("name"));
		
		
		record.put("field", new JSONObject().element("name", "old"));
		configure(proc, "field", null, true, false);
		proc.initialize();
		proc.process(record);
		proc.dispose();
		
		assertEquals("Field value was not replaced when override was enabled.", "new", record.getJSONObject("field").getString("name"));
		
	}
	
