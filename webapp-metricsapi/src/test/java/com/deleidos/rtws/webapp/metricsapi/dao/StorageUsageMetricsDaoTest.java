package com.deleidos.rtws.webapp.metricsapi.dao;

import static com.deleidos.rtws.testutil.net.jmx.JmxTestUtils.*;
import static org.junit.Assert.*;
import static org.mockito.Matchers.same;
import static org.mockito.Mockito.*;

import java.util.Arrays;
import java.util.Collection;
import java.util.Collections;
import java.util.HashSet;
import java.util.LinkedHashSet;
import java.util.Set;

import javax.management.Attribute;
import javax.management.AttributeList;
import javax.management.ObjectName;
import javax.management.QueryExp;

import org.junit.Test;

import com.deleidos.rtws.commons.net.jmx.JmxConnectionFactory;
import com.deleidos.rtws.commons.net.jmx.JmxMBeanServerConnection;
import com.deleidos.rtws.commons.net.jmx.MBeanProxy;
import com.deleidos.rtws.webapp.metricsapi.dao.StorageUsage;
import com.deleidos.rtws.webapp.metricsapi.dao.StorageUsageMetricsDao;

public class StorageUsageMetricsDaoTest {

	@Test
	public void retrieveStorageUsageByGroup() throws Exception {
		
		/////
		// Return values for stub behavior.
		
		// Objects involved in the jmx call to discover which groups publish storage metrics.
		ObjectName processGroupObjectName = new ObjectName("rtws.deleidos.com:type=ProcessGroup,name=lucene.shard");
		Attribute processGroupNameAttribute = new Attribute("Name", "lucene.shard");
		Attribute processSupportedMetricsAttribute = new Attribute("ManagementInterfaces", new String[]{"jmxhost.external.rtws.deleidos.com"});
		AttributeList processGroupAttributeList = new AttributeList(Arrays.asList(processGroupNameAttribute, processSupportedMetricsAttribute));
		
		// Objects involved in the jmx call to discover which nodes are a member of the lucene group.
		ObjectName process1ObjectName = new ObjectName("rtws.deleidos.com:type=Process,group=lucene.shard,name=lucene-node1.rtws.deleidos.com");
		ObjectName process2ObjectName = new ObjectName("rtws.deleidos.com:type=Process,group=lucene.shard,name=lucene-node2.rtws.deleidos.com");
		
		// Objects involved in the jmx call to retrieve storage usage.
		ObjectName diskUsageObjectName = new ObjectName("jmxhost.external.rtws.deleidos.com:type=DiskInfo,volume=/dev/md0");
		Attribute diskUsageVolumeAttribute = new Attribute("Volume", "/dev/md0");
		Attribute diskUsage1TotalSpaceAttribute = new Attribute("TotalSpace",   Long.valueOf(10000000000L));
		Attribute diskUsage1UsableSpaceAttribute = new Attribute("UsableSpace",  Long.valueOf(5000000000L));
		AttributeList diskUsage1AttributeList = new AttributeList(Arrays.asList(diskUsageVolumeAttribute, diskUsage1TotalSpaceAttribute, diskUsage1UsableSpaceAttribute));
		Attribute diskUsage2TotalSpaceAttribute = new Attribute("TotalSpace",   Long.valueOf(30000000000L));
		Attribute diskUsage2UsableSpaceAttribute = new Attribute("UsableSpace", Long.valueOf(25000000000L));
		AttributeList diskUsage2AttributeList = new AttributeList(Arrays.asList(diskUsageVolumeAttribute, diskUsage2TotalSpaceAttribute, diskUsage2UsableSpaceAttribute));
		
		
		
		/////
		// Stub behavior.
		
		JmxConnectionFactory jmx = mock(JmxConnectionFactory.class);
		JmxMBeanServerConnection connection = mock(JmxMBeanServerConnection.class);
		
		when(jmx.getConnection()).thenReturn(connection);
		when(jmx.getConnection(anyString(), anyInt(), anyString())).thenReturn(connection);
		
		when(connection.queryNames(containsNameKey("type", "ProcessGroup"), any(QueryExp.class))).thenReturn(Collections.singleton(processGroupObjectName));
		when(connection.getAttributes(same(processGroupObjectName), any(String[].class))).thenReturn(processGroupAttributeList);
		
		when(connection.queryNames(containsNameKey("type", "Process"), any(QueryExp.class))).thenReturn(new HashSet<ObjectName>(Arrays.asList(process1ObjectName, process2ObjectName)));
		
		when(connection.queryNames(containsNameKey("type", "DiskInfo"), any(QueryExp.class))).thenReturn(Collections.singleton(diskUsageObjectName));
		when(connection.proxy(same(diskUsageObjectName))).thenReturn(new MBeanProxy(connection, diskUsageObjectName));
		when(connection.getAttributes(same(diskUsageObjectName), any(String[].class))).thenReturn(diskUsage1AttributeList, diskUsage2AttributeList);
		
		
		/////
		// Do Test.
		
		StorageUsageMetricsDao dao = new StorageUsageMetricsDao();
		dao.setConnectionFactory(jmx);
		dao.setRefreshInterval(-1);
		dao.initialize();
		dao.refresh();
		
		Collection<StorageUsage> results = dao.retrieveStorageUsageByGroup();
		
		/////
		// Verify results.
		
		assertEquals(1, results.size());
		StorageUsage usage = results.iterator().next();
		assertEquals(40000000000L, usage.getTotal());
		assertEquals(30000000000L, usage.getFree());
		assertEquals(10000000000L, usage.getUsed());
		
	}

	@Test
	public void retrieveStorageUsageByNode() throws Exception {
		
		/////
		// Return values for stub behavior.
		
		// Objects involved in the jmx call to discover which groups publish storage metrics.
		ObjectName processGroup1ObjectName = new ObjectName("rtws.deleidos.com:type=ProcessGroup,name=lucene.shard");
		ObjectName processGroup2ObjectName = new ObjectName("rtws.deleidos.com:type=ProcessGroup,name=jms.external");
		Attribute processSupportedMetricsAttribute = new Attribute("SupportedMetrics", new String[]{"jmxhost.external.rtws.deleidos.com"});
		Attribute processGroup1NameAttribute = new Attribute("Name", "lucene.shard");
		Attribute processGroup2NameAttribute = new Attribute("Name", "jms.external");
		AttributeList processGroup1AttributeList = new AttributeList(Arrays.asList(processGroup1NameAttribute, processSupportedMetricsAttribute));
		AttributeList processGroup2AttributeList = new AttributeList(Arrays.asList(processGroup2NameAttribute, processSupportedMetricsAttribute));
		
		// Objects involved in the jmx call to discover which nodes are a member of the above groups.
		ObjectName process1ObjectName = new ObjectName("rtws.deleidos.com:type=Process,group=lucene.shard,name=lucene-node1.rtws.deleidos.com");
		ObjectName process2ObjectName = new ObjectName("rtws.deleidos.com:type=Process,group=lucene.shard,name=lucene-node2.rtws.deleidos.com");
		ObjectName processOtherObjectName = new ObjectName("rtws.deleidos.com:type=Process,group=jms.external,name=jms-ext-node.rtws.deleidos.com");
		
		// Objects involved in the jmx call to retrieve storage usage.
		ObjectName diskUsageObjectName = new ObjectName("jmxhost.external.rtws.deleidos.com:type=DiskInfo,volume=/dev/md0");
		Attribute diskUsageVolumeAttribute = new Attribute("Volume", "/dev/md0");
		Attribute diskUsage1TotalSpaceAttribute = new Attribute("TotalSpace",  Long.valueOf(10000000000L));
		Attribute diskUsage1UsableSpaceAttribute = new Attribute("UsableSpace", Long.valueOf(3000000000L));
		AttributeList diskUsage1AttributeList = new AttributeList(Arrays.asList(diskUsageVolumeAttribute, diskUsage1TotalSpaceAttribute, diskUsage1UsableSpaceAttribute));
		Attribute diskUsage2TotalSpaceAttribute = new Attribute("TotalSpace",   Long.valueOf(30000000000L));
		Attribute diskUsage2UsableSpaceAttribute = new Attribute("UsableSpace", Long.valueOf(25000000000L));
		AttributeList diskUsage2AttributeList = new AttributeList(Arrays.asList(diskUsageVolumeAttribute, diskUsage2TotalSpaceAttribute, diskUsage2UsableSpaceAttribute));
		Attribute diskUsageOtherTotalSpaceAttribute = new Attribute("TotalSpace",   Long.valueOf(500000000L));
		Attribute diskUsageOtherUsableSpaceAttribute = new Attribute("UsableSpace", Long.valueOf(500000000L));
		AttributeList diskUsageOtherAttributeList = new AttributeList(Arrays.asList(diskUsageVolumeAttribute, diskUsageOtherTotalSpaceAttribute, diskUsageOtherUsableSpaceAttribute));
		
		
		
		/////
		// Stub behavior.
		
		JmxConnectionFactory jmx = mock(JmxConnectionFactory.class);
		JmxMBeanServerConnection connection = mock(JmxMBeanServerConnection.class);
		
		when(jmx.getConnection()).thenReturn(connection);
		when(jmx.getConnection(anyString(), anyInt(), anyString())).thenReturn(connection);
		
		Set<ObjectName> groups = new LinkedHashSet<ObjectName>(Arrays.asList(processGroup1ObjectName, processGroup2ObjectName));
		when(connection.queryNames(containsNameKey("type", "ProcessGroup"), any(QueryExp.class))).thenReturn(groups);
		when(connection.getAttributes(same(processGroup1ObjectName), any(String[].class))).thenReturn(processGroup1AttributeList);
		when(connection.getAttributes(same(processGroup2ObjectName), any(String[].class))).thenReturn(processGroup2AttributeList);
		
		Set<ObjectName> processes = new LinkedHashSet<ObjectName>(Arrays.asList(process1ObjectName, process2ObjectName, processOtherObjectName));
		when(connection.queryNames(containsNameKey("type", "Process"), any(QueryExp.class))).thenReturn(processes);
		
		when(connection.queryNames(containsNameKey("type", "DiskInfo"), any(QueryExp.class))).thenReturn(Collections.singleton(diskUsageObjectName));
		when(connection.proxy(same(diskUsageObjectName))).thenReturn(new MBeanProxy(connection, diskUsageObjectName));
		when(connection.getAttributes(same(diskUsageObjectName), any(String[].class))).thenReturn(diskUsage1AttributeList, diskUsage2AttributeList, diskUsageOtherAttributeList);
		
		
		/////
		// Do Test.
		
		StorageUsageMetricsDao dao = new StorageUsageMetricsDao();
		dao.setConnectionFactory(jmx);
		dao.setRefreshInterval(-1);
		dao.initialize();
		dao.refresh();
		
		Collection<StorageUsage> results = dao.retrieveStorageUsageByNode("lucene.shard");
		
		/////
		// Verify results.
		
		assertEquals(2, results.size());
		for (StorageUsage usage : results) {
			if (usage.getName().equals("lucene-node1.rtws.deleidos.com")) {
				assertEquals(10000000000L, usage.getTotal());
				assertEquals( 3000000000L, usage.getFree());
				assertEquals( 7000000000L, usage.getUsed());
			} else if (usage.getName().equals("lucene-node2.rtws.deleidos.com")) {
				assertEquals(30000000000L, usage.getTotal());
				assertEquals(25000000000L, usage.getFree());
				assertEquals( 5000000000L, usage.getUsed());
			} else {
				fail("Results included data for wrong group.");
			}
		}
		
	}

}
