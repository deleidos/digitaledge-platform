function PostSuccess(uuid){
	
	var intervalHandle = setInterval(function() {
		$.ajax({
			url: "status",
			type: "GET",
			data: {uuid:uuid},
			success: function(status){
				HandleSuccess(status);
			},
			error: function() {
				if (creationSuccess){
					window.location.replace(redirectAddress);
				} else {
					alert("Internal Server Error. Please contact the DigitalEdge team.");
				}
				clearInterval(intervalHandle);
				return false;
			}
		});	
	}, 5000);
}

function HandleSuccess(status){
	
	var jsonState = JSON.parse(status);
	var state = jsonState.STEP;
	var msg = jsonState.STATUS;
	
	if(state == "ERROR"){
		document.getElementById("progress-info").style.backgroundColor="#FF0000";
		document.getElementById("progress-register").style.backgroundColor="#FF0000";
		document.getElementById("progress-ami").style.backgroundColor="#FF0000";
		document.getElementById("progress-gateway").style.backgroundColor="#FF0000";
		document.getElementById("progress-welcome").style.backgroundColor="#FF0000";
		alert("An error occured. Please contact the DigitalEdge team. " + msg);
	} else if(state == "REGISTER_TMS"){
		document.getElementById("progress-register").style.backgroundColor="#850F89";
		return false;
	} else if(state == "CREATE_AMI"){
		document.getElementById("progress-register").style.backgroundColor="#850F89";
		document.getElementById("progress-ami").style.backgroundColor="#850F89";
		return false;
	} else if(state == "LAUNCH_GATEWAY"){
		document.getElementById("progress-register").style.backgroundColor="#850F89";
		document.getElementById("progress-ami").style.backgroundColor="#850F89";
		document.getElementById("progress-gateway").style.backgroundColor="#850F89";
		return false;
	} else if(state == "COMPLETE"){
		creationSuccess = true;
		document.getElementById("progress-register").style.backgroundColor="#850F89";
		document.getElementById("progress-ami").style.backgroundColor="#850F89";
		document.getElementById("progress-gateway").style.backgroundColor="#850F89";
		document.getElementById("progress-welcome").style.backgroundColor="#850F89";
		window.location.replace(redirectAddress);
	}

};
	
