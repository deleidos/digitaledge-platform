package com.deleidos.rtws.transport.Services;

import java.io.File;
import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.IOException;
import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.ResultSetMetaData;

import javax.sql.DataSource;
import javax.validation.constraints.Max;
import javax.validation.constraints.Min;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

import com.deleidos.rtws.commons.cloud.platform.StorageInterface;
import com.deleidos.rtws.commons.cloud.util.InterfaceConfig;
import com.deleidos.rtws.core.framework.Description;
import com.deleidos.rtws.core.framework.SystemConfigured;
import com.deleidos.rtws.core.framework.UserConfigured;
import com.deleidos.rtws.transport.AbstractTransportService;

@Description("A polling transport which transmits the results of the SQL query into the system.")
public class DatabaseWatcherTransportService extends AbstractTransportService {

	private DataSource dataSource;
	private String selectDataStatement;
	private int memoryKeyColumn;
	private int sleepTime;
	private String fieldSep;
	private StorageInterface storage;
	private String bucketName;
	private String fileKey;

	private boolean exit = false;

	public void execute() {
		try {
			Connection connection = dataSource.getConnection();
			PreparedStatement getDataStatement = connection
					.prepareStatement(selectDataStatement);
			int lastRecord = 0;
			while (!exit) {
				// get memory value
				if (lastRecord == 0) {
					lastRecord = getStoredInfo();
				}

				getDataStatement.setObject(1, lastRecord);
				ResultSet dataSet = getDataStatement.executeQuery();
				ResultSetMetaData metaData = dataSet.getMetaData();
				boolean rows_returned = false;
				while (dataSet.next()) {
					rows_returned = true;
					String thisRow = "";
					String sep = "";
					for (int i = 1; i <= metaData.getColumnCount(); i++) {
						thisRow = thisRow + sep + dataSet.getString(i);
						sep = fieldSep;
						if (i == memoryKeyColumn)
							lastRecord = dataSet.getInt(i);// Integer.parseInt(val);
					}
					SendJMSMessage(thisRow);
				}
				if (rows_returned) {
					storeInfo(lastRecord);
				}
				WakeableSleepHelper sleepHelper = new WakeableSleepHelper(sleepTime);
				while (sleepHelper.continueSleeping(exit)); // breaks when time to wake up
			}
		} catch (Exception e) {
			log.error("Error connecting or retrieving from database.", e);
			// e.printStackTrace();
		}
	}

	public void terminate() {
		exit = true;
	}

	public int getStoredInfo() {
		FileInputStream fs = null;
		try {
			File f = File.createTempFile("dbwatcherTemp", "tmp");
			getStorageInterface().getFile(bucketName, fileKey, f);
			fs = new FileInputStream(f);
			StringBuffer sb = new StringBuffer();
			byte[] buff = new byte[256];
			int read = fs.read(buff);
			while (read == 256) {
				sb.append(new String(buff));
				read = fs.read(buff);
			}
			String lastRead = new String(buff);
			sb.append(lastRead.substring(0, read));
			return Integer.parseInt(sb.toString());
		} catch (Exception e) {
			log.warn(
					"Could not retrieve stored position from storage service.  If this is a new transport, ignore this message.",
					e);
			return 0;
		} finally {
			if (fs != null)
				try {
					fs.close();
				} catch (IOException e) {
					log.error(e);
				}
		}
	}

	public void storeInfo(int last) {
		try {
			File f = File.createTempFile("dbwatcherTemp", "tmp");
			FileOutputStream fs = new FileOutputStream(f);
			fs.write((last + "").getBytes());
			fs.flush();
			fs.close();
			getStorageInterface().storeFile(bucketName, fileKey, f);
		} catch (Exception e) {
			log.error(
					"There was an error storing last record file for DBwatcher transport.  If this error continues, you may need to allow write access to the temp directory.",
					e);
		}
	}

	@NotNull
	public DataSource getDataSource() {
		return dataSource;
	}

	public void setDataSource(DataSource dataSource) {
		this.dataSource = dataSource;
	}

	@NotNull
	@Size(min = 10, max = 1024)
	public String getSelectDataStatement() {
		return selectDataStatement;
	}

	@UserConfigured(description = "A SQL statement to execute for data to transmit.", value = "select trackingkey, bank, account from "
			+ "tracking where trackingkey > ? "
			+ "order by trackingkey", flexValidator = { "StringValidator minLength=10 maxLength=1024" })
	public void setSelectDataStatement(String selectDataStatement) {
		this.selectDataStatement = selectDataStatement;
	}

	public String getFieldSeparator() {
		return fieldSep;
	}

	public void setFieldSeparator(String val) {
		this.fieldSep = val;
	}

	@Min(1)
	@Max(Integer.MAX_VALUE)
	public String getMemoryKeyColumn() {
		return memoryKeyColumn + "";
	}

	@UserConfigured(description="Which column number to read in the SQL query to identify new records ingested since the last read.", value = "1", flexValidator = { "NumberValidator minValue=1 maxValue="
			+ Integer.MAX_VALUE })
	public void setMemoryKeyColumn(String memoryKeyColumn) {
		this.memoryKeyColumn = Integer.parseInt(memoryKeyColumn);
	}

	public String getSleepTime() {
		return sleepTime + "";
	}

	@UserConfigured(description = "The interval in which this transport will poll "
			+ "the S3 bucket for data to transmit.", value = "0", flexValidator = {
			"NumberValidator minValue=0 maxValue=" + Integer.MAX_VALUE,
			"TestFlexValidator doSomething=false" })
	@Min(0)
	@Max(Integer.MAX_VALUE)
	public void setSleepTime(String value) {
		sleepTime = Integer.parseInt(value);
	}

	public StorageInterface getStorageInterface() {
		// See if the storage interface is currently configured,
		// if not, configure it from the interfaces configuration
		if(storage == null) {
			storage = InterfaceConfig.getInstance().getStorageInterface();
		}
		
		return storage;
	}

	@SystemConfigured(value = "java:com.deleidos.rtws.commons.cloud.platform.jetset.JetSetStorageService")
	public void setStorageInterface(StorageInterface val) {
		this.storage = val;
	}
	
	@NotNull
	@Size(min = 3, max = 1024)
	public String getFileKey() {
		return fileKey;
	}

	@UserConfigured(description = "Name of a file that will be placed in the S3 bucket.", value = "dbwatcherStorageFile.txt", flexValidator = { "StringValidator minLength=3 maxLength=1024" })
	public void setFileKey(String val) {
		fileKey = val;
	}

	@Size(min = 3, max = 63)
	public String getBucketName() {
		return bucketName;
	}

	@UserConfigured(description = "The S3 bucket to poll for data to "
			+ "transmit into the system.", value = "stuff-polling", flexValidator = { "StringValidator minLength=3 maxLength=63" })
	public void setBucketName(String val) {
		bucketName = val;
	}

}
