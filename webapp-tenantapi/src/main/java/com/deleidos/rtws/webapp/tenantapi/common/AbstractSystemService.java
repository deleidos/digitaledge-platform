import com.deleidos.rtws.webapp.tenantapi.cache.CacheManager;
import com.deleidos.rtws.webapp.tenantapi.client.RepositoryApiRestClient;
import com.deleidos.rtws.webapp.tenantapi.client.RestClientManager;
import com.deleidos.rtws.webapp.tenantapi.exception.GatewayApiException;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterDataModelDependencies;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterDimTableDependencies;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterState;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterStatus;
import com.deleidos.rtws.webapp.tenantapi.model.ClusterSystemCfgDependency;
import com.deleidos.rtws.webapp.tenantapi.worker.queue.DelaySystemRequestQueue;
import com.deleidos.rtws.webapp.tenantapi.worker.queue.QueueManager;
import com.deleidos.rtws.webapp.tenantapi.worker.request.SystemAction;

public abstract class AbstractSystemService extends BaseService {
	
	private Logger logger = Logger.getLogger(AbstractSystemService.class);
	
	private static final List<String> DIM_TABLE_DEPENDENT_ENRICHMENT_TYPES = Arrays.asList("dimension_table");
	
	private static final List<String> DIM_TABLE_CUSTOM_PARAM_NAMES = Arrays.asList("table");
	
	protected JSONObject processClusterInTransitionalState(JSONObject jsonCluster) {
		
		String domain = null;
		
		if (jsonCluster.has("domain")) {
			domain = jsonCluster.getString("domain");
		}
		
		DelaySystemRequestQueue queue = QueueManager.getSystemRequestQueue();
		
		if (queue.isRequestAlreadyOnQueue(domain, SystemAction.Create, SystemAction.Start)) {
			jsonCluster.put("status", ClusterStatus.createStartingStatus());
		} else if (queue.isRequestAlreadyOnQueue(domain, SystemAction.UpdateSystemConfig)) {
			jsonCluster.put("status", ClusterStatus.createUpdatingStatus());
		} else if (queue.isRequestAlreadyOnQueue(domain, SystemAction.Delete)) {
			jsonCluster.put("status", ClusterStatus.createDeletingStatus());
		}
		
		return jsonCluster;
		
	}
	
	protected ClusterSystemCfgDependency getSystemCfgDependencies(String tenantId, String region, String sysDomain, String cfgFilename) {
		
		try{
			AccountEntry entry = CacheManager.getTenantAccountCache().get(tenantId);
			Integer accountId = entry.getIAASAccount().getAccountId();
			
			String cluster = RestClientManager.getGatewayAPIClient().getCluster(accountId, region, sysDomain);
		
			AccountSystem system = CacheManager.getSystemsCache().get(accountId, sysDomain);
			ClusterSystemCfgDependency clusterCfg = new ClusterSystemCfgDependency(sysDomain);
			
			if (system != null && StringUtils.isNotBlank(system.getSystemDomain())) {
				JSONObject jsonCluster = JSONObject.fromObject(cluster);
				JSONObject jsonStatus = jsonCluster.getJSONObject("status");
				ClusterStatus status = new ClusterStatus();
				status.setMessage(jsonStatus.getString("message"));
				status.setState(ClusterState.valueOf(ClusterState.class, jsonStatus.getString("state")));
				clusterCfg.setStatus(status);
				clusterCfg.setSystemConfigFilename(cfgFilename);
			}
		
			return clusterCfg;
		} catch(GatewayApiException e) {
			logger.warn("Exception in GatewayAPI.", e);
		}
		
		return null;
	}
	
	protected ClusterDataModelDependencies getSystemDataModelDependencies(String tenantId, String region, String sysDomain, String cfgFilename) {
		
		ClusterSystemCfgDependency clusterCfg = getSystemCfgDependencies(tenantId, region, sysDomain, cfgFilename);
	
		if (clusterCfg != null) {
			RepositoryApiRestClient client = RestClientManager.getRespositoryApiRestClient();
		
			ClusterDataModelDependencies currClusterDataModelDeps = new ClusterDataModelDependencies(clusterCfg);
			currClusterDataModelDeps.setDataModels(new HashSet<String>());
			
			if (StringUtils.isNotBlank(cfgFilename)) {
				try {
					SystemConfig sysCfg = client.fetchUserSystemConfig(clusterCfg.getDomain(), clusterCfg.getSystemConfigFilename());
				
					if(sysCfg != null && sysCfg.getDataModels() != null) {
						currClusterDataModelDeps.getDataModels().addAll(sysCfg.getDataModels());
					}
				} catch(IOException ioException) {
					String sysCfgReadErrorMsg = "Failed to read system config for " + currClusterDataModelDeps.getDomain() + " " + currClusterDataModelDeps.getSystemConfigFilename() + ".";
					
					logger.warn(sysCfgReadErrorMsg, ioException);
				
					ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
					responseBuilder.type(MediaType.TEXT_PLAIN_TYPE);
					responseBuilder.entity(sysCfgReadErrorMsg);
					
					throw new WebApplicationException(responseBuilder.build());
				}
			}
			
			return currClusterDataModelDeps;
		}
		
		return null;
		
	}
	
	protected ClusterDimTableDependencies getSystemDimTableDependencies(String tenantId, String region, String sysDomain, String cfgFilename) {
		
		ClusterDataModelDependencies systemDmDeps = getSystemDataModelDependencies(tenantId, region, sysDomain, cfgFilename);

		if (systemDmDeps != null) {
			RepositoryApiRestClient client = RestClientManager.getRespositoryApiRestClient();
			
			ClusterDimTableDependencies currClusterDimDeps = new ClusterDimTableDependencies(systemDmDeps, new HashSet<String>());
			
			Set<String> dataModels = systemDmDeps.getDataModels();
			if (dataModels != null) {
				for(String currDataModelFilename : dataModels) {
					if (StringUtils.isNotBlank(currDataModelFilename)) {
						DataModelZipFile currDataModel = null;
						
						try {
							currDataModel = client.fetchDataModel(tenantId, currDataModelFilename);
						} catch (IOException ioException) {
							String dmReadFailureMsg = "Failed to read data model '" + currDataModelFilename + "'"; 
							
							logger.warn(dmReadFailureMsg, ioException);
							
							ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
							responseBuilder.type(MediaType.TEXT_PLAIN_TYPE);
							responseBuilder.entity(dmReadFailureMsg);
							throw new WebApplicationException(responseBuilder.build());
						}
						
						if (currDataModel != null) {
							try {
								List<JSONObject> enrichmentConfigs = EnrichmentCfgParseUtils.getEnrichmentCfgs(currDataModel, DIM_TABLE_DEPENDENT_ENRICHMENT_TYPES);
								for(JSONObject currEnrichmentCfg : enrichmentConfigs) {
									List<SimpleEntry<String, String>> customParams = EnrichmentCfgParseUtils.getCustomParams(currEnrichmentCfg);
									for(SimpleEntry<String, String> keyValuePair : customParams) {
										if (DIM_TABLE_CUSTOM_PARAM_NAMES.contains(keyValuePair.getKey()) && StringUtils.isNotBlank(keyValuePair.getValue())) {
											currClusterDimDeps.getDimTables().add(keyValuePair.getValue());
										}
									}
								}
							} catch (IOException ioException) {
								String errorMsg = "Failed to read the enrichment config for the data model '" + currDataModelFilename + "'.";
								
								logger.warn(errorMsg, ioException);
								
								ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
								responseBuilder.type(MediaType.TEXT_PLAIN_TYPE);
								responseBuilder.entity(errorMsg);
								
								throw new WebApplicationException(responseBuilder.build());
							} catch (JSONException jsonException) {
								String errorMsg = "Failed to parse the enrichment config for the data model '" + currDataModelFilename + "'.";
								
								logger.warn(errorMsg, jsonException);
								
								ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
								responseBuilder.type(MediaType.TEXT_PLAIN_TYPE);
								responseBuilder.entity(errorMsg);
								
								throw new WebApplicationException(responseBuilder.build());
							} catch(ParseException parseException) {
								String errorMsg = "Failed to parse an enrichment's custom parameters for the data model '" + currDataModelFilename + "'";
								
								logger.warn(errorMsg, parseException);
								
								ResponseBuilder responseBuilder = Response.status(Response.Status.INTERNAL_SERVER_ERROR);
								responseBuilder.type(MediaType.TEXT_PLAIN_TYPE);
								responseBuilder.entity(errorMsg);
								
								throw new WebApplicationException(responseBuilder.build());
							}
						}
					}
				}
			}
			
			return currClusterDimDeps;
		}
		
		return null;
		
	}
	
}