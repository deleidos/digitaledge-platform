	@Override
	public void initialize() throws InitializationException {
		String templateFile = null;
		String baseDir = (String) RtwsConfig.getInstance().getConfiguration().getProperty("sysbuilder.template.basedir");
		
		//initialize template file locations
		//template file property sets, for lookup in composer later
		//set template file in properties
		templateFile = String.format("%s/%s", baseDir, "activemq-template.xml");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.activemq.template", templateFile);
		
		//set default property composer property file template
		templateFile = String.format("%s/%s", baseDir, "default.properties");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.service.default.properties", templateFile);

		//set template dir base
		this.templateDir = String.format("%s/", baseDir);
	}

	/**
	 * Writes JMS Cluster configuration files.
	 */
	@Override
	public void writeFile(String version, String fileName) {
		if(fileName != null){
			parser.writeXmlFile(fileName);
		}

		try {
			// write credentials file
			String toFilename = this.outputDir + credentialsPropertiesName;
			InputStream is = getClass().getResourceAsStream(templateDir + credentialsPropertiesName);
			if (is == null) {
				throw new IOException("Cannot read classpath resource " + templateDir + credentialsPropertiesName);
			}
			String data = IOUtils.toString(is, "UTF-8");
			is.close();
			
			RtwsProperties creds = new RtwsProperties();
			is = getClass().getResourceAsStream(templateDir + credentialsPropertiesName);
			creds.load(is);
			creds.encryptCredentials(this.domain);
			
			// this is ugly, but need to keep the license (comment header) in the properties file
			StringBuilder header = new StringBuilder();
			String line;
			BufferedReader br = new BufferedReader(new StringReader(data));
 			while ((line = br.readLine()) != null) {
				if (line.trim().equals("") || line.trim().startsWith("#")) {
					header.append(line + System.getProperty("line.separator"));
				} else {
					break;
				}
			}
 			// store the re encrypted creds with the header
			creds.store(new FileOutputStream(toFilename), header.toString());
			
			
			// write jetty configuration file
			toFilename = this.outputDir + jettyXmlName;
			is = getClass().getResourceAsStream(templateDir + jettyXmlName);
			if (is == null) {
				throw new IOException("Cannot read classpath resource " + templateDir + jettyXmlName);
			}
			data = IOUtils.toString(is, "UTF-8");
			is.close();
			FileUtils.writeStringToFile(new File(toFilename), data);

			// write jetty realm file
			toFilename = this.outputDir + jettyRealmPropertiesName;
			is = getClass().getResourceAsStream(templateDir + jettyRealmPropertiesName);
			if (is == null) {
				throw new IOException("Cannot read classpath resource " + templateDir + jettyRealmPropertiesName);
			}
			data = IOUtils.toString(is, "UTF-8");
			is.close();
			FileUtils.writeStringToFile(new File(toFilename), data);
		} catch (Exception e) {
			logger.error(e.toString(), e);
			e.printStackTrace();
		}
	}

	/**
	 * Creates the JMS cluster configuration files, based on user configured cluster size.
	 */
	@Override
	public ComposerStatus compose(SystemConfig config, SystemContext systemContext) throws ComposerException{
		ComposerStatus status = ComposerStatus.done;
		//create a activemq.xml file for each node in the cluster
		//and setup the connectors for each node
		try{
			//create properties, used in the cluster config files
			createProperties(config, systemContext);
			
			if(systemContext.getSystemSize().getCombineIntExtFlag() == false){
				createExternalNodes(config, systemContext);
				createInternalNodes(config, systemContext);
			}else{
				//if combined flag true
				createCombinedNode(config);
			}
			createNonStandardNode();
		}
		catch(Exception e){
			status = ComposerStatus.error;
			logger.error(e.toString(), e);
			throw new ComposerException(e.toString(), e);
		}
		
		return status;
	}

	/**
	 * Loads default configurations/templates for composer.
	 */
	@Override
	public void loadDefaults(String version, SystemConfig config, SystemContext systemContext, RtwsProperties properties) throws MarshalException, DefaultConfigurationException {
		this.properties = properties;  //initialize properties
		//get activemq.xml template file
		this.templateFile = RtwsConfig.getInstance().getString("sysbuilder.activemq.template");
		this.domain = config.getSystemDomain();
		parser.readXmlFile(templateFile);  //parse to dom object
		
		//setup local compose values
		buildLocalConfig(config, systemContext);
	}

	@Override
	public Properties getProperties() {
		return properties;
	}

	/**
	 * Setup configuration values/parameters based on SystemConfig and SystemContext.
	 * 
	 * @param config
	 * @param systemContext
	 * @throws DefaultConfigurationException
	 */
	private void buildLocalConfig(SystemConfig config, SystemContext systemContext) throws DefaultConfigurationException{
		this.outputDir = config.getOutputConfDir() + confDir;
		SystemBuilderUtil.mkdir(this.outputDir);  //create directory
		if(systemContext.getSystemSize() != null){
			this.numNodes = systemContext.getSystemSize().getJmsInstanceCount();
		}
		else{
			logger.warn("No record was returned from the SystemSizing table for the user defined system size.");
			throw new DefaultConfigurationException("No record was returned from the SystemSizing table for the user defined system size.");
		}
		
	}
	
	/**
	 * Create external ActiveMG node config files.
	 * 
	 * @param config
	 */
	private void createExternalNodes(SystemConfig config, SystemContext systemContext){
		for(int i = 1; i <= this.numNodes; i++){
			String fileName = extTemplate.replace('?', (char)(i + 48));
			
			//add cursor type to queue and subscriber
			Element element = parser.getElement("pendingSubscriberPolicy");
			element.appendChild(createCursor(false, CursorType.VM_CURSOR, true));
			
			element = parser.getElement("pendingQueuePolicy");
			element.appendChild(createCursor(true, CursorType.STORE_CURSOR, true));
			
			//add connector for each node in cluster
			/*element = parser.getElement("networkConnectors");
			for(int k = 1; k <= this.numNodes; k++){
				if(i != k){
					element.appendChild(createConnector(k, parser.createElement("networkConnector"), extPropName));
				}
			}*/
			
			//create config file for node
			parser.writeXmlFile(this.outputDir + fileName);
			parser.readXmlFile(this.templateFile);  //reset
		}
	}
	
	/**
	 * Create internal ActiveMQ node config files.
	 * 
	 * @param config
	 */
	private void createInternalNodes(SystemConfig config, SystemContext systemContext){

		// Determine if the JMS internal process group uses persistence
		Boolean persistence = false;
		for(UserProcessGroupConfig upgc : config.getProcessGroups()){
			if("jms.internal".equals(upgc.getProcessGroupName())){
				persistence = (upgc.getJmsPersistentMessage() == true);
				break;
			}
		}
		
		for(int i = 1; i <= this.numNodes; i++){
			String fileName = intTemplate.replace('?', (char)(i + 48));
			
			
			//add cursor type to queue and subscriber
			Element element = parser.getElement("pendingSubscriberPolicy");
			element.appendChild(createCursor(false, CursorType.VM_CURSOR, persistence));
			
			element = parser.getElement("pendingQueuePolicy");
			element.appendChild(createCursor(true, CursorType.VM_CURSOR, persistence));
			
			//add connector for each node in cluster
			/*element = parser.getElement("networkConnectors");
			for(int k = 1; k <= this.numNodes; k++){
				if(i != k){
					element.appendChild(createConnector(k, parser.createElement("networkConnector"), intPropName));
				}
			}*/
			
			//create config file for node.
			parser.writeXmlFile(this.outputDir + fileName);
			parser.readXmlFile(this.templateFile);  //reset
		}
	}
	
	/**
	 * Create cursor Node.
	 * 
	 * @param isQueue indicates if the cursor is for a queue or a topic
	 * @param type {@link CursorType} cursor type
	 * @param persistence true if the JMS broker should use persistence for storing messages
	 * @return {@link Element} node to add to document
	 */
	private Element createCursor(boolean isQueue, CursorType type, boolean persistence){
		Element element = null;
		
		if(type == CursorType.STORE_CURSOR){
			element = parser.createElement("storeCursor");
		}
		else if(type == CursorType.VM_CURSOR){
			if(isQueue == true){
				element = parser.createElement("vmQueueCursor");
			}
			else{
				element = parser.createElement("vmCursor");
			}
		}
		else{
			//type not recognized
		}
		
		if (persistence) {
			this.parser.getElement("broker").getAttributeNode("persistent").setTextContent("true");			
		}
		else {
			this.parser.getElement("broker").getAttributeNode("persistent").setTextContent("false");			
		}
		
		return element;
	}
			
	
	/**
	 * Create network connector Node.
	 * 
	 * @param id an identification number
	 * @param connector {@link Element} a document node
	 * @param uriTemplate a template to perform name replacement
	 * @return
	 */
	@SuppressWarnings("unused")
	private Element createConnector(int id, Element connector, final String propName){
		String propValue = properties.get(propName.replace('?', (char) (id + 48)));
		String uri = uriTemplate.replace("?", propValue);
		
		connector.setAttribute("name", String.format("connector%d", id));
		connector.setAttribute("uri", uri);
		connector.setAttribute("networkTTL", "2");
		connector.setAttribute("decreaseNetworkConsumerPriority", "true");
		
		return connector;
	}
	
	/**
	 * Creates the internal ActiveMQ node properties.
	 * 
	 * @param config {@link ActiveMQConfig} configuration object
	 */
	public void createInternalNodeProperties(SystemConfig config){
		StringBuilder connURL = new StringBuilder("");
		
		//node name properties 
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = (intNodeName.replace('?', (char)(i + 48))).replace("@", config.getSystemDomain());
			properties.setProperty(intPropName.replace('?', (char)(i + 48)), nodeValue);
			connURL.append(failOverNode.replace("@", nodeValue) + ",");
		}
		
		connURL.replace(connURL.length() - 1, connURL.length(), "");
		
		//jmx properties
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = (intNodeName.replace('?', (char)(i + 48))).replace("@", config.getSystemDomain());
			properties.setProperty(intJMXPropName.replace('?', (char)(i + 48)), jmxURL.replace("?", nodeValue));
		}
		
		//generate connection url with failover for clustering
		if(this.numNodes > 1){
			properties.setProperty(intURLPropName, failOverURL.replace("@", connURL));
		}	
		else{
			properties.setProperty(intURLPropName, connURL.toString());
		}
		
	}
	
	/**
	 * Creates the external ActiveMQ node properties.
	 * 
	 * @param config {@link ActiveMQConfig} configuration object
	 */
	public void createExternalNodeProperties(SystemConfig config){
		StringBuilder connURL = new StringBuilder();
		
		//node name properties 
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = (extNodeName.replace('?', (char)(i + 48))).replace("@", config.getSystemDomain());
			properties.setProperty(extPropName.replace('?', (char)(i + 48)), nodeValue);
			connURL.append(failOverNode.replace("@", nodeValue) + ",");
			
			//specifying only the first jms ext node as the node to receive alerts
			if (i == 1) {
				String alertsURL = connURL.toString().substring(0, connURL.length() - 1);
				alertsURL+= "?jms.prefetchPolicy.all=3?wireFormat.maxInactivityDuration=300000?jms.useCompression=true";
				properties.setProperty(alertsURLPropName, alertsURL);
			}
		}
		connURL.replace(connURL.length() - 1, connURL.length(), "");
		
		//jmx properties
		for(int i = 1; i <= this.numNodes; i++){
			//build properties file
			String nodeValue = (extNodeName.replace('?', (char)(i + 48))).replace("@", config.getSystemDomain());
			properties.setProperty(extJMXPropName.replace('?', (char)(i + 48)), jmxURL.replace("?", nodeValue));
		}
		
		//generate connection url with failover for clustering
		if(this.numNodes > 1){
			properties.setProperty(extURLPropName, failOverURL.replace("@", connURL));
		}
		else{
			properties.setProperty(extURLPropName, connURL.toString());
		}
		
	}
	
