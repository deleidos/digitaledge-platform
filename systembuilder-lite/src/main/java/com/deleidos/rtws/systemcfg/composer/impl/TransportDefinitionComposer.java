import com.sun.jersey.api.client.Client;
import com.sun.jersey.api.client.ClientResponse;
import com.sun.jersey.api.client.WebResource;

public class TransportDefinitionComposer implements DefinitionComposer{

	private static Logger logger = LogManager.getLogger(TransportDefinitionComposer.class);
	private XMLParser parser;
	private String templateFile;
	private String outDir;
	private RtwsProperties properties;
	private String defaultOutputFilename = "transports.default.xml";
	private Pattern p;
	private final String confDir = "/transport/";
	private String tenantId;
	private Client client;
	private JSONArray transportsJson;
	
	@Override
	public void initialize() throws InitializationException {
		p = Pattern.compile(".*?@.*@.*?");
		
		String templateFile = null;
		String baseDir = (String) RtwsConfig.getInstance().getConfiguration().getProperty("sysbuilder.template.basedir");
		
		//initialize template file locations
	
		//set template file in properties
		templateFile = String.format("%s/%s", baseDir, "transport-template.xml");
		RtwsConfig.getInstance().getConfiguration().setProperty("sysbuilder.transport.template", templateFile);

		//setup generic REST client
		this.client = Client.create(JerseyClientConfig.getInstance().getInternalConfig());
	}

	@Override
	public void dispose() {
		//not implemented
	}

	@Override
	public void writeFile(String version, String fileName) {
		if(fileName == null){
			parser.writeXmlFile(outDir + defaultOutputFilename);
		}
		else{
			parser.writeXmlFile(outDir + fileName);
		}
	}

	@Override
	public void loadDefaults(String version, SystemConfig config, SystemContext systemContext, RtwsProperties properties) 
													throws MarshalException, DefaultConfigurationException {
		this.properties = properties;  //initialize properties
		this.outDir = config.getOutputConfDir() + confDir;  //set output directory
		SystemBuilderUtil.mkdir(this.outDir);  //create directory
		try{
			//get transports template file
			this.templateFile = RtwsConfig.getInstance().getString("sysbuilder.transport.template");
			parser.readXmlFile(templateFile);  //parse to dom object

			//load ingest api json information
			ingestApiTransports();
		}
		catch(Exception e){
			logger.error(e.toString(), e);
			e.printStackTrace();
			throw new DefaultConfigurationException(e.toString(), e);
		}
		
	}

	@Override
	public ComposerStatus compose(SystemConfig config, SystemContext systemContext) throws ComposerException{
		ComposerStatus status = ComposerStatus.done;
		try{
			//create transport file, uses templates from ingestapi then replaces properties and add parameters
			createTransportFile(config, systemContext);
		}
		catch(Exception e){
			logger.error(e.toString(), e);
			e.printStackTrace();
			status = ComposerStatus.error;
			throw new ComposerException(e.toString(), e);
		}
		return status;
	}

	/**
	 * Get properties object.
	 */
	@Override
	public Properties getProperties() {
		return properties;
	}
	
	/**
	 * Sets the XML parser used by the composer.
	 * @param parser
	 */
	public void setXMLParser(XMLParser parser){
		this.parser = parser;
	}

	public String getTenantId() {
		return tenantId;
	}

	public void setTenantId(String tenantId) {
		this.tenantId = tenantId;
	}

	/**
	 * Get Transports information from Ingest API.
	 * 
	 * @throws MalformedURLException
	 * @throws ComposerException 
	 */
	private void ingestApiTransports() throws MalformedURLException, ComposerException {
		String url = String.format("%s/json/service/transports/%s", RtwsConfig.getInstance().getString("sys.builder.ingest.api"),
				getTenantId());
		WebResource resource = this.client.resource(url);
		
		ClientResponse clientResponse = resource.get(ClientResponse.class);
		if(clientResponse.getStatus() == 503){
			throw new ComposerException("IngestAPI is currently syncing/updating its cache, try again later.");
		}
		
		String response = clientResponse.getEntity(String.class);

		this.transportsJson = JSONArray.fromObject(response);
	}

	/**
	 * Get transport JSONObject from JSONArray transports list.
	 * 
	 * @param transportName String transportName
	 * @return {@link JSONObject}
	 */
	private JSONObject getTransport(String transportName){
		JSONObject json = null;
		
		Iterator<?> nodeit = transportsJson.iterator();
		while (nodeit.hasNext()) {
			JSONObject tmp = (JSONObject) nodeit.next();
			
			if(((String) tmp.get("fqn")).contains(transportName)){
				json = tmp;
				break;
			}
		}
		
		return json;
	}

	/**
	 * Create the default transport xml file.
	 * 
	 * @param config {@link SystemConfig}
	 * @param systemContext {@link SystemContext}
	 * @throws IOException
	 * @throws ComposerException 
	 */
	private void createTransportFile(SystemConfig config, SystemContext systemContext) throws IOException, ComposerException{
		
		//for each transport add to template a transport-service
		for(Transport transport : config.getTransports()){
			
			//if user transport is in the ingestapi list, continue
			JSONObject transportJson = this.getTransport(transport.getFqn());
			if (transportJson != null) {
				
				XMLParser tmpParser = new XMLParser(true);  //create parser
				String transportTemplate = buildTransportXmlTemplate(transport.getFqn(), transportJson);
				
				//recursively replace properties in transport template
				InputStream is = new ByteArrayInputStream(properties.filterProperty(transportTemplate).getBytes());
				tmpParser.readXmlFileFromStream(is);  //parse template to document
				is.close();
				
				//search document for parameter names, if parameter exists then set with user parameter value
				replaceTemplateParameters(transport, tmpParser, tmpParser.getDocumentElement(), tmpParser.getDocumentElement().getNodeName());
			
				parser.importNode(tmpParser.getElement("transport-services"), true);  //import to transport template root
				
			} else {
				logger.error("Not producing configs for unknown transport: " +transport.getFqn());
			}
		}
	}
	
	/**
	 * Recursively replace parameters in an xml template string.
	 * 
	 * @param transport {@link Transport}
	 * @param tmpParser {@link XMLParser}
	 * @param element {@link Node}
	 * @param nodeName String value
	 * @return {@link XMLParser}
	 */
	private XMLParser replaceTemplateParameters(Transport transport, XMLParser tmpParser, Node element, String nodeName){
		//if user parameters exist for data sinks
		if(transport.getParams() != null){
			for(Pair pair : transport.getParams()){
				if(element != null){
					//for each node recurse
					for(int i = 0; i < element.getChildNodes().getLength(); i++){
						tmpParser = replaceTemplateParameters(transport, tmpParser, element.getChildNodes().item(i), element.getChildNodes().item(i).getNodeName());
					}
					if(pair.getName().equals(nodeName)){
						if( p.matcher(element.getTextContent()).matches()){
							properties.put(pair.getName(), pair.getValue());  //add to properties
						}
						element.setTextContent(pair.getValue());
					}
				}//end if
				//for each node check attributes, check if parameter is an attribute
				NodeList nodeList = element.getChildNodes();
				for(int i = 0; i < nodeList.getLength(); i++){
					Node node = nodeList.item(i);
					NamedNodeMap map = node.getAttributes();
					if(map != null){
						Node attrNode = map.getNamedItem(pair.getName());
						if(attrNode != null){
							if(p.matcher(attrNode.getTextContent()).matches()){
								properties.put(pair.getName(), pair.getValue());  //add to properties
							}
							attrNode.setTextContent(pair.getValue());  //set value
						}//end if
					}//end if
				}//end for
			}//end for
		}//end if
		
		return tmpParser;
	}

	private String buildTransportXmlTemplate(String transportFqn, JSONObject transportJson) {
		StringBuffer xml = new StringBuffer();
		xml.append(startTransportXmlTemplate(transportFqn));
		xml.append(SystemBuilderUtil.createSystemParametersXml(transportJson));
		xml.append(SystemBuilderUtil.createInputParametersXml(transportJson));
		xml.append(endTransportXmlTemplate());
		return xml.toString();
	}

	private String startTransportXmlTemplate(String transportFqn) {
		StringBuffer xml = new StringBuffer("\t<transport-services xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\"\n");
		xml.append("\txsi:type=\"java:");
		xml.append(transportFqn);
		xml.append("\">\n");
		return xml.toString();
	}

	private String endTransportXmlTemplate() {
		return "</transport-services>";
	}
}
