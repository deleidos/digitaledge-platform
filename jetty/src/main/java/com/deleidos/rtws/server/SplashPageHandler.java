package com.deleidos.rtws.server;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;
import java.util.List;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.commons.io.IOUtils;
import org.apache.commons.lang.StringUtils;
import org.eclipse.jetty.http.HttpMethods;
import org.eclipse.jetty.http.MimeTypes;
import org.eclipse.jetty.server.Handler;
import org.eclipse.jetty.server.Request;
import org.eclipse.jetty.server.Server;
import org.eclipse.jetty.server.handler.AbstractHandler;
import org.eclipse.jetty.server.handler.ContextHandler;
import org.eclipse.jetty.util.ByteArrayISO8859Writer;

public class SplashPageHandler extends AbstractHandler {
	
	private String[] handledContextExclusionPatterns = null;
	private List<Pattern> handledContextExclusions = new ArrayList<Pattern>();
	
	private String[] shownContextsExclusionPatterns = null;
	private List<Pattern> shownContextsExclusions = new ArrayList<Pattern>();
	
	private String rootContext = null;
	
	private Comparator<ContextHandler> customCtxtComparator = null;
	private Comparator<ContextHandler> ctxtComparator = null;

	@Override
	public void handle(String target, Request baseRequest, HttpServletRequest request,
			HttpServletResponse response) throws IOException, ServletException {
		
		if(response.isCommitted() || baseRequest.isHandled()) {
			return;
		}
		
		baseRequest.setHandled(true);
		
		String method=request.getMethod();
		
		if(!method.equals(HttpMethods.GET) || !isHandledContext(request))
    {
        response.sendError(HttpServletResponse.SC_NOT_FOUND);
        return;   
    }
		
		boolean isRootRequest = "".equals(getContextPath(request));
		
		ContextHandler[] shownCtxts = getShownContexts();
		
		// Special auto-redirect cases
		String redirectUrl = null;
		if(shownCtxts.length == 1 && shownCtxts[0].isRunning()) {
			redirectUrl = buildUrl(shownCtxts[0], request);
		}
		else if(rootContext != null && isRootRequest){
			// The root request can have a default assigned
			for(ContextHandler currCtxtHandler : shownCtxts) {
				if(currCtxtHandler.getContextPath().equals(rootContext)) {
					redirectUrl = buildUrl(currCtxtHandler, request);
					break;
				}
			}
		}
		
		if(redirectUrl != null) {
			response.sendRedirect(redirectUrl);
			return;
		}
		
	response.setStatus(HttpServletResponse.SC_NOT_FOUND);
    response.setContentType(MimeTypes.TEXT_HTML);
    OutputStream out = null;
    ByteArrayISO8859Writer writer = null;
    try {
    	writer = new ByteArrayISO8859Writer(1500);
    	writer.write(buildSplashPageMarkup(shownCtxts, request, (isRootRequest == false)));
    	writer.flush();
    	response.setContentLength(writer.size());
    	out=response.getOutputStream();
    	writer.writeTo(out);
    
    	
    	
    	} finally {
    		if (out != null)
    			out.close();
    		
    		if (writer != null) {
    			writer.close();
    	        writer.destroy();
    		}   
    	}
   
	}
	
	protected String buildSplashPageMarkup(ContextHandler[] shownCtxts, HttpServletRequest request, boolean isNotFoundResponse) {
		StringBuilder responseBuilder = new StringBuilder();
    
    responseBuilder.append("<!DOCTYPE html>");
    responseBuilder.append("\n<html lang=\"en\">");
    responseBuilder.append("\n<head>");
    responseBuilder.append("\n<title>DigitalEdge - ").append(isNotFoundResponse ? "Page Not Found" : "Welcome").append("</title>");
    
    responseBuilder.append("\n<style>");
    InputStream cssStream = null;
    try {
    	cssStream = Thread.currentThread().getContextClassLoader().getResourceAsStream("css/appPicker.css");
    	responseBuilder.append(IOUtils.toString(cssStream, "UTF-8"));
    }
    catch(Exception e) {
    }
    finally {
    	IOUtils.closeQuietly(cssStream);
    }
    responseBuilder.append("\n</style>");
    
    responseBuilder.append("\n</head>");
    
    responseBuilder.append("\n<body>");
    
    responseBuilder.append("<div class=\"textWell\">");
    responseBuilder.append("<h1>");
    if(isNotFoundResponse) {
    	responseBuilder.append("DigitalEdge - Page Not Found");
    }
    else {
    	responseBuilder.append("Welcome to DigitalEdge");
    }
    responseBuilder.append("</h1>");
    
    responseBuilder.append("<p>");
    if(isNotFoundResponse) {
    	responseBuilder.append("Sorry, but no page was found at the specified location.");
    }
    if(shownCtxts != null && shownCtxts.length > 0) {
    	responseBuilder.append("  Please select an available app.");
    }
    responseBuilder.append("</p>");
    responseBuilder.append("</div>");
    
    if(shownCtxts != null && shownCtxts.length > 0) {
	    responseBuilder.append("<ul>");
	    for(int index=0; index < shownCtxts.length; index++) {
	    	ContextHandler context = (ContextHandler)shownCtxts[index];
	    	String ctxtUrl = buildUrl(context, request);
	    	String ctxtDisplayName = StringUtils.isNotBlank(context.getDisplayName()) ? context.getDisplayName() : context.getContextPath();
	    	boolean isAppAvailable = context.isRunning() && ctxtUrl != null;
	    	
	    	String statusStyle = null;
	    	String statusText = null;
	    	if(isAppAvailable) {
	    		statusStyle = "available";
	    		statusText = "Running";
	    	}
	    	else if(context.isFailed()) {
	    		statusStyle = "failed";
	    		statusText = "Failed";
	    	}
	    	else {
	    		statusStyle = "unavailable";
	    		statusText = "Down";
	    	}
	    	
	    	responseBuilder.append("<li class=\"appSwatch ").append(statusStyle).append("\">");
	    	if(isAppAvailable) {
	      	responseBuilder.append("<a href=\"").append(ctxtUrl).append("\">");
	    	}
	    	
	    	responseBuilder.append("<div class=\"appDetails\">");
	    	responseBuilder.append("<div class=\"title\" title=\"").append(ctxtDisplayName).append("\">").append(ctxtDisplayName).append("</div>");
	    	responseBuilder.append("<div class=\"status\">Status: ").append(statusText).append("</div>");
	    	responseBuilder.append("</div>");
	    	
	    	if(isAppAvailable) {
	    		responseBuilder.append("</a>");
	    	}
	    	responseBuilder.append("</li>");
	    	
	    }
	    responseBuilder.append("</ul>");
    }
    
    responseBuilder.append(StringUtils.repeat("<br />\n<!-- Padding for IE                  -->", 10));
    
    InputStream bgFillStream = null;
    try {
    	bgFillStream = Thread.currentThread().getContextClassLoader().getResourceAsStream("html/rtwsBgFillSnippet.html");
    	responseBuilder.append(IOUtils.toString(bgFillStream, "UTF-8"));
    }
    catch(Exception e) {
    }
    finally {
    	IOUtils.closeQuietly(bgFillStream);
    }
    
    responseBuilder.append("\n</body>");
    responseBuilder.append("\n</html>\n");
    
    return responseBuilder.toString();
	}
	
	protected String buildUrl(ContextHandler contextHandler, HttpServletRequest request) {
		StringBuilder resultBuilder = new StringBuilder();
		
		if(contextHandler.getVirtualHosts() != null && contextHandler.getVirtualHosts().length > 0)
		{
			resultBuilder.append("http://");
			resultBuilder.append(contextHandler.getVirtualHosts()[0]);
			resultBuilder.append(":");
			resultBuilder.append(request.getLocalPort());
		}
		
		resultBuilder.append(contextHandler.getContextPath());
		
    // Seems like this should be endsWith == false, but this was the orig condition.
		if(contextHandler.getContextPath().length() > 1 && contextHandler.getContextPath().endsWith("/")) {
    	resultBuilder.append("/");
    }
	    
		return resultBuilder.toString();
	}
	
	protected ContextHandler[] getShownContexts() {
		
		List<ContextHandler> result = new ArrayList<ContextHandler>();
		
		Server server = getServer();
    
		Handler[] allHandlers = (server == null ? null : server.getChildHandlersByClass(ContextHandler.class));

		if(allHandlers != null) {
	    for (int index=0; index < allHandlers.length; index++)
	    {
	    	ContextHandler context = (ContextHandler)allHandlers[index];
	      
	    	if(isHiddenContext(context)) {
	    		continue;
	    	}
	    	else {
	    		result.add(context);
	    	}
		  }
		}
		
		if(result.size() > 0) {
			try {
				Collections.sort(result, ctxtComparator);
			}
			catch(Exception ignored) {
				// Ignoring the sort failure of a custom sort algorithm.
			}
		}
		
		return result.toArray(new ContextHandler[result.size()]);
	}
	
	protected boolean isHandledContext(HttpServletRequest request) {
		boolean isExcluded = false;
		
		Matcher currMatcher = null;
		for(Pattern exclusionPattern : handledContextExclusions) {
			currMatcher = exclusionPattern.matcher(getContextPath(request)); 
			if(currMatcher != null && currMatcher.matches()) {
				isExcluded = true;
				break;
			}
		}
		
		return (isExcluded == false);
	}
	
	protected boolean isHiddenContext(ContextHandler ctxtHandler) {
		boolean isExcluded = false;
		
		Matcher currMatcher = null;
		for(Pattern exclusionPattern : shownContextsExclusions) {
			currMatcher = exclusionPattern.matcher(ctxtHandler.getContextPath());
			if(currMatcher != null && currMatcher.matches()) {
				isExcluded = true;
				break;
			}
		}
		
		return isExcluded;
	}
	
	protected String getContextPath(HttpServletRequest request) {
		String result = null;
		
		if(request != null) {
			String requestUri = request.getRequestURI();
			
			if(requestUri == null || requestUri.length() <= 1) {
				result = "";
			}
			else {
				int secondSeparatorIndex = requestUri.indexOf("/", 1);
				
				if(secondSeparatorIndex > 0) {
					result = requestUri.substring(0, secondSeparatorIndex);
				}
				else {
					result = requestUri;
				}
			}
		}
		
		return result;
	}
	
	
	@Override
	protected void doStart() throws Exception {
		if(handledContextExclusionPatterns != null) {
			for(String currPattern : handledContextExclusionPatterns) {
				handledContextExclusions.add(Pattern.compile(currPattern));
			}
		}
		
		if(shownContextsExclusionPatterns != null) {
			for(String currPattern : shownContextsExclusionPatterns) {
				shownContextsExclusions.add(Pattern.compile(currPattern));
			}
		}
		
		if(customCtxtComparator != null) {
			ctxtComparator = customCtxtComparator;
		}
		else {
			ctxtComparator = new ContextNameComparator();
		}
		
		super.doStart();
	}
	
	public void setHandledContextExclusionPatterns(String[] contextExclusionPatterns) {
		this.handledContextExclusionPatterns = contextExclusionPatterns;
	}
	
	public void setShownContextsExclusionPatterns(String[] shownContextsExclusionPatterns) {
		this.shownContextsExclusionPatterns = shownContextsExclusionPatterns;
	}
	
	public void setRootContext(String rootContext) {
		this.rootContext = rootContext;
	}
	
	public void setCustomCtxtComparator(Comparator<ContextHandler> customCtxtComparator) {
		this.customCtxtComparator = customCtxtComparator;
	}
}
