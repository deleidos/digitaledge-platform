package com.deleidos.rtws.core.framework.factory;

import java.util.Collection;
import java.util.HashMap;

import com.deleidos.rtws.commons.config.XMLUtils;
import com.deleidos.rtws.commons.exception.InitializationException;
import com.deleidos.rtws.commons.util.Initializable;
import com.deleidos.rtws.core.beans.FactoryDefinition;
import com.deleidos.rtws.core.beans.FactoryEntry;

/**
 * Base class for factory implementations that load object definitions from an XML file. 
 */
public abstract class AbstractFactory<T> implements Factory<T>, Initializable {
	
	/** Constants defining options for object instantiation. */
	protected static enum InstantiationPolicy {
		/** Each call to getInstance should return a new copy of the requested object. */
		COPY,  
		/** Each call to getInstance should return a shared instance of the requested object. */
		REUSE 
	};
	
	
	
	/** List of resources that need to be injected into the factory entries as dependencies. */
	private Injector<T>[] resources;
	
	/** The objects that are return by this factory. */
	private HashMap<String, T> entries = new HashMap<String, T>();
	
	/** The object that is returned when a client request an unrecognized name. */
	private T defaultEntry = null;
	
	/** Defines how objects will be instantiated. */
	private InstantiationPolicy policy;
	
	/** Property referencing the XML file that should be used to initialize this factory. */
	private String definitionFile;
	
	
	
	/**
	 * Constructor.
	 * 
	 * @param policy Defines how this factory show instantiate objects.
	 */
	protected AbstractFactory(InstantiationPolicy policy) {
		super();
		this.policy = policy;
	}
	
	/**
	 * The XML file from which this factory is initialized.
	 */
	public String getDefinitionFile() {
		return definitionFile;
	}
	public void setDefinitionFile(String value) {
		this.definitionFile = value;
	}
	
	/**
	 * Returns the list of named objects that can be instantiated by this factory. 
	 */
	public String[] getNames() {
		return entries.keySet().toArray(new String[entries.size()]);
	}
	
	/**
	 * Return the instantiated objects controlled by this factory. If this factory's
	 * instantiation policy is set to REUSE, then the result is handles to the shared
	 * instances. If the policy is set to COPY, this returns only the un-initialized
	 * templates from which the real objects are copied.
	 */
	protected Collection<T> getEntries() {
		return entries.values();
	}
	
	/**
	 * Indicates whether this factory has a default object that can be returned when
	 * an unrecognized name is requested.
	 */
	protected boolean hasDefaultEntry() {
		return defaultEntry != null;
	}
	
	/**
	 * Return the default object that is returned when an unrecognized name is requested.
	 */
	protected T getDefaultEntry() {
		return defaultEntry;
	}
	
	/**
	 * Instantiates the requested object or returns the shared instance based on
	 * the configured instantiation policy.
	 */
	public T getInstance(String name) {
		if(entries.containsKey(name)) {
			return retrieveEntry(entries.get(name));
		} else {
			return retrieveEntry(defaultEntry);
		}
	}
	
	/**
	 * Initializes the factory form the given XML file.
	 */
	@SuppressWarnings("unchecked")
	public void initialize() {
		try {
			FactoryDefinition definition = loadDefinitionFile(getDefinitionFile());
			resources = (Injector<T>[])definition.getResources();
			defaultEntry = allocateEntry(loadEntry(definition.getDefaultEntry()));
			if(!definition.isEmpty()) {
				for(FactoryEntry entry : definition.getEntries()) {
					entries.put(entry.getName(), allocateEntry(loadEntry(entry.getBean())));
				}
			}
		} catch(Exception e) {
			throw new InitializationException("Unable to load configuration file '" + definitionFile + "'.", e);
		}
	}
	
	/**
	 * Load the given XML file.
	 */
	protected FactoryDefinition loadDefinitionFile(String file) throws Exception {
		return XMLUtils.loadXML(definitionFile, FactoryDefinition.class);
	}
	
	/**
	 * Must be implemented by subclasses to define the type of object controlled by this factory.
	 */
	protected abstract Class<T> entryType();
	
	/**
	 * Subclasses must implement this method to convert whatever object was read from in the XML into
	 * the object that this factory should control. In most cases, the only thing that needs to be is
	 * to cast the object to the appropriate time. For cases where the object is too complicated to be
	 * declared as a simple bean, this method can be used to handle more complicated object building.   
	 */
	protected abstract T loadEntry(Object bean);
	
	/**
	 * Subclasses should implement this method to handle object initialization. Note that this is
	 * different from loadEntry() (which is intended only to convert XML to a meaningful object,
	 * and is performed before the object is added to the factories list of controlled objects).
	 * Initialization occurs just before a newly created object instance is returned to a client.
	 */
	protected abstract void initializeEntry(T bean);
	
	
	/**
	 * Subclasses must implement this method to create a copy of an object. This is only necessary for
	 * factories that use the COPY instantiation policy. Typically, calling clone() is an appropriate
	 * implementation.
	 */
	protected T copy(T template) {
		throw new UnsupportedOperationException("Factory uses COPY instantiation policy but does not implement copy method.");
	}
	
	/**
	 * Called just before an object is added to the factory.
	 */
	private final T allocateEntry(T bean) {
		if(bean != null && policy == InstantiationPolicy.REUSE) {
			injectResources(bean);
			initializeEntry(bean);
		}
		return bean;
	}
	
	/**
	 * Called just after an object is retrieved from the factory (but before the object is
	 * initialized and returned to the client.
	 */
	private final T retrieveEntry(T bean) {
		if(bean != null && policy == InstantiationPolicy.COPY) {
			bean = copy(bean);
			injectResources(bean);
			initializeEntry(bean);
		}
		return bean;
	}
	
	/**
	 * Performs dependency injection on the given bean for each of the defined resources.
	 */
	private final void injectResources(T bean) {
		if(resources != null) {
			for(Injector<T> resource : resources) {
				resource.inject(bean);
			}
		}
	}
	
}
