	@Test
	public void listAllInstances() {
		AmazonEC2Client client = null;
		try {

			AwsServiceInterface service = new AwsServiceInterface();
			AwsConnectionFactory value = new AwsConnectionFactory(accessKey, secretKey);
			value.setServiceEndpoint(serviceEndpoint);
			service.setConnectionFactory(value);

			client = value.getAmazonEC2Client();

			List<Instance> runningInstances = new LinkedList<Instance>();

			DescribeInstancesRequest describeInstancesRequest = new DescribeInstancesRequest().withFilters(new Filter("instance-state-name").withValues(InstanceStateName.Pending.toString(),
					InstanceStateName.Running.toString(), InstanceStateName.ShuttingDown.toString()));
			DescribeInstancesResult result = client.describeInstances(describeInstancesRequest);

			List<Reservation> reservations = result.getReservations();
			for (Reservation reservation : reservations) {
				List<Instance> instances = reservation.getInstances();
				for (Instance instance : instances) {
					runningInstances.add(instance);
				}
			}
			
			assertTrue(runningInstances.size() > 0);
		} finally {
			if (client != null)
				client.shutdown();
		}

	}
