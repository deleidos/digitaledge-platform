package com.deleidos.rtws.webapp.ingestapi.loader;

import java.io.File;
import java.io.IOException;
import java.net.URL;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

import org.apache.log4j.Logger;
import org.codehaus.plexus.util.StringUtils;

import com.deleidos.rtws.commons.config.RtwsConfig;
import com.deleidos.rtws.splitter.SplitterFactory;
import com.deleidos.rtws.webapp.ingestapi.cache.ServiceCache;
import com.deleidos.rtws.webapp.ingestapi.commons.JarUtil;
import com.deleidos.rtws.webapp.ingestapi.exception.IngestServiceException;
import com.deleidos.rtws.webapp.ingestapi.factory.ServiceFactoryUtils;
import com.deleidos.rtws.webapp.ingestapi.factory.ServiceType;
import com.deleidos.rtws.webapp.ingestapi.model.Service;
import com.deleidos.rtws.webapp.ingestapi.model.SplitterService;

/**
 * This class knows how to extract information from service jars.
 */
public class ServiceLoader {
	
	/** Logger. */
	private static Logger logger = Logger.getLogger(ServiceLoader.class);
	
	private static String SERVICE_SUPERCLASS_PACKAGE = "com.deleidos.rtws";
	
	public static String SERVICE_JAR_MATCH = "deleidos-rtws.*\\.jar";
	
	/** The fully qualified name of the parser class/interface in which all parsers must inherit */
	private String parserTypeFQN;
	
	/** The fully qualified name of the processor class/interface in which all processors must inherit */
	private String processorTypeFQN;
	
	/** The fully qualified name of the processor class/interface in which all splitters must inherit */
	private String splitterTypeFQN;
	
	/** The fully qualified name of the data sink class/interface in which all data sinks must inherit */
	private String dataSinkTypeFQN;
	
	/** The fully qualified name of the transport class/interface in which all transports must inherit */
	private String transportsTypeFQN;
	
	/**
	 * Constructor.
	 */
	public ServiceLoader() {
		try {
			parserTypeFQN = RtwsConfig.getInstance().getString("webapp.ingestapi.parser.type");
			processorTypeFQN = RtwsConfig.getInstance().getString("webapp.ingestapi.processor.type");
			dataSinkTypeFQN = RtwsConfig.getInstance().getString("webapp.ingestapi.datasink.type");
			transportsTypeFQN = RtwsConfig.getInstance().getString("webapp.ingestapi.transports.type");
			splitterTypeFQN = RtwsConfig.getInstance().getString("webapp.ingestapi.splitter.type","com.deleidos.rtws.splitter.Splitter");
			loadSuperclassesInClasspath();
		} catch(Exception ex) {
			logger.error("ServiceLoader - Exception: " + ex.getMessage(), ex);
		}
	}
	
	private void loadSuperclassesInClasspath() {
		String classpath = getClass().getProtectionDomain().getCodeSource().getLocation().getPath()+"../lib";

		// Find a filtered list of jar files in the classpath
		List<File> jars = JarUtil.findFromDirectory(SERVICE_JAR_MATCH, classpath);
		
		// Introspect each jar file
		int jarcount = 0;
		for (File jar : jars) {
			processJarForSuperclasses(jar, SERVICE_SUPERCLASS_PACKAGE, true);
			jarcount++;
		}

		logger.info(String.format("Loaded ingest service superclasses from %d jar files in directory %s", jarcount, classpath));
	}

	/**
	 * Loads in all the configurable ingest services given
	 * a directory path.
	 */
	public Map<String, Service> load(List<File> jars) {
		
		for (File jar : jars) {
			processJarForSuperclasses(jar, "", false);
		}
		
		Map<String, Service> services = new HashMap<String, Service>();
		
		// Introspect each jar file
		for (File jar : jars) {
			try {
				services.putAll(processJar(jar));
			} catch (IOException ioe) {
				logger.error(String.format("loadServices - Exception loading %s : %s", jar.toURI(), ioe.getMessage()), ioe);
			}
		}
		
		return services;
		
	}
	
	/**
	 * Loads in all the configurable ingest services contained inside one jar file.
	 */
	public Map<String, Service> load(File jar) {
		
		processJarForSuperclasses(jar, "", false);
		
		Map<String, Service> services = new HashMap<String, Service>();
		
		// Introspect the jar file
		try {
			services.putAll(processJar(jar));
		} catch (IOException ioe) {
			logger.error(String.format("loadServices - Exception loading %s : %s", jar.toURI(), ioe.getMessage()), ioe);
		}
		try {
			SplitterFactory.processJar(jar);
		} catch (IOException ioe) {
			logger.error(String.format("loadServices - Exception loading splitter from jar %s : %s", jar.toURI(), ioe.getMessage()), ioe);
		}
		
		return services;
		
	}
	
	/**
	 * Introspect each jar file and extract information about specific services
	 */
	private Map<String, Service> processJar(File jar)
			throws IOException {

		logger.info("Loading ingest services from jar file " + jar.getAbsolutePath());
		
		Map<String, Service> services = new HashMap<String, Service>();
		
		Map<String, String> serviceMap = JarUtil.findServices(jar.toURI().toURL());
		
		for (String serviceClassImpl : serviceMap.keySet()) {
			try {
				String serviceClassItf = serviceMap.get(serviceClassImpl);
				
				if (serviceClassItf.equals(parserTypeFQN))
				{
					Service parser = ServiceFactoryUtils.createService(serviceClassImpl, jar.toURI().toURL(),
							ServiceType.PARSER);
					services.put(serviceClassImpl, parser);
					logger.info("Loaded parser service " + serviceClassImpl);
				}
				else if (serviceClassItf.equals(splitterTypeFQN))
				{
					Service splitter = ServiceFactoryUtils.createService(serviceClassImpl, jar.toURI().toURL(),
							ServiceType.SPLITTER);
					String type = ((SplitterService)splitter).getType();
					if(type!=null && type.length()>0){
						services.put(serviceClassImpl, splitter);
						logger.info("Loaded splitter service " + serviceClassImpl +", type "+type);
					}
				}
				else if (serviceClassItf.equals(processorTypeFQN))
				{
					Service processor = ServiceFactoryUtils.createService(serviceClassImpl, jar.toURI().toURL(),
							ServiceType.PROCESSOR);
					services.put(serviceClassImpl, processor);
					logger.info("Loaded processor service " + serviceClassImpl);
				}
				else if (serviceClassItf.equals(dataSinkTypeFQN))
				{
					Service datasink = ServiceFactoryUtils.createService(serviceClassImpl, jar.toURI().toURL(),
							ServiceType.DATASINK);
					services.put(serviceClassImpl, datasink);
					logger.info("Loaded data sink service " + serviceClassImpl);
				}
				else if (serviceClassItf.equals(transportsTypeFQN))
				{
					Service transport = ServiceFactoryUtils.createService(serviceClassImpl, jar.toURI().toURL(),
							ServiceType.TRANSPORT);
					services.put(serviceClassImpl, transport);
					logger.info("Loaded transport service " + serviceClassImpl);
				}
				else 
				{
					logger.debug("Unknown service class type " + serviceClassItf);
				}
			} catch (IngestServiceException ise) {
				logger.error("loadServices [" + serviceClassImpl + "] - IngestServiceException: " + ise.getMessage(), ise);
			} catch (Throwable th) {
				logger.error("loadServices [" + serviceClassImpl + "] - Throwable: " + th.getMessage(), th);
			}
		}
		
		return services;
		
	}

	/**
	 * Introspect a jar file and extract information about all services in the provided package.
	 */
	private void processJarForSuperclasses(File jar, String superclassPackage, boolean isabstract) {

		logger.info("Loading superclasses from file " + jar.getAbsolutePath());
		
		try {
			URL jarurl = jar.toURI().toURL();
			
			for (String classname : JarUtil.findClassesInPackage(jarurl, superclassPackage, isabstract, true)) {
				// Create Service for classname, put it into superclass ServiceCache
				Service service = ServiceFactoryUtils.createService(classname, jarurl,  ServiceType.GENERIC);
				if (service != null &&
						(! StringUtils.isBlank(service.getDescription()) ||
								! service.getInputParameters().isEmpty() ||
								! service.getSystemParameters().isEmpty())) {
					ServiceCache.getInstance().addSuperclassService(classname, service);
					logger.info("Loaded superclass service " + classname);
				}
			}
		} catch (IOException ioe) {
			logger.error(String.format("loadSuperclasses - Exception processing %s : %s", jar.toURI(), ioe.getMessage()), ioe);
		} catch (IngestServiceException ise) {
			logger.error(String.format("loadSuperclasses - Exception processing %s : %s", jar.toURI(), ise.getMessage()), ise);
		}
		
	}

}