	public static void encryptAndStoreCertificate(InputStream is) throws StorageUtilsException {
		
		try {
			//if a public certificate already exists then revoke and delete all public certs
			//takes care of case when a user uploads new public cert pair
			StringBuilder certPath = new StringBuilder();
			certPath.append(CAProperties.getPublicPkiDir()).append('/').append(CERTIFICATE_FILENAME);
			if(new File(certPath.toString()).exists()){
				revokeAllPublic();
			}
			
			String password = PasswordBuilder.getInstance().getPassword();
		
			byte [] plaintext = IOUtils.toByteArray(is);
			byte [] encrypted = encrypt(plaintext, password);
		
			storeCertificate(new ByteArrayInputStream(encrypted));
		} catch (Exception ex) {
			logger.debug("Error encrypting and storing certificate.", ex);
			throw new StorageUtilsException("Error encrypting and storing certificate.", ex);
		}
		
	}
	
	private static void storeCertificate(InputStream is) throws StorageUtilsException {
		
		String tempDirPath = makeTempDir();
		
		try {
			File tempFile = new File(tempDirPath + '/' + CERTIFICATE_FILENAME);
		
			FileUtils.copyInputStreamToFile(is, tempFile);
		
			copy(tempFile.getAbsolutePath(), CAProperties.getPublicPkiDir());
		} catch (Exception ex) {
			logger.debug("Error storing certificate.", ex);
			throw new StorageUtilsException("Error storing certificate.", ex);
		} finally {
			try {
				FileUtils.deleteDirectory(new File(tempDirPath)); 
			} catch (Exception ex) {
				logger.debug("Fail to delete temp directory '" + tempDirPath + "'. Message: " + ex.getMessage());
			}
		}
		
	}
	
