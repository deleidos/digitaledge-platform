package com.saic.rtws.commons.model.user;

import java.util.List;

public class UserWatchlist
{
	private String username;
	private List<String> watchedFilterIds;
	
	public String getUsername()
	{
		return username;
	}
	public void setUsername(String username)
	{
		this.username = username;
	}
	public List<String> getWatchedFilterIds()
	{
		return watchedFilterIds;
	}
	public void setWatchedFilterIds(List<String> watchedFilterIds)
	{
		this.watchedFilterIds = watchedFilterIds;
	}
}
