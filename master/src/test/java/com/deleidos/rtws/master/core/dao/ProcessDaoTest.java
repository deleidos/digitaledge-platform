	@Test
	public void testStoreDuplicate() throws Exception {

		Process processA = new Process();
		processA.setId("p1");
		processA.setPersistentDnsName("hostA.domain.com");
		processA.setPersistentIpAddress("1.1.1.1");
		
		Process processB = new Process();
		processB.setId("p2");
		processB.setPersistentDnsName("hostB.domain.com");
		processB.setPersistentIpAddress("2.2.2.2");
		
		Process duplicate = new Process();
		duplicate.setId("p3");
		duplicate.setPersistentDnsName(processA.getPersistentDnsName());
		duplicate.setPersistentIpAddress(processB.getPersistentIpAddress());
		
		
		MockDao dao = new MockDao();
		dao.store(processA);
		dao.store(processB);

		try {
			dao.store(duplicate);
			fail("No exception thrown when storing duplicate entry.");
		} catch (IOException e) {
			// Expected.
		}

	}

	@Test
	public void testErrorOnStore() throws Exception {

		MockDao dao = new MockDao();
		dao.setException(new IOException());
		try {
			dao.store(process);
			fail("Exception not raised when throws by write.");
		} catch(Exception e) {
			
			assertNull("Store failed, but process still found in cache.", dao.findById(process.getId()));
			
			TransactedFile mock = dao.getWrapper();
			Mockito.verify(mock).revert();
			Mockito.verify(mock).release();
			
			// This also needs to verify that the stream used to write the files was closed,
			// and that the close occurred before the revert/release. I don't know how.
			
		}

	}

	private void storeExtras(MockDao dao) throws Exception {
		Process extra;
		for (int i = 0; i < 5; i++) {
			extra = new Process();
			extra.setId("aws-0000" + i);
			extra.setGroup("test" + i);
			extra.setPersistentDnsName("host" + i + ".rtws.deleidos.com");
			dao.store(extra);
		}
	}

	private void verify(Process result) {
		assertEquals("Incorrect value for process id.", process.getId(), result.getId());
		assertEquals("Incorrect value for role.", process.getGroup(), result.getGroup());
		assertEquals("Incorrect value for persistent dns.", process.getPersistentDnsName(), result.getPersistentDnsName());
		assertEquals("Incorrect value for persistent ip.", process.getPersistentIpAddress(), result.getPersistentIpAddress());
		assertNull("Incorrect value for private dns", result.getPrivateDnsName());
		assertNull("Incorrect value for private ip", result.getPrivateIpAddress());
		assertNull("Incorrect value for public dns", result.getPublicDnsName());
		assertNull("Incorrect value for public ip", result.getPublicIpAddress());
	}

	private static class MockDao extends FileProcessDao {

		private IOException exception;
		private ByteArrayOutputStream output = new ByteArrayOutputStream();
		private ByteArrayInputStream input;
		private TransactedFile wrapper;

		public MockDao() {
			super(new File("test.xml"), null);
		}

		public String getOutput() {
			return output.toString();
		}

		public void setInput(String xml) {
			input = new ByteArrayInputStream(xml.getBytes());
		}

		public void setException(IOException e) {
			exception = e;
		}
		
		public TransactedFile getWrapper() {
			return wrapper;
		}
		
		protected InputStream createStreamToLoadCache() throws IOException {
			if(exception == null) {
				return input;
			} else {
				throw exception;
			}
		}

		protected OutputStream createStreamToStoreCache() throws IOException {
			if(exception == null) {
				return output;
			} else {
				throw exception;
			}
		}

		protected TransactedFile createTransationWrapper(File file) {
			wrapper = mock(TransactedFile.class);
			when(wrapper.getTempFile()).thenReturn(new File(".test.xml"));
			return wrapper;
		}
		
	}
	
